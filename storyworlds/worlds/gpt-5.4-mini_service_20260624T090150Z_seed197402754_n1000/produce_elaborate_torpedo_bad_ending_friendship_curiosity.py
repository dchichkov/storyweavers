def _r_scare(world: World) -> list[str]:
    out: list[str] = []
    for actor in world.characters():
        if actor.memes.get("fear", 0.0) < THRESHOLD:
            continue
        sig = ("scare", actor.id)
        if sig in world.fired:
            continue
        world.fired.add(sig)
        actor.memes["panic"] = actor.memes.get("panic", 0.0) + 1
        out.append(f"{actor.id} felt a cold shiver from the dark room.")
    return out


def _r_break(world: World) -> list[str]:
    out: list[str] = []
    for item in list(world.entities.values()):
        if item.meters.get("wobble", 0.0) < THRESHOLD:
            continue
        sig = ("break", item.id)
        if sig in world.fired:
            continue
        world.fired.add(sig)
        item.meters["broken"] = item.meters.get("broken", 0.0) + 1
        out.append(f"{item.label} gave a sad little crack.")
    return out


CAUSAL_RULES = [Rule("scare", _r_scare), Rule("break", _r_break)]


def propagate(world: World, narrate: bool = True) -> list[str]:
    produced: list[str] = []
    changed = True
    for _ in range(len(globals().get("CAUSAL_RULES", [])) + 4):
        changed = False
        for rule in CAUSAL_RULES:
            sents = rule.apply(world)
            if sents:
                changed = True
                produced.extend(sents)
    if narrate:
        for s in produced:
            world.say(s)
    return produced


def setting_detail(setting: Setting) -> str:
    if setting.indoors:
        return f"The {setting.place} was hushed, and every floorboard seemed to listen."
    return f"The {setting.place} looked calm, but the air carried a spooky stillness."


def predict(world: World, actor: Entity, activity: Activity, prize_id: str) -> dict:
    sim = world.copy()
    _do_activity(sim, sim.get(actor.id), activity, narrate=False)
    prize = sim.entities.get(prize_id)
    return {
        "broken": bool(prize and prize.meters.get("broken", 0.0) >= THRESHOLD),
        "fear": sum(e.memes.get("fear", 0.0) for e in sim.characters()),
    }


def _do_activity(world: World, actor: Entity, activity: Activity, narrate: bool = True) -> None:
    if activity.id not in world.setting.affords:
        return
    world.zone = set(activity.zone)
    actor.meters[activity.mess] = actor.meters.get(activity.mess, 0.0) + 1
    actor.memes["curiosity"] = actor.memes.get("curiosity", 0.0) + 1
    propagate(world, narrate=narrate)


def select_gear(activity: Activity, prize: Prize) -> Optional[Gear]:
    for gear in GEAR:
        if activity.mess in gear.guards and prize.region in gear.covers:
            return gear
    return None


def prize_at_risk(activity: Activity, prize: Prize) -> bool:
    return prize.region in activity.zone


def introduce(world: World, hero: Entity) -> None:
    trait = next((t for t in hero.traits if t != "little"), "curious")
    world.say(f"{hero.id} was a little {trait} {hero.type} who liked spooky stories.")


def friendship(world: World, hero: Entity, friend: Entity) -> None:
    hero.memes["friendship"] = hero.memes.get("friendship", 0.0) + 1
    friend.memes["friendship"] = friend.memes.get("friendship", 0.0) + 1
    world.say(f"{hero.id} and {friend.id} were best friends, and they went everywhere together.")


def wants(world: World, hero: Entity, activity: Activity, prize: Entity) -> None:
    hero.memes["curiosity"] = hero.memes.get("curiosity", 0.0) + 1
    world.say(
        f"{hero.id} wanted to {activity.verb} because {hero.pronoun('subject')} was curious about "
        f"the shape of {prize.phrase}."
    )


def warn(world: World, parent: Entity, hero: Entity, activity: Activity, prize: Entity) -> bool:
    pred = predict(world, hero, activity, prize.id)
    if not pred["broken"]:
        return False
    world.facts["predicted_break"] = True
    world.say(
        f'"If you try to {activity.verb}, your {prize.label} may get {activity.soil}," '
        f"{parent.pronoun('possessive')} {parent.type} said softly."
    )
    return True


def defy(world: World, hero: Entity, activity: Activity) -> None:
    hero.memes["stubborn"] = hero.memes.get("stubborn", 0.0) + 1
    world.say(f"But {hero.id} still wanted to see what would happen in the dark.")


def frighten(world: World, hero: Entity, friend: Entity, activity: Activity) -> None:
    hero.memes["fear"] = hero.memes.get("fear", 0.0) + 1
    friend.memes["fear"] = friend.memes.get("fear", 0.0) + 1
    world.say(f"Together, they took one careful step toward the shadowy {activity.keyword}.")


def compromise(world: World, parent: Entity, hero: Entity, friend: Entity, activity: Activity, prize: Entity) -> Optional[Gear]:
    gear_def = select_gear(activity, prize)
    if gear_def is None:
        return None
    gear = world.add(Entity(
        id=gear_def.id,
        type="gear",
        label=gear_def.label,
        owner=hero.id,
        caretaker=parent.id,
        held_by=hero.id,
        plural=gear_def.plural,
    ))
    if predict(world, hero, activity, prize.id)["broken"]:
        del world.entities[gear.id]
        return None
    world.say(
        f"{parent.id} pointed at the {gear.label} and said they should use it first."
    )
    return gear_def


def resolve(world: World, hero: Entity, friend: Entity, activity: Activity, prize: Entity, gear_def: Gear) -> None:
    hero.memes["fear"] = 0.0
    friend.memes["fear"] = 0.0
    hero.memes["friendship"] = hero.memes.get("friendship", 0.0) + 1
    friend.memes["friendship"] = friend.memes.get("friendship", 0.0) + 1
    world.say(
        f"{hero.id} nodded, and {friend.id} stayed close. They used the {gear_def.label}, "
        f"so {hero.id} could still {activity.verb} without ruining {prize.phrase}."
    )
    world.say(
        f"At the end, the plan looked strange, the room stayed quiet, and {prize.label} remained whole."
    )


