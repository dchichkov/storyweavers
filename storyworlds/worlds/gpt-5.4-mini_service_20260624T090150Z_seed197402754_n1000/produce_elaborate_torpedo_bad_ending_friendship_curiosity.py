    parent = world.add(Entity(id=parent_name, kind="character", type="mother", label="the parent"))
    prize = world.add(Entity(
        id="prize",
        type=prize_cfg.type,
        label=prize_cfg.label,
        phrase=prize_cfg.phrase,
        owner=hero.id,
        caretaker=parent.id,
        plural=prize_cfg.plural,
    ))

    introduce(world, hero)
    friendship(world, hero, friend)
    world.say(setting_detail(setting))
    world.say(f"One night, {hero.id} and {friend.id} found a place to {activity.gerund}.")
    world.say(f"They wanted to produce something elaborate, almost like a toy {activity.keyword}.")
    wants(world, hero, activity, prize)
    warn(world, parent, hero, activity, prize)
    defy(world, hero, activity)
    frighten(world, hero, friend, activity)

    world.para()
    gear_def = compromise(world, parent, hero, friend, activity, prize)
    if gear_def is not None:
        resolve(world, hero, friend, activity, prize, gear_def)
    else:
        world.say(f"The strange plan fell apart, and the night ended in a bad ending for the toy.")

    world.facts.update(hero=hero, friend=friend, parent=parent, prize=prize, activity=activity, setting=setting, gear=gear_def)
    return world


