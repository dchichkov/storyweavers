def valid_combos() -> list[tuple[str, str, str]]:
    out = []
    for place, setting in SETTINGS.items():
        for act_id in setting.affords:
            act = _safe_lookup(ACTIVITIES, act_id)
            for prize_id, prize in PRIZES.items():
                if prize.region in act.zone:
                    out.append((place, act_id, prize_id))
    return out


def resolve_params(args: argparse.Namespace, rng: random.Random) -> StoryParams:
    if getattr(args, "activity", None) and getattr(args, "prize", None):
        act, pr = _safe_lookup(ACTIVITIES, getattr(args, "activity", None)), _safe_lookup(PRIZES, getattr(args, "prize", None))
        if pr.region not in act.zone:
            return _fallback_storyparams(args, rng, StoryParams, globals())
    combos = [c for c in valid_combos()
              if (getattr(args, "place", None) is None or c[0] == getattr(args, "place", None))
              and (getattr(args, "activity", None) is None or c[1] == getattr(args, "activity", None))
              and (getattr(args, "prize", None) is None or c[2] == getattr(args, "prize", None))]
    if not combos:
        return _fallback_storyparams(args, rng, StoryParams, globals())
    place, activity, prize = rng.choice(list(combos))
    gender = getattr(args, "gender", None) or rng.choice(["girl", "boy"])
    name = getattr(args, "name", None) or rng.choice(GIRL_NAMES if gender == "girl" else BOY_NAMES)
    parent = getattr(args, "parent", None) or rng.choice(["mother", "father"])
    trait = rng.choice(TRAITS)
    return StoryParams(place=place, activity=activity, prize=prize, name=name, gender=gender, parent=parent, trait=trait)


def _do_activity(world: World, actor: Entity, activity: Activity, narrate: bool = True) -> None:
    world.zone = set(activity.zone)
    actor.meters[activity.mess] = actor.meters.get(activity.mess, 0.0) + 1.0
    actor.memes["curiosity"] = actor.memes.get("curiosity", 0.0) + 1.0
    for item in list(world.entities.values()):
        if item.worn_by == actor.id and not item.label == "rain boots":
            if item.label == "target" and item.region in world.zone and not world.covered(actor, item.region):
                item.meters[activity.mess] = item.meters.get(activity.mess, 0.0) + 1.0
                item.meters["dirty"] = item.meters.get("dirty", 0.0) + 1.0
                if narrate:
                    world.say(f"The {item.label} got {activity.mess}.")
    if narrate:
        world.say(f"{actor.id} was {activity.gerund}.")


def tell(setting: Setting, activity: Activity, prize_cfg: Prize, hero_name: str, hero_type: str, parent_type: str) -> World:
    world = World(setting)
    hero = world.add(Entity(id=hero_name, kind="character", type=hero_type, meters={}, memes={}))
    parent = world.add(Entity(id="Parent", kind="character", type=parent_type, label="the parent", meters={}, memes={}))
    prize = world.add(Entity(id="prize", type=prize_cfg.type, label=prize_cfg.label, phrase=prize_cfg.phrase, owner=hero.id,
                             caretaker=parent.id, region=prize_cfg.region, plural=prize_cfg.plural, meters={}, memes={}))
    hero.worn_by = None
    prize.worn_by = hero.id

    world.say(f"{hero.id} was a {hero.pronoun('possessive')} little {hero.type} who liked clues and quiet looks.")
    world.say(f"{hero.pronoun().capitalize()} and {hero.pronoun('possessive')} {parent.label or 'parent'} loved a good whodunit.")
    world.say(f"One day, {hero.id} noticed {hero.pronoun('possessive')} {prize.label} near {setting.place}.")
    world.para()
    world.say(f'"Someone made a mess," {hero.id} said.')
    world.say(f'"Not in my {prize.label}," said {hero.pronoun('possessive')} {parent.label_word if hasattr(parent, "label_word") else "parent"}.')
    world.say(f"They stepped to {setting.place} together, and the clue felt plain: {activity.clue_rhyme}.")
    world.say(f'"If the mud is new," {hero.id} said, "then the muddy foot did it too."')
    world.para()
    _do_activity(world, hero, activity, narrate=True)
    world.say(f'"What do we do now?" asked {hero.id}.')
    world.say(f'"We choose the safe fix," said the parent. "Put on the rain boots first."')
    boots = world.add(Entity(id="boots", type="boots", label="rain boots", plural=True, owner=hero.id, caretaker=parent.id))
    boots.worn_by = hero.id
    hero.memes["relief"] = hero.memes.get("relief", 0.0) + 1.0
    world.say(f'They {GEAR[0].tail}, and then {hero.id} could keep looking without ruining the {prize.label}.')
    world.say(f'"Case closed," said {hero.id}. "The clue was muddy, but the ending was tidy."')

    world.facts.update(hero=hero, parent=parent, prize=prize, activity=activity, setting=setting, gear=GEAR[0])
    return world


