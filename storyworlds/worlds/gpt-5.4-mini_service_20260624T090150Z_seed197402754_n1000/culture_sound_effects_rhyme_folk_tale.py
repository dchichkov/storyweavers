def resolve_params(args: argparse.Namespace, rng: random.Random) -> StoryParams:
    combos = [c for c in valid_combos()
              if (getattr(args, "place", None) is None or c[0] == getattr(args, "place", None))
              and (getattr(args, "activity", None) is None or c[1] == getattr(args, "activity", None))
              and (getattr(args, "prize", None) is None or c[2] == getattr(args, "prize", None))]
    if not combos:
        return _fallback_storyparams(args, rng, StoryParams, globals())
    place, activity, prize = rng.choice(list(combos))
    gender = getattr(args, "gender", None) or rng.choice(["girl", "boy"])
    if getattr(args, "prize", None) and getattr(args, "gender", None) is not None:
        pass
    name = getattr(args, "name", None) or rng.choice(GIRL_NAMES if gender == "girl" else BOY_NAMES)
    helper = getattr(args, "helper", None) or rng.choice(["mother", "father", "grandparent"])
    trait = rng.choice(TRAITS)
    return StoryParams(place, activity, prize, name, gender, helper, trait)


def reasonableness_gate(params: StoryParams) -> None:
    if not compatible(params.place, params.activity, params.prize):
        pass


def simulate(params: StoryParams) -> World:
    reasonableness_gate(params)
    world = World(_safe_lookup(SETTINGS, params.place))
    hero = world.add(Entity(params.name, kind="character", type=params.gender))
    helper = world.add(Entity("Helper", kind="character", type=params.helper, label=params.helper))
    prize = world.add(Entity("Prize", type=_safe_lookup(PRIZES, params.prize).type, label=_safe_lookup(PRIZES, params.prize).label, phrase=_safe_lookup(PRIZES, params.prize).phrase, owner=hero.id, caretaker=helper.id))
    act = _safe_lookup(ACTIVITIES, params.activity)
    remedy = REMEDIES[{"song": "cover", "dance": "tie", "drum": "patch"}[params.activity]]

    world.facts.update(hero=hero, helper=helper, prize=prize, activity=act, remedy=remedy)

    world.say(f"Long ago, in {world.place.name}, there lived a {params.trait} {params.gender} named {hero.id}.")
    world.say(f"{hero.id} loved the old culture of the village, especially {act.gerund}, and every child knew {act.sound} when the day was bright.")

    world.para()
    world.say(f"One day, {hero.id} brought out {prize.phrase}.")
    world.say(f"{hero.id} wanted to {act.verb}, but {prize.phrase} might {_safe_lookup(PRIZES, params.prize).loss}.")
    world.say(f'"{act.sound}," said the drums in the tale, "but mind the {act.rhyme}."')

    if params.activity == "song":
        world.say(f"{hero.id} tried to sing, but a gust might lift the pages and make the tune wobble.")
    elif params.activity == "dance":
        world.say(f"{hero.id} tried to step, but the scarf could tangle in the quick turns.")
    else:
        world.say(f"{hero.id} tried to drum, but the skin sounded weak and old.")

    world.para()
    world.say(f"{helper.label.capitalize()} came near and smiled.")
    world.say(f'"No need to fret," {helper.pronoun("subject")} said. "We can {remedy.action} first, and then your {act.verb} will stay merry."')
    world.say(f"So they chose {remedy.label}, and {remedy.result}.")
    world.say(f"{act.sound}! {act.sound}! Soon the whole place echoed with {act.rhyme}, and the old culture felt warm as bread.")

    world.para()
    world.say(f"At the end, {hero.id} was {act.gerund}, {prize.label} safe and sound, while {helper.label} clapped along.")
    world.say(f"And the tale says: {act.sound}, {act.sound}, keep the old song round and round.")
    return world


def generation_prompts(world: World) -> list[str]:
    f = world.facts
    hero, act, prize = f["hero"], f["activity"], f["prize"]
    return [
        f'Write a gentle folk tale about culture, sound effects, and rhyme with "{act.sound}" and "{act.rhyme}".',
        f"Tell a story where {hero.id} wants to {act.verb} but must protect {prize.phrase}.",
        f"Write a child-friendly tale with a small problem, a helper, and a happy ending in {world.place.name}.",
    ]


def story_qa(world: World) -> list[QAItem]:
    f = world.facts
    hero, helper, prize, act, remedy = f["hero"], f["helper"], f["prize"], f["activity"], f["remedy"]
    return [
        QAItem(question=f"Who was the story about?", answer=f"It was about {hero.id}, who loved the old culture and wanted to {act.verb}."),
        QAItem(question=f"What problem did {hero.id} have?", answer=f"{hero.id} worried that {prize.phrase} might {_safe_lookup(PRIZES, prize.type).loss}."),
        QAItem(question=f"How did the helper solve it?", answer=f"{helper.label.capitalize()} said to {remedy.action} first, and then the music and rhyme could go on safely."),
        QAItem(question=f"What happened at the end?", answer=f"{hero.id} was {act.gerund}, and the whole place rang with {act.sound} and {act.rhyme}."),
    ]


def world_knowledge_qa(world: World) -> list[QAItem]:
    f = world.facts
    act = _safe_fact(world, f, "activity")
    qs = []
    if "culture" in act.tags:
        qs.append(QAItem(question="What is culture?", answer="Culture is the shared songs, stories, dances, and ways of life that people keep and pass on."))
    qs.append(QAItem(question="What is a sound effect in a story?", answer="A sound effect is a word or phrase that copies a noise, like boom, tap-tap, or swish."))
    qs.append(QAItem(question="What is rhyme?", answer="Rhyme happens when the ends of words sound alike, like stone and bone."))
    return qs


ASP_RULES = r"""
place(village). place(hall). place(market).
activity(song). activity(dance). activity(drum).
prize(songbook). prize(scarf). prize(drum).
compatible(P,A,R) :- place(P), activity(A), prize(R).
valid(P,A,R) :- compatible(P,A,R).
#show valid/3.
"""


def asp_facts() -> str:
    import asp
    return "\n".join([asp.fact("place", p) for p in SETTINGS] +
                     [asp.fact("activity", a) for a in ACTIVITIES] +
                     [asp.fact("prize", r) for r in PRIZES])


def asp_program(show: str) -> str:
    return f"{asp_facts()}\n{ASP_RULES}\n{show}\n"


def asp_valid_combos() -> list[tuple]:
    import asp
    model = asp.one_model(asp_program("#show valid/3."))
    return sorted(set(asp.atoms(model, "valid")))


def asp_verify() -> int:
    py = set(valid_combos())
    asp_set = set(asp_valid_combos())
    if py == asp_set:
        print(f"OK: clingo gate matches valid_combos() ({len(py)} combos).")
        return 0
    print("MISMATCH between clingo and valid_combos():")
    if py - asp_set:
        print("  only in python:", sorted(py - asp_set))
    if asp_set - py:
        print("  only in clingo:", sorted(asp_set - py))
    return 1


def format_qa(sample: StorySample) -> str:
    out = ["== (1) Generation prompts =="]
    for i, p in enumerate(sample.prompts, 1):
        out.append(f"{i}. {p}")
    out.append("")
    out.append("== (2) Story questions ==")
    for qa in sample.story_qa:
        out.append(f"Q: {qa.question}")
        out.append(f"A: {qa.answer}")
    out.append("")
    out.append("== (3) World-knowledge questions ==")
    for qa in sample.world_qa:
        out.append(f"Q: {qa.question}")
        out.append(f"A: {qa.answer}")
    return "\n".join(out)


def dump_trace(world: World) -> str:
    lines = ["--- world model state ---"]
    for e in list(world.entities.values()):
        bits = []
        if e.owner:
            bits.append(f"owner={e.owner}")
        if e.caretaker:
            bits.append(f"caretaker={e.caretaker}")
        if e.worn_by:
            bits.append(f"worn_by={e.worn_by}")
        if e.meters:
            bits.append(f"meters={e.meters}")
        if e.memes:
            bits.append(f"memes={e.memes}")
        lines.append(f"  {e.id:8} ({e.type:8}) {' '.join(bits)}")
    return "\n".join(lines)


def generate(params: StoryParams) -> StorySample:
    world = simulate(params)
    return StorySample(
        params=params,
        story=world.render(),
        prompts=generation_prompts(world),
        story_qa=story_qa(world),
        world_qa=world_knowledge_qa(world),
        world=world,
    )


def emit(sample: StorySample, *, trace: bool = False, qa: bool = False, header: str = "") -> None:
    if header:
        print(header)
    print(sample.story)
    if trace and sample.world is not None:
        print(dump_trace(sample.world))
    if qa:
        print()
        print(format_qa(sample))


