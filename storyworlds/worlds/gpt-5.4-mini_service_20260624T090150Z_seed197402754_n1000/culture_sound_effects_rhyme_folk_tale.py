def compatible(place: str, activity: str, prize: str) -> bool:
    return activity in {"song", "dance", "drum"} and prize in {"songbook", "scarf", "drum"}


def valid_combos() -> list[tuple[str, str, str]]:
    out = []
    for p in SETTINGS:
        for a in ACTIVITIES:
            for r in PRIZES:
                if compatible(p, a, r):
                    out.append((p, a, r))
    return out


