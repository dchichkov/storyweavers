class World:
    def __init__(self, setting: Setting) -> None:
        self.setting = setting
        self.entities: dict[str, Entity] = {}
        self.paragraphs: list[list[str]] = [[]]
        self.fired: set[tuple] = set()
        self.zone: set[str] = set()
        self.facts: dict = {}

    def add(self, ent: Entity) -> Entity:
        self.entities[ent.id] = ent
        return ent

    def get(self, eid: str) -> Entity:
        if eid not in self.entities:
            label = str(eid).replace("_", " ")
            self.entities[eid] = Entity(str(eid), label=label)
        return self.entities[eid]

    def characters(self) -> list[Entity]:
        return [e for e in self.entities.values() if e.kind == "character"]

    def worn_items(self, actor: Entity) -> list[Entity]:
        return [e for e in self.entities.values() if e.worn_by == actor.id]

    def covered(self, actor: Entity, region: str) -> bool:
        return any(e.id in CHARM_BY_ID and region in CHARM_BY_ID[e.id].covers for e in self.worn_items(actor))

    def say(self, text: str) -> None:
        if text:
            self.paragraphs[-1].append(text)

    def para(self) -> None:
        if self.paragraphs[-1]:
            self.paragraphs.append([])

    def render(self) -> str:
        return "\n\n".join(" ".join(p) for p in self.paragraphs if p)


CHARM_BY_ID: dict[str, Charm] = {}


def prize_at_risk(activity: Activity, prize: Prize) -> bool:
    return prize.region in activity.zone


def select_charm(activity: Activity, prize: Prize) -> Optional[Charm]:
    for charm in CHARMS:
        if activity.mess in charm.guards and prize.region in charm.covers:
            return charm
    return None


def predict_mess(world: World, actor: Entity, activity: Activity, prize_id: str) -> dict:
    sim = World(world.setting)
    sim.entities = {k: Entity(**vars(v)) for k, v in world.entities.items()}
    sim.zone = set(world.zone)
    sim.fired = set(world.fired)
    sim.facts = dict(world.facts)
    a = sim.get(actor.id)
    a.meters[activity.mess] = a.meters.get(activity.mess, 0) + 1
    prize = sim.entities.get(prize_id)
    soiled = bool(prize and prize.meters.get("dirty", 0) >= THRESHOLD)
    return {"soiled": soiled, "workload": 0}


def _do_activity(world: World, actor: Entity, activity: Activity) -> None:
    world.zone = set(activity.zone)
    actor.meters[activity.mess] = actor.meters.get(activity.mess, 0) + 1


def apply_rules(world: World) -> None:
    for actor in world.characters():
        if actor.meters.get("boast", 0) >= THRESHOLD and actor.meters.get("kindness", 0) < THRESHOLD:
            for item in world.worn_items(actor):
                if item.id in CHARM_BY_ID and item.region in world.zone:
                    if actor.meters.get("mess", 0) >= THRESHOLD:
                        item.meters["dirty"] = item.meters.get("dirty", 0) + 1


def tell(setting: Setting, activity: Activity, prize_cfg: Prize, name: str, gender: str,
         traits: list[str], parent_type: str = "mother") -> World:
    world = World(setting)
    hero = world.add(Entity(id=name, kind="character", type=gender, meters={}, memes={}))
    parent = world.add(Entity(id="Parent", kind="character", type=parent_type, label="the elder"))
    prize = world.add(Entity(
        id="prize", type=prize_cfg.type, label=prize_cfg.label, phrase=prize_cfg.phrase,
        owner=hero.id, caretaker=parent.id, plural=prize_cfg.plural
    ))

    hero.memes["love"] = 1
    hero.memes["desire"] = 1
    hero.memes["brag"] = 0
    hero.memes["kindness"] = 0
    prize.worn_by = hero.id

    world.say(f"In a fairy tale land, {hero.id} was a {traits[0]} little {gender} who loved {activity.keyword}.")
    world.say(f"{hero.id}'s {parent.label} had given {hero.pronoun('object')} {prize.phrase}.")
    world.say(f"One evening, the kingdom rang with hoopla, and the bells seemed to emote in the moonlight.")

    world.para()
    world.say(f"{hero.id} went to {setting.place}. The air felt bright, and the stones waited like silent witnesses.")
    world.say(f"{hero.id} wanted to {activity.verb}, but {hero.pronoun('possessive')} shiny {prize.label} was at risk.")
    pred = predict_mess(world, hero, activity, prize.id)
    if pred["soiled"]:
        pass
    world.say(f"{hero.id} began to emote with a proud grin and started to brag about how splendid {hero.pronoun('subject')} was.")
    hero.memes["brag"] += 1
    hero.meters["mess"] = hero.meters.get("mess", 0) + 1

    world.para()
    if hero.memes["brag"] >= THRESHOLD:
        world.say(f"The proud words made the sprites frown, and the hoopla grew sharp instead of merry.")
        world.say(f"{parent.label.capitalize()} warned, \"Child, a brag can bruise a heart and turn joy sour.\"")
        hero.memes["hurt"] = 1

    charm = select_charm(activity, prize)
    if charm:
        CHARM_BY_ID[charm.id] = charm
        hero.meters["kindness"] = hero.meters.get("kindness", 0) + 1
        world.say(f"Then {hero.id} remembered kindness.")
        world.say(f"{parent.label.capitalize()} offered {charm.label}, saying, \"{charm.prep}.\"")
        hero.memes["brag"] = 0
        hero.memes["kindness"] += 1
        world.say(f"{hero.id} bowed {hero.pronoun('possessive')} head and chose the gentle way.")
        world.say(f"They {charm.tail}. Soon {hero.id} was {activity.gerund}, and {prize.label} stayed clean.")
    else:
        world.say(f"No kind charm could fit the trouble, and the story slipped toward a bad ending.")
        world.say(f"{hero.id} kept bragging until the moon looked cold and the garden went quiet.")
        prize.meters["dirty"] = 1
        world.facts["bad_ending"] = True

    world.facts.update(
        hero=hero,
        parent=parent,
        prize=prize,
        activity=activity,
        setting=setting,
        charm=charm,
        bad_ending=world.facts.get("bad_ending", False),
    )
    return world


