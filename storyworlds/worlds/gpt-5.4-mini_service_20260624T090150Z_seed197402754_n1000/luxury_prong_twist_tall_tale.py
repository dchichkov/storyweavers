KNOWLEDGE_ORDER = ["luxury", "prong", "twist", "sticky", "wobble"]


def generation_prompts(world: World) -> list[str]:
    f = world.facts
    hero, parent, act, prize = f["hero"], f["parent"], f["activity"], f["prize_cfg"]
    return [
        f'Write a tall tale for a young child about {hero.id}, a luxury hotel, and a twist involving a prong.',
        f"Tell a grand, funny story where {hero.id} wants to {act.verb} while {hero.pronoun('possessive')} {parent.label} worries about {prize.phrase}.",
        f'Write a story that includes the words "luxury", "prong", and "twist" and ends with a clever rescue.',
    ]


def story_qa(world: World) -> list[QAItem]:
    f = world.facts
    hero, parent, prize, act = f["hero"], f["parent"], f["prize"], f["activity"]
    gear = _safe_fact(world, f, "gear")
    return [
        QAItem(
            question=f"Who was the story about?",
            answer=f"The story was about {hero.id}, a little {hero.meme_label} child who loved fancy things and big chances.",
        ),
        QAItem(
            question=f"Why did {parent.label} worry about the {prize.label}?",
            answer=f"{parent.label.capitalize()} worried because if {hero.id} hurried to {act.verb}, the {prize.label} could get {act.soil}.",
        ),
        QAItem(
            question=f"What changed because of the twist near the prong?",
            answer=f"The ribbon curled around the prong and turned it into a steady hook, which helped the dessert stay safe.",
        ),
        QAItem(
            question=f"How did the grown-up help {hero.id} in the end?",
            answer=f"They suggested {gear.label} first, and that let {hero.id} {act.verb} without making a mess.",
        ),
    ]


def world_knowledge_qa(world: World) -> list[QAItem]:
    tags = set(world.facts["activity"].tags)
    out: list[QAItem] = []
    for tag in globals().get("KNOWLEDGE_ORDER", sorted(globals().get("KNOWLEDGE", []))):
        if tag in tags or tag == "luxury":
            out.extend(QAItem(question=q, answer=a) for q, a in KNOWLEDGE[tag])
    return out


def format_qa(sample: StorySample) -> str:
    lines = ["== Generation prompts =="]
    for i, p in enumerate(sample.prompts, 1):
        lines.append(f"{i}. {p}")
    lines.append("")
    lines.append("== Story questions ==")
    for item in sample.story_qa:
        lines.append(f"Q: {item.question}")
        lines.append(f"A: {item.answer}")
    lines.append("")
    lines.append("== World knowledge questions ==")
    for item in sample.world_qa:
        lines.append(f"Q: {item.question}")
        lines.append(f"A: {item.answer}")
    return "\n".join(lines)


