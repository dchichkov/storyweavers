def setting_detail(setting: Setting) -> str:
    return f"{setting.place.capitalize()} shimmered like a jewel box, all lamps and polished brass."


def tell(setting: Setting, activity: Activity, prize_cfg: Prize,
         hero_name: str, hero_type: str, parent_type: str, trait: str) -> World:
    world = World(setting)
    world.weather = ""

    hero = world.add(Entity(
        id=hero_name, kind="character", type=hero_type,
        meme_label=trait,
    ))
    parent = world.add(Entity(id="Parent", kind="character", type=parent_type, label="the grown-up"))
    prize = world.add(Entity(
        id="prize", type=prize_cfg.type, label=prize_cfg.label,
        phrase=prize_cfg.phrase, owner=hero.id, caretaker=parent.id,
        region=prize_cfg.region,
    ))

    hero.memes["love"] = 1
    prize.held_by = hero.id

    world.say(f"{setting_detail(setting)}")
    world.say(f"{hero.id} loved {prize.phrase} almost as much as the shine of the whole hotel.")
    world.say(f"Most of all, {hero.id} loved the golden prong on the dessert cart, because it gleamed like a tiny sun.")

    world.para()
    world.say(f"One windy afternoon, {hero.id} wanted to {activity.verb} at the biggest table in the room.")
    world.say(f"'{hero.id},' said {parent.label}, 'that might turn your {prize.label} {activity.soil}.'")
    hero.memes["worry"] = 1
    world.say(f"But {hero.id} did not want the day to feel small, so {hero.pronoun().capitalize()} hurried toward the cart.")

    world.para()
    hero.meters["wobble"] = 1
    world.say(f"Then came the twist: a ribbon from the hotel banner curled through the air and looped around the prong.")
    world.say(f"The ribbon did not ruin the plan. It made the prong into a steady little hook.")
    gear = select_gear(activity, prize)
    if gear:
        world.say(f"{parent.label.capitalize()} smiled and said, 'Use {gear.label} first.'")
        world.say(f"{hero.id} did, and the tall dessert stopped wobbling like a loose top in a storm.")
        world.say(f"Together they {gear.tail}, and {hero.id} could {activity.verb} without spoiling the {prize.label}.")
        hero.memes["joy"] += 2
        hero.memes["pride"] += 1
        prize.meters["safe"] = 1
    else:
        pass

    world.facts.update(hero=hero, parent=parent, prize=prize, prize_cfg=prize_cfg,
                       activity=activity, setting=setting, gear=gear, resolved=True)
    return world


