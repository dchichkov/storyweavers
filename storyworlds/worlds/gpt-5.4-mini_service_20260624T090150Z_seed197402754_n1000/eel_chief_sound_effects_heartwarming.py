class World:
    def __init__(self, setting: Setting) -> None:
        self.setting = setting
        self.entities: dict[str, Entity] = {}
        self.paragraphs: list[list[str]] = [[]]
        self.facts: dict = {}
        self.fired: set[tuple] = set()
        self.active_effect: Optional[str] = None
        self.active_tool: Optional[str] = None

    def add(self, ent: Entity) -> Entity:
        self.entities[ent.id] = ent
        return ent

    def get(self, eid: str) -> Entity:
        if eid not in self.entities:
            label = str(eid).replace("_", " ")
            self.entities[eid] = Entity(str(eid), label=label)
        return self.entities[eid]

    def say(self, text: str) -> None:
        if text:
            self.paragraphs[-1].append(text)

    def para(self) -> None:
        if self.paragraphs[-1]:
            self.paragraphs.append([])

    def render(self) -> str:
        return "\n\n".join(" ".join(p) for p in self.paragraphs if p)

    def copy(self) -> "World":
        import copy
        clone = World(self.setting)
        clone.entities = copy.deepcopy(self.entities)
        clone.paragraphs = [[]]
        clone.facts = dict(self.facts)
        clone.fired = set(self.fired)
        clone.active_effect = self.active_effect
        clone.active_tool = self.active_tool
        return clone


def _r_wet_tool(world: World) -> list[str]:
    out = []
    if not world.active_effect:
        return out
    effect = _safe_lookup(SOUNDS, world.active_effect)
    for ent in list(world.entities.values()):
        if ent.kind != "character":
            continue
        if ent.meters["soaked"] < THRESHOLD:
            continue
        if world.active_tool is None:
            continue
        tool = world.get(world.active_tool)
        if tool.kind != "tool":
            continue
        if effect.risk not in tool.guards:
            continue
        sig = ("safe", ent.id, tool.id, effect.id)
        if sig in world.fired:
            continue
        world.fired.add(sig)
        ent.memes["relief"] += 1
        out.append(f"The {tool.label} kept the sound gentle.")
    return out


def propagate(world: World, narrate: bool = True) -> list[str]:
    produced = []
    changed = True
    for _ in range(len(globals().get("CAUSAL_RULES", [])) + 4):
        changed = False
        for rule in [_r_wet_tool]:
            lines = rule(world)
            if lines:
                changed = True
                produced.extend(lines)
    if narrate:
        for line in produced:
            world.say(line)
    return produced


def effect_risk(effect: SoundEffect, prop: Prop) -> bool:
    return prop.kind in effect.tags


def select_tool(effect: SoundEffect, prop: Prop) -> Optional[Tool]:
    for tool in TOOLS:
        if prop.kind in tool.guards:
            return tool
    return None


def predict_change(world: World, effect: SoundEffect, prop_id: str) -> dict:
    sim = world.copy()
    prop = sim.get(prop_id)
    apply_effect(sim, sim.get(world.facts["eel"].id), effect, prop, narrate=False)
    return {
        "worsened": prop.meters["worn"] >= THRESHOLD or prop.meters["dusty"] >= THRESHOLD,
        "relief": sim.get(world.facts["eel"].id).memes["relief"],
    }


def apply_effect(world: World, eel: Entity, effect: SoundEffect, prop: Entity, narrate: bool = True) -> None:
    world.active_effect = effect.id
    eel.memes["curiosity"] += 1
    eel.meters["loudness"] += 1
    prop.meters["worn"] += 1
    prop.meters["dusty"] += 1
    if narrate:
        world.say(effect.cue)
    propagate(world, narrate=narrate)


def build_scene(world: World, eel: Entity, chief: Entity, prop: Entity) -> None:
    world.say(f"{eel.id} was a little eel who loved sound effects.")
    world.say(f"{chief.id} was the chief of the tide hall, and {chief.pronoun()} smiled at careful practice.")
    world.say(f"{eel.id} treasured {eel.pronoun('possessive')} {prop.label} because it made the nicest {prop.phrase}.")

    world.para()
    world.say(f"One day, {eel.id} and {chief.id} went to {world.setting.place}.")
    world.say(f"{eel.id} wanted to make a big {world.facts['effect'].text} for the show.")
    world.say(f"The hall answered with a soft echo, as if the room itself was listening.")

    world.para()
    apply_effect(world, eel, world.facts["effect"], prop, narrate=True)
    world.say(f"But {chief.id} noticed that the {prop.label} was getting {world.facts['effect'].risk}.")
    eel.memes["worry"] += 1
    chief.memes["care"] += 1
    world.say(f'{chief.id} said, "Let’s choose a kinder sound so the {prop.label} can stay lovely."')

    world.para()
    tool = select_tool(world.facts["effect"], prop)
    if tool is not None:
        safe = world.add(Entity(
            id=tool.id, kind="tool", type="tool", label=tool.label,
            phrase=tool.phrase, protective=True, plural=tool.plural,
            guards={world.facts["effect"].risk},
        ))
        world.active_tool = safe.id
        eel.memes["joy"] += 1
        chief.memes["pride"] += 1
        world.say(f"{chief.id} brought out {tool.phrase}.")
        world.say(f'{eel.id} tried the new cue, "{world.facts["effect"].safe_cue}"')
        world.say(f"The sound felt warm and round, like a hug made of music.")
        world.say(f"{eel.id} grinned, and {chief.id} grinned too.")
        world.say(f"By the end, the {prop.label} was still fine, and the show sounded bright.")
        eel.memes["relief"] += 1
        chief.memes["closeness"] += 1
    else:
        pass


