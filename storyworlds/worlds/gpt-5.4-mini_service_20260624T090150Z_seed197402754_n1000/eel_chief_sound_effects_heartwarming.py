def resolve_params(args: argparse.Namespace, rng: random.Random) -> StoryParams:
    if getattr(args, "effect", None) and getattr(args, "prop", None):
        if not (effect_risk(_safe_lookup(SOUNDS, getattr(args, "effect", None)), _safe_lookup(PROPS, getattr(args, "prop", None))) and select_tool(_safe_lookup(SOUNDS, getattr(args, "effect", None)), _safe_lookup(PROPS, getattr(args, "prop", None)))):
            return _fallback_storyparams(args, rng, StoryParams, globals())
    combos = [c for c in valid_combos()
              if (getattr(args, "place", None) is None or c[0] == getattr(args, "place", None))
              and (getattr(args, "effect", None) is None or c[1] == getattr(args, "effect", None))
              and (getattr(args, "prop", None) is None or c[2] == getattr(args, "prop", None))]
    if not combos:
        return _fallback_storyparams(args, rng, StoryParams, globals())
    place, effect, prop = rng.choice(list(combos))
    return StoryParams(
        place=place,
        effect=effect,
        prop=prop,
        eel_name=getattr(args, "name", None) or rng.choice(EEL_NAMES),
        chief_name=getattr(args, "chief", None) or rng.choice(CHIEF_NAMES),
        trait=getattr(args, "trait", None) or rng.choice(TRAITS),
    )


def generate(params: StoryParams) -> StorySample:
    world = World(_safe_lookup(SETTINGS, params.place))
    eel = world.add(Entity(id=params.eel_name, kind="character", type="eel"))
    chief = world.add(Entity(id=params.chief_name, kind="character", type="chief"))
    prop_def = _safe_lookup(PROPS, params.prop)
    prop = world.add(Entity(id=prop_def.id, kind="prop", type=prop_def.kind, label=prop_def.label, phrase=prop_def.phrase))
    effect = _safe_lookup(SOUNDS, params.effect)
    tool = _safe_lookup(TOOLS, 0)

    world.facts.update(eel=eel, chief=chief, prop=prop, effect=effect, tool=tool, place=params.place)
    build_scene(world, eel, chief, prop)
    return StorySample(
        params=params,
        story=world.render(),
        prompts=generation_prompts(world),
        story_qa=story_qa(world),
        world_qa=world_knowledge_qa(world),
        world=world,
    )


def emit(sample: StorySample, *, trace: bool = False, qa: bool = False, header: str = "") -> None:
    if header:
        print(header)
    print(sample.story)
    if trace and sample.world is not None:
        print(dump_trace(sample.world))
    if qa:
        print()
        print(format_qa(sample))


