    eel = _safe_fact(world, f, "eel")
    chief = _safe_fact(world, f, "chief")
    prop = _safe_fact(world, f, "prop")
    effect = _safe_fact(world, f, "effect")
    return [
        QAItem(
            question=f"Who loved making sound effects in the story?",
            answer=f"{eel.id} did. {eel.id} was a little eel who loved making sound effects with {eel.pronoun('possessive')} {prop.label}.",
        ),
        QAItem(
            question=f"Why did {chief.id} ask for a kinder sound?",
            answer=f"{chief.id} asked because the {prop.label} was getting {effect.risk}, and {chief.id} wanted it to stay lovely for the show.",
        ),
        QAItem(
            question=f"What did they use to make the sound safe?",
            answer=f"They used {(f.get('tool') or next(iter(TOOLS.values()))).phrase}, which helped make a gentle sound without hurting the {prop.label}.",
        ),
        QAItem(
            question=f"How did {eel.id} feel at the end?",
            answer=f"{eel.id} felt happy and relieved, and {chief.id} felt proud too. They finished the show with a warm, cozy feeling.",
        ),
    ]


