def valid_combos() -> list[tuple[str, str]]:
    return [("attic ladder", qid) for qid in QUESTS]


def resolve_params(args: argparse.Namespace, rng: random.Random) -> StoryParams:
    quest = getattr(args, "quest", None) or rng.choice(list(QUESTS))
    prize = getattr(args, "prize", None) or ("tee" if quest == "tee" else "blanket")
    gender = getattr(args, "gender", None) or rng.choice(["girl", "boy"])
    name = getattr(args, "name", None) or rng.choice(_safe_lookup(NAMES, gender))
    parent = getattr(args, "parent", None) or rng.choice(["mother", "father"])
    if quest == "tee" and prize != "tee":
        return _fallback_storyparams(args, rng, StoryParams, globals())
    if quest == "barn" and prize not in {"tee", "blanket"}:
        return _fallback_storyparams(args, rng, StoryParams, globals())
    return StoryParams(name=name, gender=gender, parent=parent, quest=quest, prize=prize)


def asp_facts() -> str:
    import asp
    lines = [asp.fact("setting", "attic_ladder")]
    for q in QUESTS:
        lines.append(asp.fact("quest", q))
    for p in PRIZES:
        lines.append(asp.fact("prize", p))
    lines.append(asp.fact("affords", "attic_ladder", "quest"))
    lines.append(asp.fact("matches", "tee", "tee"))
    lines.append(asp.fact("matches", "barn", "blanket"))
    return "\n".join(lines)


ASP_RULES = r"""
valid(Place, Quest, Prize) :- setting(Place), affords(Place, quest), matches(Quest, Prize), quest(Quest), prize(Prize).
#show valid/3.
"""


def asp_program() -> str:
    return f"{asp_facts()}\n{ASP_RULES}\n"


def asp_valid_combos() -> list[tuple]:
    import asp
    model = asp.one_model(asp_program())
    return sorted(set(asp.atoms(model, "valid")))


def asp_verify() -> int:
    a = set(asp_valid_combos())
    b = set((p, q, pr) for p, q in valid_combos() for pr in PRIZES if (q == "tee" and pr == "tee") or (q == "barn" and pr in {"tee", "blanket"}))
    if a == b:
        print(f"OK: clingo gate matches valid_combos() ({len(a)} combos).")
        return 0
    print("MISMATCH between clingo and python:")
    print("only in asp:", sorted(a - b))
    print("only in python:", sorted(b - a))
    return 1


def dump_trace(world: World) -> str:
    lines = ["--- world model state ---"]
    for e in list(world.entities.values()):
        meters = {k: v for k, v in e.meters.items() if v}
        memes = {k: v for k, v in e.memes.items() if v}
        bits = []
        if meters:
            bits.append(f"meters={meters}")
        if memes:
            bits.append(f"memes={memes}")
        lines.append(f"  {e.id:8} ({e.type:7}) {' '.join(bits)}")
    return "\n".join(lines)


def tell(params: StoryParams) -> World:
    world = World(SETTING)
    hero = world.add(Entity(id=params.name, kind="character", type=params.gender, meters={}, memes={}))
    parent = world.add(Entity(id="parent", kind="character", type=params.parent, label=f"the {params.parent}", meters={}, memes={}))
    prize_cfg = _safe_lookup(PRIZES, params.prize)
    prize = world.add(Entity(id=prize_cfg.id, type="thing", label=prize_cfg.label, phrase=prize_cfg.phrase, owner=hero.id))
    quest = _safe_lookup(QUESTS, params.quest)

    hero.memes["love"] = 1
    hero.memes["curiosity"] = 1
    world.say(
        f"{hero.id} was a little {params.gender} who loved quiet nights in the barn."
        f" {hero.pronoun().capitalize()} liked the soft creak of the attic ladder and the hush after supper."
    )
    world.say(
        f"One bedtime, {hero.id}'s {params.parent} whispered about a tiny quest: "
        f"to {quest.goal}."
    )
    world.para()
    world.say(
        f"With a small lantern and a sleepy heart, {hero.id} and {hero.pronoun('possessive')} {params.parent} went to {quest.location}."
        f" The ladder stood straight and tall, like it was waiting for a careful climber."
    )
    hero.memes["desire"] += 1
    world.say(
        f"{hero.id} wanted to {quest.verb}, but the ladder was high and the little steps were dusty."
        f" {hero.pronoun().capitalize()} felt a tiny wobble of worry."
    )
    hero.memes["worry"] += 1
    prize.meters["at_risk"] = 1
    world.say(
        f"{params.parent.capitalize()} held up a hand and said, "
        f"\"Let's go slowly, one step at a time, so nobody gets {quest.risk}.\""
    )
    hero.memes["calm"] = 1
    world.say(
        f"{hero.id} nodded, kept {hero.pronoun('possessive')} grip on the rail, and climbed like a feather on the wind."
    )
    world.para()
    hero.meters["dust"] = 1
    world.say(
        f"At the top, the attic was gentle and still. Under a folded box, {hero.id} found {prize.phrase}."
        f" It had waited all evening for a kind pair of hands."
    )
    prize.meters["found"] = 1
    hero.memes["joy"] = 1
    world.say(
        f"{hero.id} smiled, and the worry melted away. Together they carried the {prize.label} back down the ladder,"
        f" slow as a bedtime song."
    )
    world.say(
        f"At last, {hero.id} was snug in bed, the {prize.label} was safe, and the barn was quiet again."
    )
    world.facts.update(hero=hero, parent=parent, prize=prize, quest=quest, params=params)
    return world


