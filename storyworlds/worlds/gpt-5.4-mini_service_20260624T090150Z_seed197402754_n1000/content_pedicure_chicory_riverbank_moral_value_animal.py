class World:
    def __init__(self, setting: Setting) -> None:
        self.setting = setting
        self.entities: dict[str, Entity] = {}
        self.fired: set[tuple] = set()
        self.paragraphs: list[list[str]] = [[]]
        self.zone: set[str] = set()
        self.facts: dict = {}

    def add(self, ent: Entity) -> Entity:
        self.entities[ent.id] = ent
        return ent

    def get(self, eid: str) -> Entity:
        if eid not in self.entities:
            label = str(eid).replace("_", " ")
            self.entities[eid] = Entity(str(eid), label=label)
        return self.entities[eid]

    def characters(self) -> list[Entity]:
        return [e for e in self.entities.values() if e.kind == "character"]

    def say(self, text: str) -> None:
        if text:
            self.paragraphs[-1].append(text)

    def para(self) -> None:
        if self.paragraphs[-1]:
            self.paragraphs.append([])

    def render(self) -> str:
        return "\n\n".join(" ".join(p) for p in self.paragraphs if p)


def _add_meter(ent: Entity, key: str, amount: float = 1.0) -> None:
    ent.meters[key] = ent.meters.get(key, 0.0) + amount


def _add_meme(ent: Entity, key: str, amount: float = 1.0) -> None:
    ent.memes[key] = ent.memes.get(key, 0.0) + amount


def prize_at_risk(activity: Activity, prize: Prize) -> bool:
    return prize.region in activity.zone


def select_gear(activity: Activity, prize: Prize) -> Optional[Gear]:
    for gear in GEAR:
        if activity.mess in gear.guards and prize.region in gear.covers:
            return gear
    return None


def valid_combos() -> list[tuple[str, str]]:
    combos = []
    for place, setting in SETTINGS.items():
        for act_id in setting.affords:
            act = _safe_lookup(ACTIVITIES, act_id)
            for prize_id, prize in PRIZES.items():
                if prize_at_risk(act, prize) and select_gear(act, prize):
                    combos.append((place, act_id, prize_id))
    return combos


def _do_activity(world: World, actor: Entity, activity: Activity, narrate: bool = True) -> None:
    if activity.id not in world.setting.affords:
        pass
    world.zone = set(activity.zone)
    _add_meter(actor, activity.mess, 1.0)
    _add_meme(actor, "joy", 1.0)
    for item in list(world.entities.values()):
        if item.worn_by == actor.id and not item.kind == "character" and item.region in world.zone:
            if activity.mess not in item.traits:
                _add_meter(item, activity.mess, 1.0)
                _add_meter(item, "dirty", 1.0)


def predict_mess(world: World, actor: Entity, activity: Activity, prize_id: str) -> dict:
    sim = World(world.setting)
    sim.entities = {k: Entity(**{
        "id": v.id, "kind": v.kind, "type": v.type, "label": v.label, "phrase": v.phrase,
        "traits": list(v.traits), "owner": v.owner, "caretaker": v.caretaker, "worn_by": v.worn_by,
        "plural": v.plural, "meters": dict(v.meters), "memes": dict(v.memes)
    }) for k, v in world.entities.items()}
    _do_activity(sim, sim.get(actor.id), activity, narrate=False)
    prize = sim.entities[prize_id]
    return {"soiled": prize.meters.get("dirty", 0.0) >= THRESHOLD}


def introduce(world: World, hero: Entity) -> None:
    world.say(f"{hero.id} was a little {hero.type} who lived near {world.setting.place}.")


def loves_chicory(world: World, hero: Entity, activity: Activity) -> None:
    _add_meme(hero, "content", 1.0)
    world.say(f"{hero.pronoun().capitalize()} felt content when the air smelled green and the chicory flowers nodded by the water.")


def buys(world: World, helper: Entity, hero: Entity, prize: Entity) -> None:
    world.say(f"One morning, {helper.label} brought {hero.pronoun('object')} {prize.phrase}.")


def loves_prize(world: World, hero: Entity, prize: Entity) -> None:
    _add_meme(hero, "love", 1.0)
    prize.worn_by = hero.id
    world.say(f"{hero.id} loved {hero.pronoun('possessive')} {prize.label} and wore {prize.it()} proudly.")


def arrive(world: World, hero: Entity, helper: Entity) -> None:
    world.say(f"One breezy day, {hero.id} and {hero.pronoun('possessive')} {helper.label} went to {world.setting.place}.")


def wants(world: World, hero: Entity, activity: Activity) -> None:
    _add_meme(hero, "desire", 1.0)
    world.say(f"{hero.id} wanted to {activity.verb}, because {activity.keyword} sounded sweet and bright.")


def warn(world: World, helper: Entity, hero: Entity, activity: Activity, prize: Entity) -> bool:
    pred = predict_mess(world, hero, activity, prize.id)
    if not pred["soiled"]:
        return False
    world.facts["predicted"] = True
    world.say(f'"If you rush at the chicory patch, your {prize.label} will get {activity.soil}," {helper.label} said.')
    return True


def defies(world: World, hero: Entity, activity: Activity) -> None:
    _add_meme(hero, "restless", 1.0)
    world.say(f"{hero.id} still ran toward the stems and tried to {activity.rush}.")


def offer_compromise(world: World, helper: Entity, hero: Entity, activity: Activity, prize: Entity) -> Optional[Gear]:
    gear = select_gear(activity, prize)
    if gear is None:
        return None
    world.say(f"Then {helper.label} smiled. {gear.prep.capitalize()}, and we can still enjoy the chicory together.\"")
    return gear


def accept(world: World, helper: Entity, hero: Entity, activity: Activity, prize: Entity, gear: Gear) -> None:
    _add_meme(hero, "content", 1.0)
    hero.memes["restless"] = 0.0
    world.say(f"{hero.id} nodded, put on the {gear.label}, and felt calm again.")
    world.say(f"Soon {hero.id} was {activity.gerund}, {prize.label} stayed clean, and the river sparkled beside {hero.pronoun('object')}.")
    world.say(f"The moral was simple: being content can be wiser than chasing one more muddy moment.")


def tell(setting: Setting, activity: Activity, prize_cfg: Prize, hero_name: str = "Milo") -> World:
    world = World(setting)
    hero = world.add(Entity(id=hero_name, kind="character", type="rabbit", traits=["little", "curious"]))
    helper = world.add(Entity(id="Helper", kind="character", type="mother", label="Milo's mother"))
    prize = world.add(Entity(
        id="prize", type=prize_cfg.type, label=prize_cfg.label, phrase=prize_cfg.phrase,
        owner=hero.id, caretaker=helper.id, region=prize_cfg.region, plural=prize_cfg.plural
    ))

    introduce(world, hero)
    loves_chicory(world, hero, activity)
    buys(world, helper, hero, prize)
    loves_prize(world, hero, prize)

    world.para()
    arrive(world, hero, helper)
    wants(world, hero, activity)
    warn(world, helper, hero, activity, prize)
    defies(world, hero, activity)

    world.para()
    gear = offer_compromise(world, helper, hero, activity, prize)
    if gear is None:
        pass
    accept(world, helper, hero, activity, prize, gear)

    world.facts.update(hero=hero, helper=helper, prize=prize, activity=activity, gear=gear, setting=setting)
    return world


