MESS_KINDS = {"wet", "muddy", "sparkled", "smudged"}


def _r_soak(world: World) -> list[str]:
    out: list[str] = []
    for actor in world.characters():
        for mess in MESS_KINDS:
            if actor.meters[mess] < THRESHOLD:
                continue
            for item in world.worn_items(actor):
                if item.protective or item.region not in world.zone:
                    continue
                if world.covered(actor, item.region):
                    continue
                sig = ("soak", item.id, mess)
                if sig in world.fired:
                    continue
                world.fired.add(sig)
                item.meters[mess] += 1
                item.meters["dirty"] += 1
                out.append(f"{actor.pronoun('possessive').capitalize()} {item.label} got {mess} and dirty.")
    return out


def _r_magic_glow(world: World) -> list[str]:
    out: list[str] = []
    for actor in world.characters():
        if actor.memes["spell"] < THRESHOLD:
            continue
        sig = ("glow", actor.id)
        if sig in world.fired:
            continue
        world.fired.add(sig)
        actor.meters["sparkled"] += 1
        out.append("The magic made a soft glow around her.")
    return out


CAUSAL_RULES: list[Rule] = [
    Rule(name="soak", tag="physical", apply=_r_soak),
    Rule(name="magic_glow", tag="magic", apply=_r_magic_glow),
]


def propagate(world: World, narrate: bool = True) -> list[str]:
    produced: list[str] = []
    changed = True
    for _ in range(len(globals().get("CAUSAL_RULES", [])) + 4):
        changed = False
        for rule in CAUSAL_RULES:
            sents = rule.apply(world)
            if sents:
                changed = True
                produced.extend(sents)
    if narrate:
        for s in produced:
            world.say(s)
    return produced


def prize_at_risk(activity: Activity, prize: Prize) -> bool:
    return prize.region in activity.zone


def select_gear(activity: Activity, prize: Prize) -> Optional[Gear]:
    for gear in GEAR:
        if activity.mess in gear.guards and prize.region in gear.covers:
            return gear
    return None


def predict_mess(world: World, actor: Entity, activity: Activity, prize_id: str) -> dict:
    sim = world.copy()
    _do_activity(sim, sim.get(actor.id), activity, narrate=False)
    prize = sim.entities.get(prize_id)
    return {"soiled": bool(prize and prize.meters["dirty"] >= THRESHOLD)}


def _do_activity(world: World, actor: Entity, activity: Activity, narrate: bool = True) -> None:
    if activity.id not in world.setting.affords:
        return
    world.zone = set(activity.zone)
    actor.meters[activity.mess] += 1
    actor.memes["joy"] += 1
    propagate(world, narrate=narrate)


def introduce(world: World, hero: Entity) -> None:
    trait = next((t for t in hero.traits if t != "little"), "")
    world.say(f"{hero.id} was a little {trait} bride who loved a quiet smile and a bright plan.")


def loves_prize(world: World, hero: Entity, prize: Entity) -> None:
    hero.memes["love"] += 1
    prize.worn_by = hero.id
    world.say(f"{hero.id} loved her {prize.label} and wore it as if it were a promise.")


def loves_activity(world: World, hero: Entity, activity: Activity) -> None:
    world.say(f"She also loved a good {activity.keyword}, especially when it came with {activity.sound}.")
    hero.memes["curiosity"] += 1


def arrive(world: World, hero: Entity, helper: Entity, activity: Activity) -> None:
    world.say(f"One evening, {hero.id} and {helper.label_word} went to {world.setting.place}.")
    world.say(f"The path went {activity.sound} through the grass, and the air felt full of wonder.")


def wants(world: World, hero: Entity, helper: Entity, activity: Activity) -> None:
    hero.memes["desire"] += 1
    world.say(f"{hero.id} wanted to {activity.verb} at once, but the road was not kind to fine things.")


def warn(world: World, helper: Entity, hero: Entity, activity: Activity, prize: Entity) -> bool:
    pred = predict_mess(world, hero, activity, prize.id)
    if not pred["soiled"]:
        return False
    world.facts["predicted_soil"] = activity.soil
    world.say(f'"If you go now," said {helper.label_word}, "your {prize.label} will get {activity.soil}."')
    return True


def defies(world: World, hero: Entity, activity: Activity) -> None:
    hero.memes["defiance"] += 1
    world.say(f"{hero.id} took a breath and tried to {activity.rush}, even though the warning was wise.")


def grab_hand(world: World, helper: Entity, hero: Entity, activity: Activity) -> None:
    hero.memes["held_back"] += 1
    world.say(f"Then {helper.label_word} held her hand and said, \"Not without a little magic.\"")


def compromise(world: World, helper: Entity, hero: Entity, activity: Activity, prize: Entity) -> Optional[Gear]:
    gear_def = select_gear(activity, prize)
    if gear_def is None:
        return None
    gear = world.add(Entity(
        id=gear_def.id,
        type="gear",
        label=gear_def.label,
        owner=hero.id,
        caretaker=helper.id,
        protective=True,
        covers=set(gear_def.covers),
        plural=gear_def.plural,
    ))
    gear.worn_by = hero.id
    if predict_mess(world, hero, activity, prize.id)["soiled"]:
        gear.worn_by = None
        del world.entities[gear.id]
        return None
    world.say(f'{helper.label_word} lifted a spell and said, "How about we use {gear_def.label} first?"')
    return gear_def


def accept(world: World, helper: Entity, hero: Entity, activity: Activity, prize: Entity, gear_def: Gear) -> None:
    hero.memes["joy"] += 1
    hero.memes["love"] += 1
    hero.memes["defiance"] = 0.0
    hero.memes["spell"] += 1
    world.say(f"{hero.id} smiled, and the old worry went light as a feather.")
    world.say(
        f"She wore the {gear_def.label}, followed the path that went {activity.sound}, "
        f"and soon was {activity.gerund} while her {prize.label} stayed clean and bright."
    )
    world.say(f"That was the end of her little quest: the magic worked, and the bride laughed all the way home.")


def tell(setting: Setting, activity: Activity, prize_cfg: Prize,
         hero_name: str = "Elara", hero_type: str = "bride",
         hero_traits: Optional[list[str]] = None, helper_type: str = "owl") -> World:
    world = World(setting)
    world.weather = activity.weather

    hero = world.add(Entity(
        id=hero_name, kind="character", type=hero_type,
        traits=["little"] + (hero_traits or ["brave", "kind"]),
    ))
    helper = world.add(Entity(id="WiseOne", kind="character", type=helper_type, label="the wise owl"))
    prize = world.add(Entity(
        id="prize",
        type=prize_cfg.type,
        label=prize_cfg.label,
        phrase=prize_cfg.phrase,
        owner=hero.id,
        caretaker=helper.id,
        region=prize_cfg.region,
        plural=prize_cfg.plural,
    ))

    introduce(world, hero)
    loves_activity(world, hero, activity)
    loves_prize(world, hero, prize)

    world.para()
    arrive(world, hero, helper, activity)
    wants(world, hero, helper, activity)
    warn(world, helper, hero, activity, prize)
    defies(world, hero, activity)
    grab_hand(world, helper, hero, activity)

    world.para()
    gear_def = compromise(world, helper, hero, activity, prize)
    if gear_def:
        accept(world, helper, hero, activity, prize, gear_def)

    world.facts.update(hero=hero, helper=helper, prize=prize, prize_cfg=prize_cfg,
                       activity=activity, setting=setting, gear=gear_def,
                       conflict=hero.memes["held_back"] >= THRESHOLD,
                       resolved=gear_def is not None)
    return world


