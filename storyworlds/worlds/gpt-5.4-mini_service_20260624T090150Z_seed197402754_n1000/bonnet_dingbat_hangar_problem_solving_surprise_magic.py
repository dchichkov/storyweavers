def can_fix(activity: Activity, prize: Prize) -> Optional[Gear]:
    for g in GEAR:
        if prize.region in g.covers and activity.mess in g.guards:
            return g
    return None


def predict_soiled(world: World, hero: Entity, activity: Activity, prize_id: str) -> bool:
    sim = world.copy()
    hero2 = sim.get(hero.id)
    hero2.meters[activity.mess] += 1
    prize = sim.get(prize_id)
    return prize.region == "head" and activity.mess in {"spark", "scrap", "dust"}


def tell(setting: Setting, activity: Activity, prize_cfg: Prize, name: str, gender: str, helper: str, trait: str) -> World:
    world = World(setting)
    hero = world.add(Entity(id=name, kind="character", type=gender))
    sidekick = world.add(Entity(id="Dingbat", kind="character", type="thing", label="dingbat"))
    prize = world.add(Entity(id="Bonnet", type=prize_cfg.type, label=prize_cfg.label, phrase=prize_cfg.phrase, owner=hero.id, region=prize_cfg.region))
    hero.memes["curiosity"] += 1

    world.say(f"{hero.id} was a {trait} little {gender} with a bonnet so bright,")
    world.say(f"and a dingbat sidekick named {helper} who loved a good light.")

    world.para()
    world.say(f"In the hangar they went, with a skip and a swish,")
    world.say(f"to {activity.verb} by the crates and make a wish.")

    world.para()
    hero.memes["surprise"] += 1
    if predict_soiled(world, hero, activity, prize.label):
        world.say(f'"Oh no," said {helper}, "that spell may make {prize.label} unfit!"')
        world.say(f"{hero.id} looked around and searched for a fix to it.")
        gear = can_fix(activity, prize)
        if gear is None:
            pass
        world.say(f"Then {hero.id} found {gear.label} and gave it a quick twist,")
        world.say(f"{gear.prep}, a neat little tryst.")
        hero.memes["joy"] += 1
        hero.memes["relief"] += 1
        world.para()
        world.say(f"The spell went pop with a glittery glow,")
        world.say(f"and {hero.id} could {activity.verb} while the bonnet stayed so.")
        world.say(f"{gear.tail}, and the dingbat cheered, “Hooray!”")
    else:
        world.say(f"The magic was mild, and the bonnet stayed light,")
        world.say(f"so they danced in the hangar, happy and bright.")

    world.facts.update(hero=hero, helper=sidekick, prize=prize, activity=activity, setting=setting)
    return world


