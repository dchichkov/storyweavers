def prize_at_risk(activity: Activity, prize: Prize) -> bool:
    return prize.region in activity.zone


def select_gear(activity: Activity, prize: Prize) -> Optional[Gear]:
    for gear in GEAR:
        if activity.mess in gear.guards and prize.region in gear.covers:
            return gear
    return None


def predict_bad_end(world: World, actor: Entity, activity: Activity, prize_id: str) -> dict:
    sim = world.copy()
    _do_activity(sim, sim.get(actor.id), activity, narrate=False)
    prize = sim.entities.get(prize_id)
    return {
        "soiled": bool(prize and prize.meters.get("wet", 0.0) >= THRESHOLD),
        "conflict": sim.get(actor.id).memes.get("conflict", 0.0) >= THRESHOLD,
    }


def _do_activity(world: World, actor: Entity, activity: Activity, narrate: bool = True) -> None:
    if activity.id not in world.setting.affords:
        pass
    world.zone = set(activity.zone)
    actor.meters[activity.mess] = actor.meters.get(activity.mess, 0.0) + 1
    actor.meters["wind"] = actor.meters.get("wind", 0.0) + 1
    if narrate:
        world.say(f"{actor.id} {activity.gerund}.")


def introduce(world: World, hero: Entity) -> None:
    trait = next((t for t in hero.memes.get("traits", ["brave"])), "brave")
    world.say(f"{hero.id} was a little {hero.type} who loved adventure on the water.")


def setup(world: World, hero: Entity, friend: Entity, parent: Entity, prize: Entity, activity: Activity) -> None:
    world.say(f"{hero.id} and {friend.id} went to {world.setting.place} with {parent.label}.")
    world.say(f"{hero.id} loved {activity.gerund}, but {hero.pronoun('possessive')} {prize.label} needed to stay safe.")
    prize.worn_by = hero.id


def wants_and_grabs(world: World, hero: Entity, activity: Activity) -> None:
    hero.memes["desire"] = hero.memes.get("desire", 0.0) + 1
    hero.memes["grabbed"] = hero.memes.get("grabbed", 0.0) + 1
    world.say(f"{hero.id} wanted to {activity.verb} right away and tried to grab the sail rope alone.")


def warn(world: World, parent: Entity, hero: Entity, activity: Activity, prize: Entity) -> bool:
    pred = predict_bad_end(world, hero, activity, prize.id)
    if not pred["soiled"]:
        return False
    world.facts["predicted_bad_end"] = True
    world.say(
        f'"If you keep pulling like that, your {prize.label} could get wet, and this trip could end badly," '
        f"{parent.label} said."
    )
    return True


def share_and_team_up(world: World, hero: Entity, friend: Entity, parent: Entity, activity: Activity, prize: Entity) -> Optional[Gear]:
    gear = select_gear(activity, prize)
    if gear is None:
        return None
    world.say(f"{friend.id} smiled and said they could share the rope and work as a team.")
    world.say(f"{parent.label} nodded and helped {hero.id} put on {gear.label}.")
    return gear


def resolve(world: World, hero: Entity, friend: Entity, parent: Entity, activity: Activity, prize: Entity, gear: Gear) -> None:
    hero.memes["joy"] = hero.memes.get("joy", 0.0) + 1
    hero.memes["teamwork"] = hero.memes.get("teamwork", 0.0) + 1
    hero.memes["sharing"] = hero.memes.get("sharing", 0.0) + 1
    hero.memes["conflict"] = 0.0
    world.say(f"{hero.id} stopped, shared the rope with {friend.id}, and listened to {parent.label}.")
    world.say(
        f"Together they steered the boat carefully, and {hero.id} kept {hero.pronoun('possessive')} {prize.label} dry."
    )
    world.say(f"That made the sail adventure feel like a happy beginning instead of a bad ending.")


def tell(setting: Setting, activity: Activity, prize_cfg: Prize, hero_name: str, hero_type: str, friend_name: str, friend_type: str, parent_type: str) -> World:
    world = World(setting)
    hero = world.add(Entity(id=hero_name, kind="character", type=hero_type, memes={"traits": ["brave", "curious"]}))
    friend = world.add(Entity(id=friend_name, kind="character", type=friend_type, memes={"traits": ["kind", "helpful"]}))
    parent = world.add(Entity(id="Parent", kind="character", type=parent_type, label="the parent"))
    prize = world.add(Entity(id="prize", type=prize_cfg.type, label=prize_cfg.label, phrase=prize_cfg.phrase, region=prize_cfg.region, plural=prize_cfg.plural, owner=hero.id, caretaker=parent.id))

    introduce(world, hero)
    setup(world, hero, friend, parent, prize, activity)
    world.para()
    wants_and_grabs(world, hero, activity)
    warn(world, parent, hero, activity, prize)
    gear = share_and_team_up(world, hero, friend, parent, activity, prize)
    world.para()
    if gear:
        resolve(world, hero, friend, parent, activity, prize, gear)

    world.facts.update(hero=hero, friend=friend, parent=parent, prize=prize, activity=activity, gear=gear, setting=setting)
    return world


