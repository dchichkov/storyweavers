def resolve_params(args: argparse.Namespace, rng: random.Random) -> StoryParams:
    combos = [c for c in valid_combos()
              if (getattr(args, "place", None) is None or c[0] == getattr(args, "place", None))
              and (getattr(args, "script", None) is None or c[1] == getattr(args, "script", None))
              and (getattr(args, "prop", None) is None or c[2] == getattr(args, "prop", None))]
    if not combos:
        return _fallback_storyparams(args, rng, StoryParams, globals())
    place, script, prop = rng.choice(list(combos))
    name = getattr(args, "name", None) or rng.choice(NAMES)
    trait = getattr(args, "trait", None) or rng.choice(TRAITS)
    return StoryParams(place=place, script=script, prop=prop, name=name, trait=trait)


def tell(setting: Setting, script: Script, prop: Prop, name: str, trait: str) -> World:
    world = World(setting)
    hero = world.add(Entity(id=name, kind="character", type="boy", meters={}, memes={"joy": 0.0, "repeat": 0.0, "confidence": 0.0, "worry": 0.0}))
    teacher = world.add(Entity(id="Teacher", kind="character", type="teacher", label="the teacher"))
    prize = world.add(Entity(id=prop.id, type=prop.type, label=prop.label, phrase=prop.phrase, owner=hero.id, caretaker=teacher.id))

    world.facts.update(hero=hero, teacher=teacher, prop=prize, script=script, trait=trait)

    world.say(f"{hero.id} was a {trait} little kid who loved a rhyme.")
    world.say(f"{hero.id} liked the line, \"{script.refrain},\" because it sounded like a song with a spring.")
    world.say(f"At {setting.place}, {hero.id} carried {prize.phrase} and practiced the beat, {script.beat}.")

    world.para()
    world.say(f"Then came the time to share the rhyme.")
    hero.memes["worry"] += 1.0
    hero.memes["repeat"] += 1.0
    propagate(world, narrate=True)
    world.say(f'But when {hero.id} got nervous, the silly word "dork" popped out in a row: dork, dork, dork.')
    world.say(f"{hero.id} said the refrain again and again, but the words kept wobbling like jelly on a spoon.")

    world.para()
    world.say(f"The teacher did not frown.")
    teacher.memes["kindness"] = teacher.memes.get("kindness", 0.0) + 1.0
    world.say(f"{teacher.id} tapped the beat and smiled. \"Slow and steady,\" {teacher.pronoun('subject')} said. \"Try it with me: {script.refrain}.\"")
    world.say(f"{teacher.id} tapped {script.beat}, and {hero.id} copied the pattern.")
    hero.memes["repeat"] = 0.0
    hero.memes["confidence"] += 1.0
    hero.memes["joy"] += 1.0
    world.say(f"{hero.id} took a breath, then said the line once, then once more, and the rhyme came out clean.")
    world.say(f"At the end, {hero.id} grinned, the class clapped, and the word \"dork\" felt small and silly instead of scary.")

    world.facts["resolved"] = True
    return world


