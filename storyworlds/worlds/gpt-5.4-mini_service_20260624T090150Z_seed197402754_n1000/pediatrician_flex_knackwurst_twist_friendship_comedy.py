def tell(setting: Setting, activity: Activity, treat: Treat, name: str, gender: str, parent: str) -> World:
    world = World(setting)
    child = world.add(Entity(id=name, kind="character", type=gender, meters={"worry": 0.0}, memes={"joy": 0.0, "friendship": 0.0, "embarrassment": 0.0}))
    doctor = world.add(Entity(id="Doctor", kind="character", type="pediatrician", label="the pediatrician", meters={}, memes={"kindness": 0.0}))
    grownup = world.add(Entity(id="Grownup", kind="character", type=parent, label=f"the {parent}"))
    select_treat(world, child, treat)

    # Act 1: setup.
    world.say(
        f"{child.id} came to {setting.place} with {child.pronoun('possessive')} {grownup.label_word if hasattr(grownup, 'label_word') else parent}."
    )
    world.say(
        f"The pediatrician smiled and said {child.id} could stay brave for the checkup."
    )
    world.say(
        f"{child.id} loved {activity.gerund} and tried to look extra strong."
    )

    # Act 2: tension.
    world.para()
    child.memes["embarrassment"] += 1
    child.memes["worry"] += 1
    world.say(
        f"Then {child.id} wanted to {activity.verb}, but the waiting room felt too quiet."
    )
    world.say(
        f"{child.id} gave a tiny flex, and the pediatrician noticed the worried face hiding behind it."
    )
    world.say(
        f"That was a funny little twist: the bigger the flex, the smaller the smile."
    )

    # Act 3: resolution.
    world.para()
    doctor.memes["kindness"] += 1
    child.memes["worry"] = 0.0
    child.memes["joy"] += 1
    child.memes["friendship"] += 1
    world.say(
        f"The pediatrician said, \"How about we {activity.twist}, and then share a knackwurst treat together?\""
    )
    world.say(
        f"{child.id} laughed, the {parent} laughed too, and the room turned friendly."
    )
    world.say(
        f"By the end, {child.id} was {activity.gerund} with a grin, and the knackwurst was shared in happy little bites."
    )

    world.facts.update(
        child=child,
        doctor=doctor,
        grownup=grownup,
        activity=activity,
        treat=treat,
        setting=setting,
        resolved=True,
    )
    return world


