def prize_at_risk(activity: Activity, prize: Prize) -> bool:
    return prize.room in activity.zone


def select_gear(activity: Activity, prize: Prize) -> Optional[Gear]:
    for gear in GEAR:
        if prize.room in gear.covers and activity.keyword in gear.guards:
            return gear
    return None


def valid_combos() -> list[tuple[str, str, str]]:
    combos = []
    for place, setting in {"ship": SETTING}.items():
        for act_id in setting.affords:
            act = _safe_lookup(ACTIVITIES, act_id)
            for prize_id, prize in PRIZES.items():
                if prize_at_risk(act, prize) and select_gear(act, prize):
                    combos.append((place, act_id, prize_id))
    return combos


def _do_activity(world: World, actor: Entity, activity: Activity, narrate: bool = True) -> None:
    if activity.id not in world.setting.affords:
        return
    world.zone = set(activity.zone)
    actor.meters[activity.keyword] = actor.meters.get(activity.keyword, 0) + 1
    actor.memes["joy"] = actor.memes.get("joy", 0) + 1


def predict_mess(world: World, actor: Entity, activity: Activity, prize_id: str) -> dict:
    sim = world.copy()
    _do_activity(sim, sim.get(actor.id), activity, narrate=False)
    prize = sim.entities.get(prize_id)
    return {"soiled": bool(prize and prize.room in sim.zone), "joy": sim.get(actor.id).memes.get("joy", 0)}


def tell(setting: Setting, activity: Activity, prize_cfg: Prize,
         hero_name: str = "Lina", hero_type: str = "girl",
         parent_type: str = "captain") -> World:
    world = World(setting)
    hero = world.add(Entity(id=hero_name, kind="character", type=hero_type, traits=["little", "curious"]))
    parent = world.add(Entity(id="Parent", kind="character", type=parent_type, label="the captain"))
    prize = world.add(Entity(id="prize", type=prize_cfg.type, label=prize_cfg.label,
                             phrase=prize_cfg.phrase, owner=hero.id, caretaker=parent.id,
                             room=prize_cfg.room, plural=prize_cfg.plural))

    world.say(f"{hero.id} was a little {hero_type} who loved the ship and every soft gleam on it.")
    world.say(f"{hero.id} was especially curious about {activity.gerund}.")
    world.say(f"One day, {parent.label} gave {hero.id} {prize.phrase}.")
    world.say(f"{hero.id} loved {prize.it()} and wore {prize.it()} all around the starship.")

    world.para()
    world.say(f"Then {hero.id} went to {setting.place}, where the air was calm and the lights were low.")
    world.say(f"{hero.id} wanted to {activity.verb}, but {parent.label} noticed the {activity.spark} by {prize.label}.")
    pred = predict_mess(world, hero, activity, prize.id)
    if pred["soiled"]:
        world.facts["predicted_risk"] = activity.risk
        world.say(f"\"If you {activity.verb}, your {prize.label} could get messy,\" {parent.label} said.")
    hero.memes["curiosity"] = hero.memes.get("curiosity", 0) + 1
    world.say(f"{hero.id} peeked closer, still curious, and tried to {activity.rush}.")
    hero.memes["defiance"] = hero.memes.get("defiance", 0) + 1

    world.para()
    gear = select_gear(activity, prize)
    if gear is None:
        gear = next(iter(globals().get("GEARS", globals().get("GEAR", []))))
    gear_ent = world.add(Entity(id=gear.id, type="gear", label=gear.label, protective=True,
                                owner=hero.id, caretaker=parent.id, worn_by=hero.id,
                                plural=gear.plural))
    gear_ent.covers = set(gear.covers)
    if predict_mess(world, hero, activity, prize.id)["soiled"]:
        pass

    world.say(f"{parent.label} smiled and said, \"How about we {gear.prep} and do it together?\"")
    hero.memes["kindness"] = hero.memes.get("kindness", 0) + 1
    hero.memes["joy"] = hero.memes.get("joy", 0) + 1
    hero.memes["curiosity"] = hero.memes.get("curiosity", 0) + 1
    world.say(f"{hero.id}'s face lit up, and {hero.id} hugged {parent.label}.")
    world.say(f"\"Yes!\" {hero.id} said, and they {gear.tail}.")
    world.say(f"At the end, {hero.id} was {activity.gerund}, {prize.label} stayed clean, and the ship gave off a gentle gleam.")

    world.facts.update(hero=hero, parent=parent, prize=prize, prize_cfg=prize_cfg,
                       activity=activity, setting=setting, gear=gear, resolved=True)
    return world


