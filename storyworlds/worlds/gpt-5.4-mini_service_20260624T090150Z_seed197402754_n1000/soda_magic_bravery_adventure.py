def prize_at_risk(activity: Activity, prize: Prize) -> bool:
    return prize.region in activity.zone


def select_gear(activity: Activity, prize: Prize) -> Optional[Gear]:
    for gear in GEAR:
        if activity.mess in gear.guards and prize.region in gear.covers:
            return gear
    return None


def valid_combos() -> list[tuple[str, str, str]]:
    out: list[tuple[str, str, str]] = []
    for place, setting in SETTINGS.items():
        for act_id in setting.affords:
            act = _safe_lookup(ACTIVITIES, act_id)
            for prize_id, prize in PRIZES.items():
                if prize_at_risk(act, prize) and select_gear(act, prize):
                    out.append((place, act_id, prize_id))
    return out


def predict_spill(world: World, hero: Entity, activity: Activity, prize_id: str) -> bool:
    sim = world.copy()
    _do_activity(sim, sim.get(hero.id), activity, narrate=False)
    prize = sim.get(prize_id)
    return prize.meters["spill"] >= THRESHOLD


def _do_activity(world: World, actor: Entity, activity: Activity, narrate: bool = True) -> None:
    world.zone = set(activity.zone)
    actor.meters[activity.mess] += 1
    actor.memes["bravery"] += 1
    actor.meters["travel"] += 1
    propagate(world, narrate=narrate)


def _r_spill(world: World) -> list[str]:
    out: list[str] = []
    for actor in world.characters():
        if actor.meters["spill"] < THRESHOLD:
            continue
        for item in world.worn_items(actor):
            if item.protective or item.region not in world.zone:
                continue
            if world.covered(actor, item.region):
                continue
            sig = ("spill", item.id)
            if sig in world.fired:
                continue
            world.fired.add(sig)
            item.meters["spill"] += 1
            item.meters["sticky"] += 1
            out.append(f"{actor.id}'s {item.label} could have spilled.")
    return out


def _r_worry(world: World) -> list[str]:
    out: list[str] = []
    for item in list(world.entities.values()):
        if item.meters["sticky"] < THRESHOLD or not item.caretaker:
            continue
        sig = ("worry", item.id)
        if sig in world.fired:
            continue
        world.fired.add(sig)
        carer = world.get(item.caretaker)
        carer.memes["worry"] += 1
        out.append(f"That would have worried {carer.label}.")
    return out


CAUSAL_RULES = [
    _r_spill,
    _r_worry,
]


def propagate(world: World, narrate: bool = True) -> list[str]:
    out: list[str] = []
    changed = True
    for _ in range(len(globals().get("CAUSAL_RULES", [])) + 4):
        changed = False
        for rule in CAUSAL_RULES:
            sents = rule(world)
            if sents:
                changed = True
                out.extend(sents)
    if narrate:
        for s in out:
            world.say(s)
    return out


def tell(setting: Setting, activity: Activity, prize_cfg: Prize, hero_name: str, parent_type: str) -> World:
    world = World(setting)
    hero = world.add(Entity(id=hero_name, kind="character", type="girl" if hero_name in GIRL_NAMES else "boy"))
    parent = world.add(Entity(id="Parent", kind="character", type=parent_type, label="the parent"))
    prize = world.add(Entity(
        id="soda", type=prize_cfg.type, label=prize_cfg.label, phrase=prize_cfg.phrase,
        owner=hero.id, caretaker=parent.id, region=prize_cfg.region,
    ))

    hero.memes["joy"] += 1
    world.say(f"{hero.id} loved adventures, and {hero.pronoun('possessive')} favorite treat was {prize.phrase}.")
    world.say(f"One day, {hero.id} and {hero.pronoun('possessive')} {parent.label} headed to {setting.place}.")
    world.say(f"{hero.id} wanted to {activity.verb}, because {activity.gerund} felt like a real quest.")
    world.para()

    if predict_spill(world, hero, activity, prize.id):
        world.say(f'"If you {activity.verb}, your {prize.label} might {activity.soil}," {parent.label} warned.')
    hero.memes["fear"] += 1
    world.say(f"{hero.id} looked at the rocky path, but {hero.pronoun()} took a brave breath and nodded.")
    world.say(f'"I can be brave," {hero.id} said, and {hero.pronoun()} reached for a little bit of magic.')
    hero.memes["bravery"] += 1
    hero.memes["magic"] += 1
    gear_def = select_gear(activity, prize)
    if gear_def is not None:
        gear = world.add(Entity(
            id=gear_def.id, type="gear", label=gear_def.label,
            owner=hero.id, caretaker=parent.id, protective=True,
            covers=set(gear_def.covers), guards=set(gear_def.guards),
        ))
        gear.worn_by = hero.id
        world.say(f"{hero.id}'s {parent.label} smiled and said they could {gear_def.prep}.")
        world.say(f"With a twinkling spell, {hero.id} {gear_def.tail}.")
    world.para()
    _do_activity(world, hero, activity, narrate=True)
    if prize.meters["spill"] < THRESHOLD:
        hero.memes["joy"] += 1
        hero.memes["fear"] = 0.0
        world.say(f"Then {hero.id} was {activity.gerund}, and the {prize.label} stayed safe.")
        world.say(f"At the top of the path, {hero.id} shared a happy sip while {hero.pronoun('possessive')} {parent.label} cheered.")
    else:
        world.say(f"Even so, {prize.label} got messy, and the adventure ended in a small sigh.")

    world.facts.update(hero=hero, parent=parent, prize=prize, activity=activity, setting=setting, gear=gear_def)
    return world


