def asp_facts() -> str:
    import asp
    lines = []
    for pid in SETTINGS:
        lines.append(asp.fact("setting", pid))
    for aid, a in ACTIVITIES.items():
        lines.append(asp.fact("activity", aid))
        lines.append(asp.fact("keyword", aid, a.keyword))
    for pid in PRIZES:
        lines.append(asp.fact("prize", pid))
    lines.append(asp.fact("pairing", "garden", "patter", "seed"))
    lines.append(asp.fact("pairing", "garden", "bloom", "bud"))
    lines.append(asp.fact("pairing", "window", "bloom", "flower"))
    return "\n".join(lines)


ASP_RULES = r"""
valid(Place, Act, Prize) :- pairing(Place, Act, Prize).
#show valid/3.
"""


def asp_program() -> str:
    return f"{asp_facts()}\n{ASP_RULES}\n"


def asp_valid_combos() -> list[tuple]:
    import asp
    model = asp.one_model(asp_program())
    return sorted(set(asp.atoms(model, "valid")))


def asp_verify() -> int:
    py = {
        (p, a, r)
        for p in SETTINGS
        for a in ACTIVITIES
        for r in PRIZES
        if can_tell(p, a, r) and ((p, a, r) in {("garden", "patter", "seed"), ("garden", "bloom", "bud"), ("window", "bloom", "flower")})
    }
    cl = set(asp_valid_combos())
    if py == cl:
        print(f"OK: clingo gate matches Python gate ({len(py)} combos).")
        return 0
    print("MISMATCH:")
    print("python only:", sorted(py - cl))
    print("clingo only:", sorted(cl - py))
    return 1


