def build_story_world(params: StoryParams) -> World:
    setting = _safe_lookup(SETTINGS, params.place)
    act = _safe_lookup(ACTIVITIES, params.activity)
    prize = _safe_lookup(PRIZES, params.prize)

    world = World(setting)
    hero = world.add(Entity(
        id=params.name,
        kind="character",
        type=params.gender,
        meters={"hope": 0.0, "worry": 0.0, "joy": 0.0},
        memes={"hope": 0.0, "worry": 0.0, "joy": 0.0, "wonder": 0.0},
    ))
    plant = world.add(Entity(
        id="plant",
        type=prize.type,
        label=prize.label,
        phrase=prize.phrase,
        owner=hero.id,
        meters={"wet": 0.0, "growth": 0.0, "bloom": 0.0, "magic": 0.0},
        memes={"sleepy": 1.0},
    ))

    # Act 1
    world.say(
        f"In {setting.place}, {params.name} was a {params.trait} little {params.gender}, "
        f"with an itty-bitty hope and a seed to tend."
    )
    world.say(
        f"{hero.pronoun().capitalize()} loved {act.gerund}, and the garden air felt light and kind."
    )
    world.say(
        f"{params.name} looked at {plant.phrase} and whispered, "
        f'"Grow, little one, grow."'
    )
    _bump(hero, "hope")
    _bump(hero, "joy")
    _bump(hero, "wonder")
    _bump(plant, "growth", 0.4)

    # Act 2
    world.para()
    world.say(
        f"Then came the rain with a soft {params.activity == 'patter' and 'patter-patter' or 'tap-tap'}."
    )
    _bump(plant, "wet")
    _bump(hero, "worry")
    _bump(hero, "wonder")
    world.say(
        f"{params.name} thought, in a small inside voice, "
        f'"What if the seed is too sleepy to bloom?"'
    )
    world.say(
        f"But {hero.pronoun('possessive')} little hands stayed on the pot, warm and steady."
    )

    # Twist: magic answers the inner monologue.
    world.para()
    world.say(
        f"At once, a twinkle of magic slipped through the rain."
    )
    _bump(plant, "magic")
    _bump(plant, "growth", 0.8)
    _bump(plant, "bloom", 1.0)
    _bump(hero, "joy", 1.0)
    hero.memes["worry"] = max(0.0, hero.memes["worry"] - 1.0)
    hero.memes["hope"] += 1.0

    world.say(
        f"The seed was not sleeping after all; it was waiting for the song in {params.name}'s heart."
    )
    world.say(
        f"Then the bud popped open into a tiny bloom, bright as a button and sweet as spring."
    )
    world.say(
        f"{params.name} clapped {hero.pronoun('possessive')} hands. "
        f'"Oh!" {hero.pronoun().capitalize()} said. "I knew you could do it."'
    )
    world.say(
        f"The little flower stood up tall, and the garden kept its shiny, rainy smile."
    )

    world.facts.update(hero=hero, plant=plant, setting=setting, activity=act, prize=prize)
    return world


def generation_prompts(world: World) -> list[str]:
    f = world.facts
    hero = _safe_fact(world, f, "hero")
    act = _safe_fact(world, f, "activity")
    prize = _safe_fact(world, f, "prize")
    return [
        f'Write a nursery-rhyme story about {hero.id}, {act.keyword}, and a tiny {prize.label}.',
        f"Tell a gentle little tale where rain goes patter-patter and a seed finally blooms.",
        f'Write a short child-facing story with magic, an inner monologue, and a twist about "{act.keyword}".',
    ]


def story_qa(world: World) -> list[QAItem]:
    f = world.facts
