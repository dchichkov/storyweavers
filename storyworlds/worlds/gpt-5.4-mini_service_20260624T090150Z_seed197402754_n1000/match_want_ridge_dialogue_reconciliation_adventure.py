ASP_RULES = r"""
% A story is valid when the setting affords the action and the item is on hand.
valid(Setting, Item) :- affords(Setting, walk), item(Item).
valid(Setting, Item) :- affords(Setting, climb), item(Item).
"""


def asp_facts() -> str:
    import asp

    lines: list[str] = []
    for sid, s in SETTINGS.items():
        lines.append(asp.fact("setting", sid))
        for a in sorted(s.affords):
            lines.append(asp.fact("affords", sid, a))
    for iid in ITEMS:
        lines.append(asp.fact("item", iid))
    return "\n".join(lines)


def asp_program(show: str) -> str:
    return f"{asp_facts()}\n{ASP_RULES}\n{show}\n"


def asp_verify() -> int:
    try:
        import asp
    except Exception as exc:
        print(f"ASP unavailable: {exc}")
        return 2
    model = asp.one_model(asp_program("#show valid/2."))
    found = set(asp.atoms(model, "valid"))
    expected = {(sid, iid) for sid in SETTINGS for iid in ITEMS}
    if found == expected:
        print(f"OK: ASP parity matches Python gate ({len(found)} pairs).")
        return 0
    print("MISMATCH between ASP and Python gate.")
    print("only in asp:", sorted(found - expected))
    print("only in python:", sorted(expected - found))
    return 1


