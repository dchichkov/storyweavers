def resolve_params(args: argparse.Namespace, rng: random.Random) -> StoryParams:
    setting = getattr(args, "setting", None) or rng.choice(list(SETTINGS))
    item = getattr(args, "item", None) or rng.choice(list(ITEMS))
    gender = getattr(args, "gender", None) or rng.choice(["girl", "boy"])
    if gender not in _safe_lookup(ITEMS, item).genders:
        return _fallback_storyparams(args, rng, StoryParams, globals())
    name = getattr(args, "name", None) or rng.choice(PEOPLE[gender])
    helper = getattr(args, "helper", None) or rng.choice(HELPERS)
    trait = getattr(args, "trait", None) or rng.choice(TRAITS)
    return StoryParams(setting=setting, item=item, name=name, gender=gender, helper=helper, trait=trait)


def _story_header(world: World, hero: Entity, helper: Entity, item: Entity) -> None:
    world.say(f"{hero.id} was a {hero.memes.get('trait_word', 'curious')} {hero.type} who loved the ridge.")
    world.say(f"{hero.pronoun().capitalize()} wanted to keep {hero.pronoun('possessive')} {item.label} close on every climb.")
    world.say(f"One bright day, {hero.id} and {hero.pronoun('possessive')} {helper.label} went to {world.setting.place}.")


def _conflict(world: World, hero: Entity, helper: Entity, item: Entity) -> None:
    hero.memes["want"] += 1
    hero.memes["worry"] += 1
    world.para()
    world.say(f"Near a steep ridge, {hero.id} wanted to go faster, but {helper.id} pointed to the narrow stones.")
    world.say(f'"We should be careful," {helper.id} said. "{item.label} can slip, and so can feet."')
    world.say(f"{hero.id} held {hero.pronoun('possessive')} breath and looked down at the path.")
    world.say(f'"But I want to keep going," {hero.id} said, and the voice sounded small in the wind.')


def _reconcile(world: World, hero: Entity, helper: Entity, item: Entity) -> None:
    hero.memes["sad"] += 1
    world.para()
    world.say(f"{helper.id} sat beside {hero.id} on a flat stone and listened.")
    world.say(f'"You can want the climb and still choose a safer way," {helper.id} said.')
    world.say(f"{hero.id} nodded, and the two of them spoke softly until the worry got smaller.")
    gear = GEAR[item.id]
    world.say(f'Then {helper.id} said, "Let us {gear.prepares} before we try again."')
    hero.memes["joy"] += 1
    hero.memes["worry"] = 0.0
    world.say(f"{hero.id} smiled back. Together they did that, and {gear.resolves}.")
    world.say(f"After that, {hero.id} could keep the {item.label} safe and still enjoy the adventure.")


def generate_world(params: StoryParams) -> World:
    world = World(_safe_lookup(SETTINGS, params.setting))
    hero = world.add(Entity(id=params.name, kind="character", type=params.gender))
    helper = world.add(Entity(id=params.helper, kind="character", type="helper", label=params.helper))
    item = world.add(Entity(id=params.item, type=params.item, label=_safe_lookup(ITEMS, params.item).label, phrase=_safe_lookup(ITEMS, params.item).phrase, owner=hero.id))
    hero.memes["trait_word"] = params.trait
    item.worn_by = hero.id
    world.facts.update(hero=hero, helper=helper, item=item, params=params)
    _story_header(world, hero, helper, item)
    _conflict(world, hero, helper, item)
    _reconcile(world, hero, helper, item)
    return world


