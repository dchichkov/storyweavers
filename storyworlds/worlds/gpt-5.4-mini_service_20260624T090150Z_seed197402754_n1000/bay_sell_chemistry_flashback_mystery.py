def prize_at_risk(activity: Activity, prize: Prize) -> bool:
    return activity.id == "sell" and prize.label == "chemistry set"


def select_clue(activity: Activity, prize: Prize) -> Optional[Clue]:
    return CLUES["shell"] if prize_at_risk(activity, prize) else None


def reasonableness_gate(activity: Activity, prize: Prize) -> bool:
    return prize_at_risk(activity, prize) and select_clue(activity, prize) is not None


def _flashback(world: World, hero: Entity, clue: Clue) -> None:
    hero.memes["curiosity"] += 1
    world.say(
        f"Then {hero.id} had a flashback: earlier, {hero.pronoun('possessive')} "
        f"{clue.label} had brushed the dock step where {clue.reveals}."
    )


def tell(setting: Setting, activity: Activity, prize: Prize, clue: Clue,
         hero_name: str, hero_type: str, parent_type: str) -> World:
    world = World(setting)
    hero = world.add(Entity(
        id=hero_name, kind="character", type=hero_type,
        meters={"money": 0.0}, memes={"curiosity": 1.0, "worry": 0.0},
    ))
    parent = world.add(Entity(
        id="Parent", kind="character", type=parent_type, label="the parent",
        meters={"money": 0.0}, memes={"patience": 1.0},
    ))
    setobj = world.add(Entity(
        id="kit", type="thing", label=prize.label, phrase=prize.phrase,
        owner=hero.id, caretaker=parent.id,
    ))
    missing = world.add(Entity(
        id="badge", type="thing", label="badge", phrase="a small brass badge",
        owner=hero.id, caretaker=hero.id, meters={"hidden": 1.0}
    ))

    world.say(
        f"{hero.id} was a little {hero_type} who loved the bay and carried "
        f"{hero.pronoun('possessive')} chemistry set everywhere."
    )
    world.say(
        f"One bright day, {hero.id} and {hero.pronoun('possessive')} {parent.type} "
        f"went to {setting.place} to {activity.verb}."
    )
    world.say(setting.detail)
    world.say(
        f"{hero.id} set down {hero.pronoun('possessive')} {setobj.label} and tried to "
        f"call buyers over to the table."
    )

    world.para()
    hero.memes["worry"] += 1
    world.say(
        f"Then {hero.id} noticed the badge was gone, and the whole table felt suddenly strange."
    )
    clue_obj = world.add(Entity(id=clue.id, type="thing", label=clue.label, phrase=clue.phrase))
    world.say(f"Near the pier, {hero.id} spotted {clue_obj.phrase}.")
    _flashback(world, hero, clue)
    world.say(
        f"{hero.id} hurried back to the dock step and found the badge tucked where the shell had hidden it."
    )

    world.para()
    hero.memes["worry"] = 0.0
    hero.memes["relief"] = 1.0
    hero.meters["money"] += 1
    world.say(
        f"With the badge back, {hero.id} could smile again and keep {activity.gerund}."
    )
    world.say(
        f"By sunset, the chemistry set was still neat, the badge was safe, and the bay felt calm."
    )

    world.facts.update(
        hero=hero, parent=parent, prize=setobj, missing=missing, clue=clue_obj,
        activity=activity, setting=setting, resolved=True,
    )
    return world


