def _r_spill(world: World) -> list[str]:
    out: list[str] = []
    for actor in world.characters():
        if actor.meters.get("wobble", 0.0) < THRESHOLD:
            continue
        cup = world.entities.get("juice")
        if not cup or cup.worn_by != actor.id:
            continue
        sig = ("spill", actor.id)
        if sig in world.fired:
            continue
        world.fired.add(sig)
        cup.meters["spilled"] = cup.meters.get("spilled", 0.0) + 1
        actor.memes["worry"] = actor.memes.get("worry", 0.0) + 1
        out.append(f"The juice sloshed hard and looked ready to spill.")
    return out


def _r_cleanup(world: World) -> list[str]:
    out: list[str] = []
    cup = world.entities.get("juice")
    if not cup:
        return out
    if cup.meters.get("spilled", 0.0) < THRESHOLD:
        return out
    sig = ("cleanup", cup.id)
    if sig in world.fired:
        return out
    world.fired.add(sig)
    for e in world.characters():
        if e.kind == "character" and e.type in {"boy", "girl"}:
            e.memes["relief"] = e.memes.get("relief", 0.0) + 1
    out.append("A safe tray could stop the mess.")
    return out


CAUSAL_RULES = [
    Rule("spill", "physical", _r_spill),
    Rule("cleanup", "physical", _r_cleanup),
]


def propagate(world: World, narrate: bool = True) -> list[str]:
    produced: list[str] = []
    changed = True
    for _ in range(len(globals().get("CAUSAL_RULES", [])) + 4):
        changed = False
        for rule in CAUSAL_RULES:
            sents = rule.apply(world)
            if sents:
                changed = True
                produced.extend(sents)
    if narrate:
        for s in produced:
            world.say(s)
    return produced


def predict_mess(world: World, actor: Entity, activity: Activity, prize_id: str) -> dict:
    sim = world.copy()
    _do_activity(sim, sim.get(actor.id), activity, narrate=False)
    prize = sim.entities.get(prize_id)
    return {"soiled": bool(prize and prize.meters.get("spilled", 0.0) >= THRESHOLD)}


