    if not pred["soiled"]:
        return False
    hero.memes["misunderstanding"] = hero.memes.get("misunderstanding", 0) + 1
    hero.memes["unease"] = hero.memes.get("unease", 0) + 1
    world.say(f'"If you {activity.verb}, your {prize.label} may get all messy," said {hero.pronoun("possessive")} {parent.label}.')
    return True


