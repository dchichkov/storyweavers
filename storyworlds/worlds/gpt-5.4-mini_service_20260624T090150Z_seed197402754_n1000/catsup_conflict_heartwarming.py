def prize_at_risk(activity: Activity, prize: Item) -> bool:
    return prize.region in activity.zone


def select_gear(activity: Activity, prize: Item) -> Optional[Gear]:
    for gear in GEAR:
        if activity.mess in gear.guards and prize.region in gear.covers:
            return gear
    return None


def explain_rejection(activity: Activity, prize: Item) -> str:
    if not prize_at_risk(activity, prize):
        return (
            f"(No story: {activity.gerund} would not reach the {prize.label}, "
            f"so there is no honest worry and no real conflict.)"
        )
    return (
        f"(No story: there is no matching gentle fix for {prize.label} in this world. "
        f"The heartwarming turn needs a real way to protect the shirt or apron.)"
    )


def valid_combos() -> list[tuple[str, str, str]]:
    out = []
    for place, setting in SETTINGS.items():
        for act_id in setting.affords:
            act = _safe_lookup(ACTIVITIES, act_id)
            for prize_id, prize in PRIZES.items():
                if prize_at_risk(act, prize) and select_gear(act, prize):
                    out.append((place, act_id, prize_id))
    return out


