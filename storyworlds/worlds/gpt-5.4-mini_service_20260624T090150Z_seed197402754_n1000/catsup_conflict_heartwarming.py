def _apply_mess(world: World, actor: Entity, activity: Activity) -> list[str]:
    out: list[str] = []
    actor.meters[activity.mess] = actor.meters.get(activity.mess, 0.0) + 1
    actor.memes["worry"] = actor.memes.get("worry", 0.0) + 1
    for item in world.worn_items(actor):
        if item.protective:
            continue
        if item.region not in world.zone:
            continue
        if world.covered(actor, item.region):
            continue
        sig = ("mess", item.id, activity.mess)
        if sig in world.fired:
            continue
        world.fired.add(sig)
        item.meters[activity.mess] = item.meters.get(activity.mess, 0.0) + 1
        item.meters["dirty"] = item.meters.get("dirty", 0.0) + 1
        out.append(f"{actor.pronoun('possessive').capitalize()} {item.label} got catsup on it.")
    return out


def build_world(params: StoryParams) -> World:
    world = World(_safe_lookup(SETTINGS, params.place))
    act = _safe_lookup(ACTIVITIES, params.activity)
    prize = _safe_lookup(PRIZES, params.prize)

    hero = world.add(Entity(
        id=params.name,
        kind="character",
        type=params.gender,
        meters={},
        memes={"joy": 0.0, "conflict": 0.0, "love": 0.0, "worry": 0.0},
    ))
    parent = world.add(Entity(
        id="Parent",
        kind="character",
        type=params.parent,
        label="parent",
        meters={},
        memes={"care": 1.0, "worry": 0.0, "love": 1.0},
    ))
    shirt = world.add(Entity(
        id=prize.id,
        kind="thing",
        type=prize.label,
        label=prize.label,
        phrase=prize.phrase,
        owner=hero.id,
        caretaker=parent.id,
        worn_by=hero.id,
        plural=prize.plural,
        meters={"dirty": 0.0},
        memes={},
    ))

    # Act 1: loving setup.
    world.say(f"{hero.id} was a {params.trait} little {params.gender} who loved lunch at {world.setting.place}.")
    world.say(f"{hero.id}'s {parent.type if parent.type != 'mother' else 'mom'} had bought {hero.pronoun('object')} {shirt.phrase}.")
    world.say(f"{hero.id} liked how fresh {hero.pronoun('possessive')} {shirt.label} felt on a bright day.")

    # Act 2: conflict.
    world.para()
    world.say(f"At {world.setting.place}, {hero.id} wanted to {act.verb}.")
    world.say(f"But when {hero.id} reached for the bottle, {hero.pronoun('possessive')} {parent.type if parent.type != 'mother' else 'mom'} worried about the {shirt.label}.")
    hero.memes["desire"] = hero.memes.get("desire", 0.0) + 1
    hero.memes["conflict"] = hero.memes.get("conflict", 0.0) + 1
    world.say(f'"If you do that now, your {shirt.label} will get {act.soil}," {hero.pronoun("possessive")} {parent.type if parent.type != "mother" else "mom"} said.')

    world.say(f"{hero.id} frowned and tried to {act.rush}.")
    hero.memes["defiance"] = hero.memes.get("defiance", 0.0) + 1
    world.zone = set(act.zone)
    world.say(f"{hero.id} and {hero.pronoun('possessive')} {parent.type if parent.type != 'mother' else 'mom'} had a short conflict.")
    world.say(f"Then the bottle tipped, and the worry got bigger.")

    # A little physical consequence.
    _apply_mess(world, hero, act)

    # Act 3: gentle repair.
    world.para()
    gear = select_gear(act, prize)
    if gear is None:
        gear = next(iter(globals().get("GEARS", globals().get("GEAR", []))))
    gear_ent = world.add(Entity(
        id=gear.id,
        kind="thing",
        type=gear.id,
        label=gear.label,
        phrase=gear.label,
        protective=True,
        worn_by=hero.id,
        owner=hero.id,
        meters={},
        memes={},
    ))
    setattr(gear_ent, "covers", set(gear.covers))
    world.say(f"Then {hero.pronoun('possessive')} {parent.type if parent.type != 'mother' else 'mom'} smiled kindly.")
    world.say(f'"How about we {gear.prep} and try again?"')
    world.say(f"{hero.id} took a breath, nodded, and put on {gear.label}.")
    world.say(f"Together they {gear.tail}.")

    hero.memes["conflict"] = 0.0
    hero.memes["joy"] = hero.memes.get("joy", 0.0) + 1
    hero.memes["love"] = hero.memes.get("love", 0.0) + 1
    world.say(f"This time, {hero.id} got to {act.verb} without ruining the {shirt.label}.")
    world.say(f"The {shirt.label} stayed clean, and the lunch felt happy again.")

    world.facts.update(
        hero=hero,
        parent=parent,
        prize=shirt,
        activity=act,
        gear=gear,
        setting=world.setting,
        resolved=True,
    )
    return world


