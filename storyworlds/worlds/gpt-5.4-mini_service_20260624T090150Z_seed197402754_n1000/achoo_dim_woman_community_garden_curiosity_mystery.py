def asp_facts() -> str:
    import asp
    lines = [
        asp.fact("place", "community_garden"),
        asp.fact("affords", "community_garden", "search"),
        asp.fact("affords", "community_garden", "listen"),
        asp.fact("affords", "community_garden", "inspect"),
    ]
    for cid, c in CLUES.items():
        lines.append(asp.fact("clue", cid))
        lines.append(asp.fact("curious_about", cid, c.curious_about))
    for mid, _ in MYSTERIES.items():
        lines.append(asp.fact("mystery", mid))
    return "\n".join(lines)


ASP_RULES = r"""
suspense(place) :- place(place), affords(place, search), affords(place, inspect).
clue_path(C) :- clue(C), curious_about(C, _).
solved(M) :- mystery(M), clue_path(_).
#show suspense/1.
#show clue_path/1.
#show solved/1.
"""


def asp_program(show: str) -> str:
    return f"{asp_facts()}\n{ASP_RULES}\n{show}\n"


def asp_verify() -> int:
    import asp
    model = asp.one_model(asp_program("#show suspense/1. #show solved/1."))
    atoms = set((sym.name, tuple(str(a) for a in sym.arguments)) for sym in model)
    want = {("suspense", ("community_garden",)), ("solved", ("seed_note",))}
    if atoms == want:
        print("OK: ASP twin matches the Python story gate.")
        return 0
    print("MISMATCH:", atoms, want)
    return 1


def build_parser() -> argparse.ArgumentParser:
    ap = argparse.ArgumentParser(description="A small mystery storyworld set in a community garden.")
    ap.add_argument("--place", choices=["community_garden"], default=None)
    ap.add_argument("--clue", choices=CLUES.keys())
    ap.add_argument("--mystery", choices=MYSTERIES.keys())
    ap.add_argument("--name")
    ap.add_argument("-n", type=int, default=1)
    ap.add_argument("--seed", type=int, default=None)
    ap.add_argument("--all", action="store_true")
    ap.add_argument("--trace", action="store_true")
    ap.add_argument("--qa", action="store_true")
    ap.add_argument("--json", action="store_true")
    ap.add_argument("--asp", action="store_true")
    ap.add_argument("--verify", action="store_true")
    ap.add_argument("--show-asp", action="store_true")
    return ap


def resolve_params(args: argparse.Namespace, rng: random.Random) -> StoryParams:
    place = "community_garden"
    clue = getattr(args, "clue", None) or rng.choice(list(CLUES))
    mystery = getattr(args, "mystery", None) or rng.choice(list(MYSTERIES))
    if clue == "lantern" and mystery == "gloves":
        return _fallback_storyparams(args, rng, StoryParams, globals())
    name = getattr(args, "name", None) or rng.choice(["Mina", "Ruth", "June", "Nora", "Ada", "Iris"])
    return StoryParams(place=place, clue=clue, mystery=mystery, name=name)


def _do_search(world: World, hero: Entity, clue: Clue, mystery: str) -> None:
    hero.memes["curiosity"] = hero.memes.get("curiosity", 0) + 1
    world.say(
        f"{hero.id} was a woman with achoo-dim sneezes and a bright Curiosity that made her notice small details."
    )
    world.say(
        f"One afternoon in {world.place.name}, {hero.id} looked at {_safe_lookup(MYSTERIES, mystery)} and wondered {clue.curious_about}."
    )
    world.para()
    world.say(
        f"She followed the quiet hints {clue.found_by}, where {clue.label} waited like a tiny secret."
    )
    world.say(f"Then the clue revealed {clue.reveals}.")
    world.para()
    world.say(
        f"{hero.id} nodded, pieced the mystery together, and found that {_safe_lookup(MYSTERIES, mystery)} was not gone at all."
    )
    world.say(
        f"It had been tucked safely under the trellis, and by the end of the day {hero.id} was smiling beside the beans, glad the garden's secret was solved."
    )


def generate(params: StoryParams) -> StorySample:
    world = World(PLACE)
    hero = world.add(Entity(id=params.name, kind="character", type="woman", label=params.name))
    clue = _safe_lookup(CLUES, params.clue)
    world.facts.update(hero=hero, clue=clue, mystery=params.mystery, place=PLACE)
    _do_search(world, hero, clue, params.mystery)
    story = world.render()
    prompts = [
        "Write a short mystery story for young children set in a community garden.",
        f"Tell a story about a woman named {params.name} whose Curiosity helps solve {_safe_lookup(MYSTERIES, params.mystery)}.",
        f"Create a gentle detective tale where {params.name} follows {clue.label} to uncover a garden secret.",
    ]
    story_qa = [
        QAItem(
            question=f"Who solved the mystery in the community garden?",
            answer=f"A woman named {params.name} solved it by paying close attention and following the clue.",
        ),
        QAItem(
            question=f"What clue did {params.name} find?",
            answer=f"{params.name} found {clue.label}, which pointed her toward the hidden truth.",
        ),
        QAItem(
            question=f"What was the mystery?",
            answer=f"The mystery was {_safe_lookup(MYSTERIES, params.mystery)}.",
        ),
    ]
    world_qa = [
        QAItem(
            question="What is a community garden?",
            answer="A community garden is a shared outdoor place where people grow plants, water beds, and care for vegetables and flowers together.",
        ),
        QAItem(
            question="What does curiosity help someone do?",
            answer="Curiosity helps someone notice details, ask questions, and keep looking until they understand a puzzle.",
        ),
        QAItem(
            question="What is a clue in a mystery?",
            answer="A clue is a small piece of information that helps solve a mystery.",
        ),
    ]
    return StorySample(params=params, story=story, prompts=prompts, story_qa=story_qa, world_qa=world_qa, world=world)


def dump_trace(world: World) -> str:
    lines = ["--- world trace ---"]
    for e in list(world.entities.values()):
        lines.append(f"{e.id}: kind={e.kind} type={e.type} memes={e.memes} meters={e.meters}")
    return "\n".join(lines)


def format_qa(sample: StorySample) -> str:
    out = ["== prompts =="]
    out.extend(f"- {p}" for p in sample.prompts)
    out.append("")
    out.append("== story qa ==")
    for q in sample.story_qa:
        out.append(f"Q: {q.question}")
        out.append(f"A: {q.answer}")
    out.append("")
    out.append("== world qa ==")
    for q in sample.world_qa:
        out.append(f"Q: {q.question}")
        out.append(f"A: {q.answer}")
    return "\n".join(out)


def emit(sample: StorySample, *, trace: bool = False, qa: bool = False, header: str = "") -> None:
    if header:
        print(header)
    print(sample.story)
    if trace and sample.world is not None:
        print(dump_trace(sample.world))
    if qa:
        print()
        print(format_qa(sample))


def main() -> None:
    args = build_parser().parse_args()
    if getattr(args, "show_asp", None):
        print(asp_program("#show suspense/1. #show solved/1."))
        return
    if getattr(args, "verify", None):
        sys.exit(asp_verify())
    if getattr(args, "asp", None):
        import asp
        model = asp.one_model(asp_program("#show suspense/1. #show clue_path/1. #show solved/1."))
        print("ASP model:")
        for atom in model:
            print(atom)
        return

    base_seed = getattr(args, "seed", None) if getattr(args, "seed", None) is not None else random.randrange(2**31)
    samples: list[StorySample] = []
    if getattr(args, "all", None):
        curated = [
            StoryParams(place="community_garden", clue="spade", mystery="seed_note", name="Mina"),
            StoryParams(place="community_garden", clue="trowel", mystery="key", name="Ruth"),
            StoryParams(place="community_garden", clue="lantern", mystery="gloves", name="Iris"),
        ]
        samples = [generate(p) for p in curated]
    else:
        seen: set[str] = set()
        i = 0
        while len(samples) < getattr(args, "n", None) and i < max(50, getattr(args, "n", None) * 20):
            p = resolve_params(args, random.Random(base_seed + i))
            p.seed = base_seed + i
            s = generate(p)
            i += 1
            if s.story in seen:
                continue
            seen.add(s.story)
            samples.append(s)

    if getattr(args, "json", None):
        if len(samples) == 1:
            print(samples[0].to_json())
        else:
            print(json.dumps([s.to_dict() for s in samples], indent=2, ensure_ascii=False))
        return

    for i, sample in enumerate(samples):
        header = f"### variant {i+1}" if len(samples) > 1 else ""
        emit(sample, trace=getattr(args, "trace", None), qa=getattr(args, "qa", None), header=header)
        if i + 1 < len(samples):
            print("\n" + "=" * 70 + "\n")


if __name__ == "__main__":
    main()
