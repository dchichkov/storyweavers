def resolve_params(args: argparse.Namespace, rng: random.Random) -> StoryParams:
    if getattr(args, "activity", None) and getattr(args, "prize", None):
        act, prize = _safe_lookup(ACTIVITIES, getattr(args, "activity", None)), _safe_lookup(PRIZES, getattr(args, "prize", None))
        if not (prize_at_risk(act, prize) and select_gear(act, prize)):
            return _fallback_storyparams(args, rng, StoryParams, globals())
    if getattr(args, "gender", None) and getattr(args, "prize", None) and getattr(args, "gender", None) not in _safe_lookup(PRIZES, getattr(args, "prize", None)).genders:
        return _fallback_storyparams(args, rng, StoryParams, globals())
    combos = [c for c in valid_combos()
              if (getattr(args, "place", None) is None or c[0] == getattr(args, "place", None))
              and (getattr(args, "activity", None) is None or c[1] == getattr(args, "activity", None))
              and (getattr(args, "prize", None) is None or c[2] == getattr(args, "prize", None))]
    if not combos:
        return _fallback_storyparams(args, rng, StoryParams, globals())
    place, activity, prize = rng.choice(list(combos))
    gender = getattr(args, "gender", None) or rng.choice(["girl", "boy"])
    name = getattr(args, "name", None) or rng.choice(GIRL_NAMES if gender == "girl" else BOY_NAMES)
    parent = getattr(args, "parent", None) or rng.choice(["mother", "father"])
    trait = rng.choice(TRAITS)
    return StoryParams(place=place, activity=activity, prize=prize, name=name, gender=gender, parent=parent, trait=trait)


def _do_activity(world: World, actor: Entity, activity: Activity, narrate: bool = True) -> None:
    actor.meters["mud"] += 1
    actor.memes["worry"] += 1
    if narrate:
        world.say(f"{actor.id} burst in from the squall, and the boots left a muddy trail.")


def predict_mess(world: World, actor: Entity, activity: Activity, prize_id: str) -> bool:
    sim = world.copy()
    _do_activity(sim, sim.get(actor.id), activity, narrate=False)
    prize = sim.get(prize_id)
    return sim.get(actor.id).meters["mud"] >= THRESHOLD and prize.region == "hand"


def tell(setting: Setting, activity: Activity, prize_cfg: Prize, hero_name: str, hero_type: str, parent_type: str, trait: str) -> World:
    world = World(setting)
    hero = world.add(Entity(id=hero_name, kind="character", type=hero_type))
    parent = world.add(Entity(id="Parent", kind="character", type=parent_type, label="the parent"))
    prize = world.add(Entity(id="prize", type=prize_cfg.type, label=prize_cfg.label, phrase=prize_cfg.phrase,
                             owner=hero.id, caretaker=parent.id, region=prize_cfg.region, plural=prize_cfg.plural))
    cloak = world.add(Entity(id="cloak", type="cloak", label="a salt-stained cloak", worn_by=hero.id))
    cloak.meters["mud"] = 0.0

    world.say(f"{hero.id} was a little {trait} pirate who loved maps, songs, and a tidy mudroom.")
    world.say(f"{hero.pronoun().capitalize()} guarded {hero.pronoun('possessive')} {prize.label} as if it were a treasure chest key.")
    world.para()
    world.say("One stormy evening, the door banged open.")
    world.say(f"{hero.id} wanted to {activity.verb}, but the mud on the boots was ready to spatter the floor.")
    if predict_mess(world, hero, activity, prize.id):
        world.say(f'"Your {prize.label} will get {activity.soil}," {parent.id} said. "And then I must clean the floor too."')
    hero.memes["lesson"] += 1
    hero.memes["worry"] += 1
    world.say(f"{hero.id} frowned, because {hero.pronoun('possessive')} feet were still itching to rush ahead.")
    world.para()
    gear = select_gear(activity, prize)
    if gear:
        if gear.id == "basin":
            world.say(f"{parent.id} pointed to {gear.label} and smiled. " + f'"First we {gear.prep}," {parent.id} said.')
        else:
            world.say(f"{parent.id} pointed to {gear.label} and smiled. " + f'"First we {gear.prep}," {parent.id} said.')
        hero.memes["joy"] += 1
        world.say(f"{hero.id} obeyed, and the mud stayed on the boots instead of the floor.")
        world.say(
            f'Then {hero.id} sang a little rhyme: "Mud stays low, and boots can go; '
            f'clean first, then the sea winds blow."'
        )
        world.say(f"The mudroom looked neat again, and {hero.id}'s {prize.label} stayed clean beside the hooks.")
    world.facts.update(hero=hero, parent=parent, prize=prize, prize_cfg=prize_cfg,
                       activity=activity, setting=setting, gear=gear, trait=trait)
    return world


