def predict_mess(world: World, actor: Entity, activity: Activity, prize_id: str) -> dict:
    sim = world.copy()
    _do_activity(sim, sim.get(actor.id), activity, narrate=False)
    prize = sim.entities.get(prize_id)
    return {"soiled": bool(prize and prize.meters.get("dirty", 0) >= THRESHOLD)}


def _do_activity(world: World, actor: Entity, activity: Activity, narrate: bool = True) -> None:
    world.zone = set(activity.zone)
    actor.meters[activity.mess] = actor.meters.get(activity.mess, 0) + 1
    if narrate:
        world.say(f"{actor.id} {activity.gerund}.")
    # If the coat is at risk, tension rises.
    for item in world.worn_items(actor):
        if item.region in world.zone and not item.meters.get("safe", False):
            item.meters["dirty"] = item.meters.get("dirty", 0) + 1


def introduce(world: World, hero: Entity) -> None:
    world.say(
        f"{hero.id} was a little {next(t for t in hero.meters.get('traits', []) if t != 'little') if hero.meters.get('traits') else 'pirate'} "
        f"{hero.type} who liked shiny buttons, ship songs, and brave plans."
    )


def setting_line(world: World) -> None:
    world.say(
        "In the doctor's waiting room, the chairs were neat, the fish tank bubbled, "
        "and a wall clock ticked like a tiny drum."
    )


def suspense_line(world: World, activity: Activity) -> None:
    world.say(
        f"{activity.suspense}. The clock ticked once more, and the room felt very still."
    )


def warning_line(world: World, parent: Entity, hero: Entity, prize: Entity, activity: Activity) -> None:
    pred = predict_mess(world, hero, activity, prize.id)
    if pred["soiled"]:
        world.say(
            f'"If you hurry too fast, your {prize.label} will slip," said {parent.id}. '
            f'"Let’s fix the cinch before the doctor calls you."'
        )


def resolve_line(world: World, parent: Entity, hero: Entity, prize: Entity) -> None:
    hero.memes["joy"] = hero.memes.get("joy", 0) + 1
    world.say(
        f"{parent.id} helped {hero.id} use a spare leather tie to cinch the coat snug."
    )
    world.say(
        f"Then {hero.id} sat still in the chair, and the pirate coat stayed neat while the clock kept ticking."
    )


def tell(setting: Setting, activity: Activity, prize_cfg: Prize, hero_name: str, hero_type: str, parent_type: str, trait: str) -> World:
    world = World(setting)
    hero = world.add(Entity(
        id=hero_name,
        kind="character",
        type=hero_type,
        meters={"traits": ["little", trait]},
        memes={"hope": 1.0},
    ))
    parent = world.add(Entity(id="Parent", kind="character", type=parent_type))
    prize = world.add(Entity(
        id="prize",
        type=prize_cfg.type,
        label=prize_cfg.label,
        phrase=prize_cfg.phrase,
        region=prize_cfg.region,
        owner=hero.id,
        caretaker=parent.id,
    ))

    introduce(world, hero)
    setting_line(world)
    world.para()
    world.say(f"{hero.id} wore {hero.pronoun('possessive')} {prize.phrase} and wanted to {activity.verb}.")
    world.say(f"But {activity.suspense}.")
    warning_line(world, parent, hero, prize, activity)
    world.para()
    world.say(f"{hero.id} reached for the buckle and tried to {activity.rush}.")
    suspense_line(world, activity)
    gear = select_gear(activity, prize)
    if gear is None:
        gear = next(iter(globals().get("GEARS", globals().get("GEAR", []))))
    resolve_line(world, parent, hero, prize)
    world.facts.update(hero=hero, parent=parent, prize=prize, activity=activity, gear=gear, setting=setting)
    return world


