def predict_mess(world: World, actor: Entity, activity: Activity, prize_id: str) -> dict:
    sim = world.copy()
    _do_activity(sim, sim.get(actor.id), activity, narrate=False)
    prize = sim.entities.get(prize_id)
    return {"soiled": bool(prize and prize.meters["crease"] >= THRESHOLD)}


def _do_activity(world: World, actor: Entity, activity: Activity, narrate: bool = True) -> None:
    world.zone = set(activity.zone)
    actor.meters[activity.mess] += 1
    actor.memes["repetition"] += 1
    actor.memes["amusement"] += 1
    if narrate:
        propagate(world)


def _r_crease(world: World) -> list[str]:
    out: list[str] = []
    for actor in world.characters():
        if actor.meters["creased"] < THRESHOLD:
            continue
        for item in list(world.entities.values()):
            if item.worn_by != actor.id or item.protective:
                continue
            if item.region not in world.zone:
                continue
            if world.covered(actor, item.region):
                continue
            sig = ("crease", item.id)
            if sig in world.fired:
                continue
            world.fired.add(sig)
            item.meters["creased"] += 1
            item.meters["stress"] += 1
            out.append(f"{actor.pronoun('possessive').capitalize()} {item.label} got a little creased.")
    return out


def _r_funny(world: World) -> list[str]:
    out: list[str] = []
    for actor in world.characters():
        if actor.memes["repetition"] < THRESHOLD:
            continue
        sig = ("funny", actor.id)
        if sig in world.fired:
            continue
        world.fired.add(sig)
        actor.memes["amusement"] += 1
        out.append(f"{actor.id} kept saying the same thing again and again, which made everyone smile.")
    return out


CAUSAL_RULES = [(_r_crease), (_r_funny)]


def propagate(world: World) -> None:
    changed = True
    for _ in range(len(globals().get("CAUSAL_RULES", [])) + 4):
        changed = False
        for rule in CAUSAL_RULES:
            sents = rule(world)
            if sents:
                changed = True
                for s in sents:
                    world.say(s)


def introduce(world: World, hero: Entity) -> None:
    world.say(
        f"{hero.id} was a little {hero.memes.get('trait', 0) and 'friend' or 'kid'} "
        f"who loved the shopping mall because there were shiny floors, snack smells, and plenty of chances to laugh."
    )


def loves_activity(world: World, hero: Entity, activity: Activity) -> None:
    hero.memes["repetition"] += 1
    world.say(
        f"{hero.pronoun().capitalize()} loved {activity.gerund}; the more {hero.id} repeated it, the funnier it seemed."
    )


def loves_prize(world: World, hero: Entity, prize: Entity) -> None:
    prize.worn_by = hero.id
    hero.memes["kindness"] += 1
    world.say(
        f"{hero.id} had found {hero.pronoun('possessive')} {prize.label} at the mall and carried {prize.it()} everywhere like a tiny treasure."
    )


