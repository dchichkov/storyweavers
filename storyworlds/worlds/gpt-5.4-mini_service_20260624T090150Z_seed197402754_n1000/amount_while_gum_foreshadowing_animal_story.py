def generation_prompts(world: World) -> list[str]:
    f = world.facts
    hero = _safe_fact(world, f, "hero")
    helper = _safe_fact(world, f, "helper")
    activity = _safe_fact(world, f, "activity")
    prize = _safe_fact(world, f, "prize")
    return [
        f'Write a short Animal Story about {hero.id}, a little {hero.type}, and a clue about gum.',
        f"Tell a foreshadowing story where {helper.id} warns {hero.id} that {prize.label} may get sticky while {hero.id} wants to {activity.verb}.",
        f'Write a child-friendly story using the words "amount", "while", and "gum".',
    ]


def story_qa(world: World) -> list[QAItem]:
    f = world.facts
    hero = _safe_fact(world, f, "hero")
    helper = _safe_fact(world, f, "helper")
    prize = _safe_fact(world, f, "prize")
    activity = _safe_fact(world, f, "activity")
    place = _safe_fact(world, f, "place")
    qa = [
        QAItem(
            question=f"What did {hero.id} want to do at {place.label}?",
            answer=f"{hero.id} wanted to {activity.verb}.",
        ),
        QAItem(
            question=f"Why did {helper.id} worry about {prize.label}?",
            answer=f"{helper.id} worried because the sticky mess could reach {prize.label} while {hero.id} played.",
        ),
        QAItem(
            question="What clue was shown early in the story?",
            answer="The story showed a small amount of gum near the path, which hinted that something sticky might happen later.",
        ),
    ]
    if f.get("gear"):
        gear = _safe_fact(world, f, "gear")
        qa.append(
            QAItem(
                question=f"How did the helper solve the problem?",
                answer=f"They used {gear.label} so {hero.id} could keep playing without ruining the {prize.label}.",
            )
        )
    return qa


def world_qa(world: World) -> list[QAItem]:
    return [
        QAItem(
            question="What is gum?",
            answer="Gum is a chewy sticky candy that can stick to things if it is dropped on the ground.",
        ),
        QAItem(
            question="What does foreshadowing mean?",
            answer="Foreshadowing is when a story gives a small clue early that helps you guess what will matter later.",
        ),
        QAItem(
            question="Why is an amount of something a useful clue?",
            answer="An amount tells how much there is, and even a small amount can matter if it is sticky or hard to clean.",
        ),
    ]


def format_qa(sample: StorySample) -> str:
    lines = ["== (1) Generation prompts -- asks that would produce this story =="]
    for i, p in enumerate(sample.prompts, 1):
        lines.append(f"{i}. {p}")
    lines.append("")
    lines.append("== (2) Story questions -- answerable from the story text ==")
    for item in sample.story_qa:
        lines.append(f"Q: {item.question}")
        lines.append(f"A: {item.answer}")
    lines.append("")
    lines.append("== (3) World-knowledge questions -- child level, no story needed ==")
    for item in sample.world_qa:
        lines.append(f"Q: {item.question}")
        lines.append(f"A: {item.answer}")
    return "\n".join(lines)


def dump_trace(world: World) -> str:
    lines = ["--- world model state ---"]
    for e in list(world.entities.values()):
        meters = {k: v for k, v in e.meters.items() if v}
        memes = {k: v for k, v in e.memes.items() if v}
        bits = []
        if meters:
            bits.append(f"meters={dict(meters)}")
        if memes:
            bits.append(f"memes={dict(memes)}")
        lines.append(f"  {e.id:10} ({e.kind:9}) {' '.join(bits)}")
    lines.append(f"  fired rules: {sorted(n for n, *_ in world.fired)}")
    return "\n".join(lines)


