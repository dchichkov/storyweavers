def setup_foreshadowing(world: World, hero: Entity, activity: Activity, prize: Entity) -> None:
    world.say(
        f"One morning, {hero.id} found an odd little spot of gum near the path."
    )
    world.say(
        f"It was only a small amount of gum, but {hero.pronoun('possessive')} helper said, "
        f'"That sticky patch might matter later."'
    )
    hero.memes["curiosity"] = hero.memes.get("curiosity", 0) + 1


def introduce_story(world: World, hero: Entity, helper: Entity, prize: Entity, activity: Activity) -> None:
    world.say(
        f"{hero.id} was a little {hero.type} who loved to explore {world.place.label}."
    )
    world.say(
        f"{hero.id} also loved {activity.gerund}, and {hero.pronoun('possessive')} "
        f"{prize.label} was always kept neat for the day."
    )
    world.say(
        f"{helper.id} noticed everything and liked to help before trouble could grow."
    )


def warn(world: World, helper: Entity, hero: Entity, activity: Activity, prize: Entity) -> bool:
    if not prize_at_risk(activity, prize):
        return False
    world.facts["foreshadow"] = True
    world.say(
        f'"If you {activity.verb}, your {prize.label} could get {activity.soil}," '
        f"{helper.id} said."
    )
    return True


def do_activity(world: World, hero: Entity, activity: Activity) -> None:
    world.zone = set(activity.zone)
    hero.meters[activity.mess] = hero.meters.get(activity.mess, 0) + 1


def stick(world: World, hero: Entity, prize: Entity, activity: Activity) -> None:
    if hero.meters.get(activity.mess, 0) < 1:
        return
    sig = ("stick", prize.id)
    if sig in world.fired:
        return
    world.fired.add(sig)
    prize.meters["dirty"] = prize.meters.get("dirty", 0) + 1
    world.say(
        f"While {hero.id} played, the sticky spot spread to {hero.pronoun('possessive')} {prize.label}."
    )


def worry(world: World, helper: Entity, prize: Entity) -> None:
    if prize.meters.get("dirty", 0) >= 1:
        world.say(
            f"{helper.id} frowned a little, because cleaning {prize.label} would take time."
        )


def compromise(world: World, helper: Entity, hero: Entity, activity: Activity, prize: Entity) -> Optional[Gear]:
    gear = select_gear(activity, prize)
    if gear is None:
        return None
    world.say(
        f'{helper.id} smiled and said, "{gear.prep} and then you can {activity.verb} safely."'
    )
    return gear


def accept(world: World, helper: Entity, hero: Entity, activity: Activity, prize: Entity, gear: Gear) -> None:
    hero.memes["joy"] = hero.memes.get("joy", 0) + 1
    world.say(
        f"{hero.id} nodded, got the {gear.label}, and {gear.tail}."
    )
    world.say(
        f"After that, {hero.id} was {activity.gerund}, and {prize.label} stayed clean."
    )


def tell(place: Place, activity: Activity, prize_cfg: Prize, hero_name: str, hero_type: str, helper_type: str) -> World:
    world = World(place)
    hero = world.add(Entity(id=hero_name, kind="character", type=hero_type))
    helper = world.add(Entity(id=helper_type.capitalize(), kind="character", type=helper_type, label=f"the {helper_type}"))
    prize = world.add(Entity(id="prize", type=prize_cfg.id, label=prize_cfg.label, phrase=prize_cfg.phrase, owner=hero.id, caretaker=helper.id))

    introduce_story(world, hero, helper, prize, activity)
    world.para()
    setup_foreshadowing(world, hero, activity, prize)
    warn(world, helper, hero, activity, prize)
    do_activity(world, hero, activity)
    stick(world, hero, prize, activity)
    worry(world, helper, prize)
    world.para()
    gear = compromise(world, helper, hero, activity, prize)
    if gear:
        accept(world, helper, hero, activity, prize, gear)
    world.facts.update(hero=hero, helper=helper, prize=prize, activity=activity, gear=gear, place=place)
    return world


