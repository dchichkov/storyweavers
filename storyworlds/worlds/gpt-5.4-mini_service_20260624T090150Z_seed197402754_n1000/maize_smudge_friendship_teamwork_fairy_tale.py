class World:
    def __init__(self, setting: Setting) -> None:
        self.setting = setting
        self.entities: dict[str, Entity] = {}
        self.paragraphs: list[list[str]] = [[]]
        self.fired: set[tuple] = set()
        self.zone: set[str] = set()
        self.facts: dict = {}

    def add(self, ent: Entity) -> Entity:
        self.entities[ent.id] = ent
        return ent

    def get(self, eid: str) -> Entity:
        if eid not in self.entities:
            label = str(eid).replace("_", " ")
            self.entities[eid] = Entity(str(eid), label=label)
        return self.entities[eid]

    def characters(self) -> list[Entity]:
        return [e for e in self.entities.values() if e.kind == "character"]

    def worn_items(self, actor: Entity) -> list[Entity]:
        return [e for e in self.entities.values() if e.worn_by == actor.id]

    def covered(self, actor: Entity, region: str) -> bool:
        return any(region in g.meters.get("covers", set()) for g in self.worn_items(actor))

    def say(self, text: str) -> None:
        if text:
            self.paragraphs[-1].append(text)

    def para(self) -> None:
        if self.paragraphs[-1]:
            self.paragraphs.append([])

    def render(self) -> str:
        return "\n\n".join(" ".join(p) for p in self.paragraphs if p)


def _clean_region(world: World, actor: Entity, activity: Activity) -> list[str]:
    out: list[str] = []
    for item in world.worn_items(actor):
        if item.id == "maize" and "smudge" in actor.meters and actor.meters["smudge"] >= THRESHOLD:
            if item.meters.get("smudge", 0) < THRESHOLD:
                sig = ("smudge", item.id)
                if sig in world.fired:
                    continue
                world.fired.add(sig)
                item.meters["smudge"] = item.meters.get("smudge", 0) + 1
                out.append(f"The smudge touched the golden maize.")
    return out


def _teamwork(world: World) -> list[str]:
    out: list[str] = []
    for e in world.characters():
        if e.memes.get("shared_plan", 0) >= THRESHOLD and e.memes.get("friendship", 0) >= THRESHOLD:
            sig = ("teamwork", e.id)
            if sig in world.fired:
                continue
            world.fired.add(sig)
            e.memes["joy"] = e.memes.get("joy", 0) + 1
            out.append("They worked together with kind hearts.")
    return out


def propagate(world: World, narrate: bool = True) -> list[str]:
    produced: list[str] = []
    changed = True
    for _ in range(len(globals().get("CAUSAL_RULES", [])) + 4):
        changed = False
        for fn in (_clean_region, _teamwork):
            if fn is _clean_region:
                for actor in world.characters():
                    for act in [world.facts["activity"]]:
                        sents = fn(world, actor, act)
                        if sents:
                            changed = True
                            produced.extend(sents)
            else:
                sents = fn(world)
                if sents:
                    changed = True
                    produced.extend(sents)
    if narrate:
        for s in produced:
            world.say(s)
    return produced


def prize_at_risk(activity: Activity, prize: Prize) -> bool:
    return prize.region == "hands" and activity.mess == "smudge"


def select_gear(activity: Activity, prize: Prize) -> Optional[Gear]:
    for gear in GEAR:
        if activity.mess in gear.guards and prize.region in gear.covers:
            return gear
    return None


def build_story_name(seed: int) -> tuple[str, str]:
    girls = ["Anya", "Mira", "Lina", "Nora", "Sela", "Tia"]
