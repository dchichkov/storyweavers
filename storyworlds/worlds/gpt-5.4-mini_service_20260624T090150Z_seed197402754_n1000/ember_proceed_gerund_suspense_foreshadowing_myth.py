#!/usr/bin/env python3
"""
A standalone storyworld for a tiny mythic tale of an ember carried through dusk.

Premise:
- A small keeper must proceed on a journey with a living ember.
- The ember can be steadied or lost depending on the vessel, weather, and path.
- Suspense comes from the ember's fading heat and the long walk ahead.
- Foreshadowing comes from old signs: a crack in the sky, a warning raven, a
  remembered proverb, or a second ember waiting in reserve.

The world models physical meters and emotional memes. The story is generated by
simulating state transitions rather than swapping nouns into a frozen template.
"""

