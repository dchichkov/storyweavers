def generation_prompts(world: World) -> list[str]:
    keeper = world.facts["keeper"]
    vessel_def = world.facts["vessel_def"]
    return [
        "Write a short mythic story for a child about a keeper who must proceed with a living ember through the dark.",
        f"Tell a suspenseful folklore-style tale in which {keeper.id} carries an ember in {vessel_def.label} and learns whether it can survive the journey.",
        "Write a gentle myth with foreshadowing, a dangerous path, and a bright ending around an ember.",
    ]


def story_qa(world: World) -> list[QAItem]:
    keeper = world.facts["keeper"]
    vessel = world.facts["vessel"]
    qa = [
        QAItem(
            question=f"What was {keeper.id} carrying through the dark?",
            answer=f"{keeper.id} was carrying a living ember, hoping it would reach the hearth safely.",
        ),
        QAItem(
            question=f"Why was {vessel.label} important in the story?",
            answer=f"{vessel.label.capitalize()} mattered because it helped protect the ember on the journey.",
        ),
        QAItem(
            question="What made the middle of the story tense?",
            answer="The ember grew weaker on the road, so the keeper had to protect it before the night could take it away.",
        ),
    ]
    if world.facts["ember"].meters["glow"] >= EMBER_SAFE:
        qa.append(
            QAItem(
                question="How did the story end?",
                answer="The keeper arrived in time, and the ember was still bright enough to relight the hearth.",
            )
        )
    else:
        qa.append(
            QAItem(
                question="How did the keeper save the ember?",
                answer="The ember nearly faded, but the keeper sheltered it and found enough warmth to bring it back.",
            )
        )
    return qa


def world_knowledge_qa(world: World) -> list[QAItem]:
    return [
        QAItem(
            question="What is an ember?",
            answer="An ember is a small piece of fire that still glows after a bigger flame has died down.",
        ),
        QAItem(
            question="Why do people cover a fire from the wind?",
            answer="People cover a fire from the wind so the flame or glow does not go out too quickly.",
        ),
        QAItem(
            question="What is foreshadowing in a story?",
            answer="Foreshadowing is when a story gives an early sign that something important may happen later.",
        ),
        QAItem(
            question="What does suspense do in a story?",
            answer="Suspense makes the reader wonder what will happen next and feel worried or excited.",
        ),
    ]


def format_qa(sample: StorySample) -> str:
    lines = ["== (1) Generation prompts -- asks that would produce this story =="]
    for i, p in enumerate(sample.prompts, 1):
        lines.append(f"{i}. {p}")
    lines.append("")
    lines.append("== (2) Story questions -- answerable from the story text ==")
    for item in sample.story_qa:
        lines.append(f"Q: {item.question}")
        lines.append(f"A: {item.answer}")
    lines.append("")
    lines.append("== (3) World-knowledge questions -- child level, no story needed ==")
    for item in sample.world_qa:
        lines.append(f"Q: {item.question}")
        lines.append(f"A: {item.answer}")
    return "\n".join(lines)


