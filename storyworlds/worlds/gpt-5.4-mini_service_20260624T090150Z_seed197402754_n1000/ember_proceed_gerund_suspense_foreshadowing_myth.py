ASP_RULES = r"""
place(Valley) :- setting(Valley).
place(Woods) :- setting(Woods).
place(River) :- setting(River).

ember_safe(P, V) :- setting(P), vessel(V), guards(V, wind), guards(V, rain).
ember_safe(P, V) :- setting(P), vessel(V), guards(V, wind), not rain_setting(P).
ember_risky(P, V) :- setting(P), vessel(V), not ember_safe(P, V).

story_valid(P, V) :- setting(P), vessel(V), affords(P, carry), ember_risky(P, V).
"""


def asp_facts() -> str:
    import asp

    lines: list[str] = []
    for sid, s in SETTINGS.items():
        lines.append(asp.fact("setting", sid))
        lines.append(asp.fact("weather", sid, s.weather))
        if s.dark:
            lines.append(asp.fact("dark", sid))
        for a in sorted(s.affords):
            lines.append(asp.fact("affords", sid, a))
    for vid, v in VESSELS.items():
        lines.append(asp.fact("vessel", vid))
        for g in sorted(v.guards):
            lines.append(asp.fact("guards", vid, g))
        if v.covers_glow:
            lines.append(asp.fact("covers_glow", vid))
        if v.safe_in_wind:
            lines.append(asp.fact("safe_in_wind", vid))
        if v.safe_in_rain:
            lines.append(asp.fact("safe_in_rain", vid))
    return "\n".join(lines)


def asp_program(show: str) -> str:
    return f"{asp_facts()}\n{ASP_RULES}\n{show}\n"


def asp_valid_combos() -> list[tuple]:
    import asp
    model = asp.one_model(asp_program("#show story_valid/2."))
    return sorted(set(asp.atoms(model, "story_valid")))


def valid_combos() -> list[tuple[str, str]]:
    out = []
    for place, setting in SETTINGS.items():
        if "carry" not in setting.affords:
            continue
        for vid, v in VESSELS.items():
            risky = not (v.safe_in_wind and (v.safe_in_rain or setting.weather != "rainy"))
            if risky:
                out.append((place, vid))
    return out


def asp_verify() -> int:
    py = set(valid_combos())
    cl = set(asp_valid_combos())
    if py == cl:
        print(f"OK: clingo gate matches valid_combos() ({len(py)} combos).")
        return 0
    print("MISMATCH between clingo and valid_combos():")
    if py - cl:
        print("  only in python:", sorted(py - cl))
    if cl - py:
        print("  only in clingo:", sorted(cl - py))
    return 1


