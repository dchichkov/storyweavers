def build_world(params: StoryParams) -> World:
    setting = copy.deepcopy(SETTINGS[params.setting])
    setting.weather = params.weather
    world = World(setting)

    keeper_type, keeper_kind, keeper_name = next(x for x in KEEPERS if x[0] == params.keeper)
    helper_type, helper_kind, helper_label = next(x for x in HELPERS if x[0] == params.helper)
    vessel_def = VESSELS[params.vessel]

    keeper = world.add(Entity(
        id=keeper_name,
        kind="character",
        type=keeper_type,
        label=keeper_name,
        meters={"glow": 0.0},
        memes={"hope": 0.0, "fear": 0.0, "resolve": 0.0, "suspense": 0.0},
    ))
    helper = world.add(Entity(
        id="helper",
        kind="character",
        type=helper_type,
        label=helper_label,
        meters={},
        memes={"foresight": 1.0},
    ))
    ember = world.add(Entity(
        id="ember",
        kind="thing",
        type="ember",
        label="ember",
        phrase="a living ember",
        owner=keeper.id,
        caretaker=helper.id,
        carried_by=keeper.id,
        protected=False,
        meters={"glow": 3.0, "heat": 3.0, "ash": 0.0},
        memes={"hope": 1.0},
    ))
    vessel = world.add(Entity(
        id=vessel_def.id,
        kind="thing",
        type=vessel_def.id,
        label=vessel_def.label,
        phrase=vessel_def.phrase,
        owner=keeper.id,
        caretaker=keeper.id,
        carried_by=keeper.id,
        protected=True,
        meters={"glow": 0.0},
        memes={"steadiness": 1.0},
    ))

    world.facts.update(keeper=keeper, helper=helper, ember=ember, vessel=vessel, vessel_def=vessel_def)
    return world


def foreshadow(world: World) -> None:
    s = world.setting
    key = s.weather if s.weather in FORESHADOW_LINES else s.place.split()[-1]
    world.say(FORESHADOW_LINES.get(key, "The night seemed to lean toward the coming journey."))
    world.facts["foreshadowed"] = True


def open_story(world: World) -> None:
    keeper = world.facts["keeper"]
    world.say(
        f"{random.choice(MYTH_OPENINGS)} {keeper.id} was the one chosen to carry a single ember across {world.setting.place}."
    )
    world.say(
        f"{keeper.id} loved the ember, because the village hearth had gone cold and all the little lamps had failed."
    )


def helper_warning(world: World) -> None:
    helper = world.facts["helper"]
    vessel = world.facts["vessel"]
    keeper = world.facts["keeper"]
    world.say(
        f"{helper.label.capitalize()} looked at {vessel.label} and said, "
        f'"Do not hurry. A small fire can die if it is treated like a toy."'
    )
    keeper.memes["fear"] += 1.0
    keeper.memes["suspense"] += 1.0


def prepare_vessel(world: World) -> None:
    vessel = world.facts["vessel"]
    keeper = world.facts["keeper"]
    ember = world.facts["ember"]
    vessel.carried_by = keeper.id
    ember.carried_by = keeper.id
    world.say(f"{keeper.id} {world.facts['vessel_def'].carries}, then pressed both hands around it.")
    if vessel.id == "lantern":
        world.say("The glass held the light in a tiny golden prison, safe from the wind.")
    elif vessel.id == "bowl":
        world.say("The bowl was open, and the ember seemed brave but exposed.")
    else:
        world.say("The pine lid covered the glow, but the rain could still find a way in.")


def proceed(world: World) -> None:
    keeper = world.facts["keeper"]
    ember = world.facts["ember"]
    vessel = world.facts["vessel"]
    setting = world.setting

    # Physical drift
    if setting.weather == "windy":
        ember.meters["heat"] -= 0.9
        ember.meters["glow"] -= 0.7
        keeper.memes["suspense"] += 1.0
    elif setting.weather == "rainy":
        ember.meters["heat"] -= 1.1
        ember.meters["glow"] -= 0.9
        keeper.memes["fear"] += 1.0

    if vessel.id == "bowl":
        ember.meters["glow"] -= 0.4
    elif vessel.id == "pinebox":
        ember.meters["heat"] -= 0.2
    else:
        ember.meters["glow"] += 0.2

    if ember.meters["glow"] < 2.0:
        world.say(random.choice(SUSPENSE_LINES))
    if ember.meters["glow"] <= EMBER_GLOW_MIN:
        keeper.memes["fear"] += 1.0
        world.say(f"{keeper.id} could see the ember paling, like a star going behind a cloud.")


def helper_compassion(world: World) -> None:
    helper = world.facts["helper"]
    keeper = world.facts["keeper"]
    ember = world.facts["ember"]
    vessel = world.facts["vessel"]
    if vessel.id == "bowl":
        world.say(
            f"{helper.label.capitalize()} walked near and shielded the bowl with a cloak, making a small wall against the wind."
        )
        ember.meters["glow"] += 0.9
    elif vessel.id == "pinebox":
        world.say(
            f"{helper.label.capitalize()} whispered an old charm and held the lid steady so the ember would not leap out."
        )
        ember.meters["heat"] += 0.7
    else:
        world.say(
            f"{helper.label.capitalize()} touched the lantern once and nodded, knowing the glass would keep the flame from the storm."
        )
        ember.meters["glow"] += 0.5
    keeper.memes["hope"] += 1.0


def resolve(world: World) -> None:
    keeper = world.facts["keeper"]
    ember = world.facts["ember"]
    vessel = world.facts["vessel"]
    if ember.meters["glow"] >= EMBER_SAFE:
        keeper.memes["resolve"] += 1.0
        world.say(
            f"At last, the path ended, and {keeper.id} lifted the vessel toward the dark house waiting at the edge of {world.setting.place}."
        )
        world.say(
            f"The ember still lived, bright enough to wake the hearth."
        )
        world.say(
            f"{random.choice(MYTH_ENDINGS)}"
        )
    else:
        world.say(
            f"The ember had nearly gone out, but {keeper.id} cupped the last warmth until a coal could be found."
        )
        world.say(
            "Even then, the little fire remembered the road and learned to glow again."
        )


def tell_story(world: World) -> None:
    open_story(world)
    world.para()
    foreshadow(world)
    helper_warning(world)
    prepare_vessel(world)
    proceed(world)
    helper_compassion(world)
    proceed(world)
    world.para()
    resolve(world)


