# Reasonableness gate
# ---------------------------------------------------------------------------
def prize_at_risk(activity: Activity, prize: Prize) -> bool:
    return prize.bearer in {"arm", "shoulders"}


def select_remedy(activity: Activity, prize: Prize) -> Optional[Remedy]:
    for r in REMEDIES:
        if activity.id in r.softens:
            return r
    return None


def valid_stories() -> list[tuple[str, str, str, str]]:
    out = []
    for place, setting in SETTINGS.items():
        for act_id in setting.affords:
            act = _safe_lookup(ACTIVITIES, act_id)
            for prize_id, prize in PRIZES.items():
                if prize_at_risk(act, prize) and select_remedy(act, prize):
                    for gender in prize.genders:
                        out.append((place, act_id, prize_id, gender))
    return out


