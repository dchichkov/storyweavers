def _do_activity(world: World, actor: Entity, activity: Activity, narrate: bool = True) -> None:
    actor.meters[activity.id] = actor.meters.get(activity.id, 0.0) + 1
    actor.memes["eager"] = actor.memes.get("eager", 0.0) + 1
    if narrate:
        world.say(f"{actor.name_word()} began {activity.gerund}.")
    if activity.id == "forge":
        actor.meters["heat"] = actor.meters.get("heat", 0.0) + 1
        actor.memes["worry"] = actor.memes.get("worry", 0.0) + 1


def predict(world: World, actor: Entity, activity: Activity, prize_id: str) -> dict:
    sim = world.copy()
    _do_activity(sim, sim.get(actor.id), activity, narrate=False)
    prize = sim.get(prize_id)
    return {
        "risk": prize_at_risk(activity, prize),
        "stains": activity.id in {"forge"},
    }


def tell(setting: Setting, activity: Activity, prize_cfg: Prize,
         hero_name: str, hero_type: str, trait: str, parent_type: str) -> World:
    world = World(setting)
    hero = world.add(Entity(id=hero_name, kind="character", type=hero_type, label=hero_name))
    parent = world.add(Entity(id="Parent", kind="character", type=parent_type, label=f"the {parent_type}"))
    prize = world.add(Entity(id="Prize", kind="thing", type=prize_cfg.type, label=prize_cfg.label,
                             phrase=prize_cfg.phrase, owner=hero.id, caretaker=parent.id))
    remedy = None

    hero.memes["love"] = 1
    world.say(f"{hero.id} was a {trait} little {hero.type} who lived near {setting.place}.")
    world.say(f"{hero.id} loved {activity.gerund}, and the old {setting.place} listened like a stone ear.")
    world.say(f"One dusk, {hero.id}'s {parent_type} gave {hero.id} {prize_cfg.phrase}.")
    world.say(f"{hero.id} cherished the {prize.label}, and wore it as if it were a bright sign from the gods.")

    world.para()
    world.say(f"Then {hero.id} went to {setting.place} to {activity.verb}.")
    world.say(f"{hero.id} wanted to {activity.verb}, but {hero.id}'s {parent_type} saw danger in the old place.")

    pred = predict(world, hero, activity, prize.id)
    if pred["risk"]:
        world.say(f'"If you {activity.verb}, your {prize.label} may be ruined," said the {parent_type}.')
        hero.memes["defiance"] = 1
        world.say(f"{hero.id} reached for the {activity.rush}, but the warning stayed in the air like smoke.")
        remedy = select_remedy(activity, prize)
        if not remedy:
            pass
        world.para()
        world.say(f"Yet the {parent_type} was not cruel.")
        world.say(f"{hero.id}'s {parent_type} chose kindness and said, \"Let us {remedy.prep}.\"")
        world.say(f"{hero.id} softened, for kindness is a stronger spell than scolding.")
        hero.memes["joy"] = hero.memes.get("joy", 0.0) + 1
        hero.memes["kindness"] = hero.memes.get("kindness", 0.0) + 1
        remedy_ent = world.add(Entity(id=remedy.id, kind="thing", type="remedy", label=remedy.label))
        remedy_ent.worn_by = hero.id
        world.say(f"They {remedy.tail}, and soon {hero.id} could still {activity.verb} without harm.")
        world.say(f"At last, the {prize.label} stayed clean, and the stockade felt gentler than before.")
    world.facts.update(hero=hero, parent=parent, prize=prize, remedy=remedy, activity=activity, setting=setting)
    return world


