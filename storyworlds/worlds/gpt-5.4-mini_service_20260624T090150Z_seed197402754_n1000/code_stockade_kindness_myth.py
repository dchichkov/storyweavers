def asp_facts() -> str:
    import asp
    lines: list[str] = []
    for sid, s in SETTINGS.items():
        lines.append(asp.fact("setting", sid))
        for a in sorted(s.affords):
            lines.append(asp.fact("affords", sid, a))
    for aid, a in ACTIVITIES.items():
        lines.append(asp.fact("activity", aid))
        for tag in sorted(a.tags):
            lines.append(asp.fact("tag", aid, tag))
        # all activities here threaten the bearer item
        lines.append(asp.fact("splashes", aid, "arm"))
        lines.append(asp.fact("splashes", aid, "shoulders"))
    for pid, p in PRIZES.items():
        lines.append(asp.fact("prize", pid))
        lines.append(asp.fact("prize_on", pid, p.bearer))
        for g in sorted(p.genders):
            lines.append(asp.fact("wears", g, pid))
    for r in REMEDIES:
        lines.append(asp.fact("remedy", r.id))
        for a in sorted(r.softens):
            lines.append(asp.fact("softens", r.id, a))
    return "\n".join(lines)


def asp_program(show: str) -> str:
    return f"{asp_facts()}\n{ASP_RULES}\n{show}\n"


