def generation_prompts(world: World) -> list[str]:
    f = world.facts
    hero, parent, prize, activity = f["hero"], f["parent"], f["prize"], f["activity"]
    return [
        f"Write a short myth for children about {hero.id}, the {activity.keyword}, and the old {world.setting.place}.",
        f"Tell a gentle story where {hero.id} wants to {activity.verb} but the {parent.type} worries about the {prize.label}.",
        f"Write a story that includes kindness, a stockade, and a choice that keeps a treasured object safe.",
    ]


def story_qa(world: World) -> list[QAItem]:
    f = world.facts
    hero, parent, prize, activity = f["hero"], f["parent"], f["prize"], f["activity"]
    qa = [
        QAItem(
            question=f"Who was the story about?",
            answer=f"It was about {hero.id}, a {hero.pronoun('subject')} who lived near {world.setting.place} and loved {activity.gerund}.",
        ),
        QAItem(
            question=f"What did {hero.id} want to do at the stockade?",
            answer=f"{hero.id} wanted to {activity.verb}.",
        ),
        QAItem(
            question=f"Why did the {parent.type} worry about the {prize.label}?",
            answer=f"Because if {hero.id} kept {activity.gerund}, the {prize.label} could be ruined.",
        ),
    ]
    if f.get("remedy"):
        rem = _safe_fact(world, f, "remedy")
        qa.append(
            QAItem(
                question="How did kindness help in the end?",
                answer=f"The {parent.type} chose kindness and offered {rem.label}, so {hero.id} could keep playing while the {prize.label} stayed safe.",
            )
        )
    return qa


def world_knowledge_qa(world: World) -> list[QAItem]:
    return [
        QAItem(
            question="What is a stockade?",
            answer="A stockade is a strong fence or wall made from wooden posts that helps protect a place.",
        ),
        QAItem(
            question="What does kindness mean?",
            answer="Kindness means choosing to help, comfort, or be gentle with someone instead of being harsh.",
        ),
        QAItem(
            question="What is a code?",
            answer="A code is a set of rules or signs that people use to guide what they do.",
        ),
    ]


def format_qa(sample: StorySample) -> str:
    lines = ["== Generation prompts =="]
    for i, p in enumerate(sample.prompts, 1):
        lines.append(f"{i}. {p}")
    lines.append("")
    lines.append("== Story Q&A ==")
    for item in sample.story_qa:
        lines.append(f"Q: {item.question}")
        lines.append(f"A: {item.answer}")
    lines.append("")
    lines.append("== World Q&A ==")
    for item in sample.world_qa:
        lines.append(f"Q: {item.question}")
        lines.append(f"A: {item.answer}")
    return "\n".join(lines)


def dump_trace(world: World) -> str:
    lines = ["--- world model trace ---"]
    for e in list(world.entities.values()):
        meters = {k: v for k, v in e.meters.items() if v}
        memes = {k: v for k, v in e.memes.items() if v}
        bits = []
        if meters:
            bits.append(f"meters={meters}")
        if memes:
            bits.append(f"memes={memes}")
        if e.kind == "thing" and e.label:
            bits.append(f"label={e.label}")
        lines.append(f"  {e.id}: {e.type} {' '.join(bits)}")
    return "\n".join(lines)


