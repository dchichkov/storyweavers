def asp_facts() -> str:
    import asp
    lines = []
    for p in PLACES:
        lines.append(asp.fact("place", p))
    for a in AVATARS:
        lines.append(asp.fact("avatar", a))
    for b in BIASES:
        lines.append(asp.fact("bias", b))
    return "\n".join(lines)


def asp_program(show: str) -> str:
    return f"{asp_facts()}\n{ASP_RULES}\n{show}\n"


def valid_combos() -> list[tuple[str, str, str]]:
    return [(p, a, b) for p in PLACES for a in AVATARS for b in BIASES]


