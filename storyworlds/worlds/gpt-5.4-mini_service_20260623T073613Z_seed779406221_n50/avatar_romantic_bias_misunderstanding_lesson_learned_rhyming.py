def resolve_params(args: argparse.Namespace, rng: random.Random) -> StoryParams:
    combos = [c for c in valid_combos()
              if (getattr(args, "place", None) is None or c[0] == getattr(args, "place", None))
              and (getattr(args, "avatar", None) is None or c[1] == getattr(args, "avatar", None))
              and (getattr(args, "bias", None) is None or c[2] == getattr(args, "bias", None))]
    if not combos:
        return _fallback_storyparams(args, rng, StoryParams, globals())
    place, avatar, bias = rng.choice(list(combos))
    return StoryParams(
        place=place,
        avatar=avatar,
        bias=bias,
        name=getattr(args, "name", None) or rng.choice(NAMES),
        trait=getattr(args, "trait", None) or rng.choice(TRAITS),
    )


def tell(place: Place, avatar: Avatar, bias: Bias, name: str, trait: str) -> World:
    world = World(place)
    child = world.add(Entity(id=name, kind="character", type="child", label=name, role="listener"))
    other = world.add(Entity(id="Other", kind="character", type="child", label="the other child", role="speaker"))
    child.memes["bias"] += 1
    child.memes["wonder"] += 1

    world.say(f"In {place.label}, {name} went to play and listen, light as a kite.")
    world.say(f"There shone {avatar.label}, with {avatar.style} grace, and {avatar.kindness} in sight.")
    world.say(f"{avatar.label.capitalize()} gave {avatar.gift} and {avatar.spark}, so soft and bright.")
    world.para()
    world.say(f"But {name} had {bias.label} in mind, a cloudy little fright.")
    world.say(f"{name} thought, '{bias.worry},' and frowned at the sparkling light.")
    child.memes["misunderstanding"] += 1
    child.memes["doubt"] += 1
    world.para()
    world.say(f"{avatar.label.capitalize()} smiled and said, 'Let's talk a bit; let's make it right.'")
    world.say(f"{name} asked one kind small question, and the shadows lost their bite.")
    child.memes["lesson"] += 1
    child.memes["bias"] = 0
    child.memes["trust"] += 1
    child.memes["joy"] += 1
    world.para()
    world.say(f"Then {name} saw the true kind heart behind the glowing sight,")
    world.say(f"and learned that asking, listening, can turn a dark to light.")
    world.say(f"So {name} and {avatar.label} waved goodbye, all calm and feeling right.")
    world.facts.update(place=place, avatar=avatar, bias=bias, child=child, other=other,
                       lesson=True)
    return world


