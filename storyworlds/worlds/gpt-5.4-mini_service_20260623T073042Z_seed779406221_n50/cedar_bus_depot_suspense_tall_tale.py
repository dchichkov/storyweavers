"""
storyworlds/worlds/cedar_bus_depot_suspense_tall_tale.py
========================================================

A standalone storyworld for a small tall-tale suspense at a bus depot.

Seed tale imagination:
---
At the bus depot, a child and a grown-up wait beneath a great cedar beam that
hangs over the platform like an old tree. Every evening the depot clock ticks
loud, a missing bus threatens to strand the last riders, and somebody swears
the cedar beam "knows" when the bus will come. The child wants to dash off into
the dark platform maze, but the grown-up insists on a careful plan: listen for
the engine, watch the sign board, and stay together.

A little suspense grows as the depot lights flicker, a suitcase rolls loose,
and the last bus seems late. Then the true turn arrives: the cedar beam is not
magic, but the wooden echo beneath it carries the engine's rumble farther than
the eye can see. The riders hear the bus before it appears, calm the worry, and
board just in time.

This script models that premise as state:
- physical meters: distance, loudness, light, waiting, late, rolling, etc.
- emotional memes: worry, patience, relief, trust, wonder, suspense.

The story is deliberately small, child-facing, and state-driven. A few valid
variants are generated by changing the depot detail, the waiting roles, and the
approach to the suspenseful cue while preserving a clear beginning, middle turn,
and ending image.
"""

