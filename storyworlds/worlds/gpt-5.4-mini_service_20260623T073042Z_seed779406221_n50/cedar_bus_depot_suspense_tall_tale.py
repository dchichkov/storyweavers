def _r_echo(world: World) -> list[str]:
    out = []
    if world.facts.get("suspense", 0) < THRESHOLD:
        return out
    for bus in list(world.entities.values()):
        if bus.type != "bus":
            continue
        if bus.meters["approach"] < THRESHOLD:
            continue
        sig = ("echo", bus.id)
        if sig in world.fired:
            continue
        world.fired.add(sig)
        world.facts["heard_before_seen"] = True
        out.append("The cedar beam carried the engine's rumble farther than the eyes could look.")
    return out


def _r_relief(world: World) -> list[str]:
    out = []
    if world.facts.get("bus_arrived") and not world.facts.get("boarded"):
        return out
    if world.facts.get("boarded"):
        for e in list(world.entities.values()):
            if e.kind == "character":
                e.memes["relief"] += 1
                e.memes["worry"] = 0
        sig = ("relief",)
        if sig not in world.fired:
            world.fired.add(sig)
            out.append("The waiting turned into relief.")
    return out


CAUSAL_RULES: list[Rule] = [
    Rule("echo", "physical", _r_echo),
    Rule("relief", "social", _r_relief),
]


def propagate(world: World, narrate: bool = True) -> list[str]:
    produced = []
    changed = True
    for _ in range(len(globals().get("CAUSAL_RULES", [])) + 4):
        changed = False
        for rule in CAUSAL_RULES:
            sents = rule.apply(world)
            if sents:
                changed = True
                produced.extend(sents)
    if narrate:
        for s in produced:
            world.say(s)
    return produced


def predict_arrival(world: World) -> dict:
    sim = world.copy()
    sim.get("bus").meters["approach"] += 1
    propagate(sim, narrate=False)
    return {
        "heard": sim.facts.get("heard_before_seen", False),
        "relief": sim.get("child").memes["relief"],
    }


def setup(world: World, child: Entity, grownup: Entity, scene: Scene, cue: SuspenseCue, bus: Bus) -> None:
    child.memes["worry"] += 1
    child.memes["trust"] += 1
    grownup.memes["patience"] += 1
    world.say(f"{child.id} and {grownup.id} waited at {world.depot.place}, under {scene.cedar}.")
    world.say(f"The depot had {scene.title}, {scene.sign}, and {scene.hush}.")
    world.say(f"{child.id} watched for {cue.label} while the last riders listened for {bus.label}.")


def build_tension(world: World, child: Entity, grownup: Entity, cue: SuspenseCue, bus: Bus) -> None:
    child.memes["suspense"] += 1
    world.say(f"Then {cue.phrase} drifted through the platform like a whisper in a cornfield.")
    world.say(f"{child.id} wanted to rush, but {grownup.id} held up a calm hand and said to stay put.")
    world.say(f"Nobody could see the bus yet, only its {cue.sound} promise somewhere beyond the dark.")


def turn(world: World, child: Entity, grownup: Entity, scene: Scene, cue: SuspenseCue, bus: Bus) -> None:
    pred = predict_arrival(world)
    world.facts["predicted_heard"] = pred["heard"]
    world.facts["predicted_relief"] = pred["relief"]
    world.say(f"{grownup.id} listened hard and pointed at the cedar beam.")
    world.say(f'"That wood carries sound," {grownup.id} said. "We will hear the bus before we see it."')
    bus.meters["approach"] += 1
    bus.meters["distance"] = 0
    world.facts["bus_arrived"] = True
    propagate(world, narrate=True)


def resolve(world: World, child: Entity, grownup: Entity, scene: Scene, bus: Bus) -> None:
    child.memes["worry"] = 0
    child.memes["relief"] += 1
    child.memes["wonder"] += 1
    world.facts["boarded"] = True
    world.say(f"At last the bus rounded the depot curve, shining yellow as a lantern.")
    world.say(f"{child.id} grinned at the cedar beam, which had seemed magical but was only wise old wood.")
    world.say(f"Together they climbed aboard before the doors sighed shut, and the depot lights glowed softly behind them.")


def tell(scene: Scene, cue: SuspenseCue, bus: Bus,
         child_name: str = "Maya", child_type: str = "girl",
         grownup_name: str = "Aunt June", grownup_type: str = "woman") -> World:
    world = World(Depot(place="the bus depot", affords={"wait", "listen", "board"}))
    child = world.add(Entity(id=child_name, kind="character", type=child_type, role="child"))
    grownup = world.add(Entity(id=grownup_name, kind="character", type=grownup_type, role="grownup"))
    cedar = world.add(Entity(id="cedar", type="thing", label="cedar beam", phrase=scene.cedar))
    sign = world.add(Entity(id="sign", type="thing", label="sign board", phrase=scene.sign))
    depot_bus = world.add(Entity(id="bus", type="bus", label="bus", phrase=bus.phrase))
    world.facts.update(child=child, grownup=grownup, scene=scene, cue=cue, bus_cfg=bus,
                       cedar=cedar, sign=sign, bus=depot_bus, suspense=0)
    setup(world, child, grownup, scene, cue, depot_bus)
    world.para()
    build_tension(world, child, grownup, cue, depot_bus)
    world.facts["suspense"] = 1
    turn(world, child, grownup, scene, cue, depot_bus)
    world.para()
    resolve(world, child, grownup, scene, depot_bus)
    return world


