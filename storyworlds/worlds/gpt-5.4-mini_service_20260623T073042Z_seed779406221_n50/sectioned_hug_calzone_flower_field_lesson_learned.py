def resolve_params(args: argparse.Namespace, rng: random.Random) -> StoryParams:
    combos = [c for c in valid_combos()
              if (getattr(args, "setting", None) is None or c[0] == getattr(args, "setting", None))
              and (getattr(args, "activity", None) is None or c[1] == getattr(args, "activity", None))
              and (getattr(args, "prize", None) is None or c[2] == getattr(args, "prize", None))]
    if not combos:
        return _fallback_storyparams(args, rng, StoryParams, globals())
    setting, activity, prize = rng.choice(list(combos))
    gender = getattr(args, "gender", None) or rng.choice(["girl", "boy"])
    name = getattr(args, "name", None) or rng.choice(GIRL_NAMES if gender == "girl" else BOY_NAMES)
    helper_gender = getattr(args, "helper_gender", None) or rng.choice(["girl", "boy"])
    helper_name = getattr(args, "helper_name", None) or rng.choice(GIRL_NAMES if helper_gender == "girl" else BOY_NAMES)
    trait = getattr(args, "trait", None) or rng.choice(TRAITS)
    return StoryParams(setting, activity, prize, name, gender, helper_name, helper_gender, trait)


def tell(params: StoryParams) -> World:
    world = World(_safe_lookup(SETTINGS, params.setting))
    child = world.add(Entity(id=params.name, kind="character", type=params.gender, label=params.name, role="child"))
    helper = world.add(Entity(id=params.helper_name, kind="character", type=params.helper_gender, label=params.helper_name, role="helper"))
    prize = world.add(Entity(id=params.prize, kind="thing", type="calzone", label="calzone"))
    child.meters["unease"] = 0.0
    child.memes["joy"] = 1.0
    helper.memes["calm"] = 1.0
    world.say(f"{child.id} arrived at {world.setting.place} with a sectioned map and a warm calzone.")
    world.say(f"{child.pronoun().capitalize()} was a {params.trait} adventurer who loved every neat path.")
    world.para()
    world.say(f"The flower field stretched wide, and the map had been sectioned into little squares so the child would not lose the way.")
    world.say(f"But carrying {prize.label_word if hasattr(prize, 'label_word') else 'the calzone'} made the walk feel tricky, because the prize was warm and soft.")
    child.meters["unease"] += 1
    propagate(world, narrate=False)
    world.para()
    world.say(f"Then {helper.id} came along and offered {helper.label}.")
    world.say(f'“Let’s slow down,” {helper.id} said. {helper.id.capitalize()} {HELPERS["hug"].ending}, and the worry felt smaller.')
    child.memes["joy"] += 1
    child.memes["trust"] += 1
    world.para()
    world.say(f"They chose to walk the sectioned path together, keeping the calzone safe and the flowers bright.")
    world.say(f"In the end, {child.id} took a careful bite, {helper.id} stayed beside them, and the field felt like a finished adventure.")
    world.facts.update(child=child, helper=helper, prize=prize, params=params)
    return world


