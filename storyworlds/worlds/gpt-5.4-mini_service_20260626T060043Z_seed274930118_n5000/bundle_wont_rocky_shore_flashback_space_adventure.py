def _r_bundle_damage(world: World) -> list[str]:
    out: list[str] = []
    for actor in world.characters():
        if actor.meters.get("force", 0.0) < THRESHOLD:
            continue
        bundle = world.entities.get("bundle")
        if not bundle or bundle.meters.get("sealed", 0.0) < THRESHOLD:
            continue
        sig = ("damage", actor.id)
        if sig in world.fired:
            continue
        world.fired.add(sig)
        bundle.meters["scratched"] = bundle.meters.get("scratched", 0.0) + 1
        out.append("The bundle would get scratched.")
    return out


def _r_worry(world: World) -> list[str]:
    out: list[str] = []
    bundle = world.entities.get("bundle")
    guide = world.entities.get("guide")
    if not bundle or not guide:
        return out
    if bundle.meters.get("scratched", 0.0) < THRESHOLD:
        return out
    sig = ("worry",)
    if sig in world.fired:
        return out
    world.fired.add(sig)
    guide.memes["worry"] = guide.memes.get("worry", 0.0) + 1
    out.append("That would worry the guide.")
    return out


CAUSAL_RULES = [
    Rule(name="bundle_damage", tag="physical", apply=_r_bundle_damage),
    Rule(name="worry", tag="social", apply=_r_worry),
]


def propagate(world: World, narrate: bool = True) -> list[str]:
    produced: list[str] = []
    changed = True
    for _ in range(len(globals().get("CAUSAL_RULES", [])) + 4):
        changed = False
        for rule in CAUSAL_RULES:
            sents = rule.apply(world)
            if sents:
                changed = True
                produced.extend(sents)
    if narrate:
        for s in produced:
            world.say(s)
    return produced


def activity_at_risk(activity: Activity, prize: Prize) -> bool:
    return prize.region in {"hands", "lap", "torso"}


def select_gear(activity: Activity, prize: Prize) -> Optional[Gear]:
    for gear in GEAR:
        if activity.mess in gear.guards and prize.region in gear.covers:
            return gear
    return None


def predict_damage(world: World, actor: Entity, activity: Activity, prize_id: str) -> dict:
    sim = world.copy()
    _do_activity(sim, sim.get(actor.id), activity, narrate=False)
    bundle = sim.entities.get(prize_id)
    return {"scratched": bool(bundle and bundle.meters.get("scratched", 0.0) >= THRESHOLD)}


def _do_activity(world: World, actor: Entity, activity: Activity, narrate: bool = True) -> None:
    if activity.id not in world.setting.affords:
        pass
    world.zone = set(activity.tags)
    actor.meters["force"] = actor.meters.get("force", 0.0) + 1
    actor.memes["excitement"] = actor.memes.get("excitement", 0.0) + 1
    propagate(world, narrate=narrate)


def intro(world: World, hero: Entity) -> None:
    world.say(f"{hero.id} was a little {hero.type} astronaut who loved quiet places between waves and stars.")


def loves(world: World, hero: Entity, activity: Activity) -> None:
    hero.memes["love"] = hero.memes.get("love", 0.0) + 1
    world.say(f"{hero.pronoun().capitalize()} loved {activity.gerund}, especially when the sea wind felt like a signal from space.")


def brings_bundle(world: World, guide: Entity, hero: Entity, prize: Entity) -> None:
    world.say(f"On the rocky shore, {guide.label} had brought {hero.pronoun('object')} {prize.phrase}.")


def wants(world: World, hero: Entity, activity: Activity, prize: Entity) -> None:
    hero.memes["desire"] = hero.memes.get("desire", 0.0) + 1
    world.say(f"{hero.id} wanted to {activity.verb}, but {hero.pronoun('possessive')} hands were already reaching for the bundle.")


def flashback(world: World, guide: Entity, prize: Entity) -> None:
    world.facts["flashback"] = True
    world.say(
        f"Then the guide had a flashback: the bundle had been tied with a careful knot before the trip, "
        f"because inside were tiny star maps and a moonstone compass."
    )
    world.say(
        f'"It wont open well if you yank it," {guide.label} said softly. "That little bundle needs patience."'
    )


def warn(world: World, guide: Entity, hero: Entity, activity: Activity, prize: Entity) -> None:
    if not predict_damage(world, hero, activity, prize.id)["scratched"]:
        return
    guide.memes["worry"] = guide.memes.get("worry", 0.0) + 1
    world.facts["predicted_damage"] = True
    world.say(
        f'"If you rush to {activity.verb}, you might scratch the {prize.label}," {guide.label} said. '
        f'"Let\'s not make the bundle wobble.' '"'
    )


def refuses(world: World, hero: Entity, activity: Activity) -> None:
    hero.memes["frustration"] = hero.memes.get("frustration", 0.0) + 1
    world.say(f"{hero.id} scuffed the sand and said they wont be careful forever, though their voice sounded small.")


def compromise(world: World, guide: Entity, hero: Entity, activity: Activity, prize: Entity) -> Optional[Gear]:
    gear = select_gear(activity, prize)
    if gear is None:
        return None
    g = world.add(Entity(
        id=gear.id,
        type="gear",
        label=gear.label,
        protective=True,
        plural=gear.plural,
        owner=hero.id,
    ))
    g.worn_by = hero.id
    if predict_damage(world, hero, activity, prize.id)["scratched"]:
        g.worn_by = None
        del world.entities[g.id]
        return None
    world.say(
        f'{guide.label} smiled and said, "How about we {gear.prep} before you {activity.verb}?"'
    )
    return gear


def accept(world: World, guide: Entity, hero: Entity, activity: Activity, prize: Entity, gear: Gear) -> None:
    hero.memes["joy"] = hero.memes.get("joy", 0.0) + 1
    hero.memes["frustration"] = 0.0
    world.say(f"{hero.id} nodded and tucked the bundle into {gear.label} just long enough to keep it safe.")
    world.say(
        f"They {gear.tail}. Soon {hero.id} was {activity.gerund}, the bundle stayed smooth, and the moonstone compass gleamed like a tiny planet."
    )


