    world.say(setting.detail)
    world.say(f"{hero.id} wanted to {activity.verb}, but {parent.label} warned that the route {activity.risk}.")
    pred = predict_mess(world, hero, activity, prize.id)
    if pred["tangled"]:
        world.say(f'"If you rush," {parent.label} said, "your {prize.label} will tangle, and the diller may get scared."')
