    hero.memes["trust"] += 1
    diller.memes["joy"] += 1
    hero.memes["joy"] += 1
    world.para()
    world.say(f"Then {hero.id} and {diller.label} went on together, {activity.gerund}, with the ship curving through the dark like a silver snake.")
