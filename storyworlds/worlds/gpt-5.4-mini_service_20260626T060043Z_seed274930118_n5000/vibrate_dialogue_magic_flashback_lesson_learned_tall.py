class World:
    def __init__(self, setting: Setting) -> None:
        self.setting = setting
        self.entities: dict[str, Entity] = {}
        self.fired: set[tuple] = set()
        self.paragraphs: list[list[str]] = [[]]
        self.zone: set[str] = set()
        self.weather: str = ""
        self.facts: dict = {}

    def add(self, ent: Entity) -> Entity:
        self.entities[ent.id] = ent
        return ent

    def get(self, eid: str) -> Entity:
        if eid not in self.entities:
            label = str(eid).replace("_", " ")
            self.entities[eid] = Entity(str(eid), label=label)
        return self.entities[eid]

    def characters(self) -> list[Entity]:
        return [e for e in self.entities.values() if e.kind == "character"]

    def worn_items(self, actor: Entity) -> list[Entity]:
        return [e for e in self.entities.values() if e.worn_by == actor.id]

    def covered(self, actor: Entity, region: str) -> bool:
        return any(g.protective and region in g.covers for g in self.worn_items(actor))

    def say(self, text: str) -> None:
        if text:
            self.paragraphs[-1].append(text)

    def para(self) -> None:
        if self.paragraphs[-1]:
            self.paragraphs.append([])

    def render(self) -> str:
        return "\n\n".join(" ".join(p) for p in self.paragraphs if p)
    def copy(self):
        clone = __import__("copy").deepcopy(self)
        return clone


def _r_vibrate(world: World) -> list[str]:
    out: list[str] = []
    for actor in world.characters():
        if actor.meters.get("vibrate", 0.0) < THRESHOLD:
            continue
        for item in world.worn_items(actor):
            if item.protective or item.region not in world.zone:
                continue
            if world.covered(actor, item.region):
                continue
            sig = ("vibrate_soil", item.id)
            if sig in world.fired:
                continue
            world.fired.add(sig)
            item.meters["shaken"] = item.meters.get("shaken", 0.0) + 1
            item.meters["dirty"] = item.meters.get("dirty", 0.0) + 1
            out.append(f"{actor.pronoun('possessive').capitalize()} {item.label} rattled and got messed up.")
    return out


def _r_alarm(world: World) -> list[str]:
    out: list[str] = []
    for item in list(world.entities.values()):
        if item.meters.get("dirty", 0.0) < THRESHOLD or not item.caretaker:
            continue
        sig = ("alarm", item.id)
        if sig in world.fired:
            continue
        world.fired.add(sig)
        carer = world.get(item.caretaker)
        carer.memes["worry"] = carer.memes.get("worry", 0.0) + 1
        out.append(f"That would mean more work for {carer.label_word}.")
    return out


def _r_flashback(world: World) -> list[str]:
    out: list[str] = []
    for actor in world.characters():
        if actor.memes.get("remember", 0.0) < THRESHOLD:
            continue
        sig = ("flashback", actor.id)
        if sig in world.fired:
            continue
        world.fired.add(sig)
        actor.memes["sadness"] = actor.memes.get("sadness", 0.0) + 1
        out.append("__flashback__")
    return out


CAUSAL_RULES = [_r_vibrate, _r_alarm, _r_flashback]


def propagate(world: World, narrate: bool = True) -> list[str]:
    produced: list[str] = []
    changed = True
    for _ in range(len(globals().get("CAUSAL_RULES", [])) + 4):
        changed = False
        for rule in CAUSAL_RULES:
            sents = rule(world)
            if sents:
                changed = True
                produced.extend(s for s in sents if s not in {"__flashback__"})
    if narrate:
        for s in produced:
            world.say(s)
    return produced


def prize_at_risk(activity: Activity, prize: Prize) -> bool:
    return prize.region in activity.zone


def select_gear(activity: Activity, prize: Prize) -> Optional[Gear]:
    for gear in GEAR:
        if activity.mess in gear.guards and prize.region in gear.covers:
            return gear
    return None


def predict_mess(world: World, actor: Entity, activity: Activity, prize_id: str) -> dict:
    sim = world.copy()
    _do_activity(sim, sim.get(actor.id), activity, narrate=False)
    prize = sim.get(prize_id)
    return {
        "soiled": prize.meters.get("dirty", 0.0) >= THRESHOLD,
        "workload": sum(e.memes.get("worry", 0.0) for e in sim.characters()),
    }


def activity_color(activity: Activity) -> str:
    return {
        "bell": "a bright bronze note",
        "fiddle": "a long singing note",
        "lantern": "a humming golden glow",
        "drum": "a sky-thumping beat",
    }.get(activity.id, "a lively magical sparkle")


def _do_activity(world: World, actor: Entity, activity: Activity, narrate: bool = True) -> None:
    if activity.id not in world.setting.affords:
        return
    world.zone = set(activity.zone)
    actor.meters["vibrate"] = actor.meters.get("vibrate", 0.0) + 1
    actor.memes["joy"] = actor.memes.get("joy", 0.0) + 1
    propagate(world, narrate=narrate)


def flashback_line(hero: Entity, elder: Entity, activity: Activity, prize: Entity) -> str:
    return (
        f"Years ago, {elder.label_word} had heard that same magic hum, and back then "
        f"{prize.label} had danced off the table like a skittish sparrow."
    )


def introduce(world: World, hero: Entity, elder: Entity, activity: Activity) -> None:
    world.say(
        f"{hero.id} was a little {hero.type} with big eyes and a bigger grin, the sort "
        f"who could hear a cricket sneeze from three fences away."
    )
    world.say(
        f"{hero.id} loved {activity.gerund}, because {activity_color(activity)} made the whole day feel famous."
    )
    world.say(
        f"{hero.id} also loved a good {hero.label_word} story, especially when {elder.label_word} told one in a voice like thunder on velvet."
    )


def arrive(world: World, hero: Entity, elder: Entity, activity: Activity) -> None:
    day = {"rainy": "One rainy day, ", "sunny": "One sunny day, "}.get(world.weather, "One day, ")
    go = "were in" if world.setting.indoor else "went to"
    world.say(f"{day}{hero.id} and {elder.label_word} {go} {world.setting.place}.")
    world.say(f"The place was still as a hat on a nail, but the magic waiting there did not stay still for long.")


def wants(world: World, hero: Entity, elder: Entity, activity: Activity) -> None:
    hero.memes["desire"] = hero.memes.get("desire", 0.0) + 1
    world.say(
        f"{hero.id} wanted to {activity.verb} right away, and {hero.id} said, "
        f'"I can make it sing like a happy goose!"'
    )


def warn(world: World, elder: Entity, hero: Entity, activity: Activity, prize: Entity) -> bool:
    pred = predict_mess(world, hero, activity, prize.id)
    if not pred["soiled"]:
        return False
    world.facts["predicted_soil"] = activity.soil
    world.facts["predicted_workload"] = pred["workload"]
    world.say(
        f'"Careful," {elder.label_word} said. "That magic will make the {prize.label} shake loose."'
    )
    return True


def remembered(world: World, elder: Entity, hero: Entity, activity: Activity, prize: Entity) -> None:
    elder.memes["remember"] = elder.memes.get("remember", 0.0) + 1
    propagate(world, narrate=False)
    world.say(f"Then {elder.label_word} gave a flashback, and the room seemed to tip its hat to the past.")
    world.say(flashback_line(hero, elder, activity, prize))
    world.say(
        f'"That is why I am speaking plain," {elder.label_word} said. "I learned the hard way that magic is fine, but the table must be stout."'
    )


def defies(world: World, hero: Entity, activity: Activity) -> None:
    hero.memes["stubborn"] = hero.memes.get("stubborn", 0.0) + 1
    world.say(f"{hero.id} puffed out {hero.pronoun('possessive')} cheeks and said, \"I can do it my own way!\"")
    world.say(f"{hero.id} tried to {activity.rush}, and the magic began to tremble like a plow horse hearing fiddle music.")


def compromise(world: World, elder: Entity, hero: Entity, activity: Activity, prize: Entity) -> Optional[Gear]:
    gear_def = select_gear(activity, prize)
    if gear_def is None:
        return None
    gear = world.add(Entity(
        id=gear_def.id,
        type="gear",
        label=gear_def.label,
        owner=hero.id,
        caretaker=elder.id,
        protective=True,
        covers=set(gear_def.covers),
        plural=gear_def.plural,
    ))
    gear.worn_by = hero.id
    if predict_mess(world, hero, activity, prize.id)["soiled"]:
        gear.worn_by = None
        del world.entities[gear.id]
        return None
    world.say(
        f'Then {elder.label_word} smiled and said, "How about we {gear_def.prep} first?"'
    )
    return gear_def


def accept(world: World, elder: Entity, hero: Entity, activity: Activity, prize: Entity, gear_def: Gear) -> None:
    hero.memes["joy"] = hero.memes.get("joy", 0.0) + 1
    hero.memes["love"] = hero.memes.get("love", 0.0) + 1
    hero.memes["stubborn"] = 0.0
    world.say(
        f"{hero.id} nodded, because that old flashback had landed like a lesson in a lunch pail. "
        f'"All right," {hero.id} said. "I learned it."'
    )
    world.say(
        f"They used {gear_def.label} and let the magic {activity.id} with a safer hum. "
        f"Before long, {hero.id} was {activity.gerund}, {prize.label} stayed clean, and {elder.label_word} was laughing so hard the rafters seemed to grin."
    )
    world.say(
        f"That was the lesson learned: when magic vibrates like a fiddler on payday, the wise choice is to slow down and use the right cushion."
    )


def tell(setting: Setting, activity: Activity, prize_cfg: Prize, hero_name: str = "Milo", hero_type: str = "boy",
         hero_traits: Optional[list[str]] = None, elder_type: str = "grandmother") -> World:
    world = World(setting)
    world.weather = "" if setting.indoor else activity.weather

    hero = world.add(Entity(id=hero_name, kind="character", type=hero_type, label=hero_name))
    elder = world.add(Entity(id="Elder", kind="character", type=elder_type, label=f"the {elder_type}"))
    prize = world.add(Entity(
        id="prize",
        type=prize_cfg.type,
        label=prize_cfg.label,
        phrase=prize_cfg.phrase,
        owner=hero.id,
        caretaker=elder.id,
        region=prize_cfg.region,
        plural=prize_cfg.plural,
    ))

    introduce(world, hero, elder, activity)
    world.para()
    arrive(world, hero, elder, activity)
    wants(world, hero, elder, activity)
    warn(world, elder, hero, activity, prize)
    remembered(world, elder, hero, activity, prize)
    defies(world, hero, activity)
    world.para()
    gear_def = compromise(world, elder, hero, activity, prize)
    if gear_def:
        accept(world, elder, hero, activity, prize, gear_def)

    world.facts.update(hero=hero, elder=elder, prize=prize, activity=activity, setting=setting, gear=gear_def)
    return world


