def generation_prompts(world: World) -> list[str]:
    f = world.facts
    hero, council, act, prize = f["hero"], f["council"], f["activity"], f["prize"]
    return [
        f'Write a short rhyming story for a child about a council that must specify a safer plan before {act.keyword} fury blows in.',
        f"Tell a gentle cautionary tale where {hero.id} wants to {act.verb} but {council.label} worries about {prize.label}.",
        f"Write a story with a twist and foreshadowing, ending in a happy, safe choice under the windy sky.",
    ]


def story_qa(world: World) -> list[QAItem]:
    f = world.facts
    hero, council, prize, act = f["hero"], f["council"], f["prize"], f["activity"]
    trait = next((t for t in hero.traits if t != "little"), hero.type)
    qa = [
        QAItem(
            question=f"Who wanted to {act.verb} in the story?",
            answer=f"{hero.id}, a little {trait} {hero.type}, wanted to {act.verb}.",
        ),
        QAItem(
            question=f"Why did the council worry about {hero.id}'s {prize.label}?",
            answer=(
                f"The council worried because the windy fury could make the {prize.label} wet or worn, "
                f"so they wanted to specify a safer way first."
            ),
        ),
        QAItem(
            question="What was the twist in the story?",
            answer=(
                f"The twist was that the council did not simply say no; they offered {f['gear'].label if f.get('gear') else 'a safer helper'} "
                f"so {hero.id} could still play."
            ),
        ),
    ]
    if f.get("resolved"):
        qa.append(
            QAItem(
                question=f"How did {hero.id} get to play without ruining the {prize.label}?",
                answer=(
                    f"They used {f['gear'].label}, so {hero.id} could {act.verb} while the {prize.label} stayed safe and dry."
                ),
            )
        )
    return qa


def world_knowledge_qa(world: World) -> list[QAItem]:
    f = world.facts
    tags = set(f["activity"].tags)
    if f.get("gear"):
        tags.add(f["gear"].id)
    out: list[QAItem] = []
    for tag in ["council", "fury", "windy", "scarf", "hat", "hood"]:
        if tag in tags or tag in {"council", "fury", "windy"}:
            out.extend(QAItem(question=q, answer=a) for q, a in KNOWLEDGE.get(tag, []))
    return out


def format_qa(sample: StorySample) -> str:
    lines = ["== (1) Generation prompts =="]
    for i, p in enumerate(sample.prompts, 1):
        lines.append(f"{i}. {p}")
    lines.append("")
    lines.append("== (2) Story questions ==")
    for item in sample.story_qa:
        lines.append(f"Q: {item.question}")
        lines.append(f"A: {item.answer}")
    lines.append("")
    lines.append("== (3) World-knowledge questions ==")
    for item in sample.world_qa:
        lines.append(f"Q: {item.question}")
        lines.append(f"A: {item.answer}")
    return "\n".join(lines)


def dump_trace(world: World) -> str:
    lines = ["--- world model state ---"]
    for e in list(world.entities.values()):
        meters = {k: v for k, v in e.meters.items() if v}
        memes = {k: v for k, v in e.memes.items() if v}
        bits = []
        if meters:
            bits.append(f"meters={meters}")
        if memes:
            bits.append(f"memes={memes}")
        if e.protective:
            bits.append(f"covers={sorted(e.covers)}")
        if e.region:
            bits.append(f"region={e.region}")
        lines.append(f"  {e.id:8} ({e.type:7}) {' '.join(bits)}")
    lines.append(f"  fired rules: {sorted(n for n, *_ in world.fired)}")
    return "\n".join(lines)


