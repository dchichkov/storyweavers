def _r_fury_soak(world: World) -> list[str]:
    out: list[str] = []
    for actor in world.characters():
        if actor.memes["fury"] < THRESHOLD:
            continue
        for item in world.worn_items(actor):
            if item.protective:
                continue
            if item.region not in {"head", "torso", "hands"}:
                continue
            sig = ("soak", actor.id, item.id)
            if sig in world.fired:
                continue
            world.fired.add(sig)
            item.meters["wet"] += 1
            item.meters["torn"] += 1
            out.append(f"The fury made {actor.pronoun('possessive')} {item.label} wet and worn.")
    return out


def _r_worry(world: World) -> list[str]:
    out: list[str] = []
    for actor in world.characters():
        if actor.memes["worry"] < THRESHOLD:
            continue
        sig = ("worry", actor.id)
        if sig in world.fired:
            continue
        world.fired.add(sig)
        actor.memes["foreshadow"] += 1
        out.append(f"A cautionary cloud of worry hung over the day.")
    return out


CAUSAL_RULES: list[Rule] = [
    Rule("fury_soak", "physical", _r_fury_soak),
    Rule("worry", "social", _r_worry),
]


def propagate(world: World, narrate: bool = True) -> list[str]:
    produced: list[str] = []
    changed = True
    for _ in range(len(globals().get("CAUSAL_RULES", [])) + 4):
        changed = False
        for rule in CAUSAL_RULES:
            sents = rule.apply(world)
            if sents:
                changed = True
                produced.extend(sents)
    if narrate:
        for s in produced:
            world.say(s)
    return produced


def prize_at_risk(activity: Activity, prize: Prize) -> bool:
    return prize.region in activity.tags


def select_gear(activity: Activity, prize: Prize) -> Optional[Gear]:
    for gear in GEAR:
        if activity.danger in gear.guards and prize.region in gear.covers:
            return gear
    return None


def predict_risk(world: World, actor: Entity, activity: Activity, prize_id: str) -> dict:
    sim = world.copy()
    _do_activity(sim, sim.get(actor.id), activity, narrate=False)
    prize = sim.entities.get(prize_id)
    return {
        "ruined": bool(prize and (prize.meters["wet"] >= THRESHOLD or prize.meters["torn"] >= THRESHOLD)),
    }


def _do_activity(world: World, actor: Entity, activity: Activity, narrate: bool = True) -> None:
    if activity.id not in world.setting.affords:
        pass
    world.weather = activity.weather
    actor.memes["fury"] += 1
    actor.meters["windy"] += 1
    propagate(world, narrate=narrate)


