def caution(world: World, council: Entity, hero: Entity, activity: Activity, prize: Entity) -> bool:
    pred = predict_risk(world, hero, activity, prize.id)
    if not pred["ruined"]:
        return False
    hero.memes["worry"] += 1
    council.memes["trust"] += 1
    world.facts["predicted_ruin"] = True
    world.say(
        f'"Not now," said the council. "That {activity.keyword} fury may tug at your {prize.label}."'
    )
    world.say(
        f'"If we do not specify a wise way, your {prize.label} may get wet, and the day may turn gray."'
    )
    return True


def twist(world: World, hero: Entity, council: Entity, activity: Activity) -> None:
    hero.memes["twist"] += 1
    world.say(
        f"{hero.id} braced for a no, but the council had more in store: a safer chore."
    )


def offer_gear(world: World, hero: Entity, council: Entity, activity: Activity, prize: Entity) -> Optional[Gear]:
    gear_def = select_gear(activity, prize)
    if gear_def is None:
        return None
    gear = world.add(Entity(
        id=gear_def.id,
        type="gear",
        label=gear_def.label,
        owner=hero.id,
        caretaker=council.id,
        protective=True,
        covers=set(gear_def.covers),
        plural=gear_def.plural,
    ))
    gear.worn_by = hero.id
    if predict_risk(world, hero, activity, prize.id)["ruined"]:
        gear.worn_by = None
        del world.entities[gear.id]
        return None
    world.say(
        f'The twist was this: they gave {hero.id} {gear_def.label}, and that made the risky rhyme nice.'
    )
    world.say(
        f'"{gear_def.prep}," said the council, "and then you may {activity.verb} in a safer light."'
    )
    return gear_def


def resolve(world: World, hero: Entity, council: Entity, activity: Activity, prize: Entity, gear_def: Gear) -> None:
    hero.memes["joy"] += 1
    hero.memes["trust"] += 1
    hero.memes["worry"] = 0.0
    world.say(
        f"{hero.id} smiled a mile and gave the council a hug. The fear went snug."
    )
    world.say(
        f"They {gear_def.tail}. Soon {hero.id} was {activity.gerund}, and {prize.label} stayed dry and bright."
    )


