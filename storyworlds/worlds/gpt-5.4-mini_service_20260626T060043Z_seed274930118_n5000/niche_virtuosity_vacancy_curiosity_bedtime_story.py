def resolve_params(args: argparse.Namespace, rng: random.Random) -> StoryParams:
    if getattr(args, "activity", None) and getattr(args, "prize", None):
        act, pr = _safe_lookup(ACTIVITIES, getattr(args, "activity", None)), _safe_lookup(PRIZES, getattr(args, "prize", None))
        if not (prize_at_risk(act, pr) and select_gear(act, pr)):
            return _fallback_storyparams(args, rng, StoryParams, globals())
    combos = [c for c in valid_combos()
              if (getattr(args, "place", None) is None or c[0] == getattr(args, "place", None))
              and (getattr(args, "activity", None) is None or c[1] == getattr(args, "activity", None))
              and (getattr(args, "prize", None) is None or c[2] == getattr(args, "prize", None))]
    if not combos:
        return _fallback_storyparams(args, rng, StoryParams, globals())
    place, activity, prize = rng.choice(list(combos))
    gender = getattr(args, "gender", None) or rng.choice(["girl", "boy"])
    name = getattr(args, "name", None) or rng.choice(NAMES_GIRL if gender == "girl" else NAMES_BOY)
    parent = getattr(args, "parent", None) or rng.choice(["mother", "father"])
    trait = rng.choice(TRAITS)
    return StoryParams(place=place, activity=activity, prize=prize, name=name, gender=gender, parent=parent, trait=trait)


def _start_story(world: World, hero: Entity, parent: Entity, act: Activity) -> None:
    world.say(
        f"{hero.id} was a little {next(t for t in [hero.type,] if t)} who loved quiet nights and soft blankets."
    )
    world.say(
        f"At bedtime, {hero.id}'s thoughts kept wandering toward {act.keyword}, because curiosity made the room feel full of little questions."
    )
    world.say(
        f"Beside the bed, there was a tiny vacant niche in the wall, and that empty little space looked as if it were waiting for a secret."
    )


def _worry(world: World, hero: Entity, parent: Entity, act: Activity, prize: Entity) -> None:
    hero.memes["curiosity"] += 1
    hero.meters["wakefulness"] += 1
    world.para()
    world.say(
        f"{hero.id} wanted to {act.verb}, but {hero.pronoun('possessive')} {parent.type} worried that the search would {act.risk}."
    )
    world.say(
        f'"If we keep peeking, we might {act.consequence}," {parent.pronoun()} said softly.'
    )


def _offer(world: World, hero: Entity, parent: Entity, act: Activity, prize: Entity) -> Gear:
    gear = select_gear(act, prize)
    if gear is None:
        gear = next(iter(globals().get("GEARS", globals().get("GEAR", []))))
    hero.memes["worry"] += 1
    world.say(
        f'{parent.pronoun().capitalize()} pointed to a small lamp and said, "How about we {gear.prep} and take just one careful look?"'
    )
    return gear


def _peek(world: World, hero: Entity, parent: Entity, act: Activity, prize: Entity, gear: Gear) -> None:
    hero.memes["trust"] += 1
    hero.meters["wakefulness"] += 0.25
    hero.meters["sleepiness"] += 1
    world.para()
    world.say(
        f"{hero.id} agreed. Soon the room glowed with the tiny light, and {hero.id} {act.gerund}."
    )
    world.say(
        f"Inside the niche, the vacancy was not empty after all: a little wooden star had slipped behind a folded bedtime card."
    )
    world.say(
        f"{hero.id} lifted it out carefully, and the room felt calmer, as if the niche had found its true little purpose."
    )
    world.say(
        f"Then {hero.id} and {parent.pronoun('possessive')} {parent.type} {gear.tail}, and {hero.id} fell asleep with the star on the nightstand."
    )


def tell(setting: Setting, activity: Activity, prize: Prize, name: str, gender: str, parent_type: str, trait: str) -> World:
    world = World(setting)
    hero = world.add(Entity(id=name, kind="character", type="girl" if gender == "girl" else "boy"))
    parent = world.add(Entity(id="Parent", kind="character", type=parent_type))
    prize_ent = world.add(Entity(id="Sleep", type="sleep", label="sleep", phrase="sleep", caretaker=parent.id))
    hero.memes["curiosity"] = 1
    hero.meters["sleepiness"] = 0.75
    world.facts.update(hero=hero, parent=parent, prize=prize_ent, setting=setting, activity=activity, trait=trait)
    _start_story(world, hero, parent, activity)
    _worry(world, hero, parent, activity, prize_ent)
    gear = _offer(world, hero, parent, activity, prize_ent)
    _peek(world, hero, parent, activity, prize_ent, gear)
    world.facts["gear"] = gear
    world.facts["resolved"] = True
    return world


