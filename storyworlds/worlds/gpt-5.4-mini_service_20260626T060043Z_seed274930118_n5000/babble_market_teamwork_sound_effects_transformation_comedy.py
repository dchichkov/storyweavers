def worry(world: World, parent: Entity, display: Entity) -> None:
    parent.memes["worry"] += 1
    display.meters["wobble"] += 1
    world.say(
        f"{parent.id} frowned at the wiggly display. \"One more shake and that whole stack may scatter,\" {parent.pronoun()} warned."
    )


def team_up(world: World, child: Entity, helper: Entity, activity: Activity) -> None:
    child.memes["teamwork"] += 1
    helper.memes["teamwork"] += 1
    world.say(
        f"{child.id} and {helper.id} counted, leaned, and lifted together: one, two, heave!"
    )
    world.say(
        f"They shared the job so the {activity.keyword} noises could stay funny instead of becoming a mess."
    )


def accept_gear(world: World, child: Entity, parent: Entity, activity: Activity, prize: Entity, gear: Gear) -> None:
    child.memes["joy"] += 1
    world.say(
        f'{parent.id} grinned. "Let’s use {gear.label} first," {parent.pronoun()} said.'
    )
    world.say(
        f"They {gear.tail}. Then the {activity.keyword} sound effect had room to bounce without ruining {prize.label}."
    )


def finish(world: World, child: Entity, helper: Entity, display: Entity, prize: Entity, activity: Activity) -> None:
    if display.meters["transform"] >= THRESHOLD:
        world.say(
            f"At the end, the market stall wore a smiling face, the fruit tower stayed neat, and {child.id} was still babbling happily."
        )
        world.say(
            f"{helper.id} laughed beside {child.id}, because teamwork had turned a wobble into a joke everyone could enjoy."
        )


def tell(setting: Setting, activity: Activity, prize_cfg: Prize, name: str, gender: str, parent_type: str, helper_type: str) -> World:
    world = World(setting)
    child = world.add(Entity(id=name, kind="character", type=gender))
    parent = world.add(Entity(id="Parent", kind="character", type=parent_type))
    helper = world.add(Entity(id="Helper", kind="character", type=helper_type))
    display = world.add(Entity(id="Display", type=prize_cfg.type, label=prize_cfg.label, phrase=prize_cfg.phrase, region=prize_cfg.region))
    world.facts.update(child=child, parent=parent, helper=helper, display=display, activity=activity, prize_cfg=prize_cfg, gear=None)

    scene_opening(world, child, helper, display, setting, activity)
    world.para()
    make_sounds(world, child, activity)
    worry(world, parent, display)
    team_up(world, child, helper, activity)
    propagate(world, narrate=True)
    gear = _safe_fact((globals().get("world") or locals().get("world") or locals().get("mw") or locals().get("w")), world.facts, "gear")
    if gear:
        accept_gear(world, child, parent, activity, prize_cfg, gear)
    world.para()
    finish(world, child, helper, display, prize_cfg, activity)
    world.facts["display"] = display
    return world


