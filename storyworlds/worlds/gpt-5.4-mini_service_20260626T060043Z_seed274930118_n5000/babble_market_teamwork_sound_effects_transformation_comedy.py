def _r_teamwork(world: World) -> list[str]:
    out = []
    child = _safe_fact((globals().get("world") or locals().get("world") or locals().get("mw") or locals().get("w")), world.facts, "child")
    helper = _safe_fact((globals().get("world") or locals().get("world") or locals().get("mw") or locals().get("w")), world.facts, "helper")
    display = _safe_fact((globals().get("world") or locals().get("world") or locals().get("mw") or locals().get("w")), world.facts, "display")
    if child.memes["teamwork"] < THRESHOLD or helper.memes["teamwork"] < THRESHOLD:
        return out
    sig = ("teamwork", display.id)
    if sig in world.fired:
        return out
    world.fired.add(sig)
    display.meters["steady"] += 1
    display.meters["wobble"] = max(0.0, display.meters["wobble"] - 1)
    out.append("Together, their hands made the wobbly display stop dancing.")
    return out


def _r_transform(world: World) -> list[str]:
    out = []
    display = _safe_fact((globals().get("world") or locals().get("world") or locals().get("mw") or locals().get("w")), world.facts, "display")
    if display.meters["steady"] < THRESHOLD or display.meters["transform"] >= THRESHOLD:
        return out
    sig = ("transform", display.id)
    if sig in world.fired:
        return out
    world.fired.add(sig)
    display.meters["transform"] += 1
    display.label = "smiling fruit face"
    out.append("The plain display transformed into a smiling fruit face with a cheeky grin.")
    return out


def _r_laughter(world: World) -> list[str]:
    out = []
    child = _safe_fact((globals().get("world") or locals().get("world") or locals().get("mw") or locals().get("w")), world.facts, "child")
    display = _safe_fact((globals().get("world") or locals().get("world") or locals().get("mw") or locals().get("w")), world.facts, "display")
    if display.meters["transform"] < THRESHOLD:
        return out
    sig = ("laughter", child.id)
    if sig in world.fired:
        return out
    world.fired.add(sig)
    child.memes["joy"] += 1
    child.memes["laughter"] += 1
    out.append("That silly face made the child giggle so hard their shoulders bounced.")
    return out


