def tell(setting: Setting, activity: Activity, prize_cfg: Prize, hero_name: str,
         hero_type: str, hero_traits: Optional[list[str]] = None,
         parent_name: str = "Mom") -> World:
    world = World(setting)
    hero = world.add(Entity(
        id=hero_name, kind="character", type=hero_type,
        traits=["little"] + (hero_traits or ["gentle"]),
    ))
    parent = world.add(Entity(id=parent_name, kind="character", type="adult", label=parent_name))
    goat = world.add(Entity(id="Goat", kind="animal", type="goat", label="the goat",
                            traits=["belligerent"]))
    prize = world.add(Entity(
        id="basket", type=prize_cfg.type, label=prize_cfg.label, phrase=prize_cfg.phrase,
        owner=hero.id, caretaker=parent.id, region=prize_cfg.region,
    ))

    hero.memes["love"] = 1
    goat.memes["belligerence"] = 1

    world.say(
        f"{hero.id} was a little {hero.traits[1]} child who loved {activity.gerund} at {setting.place}."
    )
    world.say(
        f"{hero.id} carried {hero.pronoun('possessive')} {prize.label} carefully, because {hero.pronoun('possessive')} "
        f"hands were busy and the day felt important."
    )
    world.say(
        f"A belligerent little goat kept stamping nearby, and {hero.id} wanted to {activity.verb} anyway."
    )

    world.para()
    world.say(
        f"{hero.id} reached for the rope, but the goat planted its feet and gave a hard tug."
    )
    hero.memes["worry"] = 1
    hero.meters["tugged"] = 1
    world.say(
        f"{parent.id} frowned for a moment, then remembered a spring morning long ago."
    )
    world.say(
        f"In that flashback, the same goat had calmed down when someone spoke softly and moved slowly."
    )
    world.say(
        f"{parent.id} asked for {hero.id}'s input and said the kindest plan was to keep the rope gentle."
    )

    world.para()
    gear = select_gear(activity, prize)
    if gear is None:
        gear = next(iter(globals().get("GEARS", globals().get("GEAR", []))))
    world.say(
        f"{hero.id} nodded and tried {gear.prep}."
    )
    hero.memes["trust"] = 1
    goat.memes["calm"] = 1
    world.say(
        f"The goat sniffed the apple slices, stopped being belligerent, and walked beside them."
    )
    world.say(
        f"Soon {gear.tail}, and {hero.id} laughed because the basket stayed safe and the rope stayed loose."
    )
    world.say(
        f"{hero.id} and {parent.id} shared a warm smile, and the whole farmyard felt softer."
    )
    world.say(
        f"Soft rope, kind hope, and a happy group made the evening glow."
    )

    world.facts.update(
        hero=hero,
        parent=parent,
        goat=goat,
        prize=prize,
        activity=activity,
        setting=setting,
        gear=gear,
        resolved=True,
        flashback=True,
        rhyme=True,
    )
    return world


