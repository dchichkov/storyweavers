class World:
    def __init__(self, setting: Setting) -> None:
        self.setting = setting
        self.entities: dict[str, Entity] = {}
        self.fired: set[tuple] = set()
        self.zone: set[str] = set()
        self.paragraphs: list[list[str]] = [[]]
        self.facts: dict = {}
        self.flashback_used = False

    def add(self, ent: Entity) -> Entity:
        self.entities[ent.id] = ent
        return ent

    def get(self, eid: str) -> Entity:
        if eid not in self.entities:
            label = str(eid).replace("_", " ")
            self.entities[eid] = Entity(str(eid), label=label)
        return self.entities[eid]

    def characters(self) -> list[Entity]:
        return [e for e in self.entities.values() if e.kind == "character"]

    def worn_items(self, actor: Entity) -> list[Entity]:
        return [e for e in self.entities.values() if e.worn_by == actor.id]

    def covered(self, actor: Entity, region: str) -> bool:
        return any(g.region == region and g.meters.get("shield", 0) >= 1 for g in self.worn_items(actor))

    def say(self, text: str) -> None:
        if text:
            self.paragraphs[-1].append(text)

    def para(self) -> None:
        if self.paragraphs[-1]:
            self.paragraphs.append([])

    def render(self) -> str:
        return "\n\n".join(" ".join(p) for p in self.paragraphs if p)

    def copy(self) -> "World":
        import copy
        other = World(self.setting)
        other.entities = copy.deepcopy(self.entities)
        other.fired = set(self.fired)
        other.zone = set(self.zone)
        other.paragraphs = [[]]
        other.facts = dict(self.facts)
        other.flashback_used = self.flashback_used
        return other


THRESHOLD = 1.0


def _r_soil(world: World) -> list[str]:
    out: list[str] = []
    for actor in world.characters():
        if actor.meters.get("blow", 0) < THRESHOLD:
            continue
        for item in world.worn_items(actor):
            if item.region not in world.zone:
                continue
            if item.meters.get("shield", 0) >= 1:
                continue
            sig = ("soil", actor.id, item.id)
            if sig in world.fired:
                continue
            world.fired.add(sig)
            item.meters["dirty"] = item.meters.get("dirty", 0) + 1
            item.meters["soiled"] = item.meters.get("soiled", 0) + 1
            out.append(f"{actor.id}'s {item.label} got dusty by the water.")
    return out


def _r_help(world: World) -> list[str]:
    out: list[str] = []
    for ent in list(world.entities.values()):
        if ent.kind != "character":
            continue
        if ent.memes.get("worry", 0) < THRESHOLD:
            continue
        if ent.memes.get("hope", 0) >= THRESHOLD:
            sig = ("help", ent.id)
            if sig in world.fired:
                continue
            world.fired.add(sig)
            ent.memes["calm"] = ent.memes.get("calm", 0) + 1
            out.append(f"That made {ent.id} take a slower breath.")
    return out


CAUSAL_RULES = [_r_soil, _r_help]


def propagate(world: World, narrate: bool = True) -> list[str]:
    produced: list[str] = []
    changed = True
    for _ in range(len(globals().get("CAUSAL_RULES", [])) + 4):
        changed = False
        for rule in CAUSAL_RULES:
            sents = rule(world)
            if sents:
                changed = True
                produced.extend(sents)
    if narrate:
        for s in produced:
            world.say(s)
    return produced


def flashback(world: World, hero: Entity, action: Action, prize: Entity) -> None:
    if world.flashback_used:
        return
    world.flashback_used = True
    world.say(
        f"{hero.id} remembered the last launch: the rocket had wobbled, "
        f"and {prize.label} had nearly gone into the reeds. "
        f"That memory made {hero.pronoun('object')} think before {hero.pronoun()} blew again."
    )


def select_gear(action: Action, prize: Prize) -> Optional[Gear]:
    for g in GEAR:
        if action.mess in g.guards and prize.region in g.covers:
            return g
    return None


def prize_at_risk(action: Action, prize: Prize) -> bool:
    return prize.region in action.zone


def predict(world: World, hero: Entity, action: Action, prize: Entity) -> bool:
    sim = world.copy()
    _do_action(sim, sim.get(hero.id), action, narrate=False)
    return bool(sim.get(prize.id).meters.get("soiled", 0) >= THRESHOLD)


def _do_action(world: World, actor: Entity, action: Action, narrate: bool = True) -> None:
    actor.meters[action.mess] = actor.meters.get(action.mess, 0) + 1
    actor.meters["blow"] = actor.meters.get("blow", 0) + 1
    actor.memes["joy"] = actor.memes.get("joy", 0) + 1
    world.zone = set(action.zone)
    propagate(world, narrate=narrate)


def tell(setting: Setting, action: Action, prize_cfg: Prize, name: str, gender: str, guide: str, trait: str) -> World:
    world = World(setting)
    hero = world.add(Entity(id=name, kind="character", type=gender, traits=["little", trait]))
    guide_ent = world.add(Entity(id="Guide", kind="character", type=guide, label=f"the {guide}"))
    prize = world.add(Entity(
        id="prize", type=prize_cfg.type, label=prize_cfg.label, phrase=prize_cfg.phrase,
        owner=hero.id, caretaker=guide_ent.id, region=prize_cfg.region, plural=prize_cfg.plural
    ))
    gear_def = select_gear(action, prize_cfg)

    world.say(f"{hero.id} was a little {trait} {gender} who loved space stories.")
    world.say(f"{hero.id} also loved the riverbank, where the water glinted like a silver planet.")
    world.say(f"One day, {guide_ent.label} gave {hero.id} {prize.phrase}. {hero.id} loved {prize.it()}.")

    world.para()
    world.say(f"{hero.id} wanted to {action.verb} with a tiny rocket at the {setting.place}.")
    world.say(action.flashback_line)
    if predict(world, hero, action, prize):
        world.say(f"{guide_ent.label} frowned and said, \"If you {action.verb}, your {prize.label} will get {action.soil}.\"")
    hero.memes["worry"] = hero.memes.get("worry", 0) + 1
    flashback(world, hero, action, prize)
    world.say(f"{hero.id} looked at the breeze and held back for a moment.")

    world.para()
    if gear_def is None:
        pass
    gear = world.add(Entity(id=gear_def.id, type="gear", label=gear_def.label, plural=gear_def.plural))
    gear.worn_by = hero.id
    gear.meters["shield"] = 1
    world.say(
        f"{guide_ent.label} smiled. \"How about we {gear_def.prep} first?\""
    )
    hero.memes["hope"] = hero.memes.get("hope", 0) + 1
    world.say(f"{hero.id} nodded, and together they {gear_def.tail}.")
    _do_action(world, hero, action)
    world.say(
        f"At last, the rocket whooshed across the river air, {hero.id} cheered, "
        f"and {prize.label} stayed clean."
    )

    world.facts.update(hero=hero, guide=guide_ent, prize=prize, action=action, gear=gear, resolved=True)
    return world


