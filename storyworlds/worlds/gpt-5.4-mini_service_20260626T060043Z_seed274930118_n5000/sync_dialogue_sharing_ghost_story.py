@dataclass
class StoryParams:
    place: str
    activity: str
    prize: str
    name: str
    gender: str
    parent: str
    trait: str
    seed: Optional[int] = None
    @property
    def label_word(self) -> str:
        return str(getattr(self, "name", None) or getattr(self, "id", None) or getattr(self, "type", self.__class__.__name__.lower()))

    @property
    def label(self) -> str:
        return str(getattr(self, "name", None) or getattr(self, "id", None) or getattr(self, "type", self.__class__.__name__.lower()))

    @property
    def award_phrase(self) -> str:
        return str(getattr(self, "name", None) or getattr(self, "id", None) or getattr(self, "type", self.__class__.__name__.lower()))

    @property
    def phrase(self) -> str:
        return str(getattr(self, "_phrase", None) or str(getattr(self, "name", None) or getattr(self, "id", None) or getattr(self, "type", self.__class__.__name__.lower())))

    @phrase.setter
    def phrase(self, value: str) -> None:
        object.__setattr__(self, "_phrase", value)

    @property
    def meters(self):
        if "_meters" not in self.__dict__:
            object.__setattr__(self, "_meters", __import__("collections").defaultdict(float))
        return self._meters

    @property
    def memes(self):
        if "_memes" not in self.__dict__:
            object.__setattr__(self, "_memes", __import__("collections").defaultdict(float))
        return self._memes

    @property
    def tags(self):
        if "_tags" not in self.__dict__:
            object.__setattr__(self, "_tags", set())
        return self._tags

    def __getattr__(self, name: str):
        if name.startswith("__"):
            raise AttributeError(name)
        if name == "pronoun":
            return lambda case="subject": {"subject": "they", "object": "them", "possessive": "their"}.get(case, "they")
        if name in {"meters", "memes"}:
            value = __import__("collections").defaultdict(float)
            object.__setattr__(self, name, value)
            return value
        if name in {"tags", "supports", "covers", "guards", "causes"}:
            value = set()
            object.__setattr__(self, name, value)
            return value
        if name in {"phrase", "label_word", "award_phrase"}:
            return str(getattr(self, "label", None) or getattr(self, "name", None) or getattr(self, "id", ""))
        if name.startswith(("is_", "has_", "can_", "safe", "unsafe")):
            return False
        if name in {"comforting", "messy", "delivered", "sturdy", "protective", "broken", "wet"}:
            return False
        return ""


def valid_combos() -> list[tuple[str, str, str]]:
    combos = []
    for place, setting in SETTINGS.items():
        for act_id in setting.affords:
            act = _safe_lookup(ACTIVITIES, act_id)
            for prize_id, prize in PRIZES.items():
                if prize_at_risk(act, prize) and select_gear(act, prize):
                    combos.append((place, act_id, prize_id))
    return combos


def generation_prompts(world: World) -> list[str]:
    f = world.facts
    hero, guardian, act, prize = f["hero"], f["guardian"], f["activity"], f["prize_cfg"]
    return [
        f'Write a short ghost story for a small child that includes the word "{act.keyword}".',
        f"Tell a gentle story where {hero.id} and {guardian.id} try to {act.verb} while sharing {prize.phrase}.",
        f"Write a cozy spooky story with dialogue and sharing, ending with {hero.id} and {guardian.id} in sync.",
    ]


def story_qa(world: World) -> list[QAItem]:
    f = world.facts
    hero, guardian, prize, act = f["hero"], f["guardian"], f["prize"], f["activity"]
    trait = next((t for t in hero.traits if t != "little"), hero.type)
    qa = [
        QAItem(
            question=f"Who wanted to {act.verb} in the story?",
            answer=f"{hero.id} wanted to {act.verb} with {guardian.id}.",
        ),
        QAItem(
            question=f"What did {hero.id} and {guardian.id} share before they went on?",
            answer=f"They shared {prize.phrase} so it would stay safe during the night.",
        ),
        QAItem(
            question=f"Why did {guardian.id} warn {hero.id} about the {prize.label}?",
            answer=f"{guardian.id} warned {hero.id} because {prize.phrase} could get {act.soil} in that place.",
        ),
        QAItem(
            question=f"How did {hero.id} feel when the two of them finally made a plan?",
            answer=f"{trait.capitalize()} {hero.id} felt calm and close to {guardian.id} once they chose the safer way together.",
        ),
    ]
    if f.get("resolved"):
        gear = _safe_fact(world, f, "gear")
        qa.append(
            QAItem(
                question=f"What helped {hero.id} and {guardian.id} stay safe while they went back to {act.verb}?",
                answer=f"{gear.label} helped because it covered the right part and kept {prize.label} from getting messy.",
            )
        )
    return qa


def world_knowledge_qa(world: World) -> list[QAItem]:
    f = world.facts
    tags = set(f["activity"].tags)
    tags.add(f["gear"].id if f.get("gear") else "")
    out: list[QAItem] = []
    knowledge = {
        "sync": [("What does it mean to sync with someone?",
                  "To sync with someone means to move or speak at the same time, like matching steps or whispers.")],
        "whisper": [("What is a whisper?",
                      "A whisper is a very quiet way of talking.")],
        "music": [("What is a rhythm?",
                   "A rhythm is a pattern of beats that repeats, like tapping a table the same way again and again.")],
        "blanket": [("What is a blanket for?",
                     "A blanket keeps you warm and cozy.")],
        "gloves": [("What are gloves for?",
                    "Gloves keep your hands warm and clean.")],
        "bookcover": [("What does a bookcover do?",
                       "A bookcover helps protect a book from getting bent or dusty.")],
    }
    for tag, items in knowledge.items():
        if tag in tags:
            out.extend(QAItem(question=q, answer=a) for q, a in items)
    return out


def format_qa(sample: StorySample) -> str:
    lines = ["== (1) Generation prompts -- asks that would produce this story =="]
    for i, p in enumerate(sample.prompts, 1):
        lines.append(f"{i}. {p}")
    lines.append("")
    lines.append("== (2) Story questions -- answerable from the story text ==")
    for item in sample.story_qa:
        lines.append(f"Q: {item.question}")
        lines.append(f"A: {item.answer}")
    lines.append("")
    lines.append("== (3) World-knowledge questions -- child level, no story needed ==")
    for item in sample.world_qa:
        lines.append(f"Q: {item.question}")
        lines.append(f"A: {item.answer}")
    return "\n".join(lines)


def dump_trace(world: World) -> str:
    lines = ["--- world model state ---"]
    for e in list(world.entities.values()):
        meters = {k: v for k, v in e.meters.items() if v}
        memes = {k: v for k, v in e.memes.items() if v}
        bits = []
        if meters:
            bits.append(f"meters={dict(meters)}")
        if memes:
            bits.append(f"memes={dict(memes)}")
        if e.protective:
            bits.append(f"covers={sorted(e.covers)}")
        elif e.region:
            bits.append(f"region={e.region}")
        lines.append(f"  {e.id:10} ({e.type:7}) {' '.join(bits)}")
    lines.append(f"  fired rules: {sorted(set(n for n, *_ in world.fired))}")
    return "\n".join(lines)


