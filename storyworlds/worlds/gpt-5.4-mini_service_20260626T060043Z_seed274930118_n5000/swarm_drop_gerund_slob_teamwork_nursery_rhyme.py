def _r_slob_splat(world: World) -> list[str]:
    out: list[str] = []
    for actor in world.characters():
        if actor.meters.get("dropping", 0.0) < THRESHOLD:
            continue
        for item in world.worn_items(actor):
            if item.plural:
                continue
            sig = ("splat", actor.id, item.id)
            if sig in world.fired:
                continue
            world.fired.add(sig)
            item.meters["messy"] = item.meters.get("messy", 0.0) + 1.0
            item.meters["dirty"] = item.meters.get("dirty", 0.0) + 1.0
            out.append(f"{actor.pronoun('possessive').capitalize()} {item.label_word} got messy.")
    return out


def _r_caretaker_work(world: World) -> list[str]:
    out: list[str] = []
    for ent in list(world.entities.values()):
        if ent.meters.get("dirty", 0.0) < THRESHOLD or not ent.caretaker:
            continue
        sig = ("work", ent.id)
        if sig in world.fired:
            continue
        world.fired.add(sig)
        carer = world.get(ent.caretaker)
        carer.meters["work"] = carer.meters.get("work", 0.0) + 1.0
        out.append(f"That would make more work for {carer.label_word}.")
    return out


def _r_teamwork_fix(world: World) -> list[str]:
    out: list[str] = []
    swarm = world.entities.get("Swarm")
    if not swarm:
        return out
    if swarm.memes.get("help", 0.0) < THRESHOLD:
        return out
    for actor in world.characters():
        if actor.memes.get("worry", 0.0) < THRESHOLD:
            continue
        sig = ("teamwork", actor.id)
        if sig in world.fired:
            continue
        world.fired.add(sig)
        actor.memes["calm"] = actor.memes.get("calm", 0.0) + 1.0
        out.append("The swarm came in a song-like line and helped with teamwork.")
    return out


CAUSAL_RULES = [
    Rule("slob_splat", _r_slob_splat),
    Rule("caretaker_work", _r_caretaker_work),
    Rule("teamwork_fix", _r_teamwork_fix),
]


def propagate(world: World, narrate: bool = True) -> list[str]:
    out: list[str] = []
    changed = True
    for _ in range(len(globals().get("CAUSAL_RULES", [])) + 4):
        changed = False
        for rule in CAUSAL_RULES:
            sents = rule.apply(world)
            if sents:
                changed = True
                out.extend(sents)
    if narrate:
        for s in out:
            world.say(s)
    return out


def _do_activity(world: World, actor: Entity, act: Activity, narrate: bool = True) -> None:
    if act.id not in world.setting.affords:
        pass
    actor.meters["dropping"] = actor.meters.get("dropping", 0.0) + 1.0
    actor.memes["joy"] = actor.memes.get("joy", 0.0) + 1.0
    propagate(world, narrate=narrate)


def predict_mess(world: World, actor: Entity, act: Activity, prize_id: str) -> bool:
    sim = world.copy()
    _do_activity(sim, sim.get(actor.id), act, narrate=False)
    prize = sim.get(prize_id)
    return prize.meters.get("dirty", 0.0) >= THRESHOLD


