def tell(setting: Setting, act: Activity, prize_cfg: Prize, hero_name: str = "Milly", hero_type: str = "girl",
         parent_type: str = "mother") -> World:
    world = World(setting)
    hero = world.add(Entity(id=hero_name, kind="character", type=hero_type, traits=["little", "slob"]))
    parent = world.add(Entity(id="Parent", kind="character", type=parent_type, label="mother"))
    prize = world.add(Entity(id="Prize", type=prize_cfg.type, label=prize_cfg.label, phrase=prize_cfg.phrase,
                             caretaker=parent.id, owner=hero.id, plural=prize_cfg.plural))
    swarm = world.add(Entity(id="Swarm", kind="character", type="helpers", label="swarm"))
    swarm.memes["help"] = 1.0

    world.say(setting_line(setting))
    world.say(intro_line(hero))
    world.say(doxology_line(hero, act, prize))

    world.para()
    world.say(f"{hero.id} wanted to {act.verb}, in the nursery so bright.")
    world.say(warn_line(parent, hero, act, prize))
    world.say(turn_line(hero, act))
    hero.memes["worry"] = 1.0
    _do_activity(world, hero, act, narrate=True)

    world.para()
    gear = select_gear(act, prize)
    if gear is None:
        gear = next(iter(globals().get("GEARS", globals().get("GEAR", []))))
    world.say(teamwork_line(gear, hero, act, prize, parent))
    prize.worn_by = hero.id
    hero.memes["worry"] = 0.0
    hero.memes["calm"] = 1.0
    world.say(f"The tiny swarm worked together, and the muddle was gone from sight.")
    world.say(end_line(hero, act, prize))

    world.facts.update(hero=hero, parent=parent, prize=prize, activity=act, gear=gear, swarm=swarm, setting=setting)
    return world


