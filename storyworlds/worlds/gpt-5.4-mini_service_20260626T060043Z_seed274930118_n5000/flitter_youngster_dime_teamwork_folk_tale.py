def loves_activity(world: World, hero: Entity, activity: Activity) -> None:
    hero.memes["love"] += 1
    world.say(
        f"{hero.pronoun().capitalize()} loved {activity.gerund}; "
        f"to {hero.id}, it felt like a song with busy hands."
    )


