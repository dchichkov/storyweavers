def resolve_params(args: argparse.Namespace, rng: random.Random) -> StoryParams:
    if getattr(args, "place", None) and getattr(args, "activity", None) and getattr(args, "prize", None) and not reasonableness_gate(getattr(args, "place", None), getattr(args, "activity", None), getattr(args, "prize", None)):
        return _fallback_storyparams(args, rng, StoryParams, globals())
    combos = [c for c in valid_combos()
              if (not getattr(args, "place", None) or c[0] == getattr(args, "place", None))
              and (not getattr(args, "activity", None) or c[1] == getattr(args, "activity", None))
              and (not getattr(args, "prize", None) or c[2] == getattr(args, "prize", None))]
    if not combos:
        return _fallback_storyparams(args, rng, StoryParams, globals())
    place, activity, prize = (list(rng.choice(combos)) + [None, None, None])[:3]
    gender = getattr(args, "gender", None) or rng.choice(["girl", "boy"])
    name = getattr(args, "name", None) or rng.choice(GIRL_NAMES if gender == "girl" else BOY_NAMES)
    parent = getattr(args, "parent", None) or rng.choice(["mother", "father"])
    trait = rng.choice(TRAITS)
    return StoryParams(place=place, activity=activity, prize=prize, name=name, gender=gender, parent=parent, trait=trait)


def tell(params: StoryParams) -> World:
    setting = _safe_lookup(SETTINGS, params.place)
    world = World(setting)
    child = world.add(Entity(id="child", kind="character", type=params.gender, label=params.name))
    parent = world.add(Entity(id="parent", kind="character", type=params.parent, label=params.parent))
    ribbon = world.add(Entity(id="ribbon", type="ribbon", label="ribbon", phrase=_safe_lookup(PRIZES, params.prize).phrase, owner=child.id))
    child.memes["love"] = 1
    child.memes["desire"] = 1
    ribbon.worn_by = child.id

    world.say(
        f"Once in a small town, {params.name} was a {params.trait} child who rode the escalator each week with {parent.label}."
    )
    world.say(
        f"{child.pronoun().capitalize()} loved {_safe_lookup(ACTIVITIES, params.activity).gerund}, and {ribbon.phrase} brightened the ride like a little banner."
    )
    world.para()
    world.say(
        f"One weekly morning on {world.setting.place}, {child.pronoun().capitalize()} reached for the ribbon as the steps moved upward."
    )
    child.meters["ravel"] += 1
    child.memes["hurt"] = 1
    world.say(
        f"The {params.prize} {_safe_lookup(ACTIVITIES, params.activity).effect} when {child.id} {_safe_lookup(ACTIVITIES, params.activity).rush}."
    )
    parent.memes["apology"] = 1
    world.say(
        f"{parent.label.capitalize()} saw the trouble, spoke kindly, and promised to help mend it."
    )
    world.para()
    propagate(world, narrate=True)
    world.say(
        f"Then {parent.label} and {child.id} worked together, and the little french ribbon came back smooth by the time the escalator reached the top."
    )
    world.facts = {
        "child": child,
        "parent": parent,
        "ribbon": ribbon,
        "params": params,
        "activity": _safe_lookup(ACTIVITIES, params.activity),
        "setting": setting,
    }
    return world


