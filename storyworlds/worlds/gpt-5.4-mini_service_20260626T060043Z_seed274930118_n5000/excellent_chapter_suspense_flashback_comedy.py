def resolve_params(args: argparse.Namespace, rng: random.Random) -> StoryParams:
    combos = [c for c in valid_combos()
              if (getattr(args, "place", None) is None or c[0] == getattr(args, "place", None))
              and (getattr(args, "prize", None) is None or c[1] == getattr(args, "prize", None))]
    if not combos:
        return _fallback_storyparams(args, rng, StoryParams, globals())
    place, prize = rng.choice(list(combos))
    gender = getattr(args, "gender", None) or rng.choice(["girl", "boy"])
    name = getattr(args, "name", None) or rng.choice(NAMES)
    parent = getattr(args, "parent", None) or rng.choice(["mother", "father"])
    trait = rng.choice(TRAITS)
    return StoryParams(place=place, prize=prize, name=name, gender=gender, parent=parent, trait=trait)


def _do_read(world: World, hero: Entity, prize: Entity) -> None:
    hero.memes["joy"] += 1
    hero.memes["suspense"] += 1
    if world.get("drink").meters["mess"] >= THRESHOLD:
        prize.meters["dirty"] += 1


def _predict_spill(world: World) -> bool:
    sim = world.copy()
    sim.get("drink").meters["mess"] += 1
    sim.get("book").meters["dirty"] += 1
    return sim.get("book").meters["dirty"] >= THRESHOLD


def tell(setting: Setting, prize_cfg: Prize, hero_name: str, hero_type: str, parent_type: str, trait: str) -> World:
    world = World(setting)
    hero = world.add(Entity(id=hero_name, kind="character", type=hero_type))
    parent = world.add(Entity(id="parent", kind="character", type=parent_type, label="parent"))
    prize = world.add(Entity(id=prize_cfg.type, type=prize_cfg.type, label=prize_cfg.label, phrase=prize_cfg.phrase, owner=hero.id))
    drink = world.add(Entity(id="drink", type="thing", label="juice cup"))
    lamp = world.add(Entity(id="lamp", type="thing", label="lamp"))
    bookmark = world.add(Entity(id="bookmark", type="thing", label="bookmark"))

    world.say(f"{hero.id} was a little {trait} {hero.type} who loved {prize_cfg.phrase}.")
    world.say(f"{hero.id} liked the excellent chapter so much that {hero.pronoun('subject')} could quote the funny parts before bedtime.")
    world.para()
    world.say(f"One night in {setting.place}, {hero.id} found a cup of juice, a lamp, and one missing bookmark.")
    world.say(f"That made the room feel suspenseful, because the excellent chapter was open and the page marker was nowhere to be seen.")
    world.say(f"{hero.id} remembered, in a tiny flashback, the last time the bookmark had slid under the pillow during a giggle fit.")
    world.say(f"{hero.id}'s {parent.label} smiled and said, \"Let's not panic before the chapter turns into a mystery.\"")
    world.para()
    if _predict_spill(world):
        hero.memes["suspense"] += 1
        world.say(f"{hero.id} wanted to rush ahead, but {hero.pronoun('possessive')} {parent.label} pointed at the juice and the book and said, \"One wobble at a time.\"")
        world.say(f"Together they moved the juice to a tray, found the bookmark by the pillow, and turned on the lamp.")
        world.say(f"Then the excellent chapter stopped feeling scary and started feeling silly again.")
    _do_read(world, hero, prize)
    hero.memes["amused"] += 1
    hero.memes["love"] += 1
    world.say(f"{hero.id} finished the excellent chapter, and the funniest line made both of them snort-laugh.")
    world.say(f"At the end, the bookmark was safe, the juice was safe, and {hero.id} was smiling at the last page like it had told a joke on purpose.")

    world.facts.update(
        hero=hero,
        parent=parent,
        prize=prize,
        drink=drink,
        bookmark=bookmark,
        setting=setting,
        trait=trait,
        resolved=True,
    )
    return world


def generation_prompts(world: World) -> list[str]:
    f = world.facts
