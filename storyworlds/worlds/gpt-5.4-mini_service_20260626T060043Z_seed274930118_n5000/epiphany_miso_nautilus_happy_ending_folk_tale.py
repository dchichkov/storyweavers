def prize_at_risk(activity: Activity, prize: Prize) -> bool:
    return prize.region in activity.zone


def select_gear(activity: Activity, prize: Prize) -> Optional[Gear]:
    for gear in GEAR:
        if activity.mess in gear.guards and prize.region in gear.covers:
            return gear
    return None


def valid_combos() -> list[tuple[str, str, str]]:
    out = []
    for place, setting in SETTINGS.items():
        for act_id in setting.affords:
            act = _safe_lookup(ACTIVITIES, act_id)
            for prize_id, prize in PRIZES.items():
                if prize_at_risk(act, prize) and select_gear(act, prize):
                    out.append((place, act_id, prize_id))
    return out


def build_world(params: StoryParams) -> World:
    world = World(_safe_lookup(SETTINGS, params.place))
    hero = world.add(Entity(id=params.name, kind="character", type=params.gender))
    elder = world.add(Entity(id="elder", kind="character", type=params.elder, label=f"the {params.elder}"))
    prize = world.add(Entity(
        id="nautilus", type="shell", label="nautilus",
        phrase="a polished nautilus shell", owner=hero.id, caretaker=elder.id,
        region="hands",
    ))
    soup = world.add(Entity(id="miso", type="food", label="miso", phrase="a bowl of miso soup"))
    gear = world.add(Entity(
        id="pouch", type="gear", label="woven pouch", protective=True, owner=hero.id
    ))
    gear.covers = {"hands"}  # type: ignore[attr-defined]

    hero.memes["love"] = 1
    hero.memes["desire"] = 1
    prize.worn_by = hero.id
    gear.worn_by = None

    # Act 1
    world.say(f"{hero.id} lived beside {world.setting.place}, where the wind could sing to stones.")
    world.say(f"{hero.pronoun().capitalize()} loved {prize.phrase}, because it looked like a tiny moon curled by the sea.")
    world.say(f"Each evening, the {params.elder} set out a warm bowl of {soup.label} and said it was good to share before the stars came out.")
    world.say(f"{hero.id} liked the soup too, but {hero.pronoun('possessive')} eyes kept wandering to the water.")

    world.para()

    # Act 2
    world.zone = set(_safe_lookup(ACTIVITIES, params.activity).zone)
    act = _safe_lookup(ACTIVITIES, params.activity)
    world.say(f"One soft evening, {hero.id} wanted to {act.verb}.")
    world.say(f"{hero.pronoun('possessive').capitalize()} {params.elder} frowned a little, because if the shell slipped at the water's edge, it could get {act.soil}.")
    hero.memes["worry"] = 1
    hero.memes["longing"] = 1
    world.say(f"{hero.id} stepped closer anyway, until the little danger felt as big as a storm cloud.")

    world.para()

    # Resolution
    gear_def = select_gear(act, prize)
    if not gear_def:
        pass

    gear.worn_by = hero.id
    if prize_at_risk(act, prize):
        hero.memes["epiphany"] = 1
        world.say(f"Then {hero.id} had an epiphany: loving a treasure did not mean holding it loosely in a risky place.")
        world.say(f"{params.elder} smiled and said, '{gear_def.prep}, and then we can still go and listen to the water.'")
        world.say(f"{hero.id} did just that, and the shell stayed safe.")
        world.say(f"Afterward, {params.elder} and {hero.id} shared the miso soup beneath the evening lamp, and the river hummed quietly beside them.")
        world.say(f"{hero.id} felt proud, because the shell was safe, the soup was warm, and the night had become gentle again.")

    world.facts.update(hero=hero, elder=elder, prize=prize, activity=act, gear=gear_def, soup=soup, setting=world.setting)
    return world


