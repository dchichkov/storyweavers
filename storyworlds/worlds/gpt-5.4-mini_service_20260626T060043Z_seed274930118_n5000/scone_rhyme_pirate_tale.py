class World:
    def __init__(self, setting: Setting) -> None:
        self.setting = setting
        self.entities: dict[str, Entity] = {}
        self.paragraphs: list[list[str]] = [[]]
        self.zone: set[str] = set()
        self.fired: set[tuple] = set()
        self.facts: dict = {}

    def add(self, ent: Entity) -> Entity:
        self.entities[ent.id] = ent
        return ent

    def get(self, eid: str) -> Entity:
        if eid not in self.entities:
            label = str(eid).replace("_", " ")
            self.entities[eid] = Entity(str(eid), label=label)
        return self.entities[eid]

    def characters(self) -> list[Entity]:
        return [e for e in self.entities.values() if e.kind == "character"]

    def held_items(self, actor: Entity) -> list[Entity]:
        return [e for e in self.entities.values() if e.carried_by == actor.id or e.worn_by == actor.id]

    def say(self, text: str) -> None:
        if text:
            self.paragraphs[-1].append(text)

    def para(self) -> None:
        if self.paragraphs[-1]:
            self.paragraphs.append([])

    def render(self) -> str:
        return "\n\n".join(" ".join(p) for p in self.paragraphs if p)

    def copy(self) -> "World":
        import copy
        clone = World(self.setting)
        clone.entities = copy.deepcopy(self.entities)
        clone.zone = set(self.zone)
        clone.fired = set(self.fired)
        clone.paragraphs = [[]]
        return clone


def _r_crumbs(world: World) -> list[str]:
    out = []
    for actor in world.characters():
        if actor.meters.get("crumbs", 0.0) < THRESHOLD:
            continue
        for item in world.held_items(actor):
            if item.protective or item.id == "scone":
                continue
            sig = ("crumbs", actor.id, item.id)
            if sig in world.fired:
                continue
            world.fired.add(sig)
            item.meters["crumbs"] = item.meters.get("crumbs", 0.0) + 1
            out.append(f"{actor.label}'s {item.label} picked up a dusting of crumbs.")
    return out


CAUSAL_RULES = []


def propagate(world: World, narrate: bool = True) -> list[str]:
    produced: list[str] = []
    for rule in CAUSAL_RULES:
        produced.extend(rule(world))
    if narrate:
        for s in produced:
            world.say(s)
    return produced


def prize_at_risk(activity: Activity, prize: Prize) -> bool:
    return prize.region in activity.zone


def select_gear(activity: Activity, prize: Prize) -> Optional[Gear]:
    for gear in GEAR:
        if activity.mess in gear.guards and prize.region in gear.covers:
            return gear
    return None


def predict_mess(world: World, actor: Entity, activity: Activity, prize_id: str) -> dict:
    sim = world.copy()
    _do_activity(sim, sim.get(actor.id), activity, narrate=False)
    prize = sim.entities.get(prize_id)
    return {
        "soiled": bool(prize and prize.meters.get("crumbs", 0.0) >= THRESHOLD),
    }


def _do_activity(world: World, actor: Entity, activity: Activity, narrate: bool = True) -> None:
    if activity.id not in world.setting.affords:
        return
    world.zone = set(activity.zone)
    actor.meters[activity.mess] = actor.meters.get(activity.mess, 0.0) + 1
    actor.memes["joy"] = actor.memes.get("joy", 0.0) + 1
    propagate(world, narrate=narrate)


def rhyming_setup(world: World, hero: Entity, captain: Entity, activity: Activity, prize: Entity) -> None:
    world.say(
        f"On the ship with sails in a whistling breeze, {hero.id} liked to smile and tease."
    )
    world.say(
        f"{hero.pronoun().capitalize()} loved to {activity.gerund}, all merry and bright, "
        f"with a {prize.phrase} warm from the light."
    )
    world.say(
        f"But {captain.label_word} watched the deck and the sky, for crumbs can drift where gulls can fly."
    )


def arrive(world: World, hero: Entity, captain: Entity, activity: Activity) -> None:
    world.say(
        f"One breezy day, {hero.id} and {hero.pronoun('possessive')} {captain.label_word} were on {world.setting.place}, "
        f"where the ropes swayed and the waves went by."
    )
    world.say(
        f"{hero.id} wanted to {activity.verb}, fast and free, but {captain.label_word} knew the sea."
    )


def warn(world: World, captain: Entity, hero: Entity, activity: Activity, prize: Entity) -> bool:
    pred = predict_mess(world, hero, activity, prize.id)
    if not pred["soiled"]:
        return False
    world.facts["predicted_soil"] = activity.soil
    world.say(
        f"\"If you {activity.verb}, your {prize.label} may get {activity.soil},\" "
        f"{captain.label_word} said with care. \"Let's not let a good treat fray in the air.\""
    )
    return True


def defies(world: World, hero: Entity, activity: Activity) -> None:
    hero.memes["defiance"] = hero.memes.get("defiance", 0.0) + 1
    world.say(
        f"{hero.id} stamped one boot and gave a small sigh; {activity.rush} was the wish that would not die."
    )


def offer_gear(world: World, captain: Entity, hero: Entity, activity: Activity, prize: Entity) -> Optional[Gear]:
    gear_def = select_gear(activity, prize)
    if gear_def is None:
        return None
    gear = world.add(Entity(
        id=gear_def.id,
        kind="thing",
        type="gear",
        label=gear_def.label,
        protective=True,
        covers=set(gear_def.covers),
        plural=gear_def.plural,
        owner=hero.id,
        caretaker=captain.id,
    ))
    gear.carried_by = hero.id
    if predict_mess(world, hero, activity, prize.id)["soiled"]:
        del world.entities[gear.id]
        return None
    world.say(
        f"Then {captain.label_word} lifted a covered tin and smiled with a gleam: "
        f"\"How about we {gear_def.prep}? That keeps the {prize.label} neat and clean.\""
    )
    return gear


def accept(world: World, captain: Entity, hero: Entity, activity: Activity, prize: Entity, gear_def: Gear) -> None:
    hero.memes["joy"] = hero.memes.get("joy", 0.0) + 1
    hero.memes["defiance"] = 0.0
    world.say(
        f"{hero.id} grinned, then laughed, then gave {captain.label_word} a hug so tight. "
        f"\"Aye aye!\" {hero.pronoun()} cried, ready for the kinder fight."
    )
    world.say(
        f"They {gear_def.tail}, and soon {hero.id} was {activity.gerund}, with {prize.label} safe in the light; "
        f"the sea kept singing, and all was right."
    )


def tell(setting: Setting, activity: Activity, prize_cfg: Prize, hero_name: str, hero_type: str,
         captain_type: str, trait: str) -> World:
    world = World(setting)
    hero = world.add(Entity(id=hero_name, kind="character", type=hero_type, label=hero_name, traits=[trait, "brave"]))
    captain = world.add(Entity(id="Captain", kind="character", type=captain_type, label="the captain"))
    prize = world.add(Entity(id="scone", kind="thing", type=prize_cfg.type, label=prize_cfg.label,
                             phrase=prize_cfg.phrase, owner=hero.id, caretaker=captain.id,
                             region=prize_cfg.region, plural=prize_cfg.plural))
    prize.carried_by = hero.id

    rhyming_setup(world, hero, captain, activity, prize)
    world.para()
    arrive(world, hero, captain, activity)
    warn(world, captain, hero, activity, prize)
    defies(world, hero, activity)
    gear_def = offer_gear(world, captain, hero, activity, prize)
    world.para()
    if gear_def:
        accept(world, captain, hero, activity, prize, gear_def)

    world.facts.update(hero=hero, captain=captain, prize=prize, activity=activity, gear=gear_def,
                       resolved=gear_def is not None)
    return world


