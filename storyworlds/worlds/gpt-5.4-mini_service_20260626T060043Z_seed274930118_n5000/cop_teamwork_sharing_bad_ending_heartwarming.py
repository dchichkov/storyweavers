def shares_gear(world: World, hero: Entity, helper: Entity, gear_def: Gear) -> Entity:
    gear = world.add(Entity(
        id=gear_def.id,
        type="gear",
        label=gear_def.label,
        owner=hero.id,
        protective=True,
        plural=gear_def.plural,
    ))
    gear.worn_by = hero.id
    gear.covers = set(gear_def.covers)  # type: ignore[attr-defined]
    world.say(f"{helper.id} shared {gear.label} with {hero.id}, and {hero.id} shared the flashlight back.")
    return gear


