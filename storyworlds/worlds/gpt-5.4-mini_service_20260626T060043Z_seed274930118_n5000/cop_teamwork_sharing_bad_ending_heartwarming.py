def story_qa(world: World) -> list[QAItem]:
    f = world.facts
    hero = _safe_fact(world, f, "hero")
    helper = _safe_fact(world, f, "helper")
    prize = _safe_fact(world, f, "prize")
    act = _safe_fact(world, f, "activity")
    place = _safe_fact(world, f, "setting").place
    qa = [
        QAItem(
            question=f"Who is the story about?",
            answer=f"The story is about {hero.id}, a kind cop, and {helper.id}, who helps with the rainy errand at {place}.",
        ),
        QAItem(
            question=f"What did {hero.id} and {helper.id} do together?",
            answer=f"They showed teamwork by sharing gear and helping carry things safely while {act.gerund}.",
        ),
        QAItem(
            question=f"What was the important item they wanted to keep safe?",
            answer=f"They wanted to keep {prize.label} safe for {f['neighbor'].id}.",
        ),
    ]
    if f.get("gear"):
        gear = _safe_fact(world, f, "gear")
        qa.append(
            QAItem(
                question=f"How did {gear.label} help?",
                answer=f"{gear.label} helped by keeping part of {hero.id}'s clothes drier while they worked in the rain.",
            )
        )
    qa.append(
        QAItem(
            question=f"Why was the ending a little sad?",
            answer=f"The day helped someone, but the rain still ruined one small thing, so the ending was kind and a little disappointing.",
        )
    )
