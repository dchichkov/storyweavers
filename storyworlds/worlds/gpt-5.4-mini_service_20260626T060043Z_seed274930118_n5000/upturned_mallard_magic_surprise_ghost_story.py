class World:
    def __init__(self, setting: Setting) -> None:
        self.setting = setting
        self.entities: dict[str, Entity] = {}
        self.fired: set[tuple] = set()
        self.paragraphs: list[list[str]] = [[]]
        self.zone: set[str] = set()
        self.weather: str = ""
        self.facts: dict = {}

    def add(self, ent: Entity) -> Entity:
        self.entities[ent.id] = ent
        return ent

    def get(self, eid: str) -> Entity:
        if eid not in self.entities:
            label = str(eid).replace("_", " ")
            self.entities[eid] = Entity(str(eid), label=label)
        return self.entities[eid]

    def characters(self) -> list[Entity]:
        return [e for e in self.entities.values() if e.kind == "character"]

    def worn_items(self, actor: Entity) -> list[Entity]:
        return [e for e in self.entities.values() if e.worn_by == actor.id]

    def say(self, text: str) -> None:
        if text:
            self.paragraphs[-1].append(text)

    def para(self) -> None:
        if self.paragraphs[-1]:
            self.paragraphs.append([])

    def render(self) -> str:
        return "\n\n".join(" ".join(p) for p in self.paragraphs if p)

    def copy(self) -> "World":
        import copy as _copy
        clone = World(self.setting)
        clone.entities = _copy.deepcopy(self.entities)
        clone.fired = set(self.fired)
        clone.zone = set(self.zone)
        clone.weather = self.weather
        clone.paragraphs = [[]]
        clone.facts = dict(self.facts)
        return clone


MAGIC_RULES = [
    ("shimmer", lambda w: _r_shimmer(w)),
    ("ghost_peek", lambda w: _r_ghost_peek(w)),
]


def _r_shimmer(world: World) -> list[str]:
    out: list[str] = []
    for actor in world.characters():
        if actor.meters.get("magic", 0.0) < THRESHOLD:
            continue
        sig = ("shimmer", actor.id)
        if sig in world.fired:
            continue
        world.fired.add(sig)
        actor.memes["wonder"] = actor.memes.get("wonder", 0.0) + 1
        out.append(f"A small glow answered {actor.id}'s whisper.")
    return out


def _r_ghost_peek(world: World) -> list[str]:
    out: list[str] = []
    ghost = world.entities.get("ghost")
    if not ghost:
        return out
    if ghost.memes.get("shy", 0.0) < THRESHOLD:
        return out
    if world.facts.get("mallard_righted"):
        sig = ("peek", ghost.id)
        if sig in world.fired:
            return out
        world.fired.add(sig)
        ghost.memes["brave"] = ghost.memes.get("brave", 0.0) + 1
        out.append("The shy ghost drifted out to see the room.")
    return out


def propagate(world: World, narrate: bool = True) -> list[str]:
    produced: list[str] = []
    changed = True
    for _ in range(len(globals().get("CAUSAL_RULES", [])) + 4):
        changed = False
        for _, rule in MAGIC_RULES:
            sents = rule(world)
            if sents:
                changed = True
                produced.extend(sents)
    if narrate:
        for s in produced:
            world.say(s)
    return produced


def prize_at_risk(activity: Activity, prize: Prize) -> bool:
    return prize.region in activity.zone


def select_gear(activity: Activity, prize: Prize) -> Optional[Gear]:
    for gear in GEAR:
        if activity.mess in gear.guards and prize.region in gear.covers:
            return gear
    return None


def predict_mess(world: World, actor: Entity, activity: Activity, prize_id: str) -> dict:
    sim = world.copy()
    _do_activity(sim, sim.get(actor.id), activity, narrate=False)
    prize = sim.entities.get(prize_id)
    return {
        "soiled": bool(prize and prize.meters.get("dirty", 0.0) >= THRESHOLD),
        "magic": actor.meters.get("magic", 0.0),
    }


def _do_activity(world: World, actor: Entity, activity: Activity, narrate: bool = True) -> None:
    if activity.id not in world.setting.affords:
        return
    world.zone = set(activity.zone)
    actor.meters[activity.mess] = actor.meters.get(activity.mess, 0.0) + 1
    actor.meters["magic"] = actor.meters.get("magic", 0.0) + 1
    propagate(world, narrate=narrate)


def introduce(world: World, hero: Entity) -> None:
    world.say(f"{hero.id} loved ghost stories and quiet corners where secrets might hide.")


def arrives(world: World, hero: Entity, parent: Entity) -> None:
    world.say(f"One evening, {hero.id} and {hero.pronoun('possessive')} {parent.label} went to {world.setting.place}.")
    if world.setting.indoor:
        world.say("The room felt sleepy, with moonlight lying across the floor.")


def notices_mallard(world: World, hero: Entity, mallard: Entity) -> None:
    world.say(
        f"{hero.id} noticed an upturned mallard on a dusty shelf. "
        f"It looked like a duck that had fallen asleep on its back."
    )
    world.facts["mallard_seen"] = True


def wants_surprise(world: World, hero: Entity, parent: Entity) -> None:
    hero.memes["want"] = hero.memes.get("want", 0.0) + 1
    world.say(
        f"{hero.id} wanted a surprise and a little magic, but {hero.pronoun('possessive')} "
        f"{parent.label} held up a careful hand."
    )


def warn(world: World, parent: Entity, hero: Entity, activity: Activity, prize: Entity) -> bool:
    pred = predict_mess(world, hero, activity, prize.id)
    if not pred["soiled"]:
        return False
    world.say(
        f'"If you rush at the {prize.label}, you may make a dusty mess," '
        f"{parent.label} said. \"Let's try a gentler way.\""
    )
    world.facts["predicted_soil"] = activity.soil
    return True


def defies(world: World, hero: Entity, activity: Activity) -> None:
    hero.memes["defiance"] = hero.memes.get("defiance", 0.0) + 1
    world.say(f"{hero.id} was still curious, and {hero.pronoun().capitalize()} tried to {activity.rush}.")


def compromise(world: World, parent: Entity, hero: Entity, activity: Activity, prize: Entity) -> Optional[Gear]:
    gear_def = select_gear(activity, prize)
    if gear_def is None:
        return None
    if predict_mess(world, hero, activity, prize.id)["soiled"]:
        return None
    gear = world.add(Entity(
        id=gear_def.id,
        type="gear",
        label=gear_def.label,
        owner=hero.id,
        caretaker=parent.id,
        protective=True,
        covers=set(gear_def.covers),
        plural=gear_def.plural,
    ))
    gear.worn_by = hero.id
    world.say(
        f"{parent.label} smiled and said, \"How about we {gear_def.prep} and then "
        f"{activity.verb} together?\""
    )
    return gear_def


def accept(world: World, parent: Entity, hero: Entity, activity: Activity, prize: Entity, gear_def: Gear) -> None:
    hero.memes["joy"] = hero.memes.get("joy", 0.0) + 1
    hero.memes["wonder"] = hero.memes.get("wonder", 0.0) + 1
    hero.memes["defiance"] = 0.0
    world.say(
        f"{hero.id} grinned and held {hero.pronoun('possessive')} {parent.label}. "
        f"Together they {gear_def.tail}."
    )
    world.say(
        f"{hero.id} whispered a magic word, the mallard turned upright, and a hidden key "
        f"rested where the duck had been. Then the shy ghost floated out and smiled."
    )
    world.facts["mallard_righted"] = True
    propagate(world, narrate=True)
    world.say(
        f"In the end, the room felt warm and brave, and the upturned mallard was no longer upside down."
    )


def tell(
    setting: Setting,
    activity: Activity,
    prize_cfg: Prize,
    hero_name: str = "Mina",
    hero_type: str = "girl",
    hero_traits: Optional[list[str]] = None,
    parent_type: str = "mother",
) -> World:
    world = World(setting)
    world.weather = activity.weather

    hero = world.add(Entity(id=hero_name, kind="character", type=hero_type))
    parent = world.add(Entity(id="Parent", kind="character", type=parent_type, label="the parent"))
    prize = world.add(Entity(
        id="prize",
        type=prize_cfg.type,
        label=prize_cfg.label,
        phrase=prize_cfg.phrase,
        owner=hero.id,
        caretaker=parent.id,
        region=prize_cfg.region,
        plural=prize_cfg.plural,
    ))
    mallard = world.add(Entity(id="mallard", type="duck", label="mallard"))
    ghost = world.add(Entity(id="ghost", kind="character", type="ghost", label="the ghost"))
    ghost.memes["shy"] = 1.0

    introduce(world, hero)
    arrives(world, hero, parent)
    notices_mallard(world, hero, mallard)
    wants_surprise(world, hero, parent)

    world.para()
    warn(world, parent, hero, activity, prize)
    defies(world, hero, activity)

    world.para()
    gear_def = compromise(world, parent, hero, activity, prize)
    if gear_def:
        accept(world, parent, hero, activity, prize, gear_def)

    world.facts.update(
        hero=hero,
        parent=parent,
        prize=prize,
        mallard=mallard,
        ghost=ghost,
        activity=activity,
        setting=setting,
        gear=gear_def,
        resolved=gear_def is not None,
    )
    return world


