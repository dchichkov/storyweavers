def resolve_params(args: argparse.Namespace, rng: random.Random) -> StoryParams:
    combos = [c for c in valid_combos()
              if (getattr(args, "place", None) is None or c[0] == getattr(args, "place", None))
              and (getattr(args, "activity", None) is None or c[1] == getattr(args, "activity", None))
              and (getattr(args, "prize", None) is None or c[2] == getattr(args, "prize", None))]
    if not combos:
        return _fallback_storyparams(args, rng, StoryParams, globals())
    place, activity, prize = (list(rng.choice(combos)) + [None, None, None])[:3]
    name = getattr(args, "name", None) or rng.choice(GREEK_NAMES)
    companion = getattr(args, "companion", None) or "goat"
    trait = getattr(args, "trait", None) or rng.choice(TRAITS)
    return StoryParams(place=place, activity=activity, prize=prize, name=name, companion=companion, trait=trait)


def _do_activity(world: World, actor: Entity, activity: Activity, narrate: bool = True) -> None:
    world.zone = set(activity.zone)
    actor.meters[activity.mess] += 1
    actor.memes["joy"] += 1
    if narrate:
        world.say(f"{actor.id} tried to {activity.verb}, and the warm day seemed to beckon from above.")


def _apply_dust(world: World) -> list[str]:
    out: list[str] = []
    for actor in world.characters():
        if actor.meters["dusty"] < THRESHOLD:
            continue
        for item in world.worn_items(actor):
            if item.protective or item.region not in world.zone:
                continue
            if world.covered(actor, item.region):
                continue
            sig = ("dust", item.id)
            if sig in world.fired:
                continue
            world.fired.add(sig)
            item.meters["dusty"] += 1
            item.meters["dirty"] += 1
            out.append(f"{actor.id}'s {item.label} came home dusty.")
    return out


def propagate(world: World, narrate: bool = True) -> None:
    produced = _apply_dust(world)
    if narrate:
        for s in produced:
            world.say(s)


def select_gear(activity: Activity, prize: Prize) -> Optional[Gear]:
    for gear in GEAR:
        if activity.mess in gear.guards and prize.region in gear.covers:
            return gear
    return None


def predict_mess(world: World, actor: Entity, activity: Activity, prize_id: str) -> dict:
    sim = world.copy()
    _do_activity(sim, sim.get(actor.id), activity, narrate=False)
    prize = sim.entities[prize_id]
    return {"soiled": prize.meters["dirty"] >= THRESHOLD}


def tell(setting: Setting, activity: Activity, prize_cfg: Prize, name: str, companion: str, trait: str) -> World:
    world = World(setting)
    world.weather = activity.weather
    hero = world.add(Entity(id=name, kind="character", type="fox", traits=["little", trait]))
    helper = world.add(Entity(id="Companion", kind="character", type=companion, label="the goat"))
    prize = world.add(Entity(id="prize", type=prize_cfg.type, label=prize_cfg.label, phrase=prize_cfg.phrase, owner=hero.id, region=prize_cfg.region, plural=prize_cfg.plural))
    hero.meters["memory"] += 1
    world.say(f"In {setting.place}, {hero.id} was a {trait} little fox who loved the smell of ripe figs.")
    world.say(f'"Those figs look like a feast," {hero.pronoun("subject")} said, and {helper.label} answered, "Slow paws are safer than brave leaps."')
    world.say(f"Long ago, {hero.id} had slipped from a wall just like this one, and the memory still sat in {hero.pronoun('possessive')} chest.")
    world.para()
    world.say(f"Now the figs waited high on the stone ledge, and {hero.id} wanted to {activity.verb} at once.")
    hero.memes["desire"] += 1
    hero.memes["worry"] += 1
    world.say(f"Inside {hero.id}'s head, a small voice whispered, 'I want the figs, but I do not want the scratch.'")
    if predict_mess(world, hero, activity, prize.id)["soiled"]:
        world.say(f'{helper.label.capitalize()} frowned. "You will get your paws {activity.soil}," {helper.pronoun("subject")} said.')
    world.say(f"{hero.id} remembered the fall, and the memory made {hero.pronoun('possessive')} ears go warm.")
    world.para()
    gear_def = select_gear(activity, prize)
    if gear_def:
        gear = world.add(Entity(id=gear_def.id, type="gear", label=gear_def.label, owner=hero.id, caretaker=helper.id, protective=True, covers=set(gear_def.covers), plural=gear_def.plural))
        gear.worn_by = hero.id
        world.say(f'{helper.label.capitalize()} smiled and said, "Then let us {gear_def.prep}."')
        world.say(f"{hero.id} paused, then nodded. The little wooden stool made the wall less frightening.")
        _do_activity(world, hero, activity)
        propagate(world)
        hero.memes["joy"] += 1
        hero.memes["relief"] += 1
        world.say(f"{hero.id} climbed safely, and the figs stayed clean.")
        world.para()
        world.say(f"But the twist was this: the branch held more figs than one fox needed, and {hero.id} shared them with {helper.label}.")
        world.say(f'"A fable should teach," {hero.id} said, "and today it teaches that a careful friend is a better prize than a quick boast."')
    else:
        _do_activity(world, hero, activity)
        propagate(world)
        world.say(f"{hero.id} reached the figs alone, but the story would have been poorer without the helper's warning.")
    world.facts.update(hero=hero, helper=helper, prize=prize, activity=activity, setting=setting, gear=gear_def)
    return world


