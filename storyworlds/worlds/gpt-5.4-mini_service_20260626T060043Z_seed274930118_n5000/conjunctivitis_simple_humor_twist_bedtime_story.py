def setup(world: World, hero: Entity, parent: Entity, prize: Entity) -> None:
    hero.memes["comfort"] += 1
    world.say(f"{hero.id} loved {hero.pronoun('possessive')} {prize.label} and wanted it close at bedtime.")
    world.say(f"{hero.pronoun('possessive').capitalize()} {parent.type} had tucked the room into a quiet little hush.")


def arrive(world: World, hero: Entity, parent: Entity, activity: Activity) -> None:
    world.say(f"One sleepy night, {hero.id} and {hero.pronoun('possessive')} {parent.type} were in {world.room.place}.")
    world.say(f"{hero.id} wanted to {activity.verb}, but {hero.pronoun('possessive')} eye was red and itchy.")


def warn(world: World, parent: Entity, hero: Entity, activity: Activity, prize: Entity) -> bool:
    pred = predict_mess(world, hero, activity, prize.id)
    if not (pred["soiled"] or pred["germy"]):
        return False
    parent.memes["worry"] += 1
    world.facts["predicted_soil"] = activity.soil
    world.say(f"\"If you {activity.verb}, {prize.label} could get {activity.soil},\" {parent.pronoun('possessive')} {parent.type} said.")
    return True


def funny_misunderstanding(world: World, hero: Entity) -> None:
    hero.memes["humor"] += 1
    world.say(f"{hero.id} squinted at the moonlit room and gasped, as if the lamp had sprouted a tiny moustache.")


def rub_and_grumble(world: World, hero: Entity, activity: Activity) -> None:
    hero.meters["germy"] += 1
    hero.memes["worry"] += 1
    world.say(f"{hero.id} tried to {activity.rush}, then stopped and grumbled because rubbing an itchy eye was not the bedtime plan.")


def compromise(world: World, parent: Entity, hero: Entity, activity: Activity, prize: Entity) -> Optional[Gear]:
    gear_def = select_gear(activity, prize)
    if gear_def is None:
        return None
    gear = world.add(Entity(
        id=gear_def.id, type="thing", label=gear_def.label, owner=hero.id,
        caretaker=parent.id, protective=True, covers=set(gear_def.covers), plural=gear_def.plural
    ))
    gear.worn_by = hero.id
    if predict_mess(world, hero, activity, prize.id)["soiled"]:
        gear.worn_by = None
        del world.entities[gear.id]
        return None
    world.say(f"{parent.id} smiled and said, \"How about we {gear_def.prep} and make bedtime safer?\"")
    return gear_def


def resolve(world: World, parent: Entity, hero: Entity, activity: Activity, prize: Entity, gear_def: Gear) -> None:
    hero.memes["comfort"] += 2
    hero.memes["sleepy"] += 2
    hero.memes["worry"] = 0.0
    world.say(f"{hero.id} nodded, washed {hero.pronoun('possessive')} hands, and used the eye drops like tiny moon-landing lights.")
    world.say(f"Then {hero.id} {gear_def.tail}. {prize.label.capitalize()} stayed clean, the room stayed quiet, and the scary monster turned out to be {hero.pronoun('possessive')} own sleepy reflection in a spoon.")
    world.say(f"{hero.id} giggled, yawned wide, and fell asleep with one clear eye on the pillow and one pink eye getting better.")


def tell(room: Room, activity: Activity, prize_cfg: Prize,
         hero_name: str = "Mina", hero_type: str = "girl",
         parent_type: str = "mother") -> World:
    world = World(room)
    hero = world.add(Entity(id=hero_name, kind="character", type=hero_type))
    parent = world.add(Entity(id="Parent", kind="character", type=parent_type, label="the parent"))
    prize = world.add(Entity(
        id="prize", type=prize_cfg.type, label=prize_cfg.label, phrase=prize_cfg.phrase,
        owner=hero.id, caretaker=parent.id, region=prize_cfg.region, plural=prize_cfg.plural
    ))
    eye_drops = world.add(Entity(id="eye_drops", type="thing", label="eye drops", protective=True))
    eye_drops.worn_by = hero.id

    intro(world, hero)
    setup(world, hero, parent, prize)

    world.para()
    arrive(world, hero, parent, activity)
    warn(world, parent, hero, activity, prize)
    funny_misunderstanding(world, hero)
    rub_and_grumble(world, hero, activity)

    world.para()
    gear_def = compromise(world, parent, hero, activity, prize)
    if gear_def:
        resolve(world, parent, hero, activity, prize, gear_def)

    world.facts.update(hero=hero, parent=parent, prize=prize, activity=activity, gear=gear_def)
    return world


