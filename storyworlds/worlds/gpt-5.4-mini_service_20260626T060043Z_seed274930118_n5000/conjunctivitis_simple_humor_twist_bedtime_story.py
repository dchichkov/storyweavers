    StoryParams(room="bedroom", activity="hug_bunny", prize="bunny", name="Mina", gender="girl", parent="mother", trait="sleepy"),
    StoryParams(room="nursery", activity="peek_mirror", prize="pillow", name="Toby", gender="boy", parent="father", trait="curious"),
