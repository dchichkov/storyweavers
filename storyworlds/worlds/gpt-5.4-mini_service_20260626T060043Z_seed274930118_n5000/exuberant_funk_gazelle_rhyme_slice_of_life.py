class World:
    def __init__(self, setting: Setting) -> None:
        self.setting = setting
        self.entities: dict[str, Entity] = {}
        self.fired: set[tuple] = set()
        self.paragraphs: list[list[str]] = [[]]
        self.zone: set[str] = set()
        self.facts: dict = {}

    def add(self, ent: Entity) -> Entity:
        self.entities[ent.id] = ent
        return ent

    def get(self, eid: str) -> Entity:
        if eid not in self.entities:
            label = str(eid).replace("_", " ")
            self.entities[eid] = Entity(str(eid), label=label)
        return self.entities[eid]

    def characters(self) -> list[Entity]:
        return [e for e in self.entities.values() if e.kind == "character"]

    def worn_items(self, actor: Entity) -> list[Entity]:
        return [e for e in self.entities.values() if e.worn_by == actor.id]

    def covered(self, actor: Entity, region: str) -> bool:
        return any(g.id in GEAR_IDS and region in GEAR_BY_ID[g.id].covers for g in self.worn_items(actor))

    def say(self, text: str) -> None:
        if text:
            self.paragraphs[-1].append(text)

    def para(self) -> None:
        if self.paragraphs[-1]:
            self.paragraphs.append([])

    def render(self) -> str:
        return "\n\n".join(" ".join(p) for p in self.paragraphs if p)


def _r_spill(world: World) -> list[str]:
    out: list[str] = []
    for actor in world.characters():
        for mess in ("funk",):
            if actor.meters.get(mess, 0.0) < THRESHOLD:
                continue
            for item in world.worn_items(actor):
                if item.id in GEAR_IDS:
                    continue
                if item.id not in world.entities:
                    continue
                if item.meters.get("clean", 0.0) < THRESHOLD:
                    continue
                if item.region not in world.zone:
                    continue
                if world.covered(actor, item.region):
                    continue
                sig = ("spill", item.id)
                if sig in world.fired:
                    continue
                world.fired.add(sig)
                item.meters["messy"] = item.meters.get("messy", 0.0) + 1
                out.append(f"{actor.pronoun('possessive').capitalize()} {item.label} picked up some funk.")
    return out


def _r_mood(world: World) -> list[str]:
    out: list[str] = []
    for item in list(world.entities.values()):
        if item.meters.get("messy", 0.0) >= THRESHOLD and item.caretaker and ("mood", item.id) not in world.fired:
            world.fired.add(("mood", item.id))
            carer = world.get(item.caretaker)
            carer.memes["worry"] = carer.memes.get("worry", 0.0) + 1
            out.append(f"That would mean extra tidying for {carer.label}.")
    return out


CAUSAL_RULES = [_r_spill, _r_mood]


def propagate(world: World, narrate: bool = True) -> list[str]:
    produced: list[str] = []
    changed = True
    for _ in range(len(globals().get("CAUSAL_RULES", [])) + 4):
        changed = False
        for rule in CAUSAL_RULES:
            sents = rule(world)
            if sents:
                changed = True
                produced.extend(sents)
    if narrate:
        for s in produced:
            world.say(s)
    return produced


def predict_mess(world: World, actor: Entity, activity: Activity, prize_id: str) -> dict:
    sim = World(world.setting)
    sim.entities = {
        k: Entity(**{**vars(v), "meters": dict(v.meters), "memes": dict(v.memes)})
        for k, v in world.entities.items()
    }
    sim.zone = set(activity.zone)
    sim.get(actor.id).meters[activity.mess] = sim.get(actor.id).meters.get(activity.mess, 0.0) + 1
    if prize_id in sim.entities and sim.entities[prize_id].region in sim.zone:
        sim.entities[prize_id].meters["messy"] = 1
    return {"soiled": bool(sim.entities[prize_id].meters.get("messy", 0.0) >= THRESHOLD),
            "worry": sum(e.memes.get("worry", 0.0) for e in sim.characters())}


def select_gear(activity: Activity, prize: Prize) -> Optional[Gear]:
    for gear in GEAR:
        if activity.mess in gear.guards and prize.region in gear.covers:
            return gear
    return None


def prize_at_risk(activity: Activity, prize: Prize) -> bool:
    return prize.region in activity.zone


def setting_detail(setting: Setting) -> str:
    if setting.indoor:
        return "Inside, the tables were small and the windows shone with afternoon light."
    return f"{setting.place.capitalize()} felt calm and ordinary, like a place where little surprises could happen."


def introduce(world: World, hero: Entity) -> None:
    world.say(f"{hero.id} was an exuberant little {hero.type} who liked bright days and tiny surprises.")


def loves_rhyme(world: World, hero: Entity) -> None:
    hero.memes["joy"] = hero.memes.get("joy", 0.0) + 1
    world.say("They loved a rhyme that bounced like a ball across the room.")


def buy_prize(world: World, parent: Entity, hero: Entity, prize: Entity) -> None:
    world.say(f"One afternoon, {hero.id}'s {parent.label} bought {hero.pronoun('object')} {prize.phrase}.")
    prize.worn_by = hero.id


def arrive(world: World, hero: Entity, parent: Entity, activity: Activity) -> None:
    world.say(f"One day, {hero.id} and {hero.pronoun('possessive')} {parent.label} went to {world.setting.place}.")
    world.say(setting_detail(world.setting))


def wants(world: World, hero: Entity, activity: Activity) -> None:
    hero.memes["desire"] = hero.memes.get("desire", 0.0) + 1
    world.say(f"{hero.id} wanted to {activity.verb}, right away.")


def warn(world: World, parent: Entity, hero: Entity, activity: Activity, prize: Entity) -> bool:
    pred = predict_mess(world, hero, activity, prize.id)
    if not pred["soiled"]:
        return False
    world.facts["predicted_soil"] = activity.soil
    world.say(f'"You\'ll get your {prize.label} {activity.soil}," {parent.label} said. "Let\'s think of a better way."')
    return True


def defy(world: World, hero: Entity, activity: Activity) -> None:
    hero.memes["stubborn"] = hero.memes.get("stubborn", 0.0) + 1
    world.say(f"But {hero.id} still felt the funk of the moment and tried to {activity.rush}.")


def offer(world: World, parent: Entity, hero: Entity, activity: Activity, prize: Entity) -> Optional[Gear]:
    gear = select_gear(activity, prize)
    if gear is None:
        return None
    obj = world.add(Entity(id=gear.id, type="gear", label=gear.label, owner=hero.id, caretaker=parent.id, plural=gear.plural))
    obj.worn_by = hero.id
    world.say(f'"How about we {gear.prep}?" {parent.label} asked.')
    return gear


def accept(world: World, parent: Entity, hero: Entity, activity: Activity, prize: Entity, gear: Gear) -> None:
    hero.memes["joy"] = hero.memes.get("joy", 0.0) + 1
    hero.memes["stubborn"] = 0.0
    world.say(f"{hero.id} grinned, because that sounded fair.")
    world.say(f"They {gear.tail}. Soon {hero.id} was {activity.gerund}, and {prize.label} stayed clean.")


def tell(setting: Setting, activity: Activity, prize_cfg: Prize, hero_name: str = "Milo",
         hero_type: str = "boy", parent_label: str = "mom") -> World:
    world = World(setting)
    hero = world.add(Entity(id=hero_name, kind="character", type=hero_type))
    parent = world.add(Entity(id="Parent", kind="character", type="mother" if parent_label == "mom" else "father", label=parent_label))
    prize = world.add(Entity(id="prize", type=prize_cfg.type, label=prize_cfg.label, phrase=prize_cfg.phrase, owner=hero.id, caretaker=parent.id, region=prize_cfg.region, plural=prize_cfg.plural))
    introduce(world, hero)
    loves_rhyme(world, hero)
    buy_prize(world, parent, hero, prize)
    world.para()
    arrive(world, hero, parent, activity)
    wants(world, hero, activity)
    warn(world, parent, hero, activity, prize)
    defy(world, hero, activity)
    world.para()
    gear = offer(world, parent, hero, activity, prize)
    if gear:
        accept(world, parent, hero, activity, prize, gear)
    world.facts.update(hero=hero, parent=parent, prize=prize, activity=activity, gear=gear, setting=setting, resolved=gear is not None)
    return world


