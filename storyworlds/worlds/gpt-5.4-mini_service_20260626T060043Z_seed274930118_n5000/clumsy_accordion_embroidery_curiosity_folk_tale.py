class World:
    def __init__(self, setting: Setting) -> None:
        self.setting = setting
        self.entities: dict[str, Entity] = {}
        self.fired: set[tuple] = set()
        self.paragraphs: list[list[str]] = [[]]
        self.zone: set[str] = set()
        self.facts: dict = {}

    def add(self, ent: Entity) -> Entity:
        self.entities[ent.id] = ent
        return ent

    def get(self, eid: str) -> Entity:
        if eid not in self.entities:
            label = str(eid).replace("_", " ")
            self.entities[eid] = Entity(str(eid), label=label)
        return self.entities[eid]

    def characters(self) -> list[Entity]:
        return [e for e in self.entities.values() if e.kind == "character"]

    def worn_items(self, actor: Entity) -> list[Entity]:
        return [e for e in self.entities.values() if e.worn_by == actor.id]

    def covered(self, actor: Entity, region: str) -> bool:
        return any(g.protective and region in g.covers for g in self.worn_items(actor))

    def say(self, text: str) -> None:
        if text:
            self.paragraphs[-1].append(text)

    def para(self) -> None:
        if self.paragraphs[-1]:
            self.paragraphs.append([])

    def render(self) -> str:
        return "\n\n".join(" ".join(p) for p in self.paragraphs if p)

    def copy(self) -> "World":
        import copy as _copy
        clone = World(self.setting)
        clone.entities = _copy.deepcopy(self.entities)
        clone.fired = set(self.fired)
        clone.zone = set(self.zone)
        clone.paragraphs = [[]]
        clone.facts = dict(self.facts)
        return clone


def _r_risk(world: World) -> list[str]:
    out: list[str] = []
    for actor in world.characters():
        if actor.meters["clumsiness"] < THRESHOLD:
            continue
        for item in world.worn_items(actor):
            if item.protective or item.region not in world.zone or world.covered(actor, item.region):
                continue
            sig = ("risk", actor.id, item.id)
            if sig in world.fired:
                continue
            world.fired.add(sig)
            item.meters["risk"] += 1
            out.append(f"{actor.pronoun('possessive').capitalize()} {item.label} was in danger.")
    return out


def _r_work(world: World) -> list[str]:
    out: list[str] = []
    for item in list(world.entities.values()):
        if item.meters["risk"] < THRESHOLD or not item.caretaker:
            continue
        sig = ("work", item.id)
        if sig in world.fired:
            continue
        world.fired.add(sig)
        carer = world.get(item.caretaker)
        carer.meters["work"] += 1
        out.append(f"That would make more work for {carer.label}.")
    return out


def propagate(world: World, narrate: bool = True) -> list[str]:
    out: list[str] = []
    changed = True
    for _ in range(len(globals().get("CAUSAL_RULES", [])) + 4):
        changed = False
        for rule in (_r_risk, _r_work):
            sents = rule(world)
            if sents:
                changed = True
                out.extend(sents)
    if narrate:
        for s in out:
            world.say(s)
    return out


def predict_risk(world: World, actor: Entity, activity: Activity, prize_id: str) -> dict:
    sim = world.copy()
    _do_activity(sim, sim.get(actor.id), activity, narrate=False)
    prize = sim.entities.get(prize_id)
    return {"risky": bool(prize and prize.meters["risk"] >= THRESHOLD)}


def _do_activity(world: World, actor: Entity, activity: Activity, narrate: bool = True) -> None:
    if activity.id not in world.setting.affords:
        return
    world.zone = set(activity.zone)
    actor.meters["clumsiness"] += 1
    actor.memes["joy"] += 1
    propagate(world, narrate=narrate)


def intro(world: World, hero: Entity) -> None:
    trait = next((t for t in hero.traits if t != "little"), "curious")
    world.say(f"{hero.id} was a little {trait} {hero.type} who always wanted to know how things worked.")


def love_music(world: World, hero: Entity, act: Activity) -> None:
    world.say(f"{hero.pronoun().capitalize()} loved the way an accordion could sing and sigh like a person telling a tale.")


def gift(world: World, giver: Entity, hero: Entity, prize: Entity) -> None:
    world.say(f"One morning, {giver.label} brought out {hero.pronoun('possessive')} {prize.phrase} and set it on the table.")


def wants(world: World, hero: Entity, parent: Entity, act: Activity, prize: Entity) -> None:
    world.say(f"{hero.id} wanted to {act.verb} at once, but {parent.label} frowned at the {prize.label} nearby.")


def warn(world: World, parent: Entity, hero: Entity, act: Activity, prize: Entity) -> bool:
    pred = predict_risk(world, hero, act, prize.id)
    if not pred["risky"]:
        return False
    world.facts["predicted_risk"] = True
    world.say(f'"If you {act.verb}, your {prize.label} may get tangled," {parent.label} said. "Let us be careful first."')
    return True


def clumsy_try(world: World, hero: Entity, act: Activity) -> None:
    hero.memes["restless"] = hero.memes.get("restless", 0) + 1
    world.say(f"{hero.id} heard that, but {hero.pronoun('possessive')} fingers still itched to try.")
    world.say(f"{hero.pronoun().capitalize()} reached to {act.rush}.")


def redirect(world: World, parent: Entity, hero: Entity, act: Activity) -> None:
    world.say(f"Then {parent.label} gently stopped {hero.pronoun('object')} and said,")
    world.say(f"\"First we tuck the embroidery away and wrap the straps in a soft cloth. Then you may play.\"")


def accept(world: World, parent: Entity, hero: Entity, act: Activity, prize: Entity, gear: Gear) -> None:
    hero.memes["joy"] += 1
    hero.memes["calm"] = hero.memes.get("calm", 0) + 1
    world.say(f"{hero.id} nodded, and {hero.pronoun('possessive')} worry softened like dough under warm hands.")
    world.say(f"They {gear.tail}. Soon {hero.id} was {act.gerund}, {prize.label} safe and bright, while the village listened from the doorway.")


def tell(setting: Setting, activity: Activity, prize_cfg: Prize, hero_name: str, hero_type: str,
         parent_type: str = "grandmother", hero_traits: Optional[list[str]] = None) -> World:
    world = World(setting)
    hero = world.add(Entity(id=hero_name, kind="character", type=hero_type, traits=["little"] + (hero_traits or ["curious", "clumsy"])))
    parent = world.add(Entity(id="Parent", kind="character", type=parent_type, label="Grandmother"))
    prize = world.add(Entity(id="prize", type=prize_cfg.type, label=prize_cfg.label, phrase=prize_cfg.phrase,
                             owner=hero.id, caretaker=parent.id, region=prize_cfg.region, plural=prize_cfg.plural))
    gear = world.add(Entity(id="cloth", type="cloth", label="a soft scarf", protective=True, covers={"hands", "lap"}))

    intro(world, hero)
    love_music(world, hero, activity)
    gift(world, parent, hero, prize)
    world.say(f"{hero.id} cherished the {prize.label}, for it held a pattern of flowers and little stars.")
    world.para()
    wants(world, hero, parent, activity, prize)
    warn(world, parent, hero, activity, prize)
    clumsy_try(world, hero, activity)
    redirect(world, parent, hero, activity)
    world.para()
    gear.worn_by = hero.id
    gear_def = select_gear(activity, prize)
    if gear_def:
        accept(world, parent, hero, activity, prize, gear_def)
    world.facts.update(hero=hero, parent=parent, prize=prize, activity=activity, gear=gear_def, setting=setting)
    return world


