class World:
    def __init__(self, setting: Setting) -> None:
        self.setting = setting
        self.entities: dict[str, Entity] = {}
        self.fired: set[tuple] = set()
        self.paragraphs: list[list[str]] = [[]]
        self.zone: set[str] = set()
        self.facts: dict = {}

    def add(self, ent: Entity) -> Entity:
        self.entities[ent.id] = ent
        return ent

    def get(self, eid: str) -> Entity:
        if eid not in self.entities:
            label = str(eid).replace("_", " ")
            self.entities[eid] = Entity(str(eid), label=label)
        return self.entities[eid]

    def characters(self) -> list[Entity]:
        return [e for e in self.entities.values() if e.kind == "character"]

    def worn_items(self, actor: Entity) -> list[Entity]:
        return [e for e in self.entities.values() if e.worn_by == actor.id]

    def covered(self, actor: Entity, region: str) -> bool:
        return any(e.protective and region in e.covers for e in self.worn_items(actor))

    def say(self, text: str) -> None:
        if text:
            self.paragraphs[-1].append(text)

    def para(self) -> None:
        if self.paragraphs[-1]:
            self.paragraphs.append([])

    def render(self) -> str:
        return "\n\n".join(" ".join(p) for p in self.paragraphs if p)


def _apply_spill(world: World) -> list[str]:
    out: list[str] = []
    for actor in world.characters():
        if actor.meters.get("pour", 0.0) < THRESHOLD:
            continue
        for item in world.worn_items(actor):
            if item.protective or item.region not in world.zone:
                continue
            if world.covered(actor, item.region):
                continue
            sig = ("spill", actor.id, item.id)
            if sig in world.fired:
                continue
            world.fired.add(sig)
            item.meters["sticky"] = item.meters.get("sticky", 0.0) + 1
            out.append(f"{actor.id}'s {item.label} got sticky with liqueur.")
    return out


def _apply_kindness(world: World) -> list[str]:
    out: list[str] = []
    for actor in world.characters():
        if actor.memes.get("kindness", 0.0) < THRESHOLD:
            continue
        sig = ("kindness", actor.id)
        if sig in world.fired:
            continue
        world.fired.add(sig)
        actor.memes["comfort"] = actor.memes.get("comfort", 0.0) + 1
        out.append("A kind thought made the room feel warm.")
    return out


def propagate(world: World, narrate: bool = True) -> list[str]:
    produced: list[str] = []
    changed = True
    for _ in range(len(globals().get("CAUSAL_RULES", [])) + 4):
        changed = False
        for fn in (_apply_spill, _apply_kindness):
            sents = fn(world)
            if sents:
                changed = True
                produced.extend(sents)
    if narrate:
        for s in produced:
            world.say(s)
    return produced


def prize_at_risk(activity: Activity, prize: Prize) -> bool:
    return prize.region in activity.zone


def select_gear(activity: Activity, prize: Prize) -> Optional[Gear]:
    for gear in GEAR:
        if activity.mess in gear.guards and prize.region in gear.covers:
            return gear
    return None


def explain_rejection(activity: Activity, prize: Prize) -> str:
    if not prize_at_risk(activity, prize):
        return (
            f"(No story: {activity.gerund} does not reach the {prize.region}, "
            f"so {prize.label} would stay safe and there is no honest worry.)"
        )
    return (
        f"(No story: nothing in the gear list both covers the {prize.region} "
        f"and guards against {activity.mess}. The ending would not be a fair fix.)"
    )


def explain_gender(prize_id: str, gender: str) -> str:
    ok = " / ".join(sorted(_safe_lookup(PRIZES, prize_id).genders))
    return f"(No story: {_safe_lookup(PRIZES, prize_id).label} is not a typical {gender}'s item here; try {ok}.)"


def act_carry(world: World, actor: Entity, activity: Activity, narrate: bool = True) -> None:
    if activity.id not in world.setting.affords:
        return
    world.zone = set(activity.zone)
    actor.meters["pour"] = actor.meters.get("pour", 0.0) + 1
    actor.memes["hope"] = actor.memes.get("hope", 0.0) + 1
    propagate(world, narrate=narrate)


def predict_mess(world: World, actor: Entity, activity: Activity, prize_id: str) -> dict:
    sim = World(world.setting)
    sim.entities = {k: Entity(**{
        "id": v.id, "kind": v.kind, "type": v.type, "label": v.label, "phrase": v.phrase,
        "owner": v.owner, "caretaker": v.caretaker, "worn_by": v.worn_by,
        "protective": v.protective, "covers": set(v.covers), "plural": v.plural,
        "meters": dict(v.meters), "memes": dict(v.memes)
    }) for k, v in world.entities.items()}
    sim.zone = set(world.zone)
    act_carry(sim, sim.get(actor.id), activity, narrate=False)
    prize = sim.entities[prize_id]
    return {
        "soiled": prize.meters.get("sticky", 0.0) >= THRESHOLD,
        "kindness": sum(e.memes.get("comfort", 0.0) for e in sim.characters()),
    }


def tell(world: World, activity: Activity, prize_cfg: Prize, hero_name: str, hero_type: str, parent_type: str) -> World:
    hero = world.add(Entity(id=hero_name, kind="character", type=hero_type, meters={}, memes={}))
    parent = world.add(Entity(id="parent", kind="character", type=parent_type, label="the kind grown-up", meters={}, memes={}))
    prize = world.add(Entity(
        id="prize", type=prize_cfg.type, label=prize_cfg.label, phrase=prize_cfg.phrase,
        owner=hero.id, caretaker=parent.id, region=prize_cfg.region, plural=prize_cfg.plural,
        meters={}, memes={}
    ))

    hero.say = None  # type: ignore[attr-defined]
    world.say(f"Poor {hero.id} was a little {hero.type} who lived by the bright kitchen hearth.")
    world.say(f"{hero.id} loved the sweet little word {activity.keyword}, and the day rang like a nursery rhyme.")
    world.say(f"{parent.label} had bought {hero.pronoun('object')} {prize.phrase}, and {hero.id} wore {prize.them()} with care.")

    world.para()
    world.say(f"One day {hero.id} went to the {world.setting.place} to {activity.verb}.")
    world.say(f"{hero.id} wanted to be gentle, but {activity.rush} could make a mess of {prize.label}.")
    pred = predict_mess(world, hero, activity, prize.id)
    if pred["soiled"]:
        world.say(f'"If that spills," {parent.label} said, "your {prize.label} will be {activity.soil}."')
        hero.memes["worry"] = hero.memes.get("worry", 0.0) + 1
        world.say(f"Poor {hero.id} frowned, because the little pot wanted to help and not spoil the tune.")
        hero.meters["pour"] = hero.meters.get("pour", 0.0) + 1
        hero.memes["kindness"] = hero.memes.get("kindness", 0.0) + 1
        gear_def = select_gear(activity, prize)
        if gear_def:
            gear = world.add(Entity(
                id=gear_def.id, type="gear", label=gear_def.label, protective=True,
                covers=set(gear_def.covers), meters={}, memes={}
            ))
            gear.worn_by = hero.id
            world.say(f'"How about we {gear_def.prep}?" said {parent.label}.')
            world.say(f"{hero.id} nodded, and the two of them took the kinder way.")
            if predict_mess(world, hero, activity, prize.id)["soiled"]:
                pass
            world.para()
            world.say(f"They {gear_def.tail}, and {hero.id} {activity.gerund} so carefully that no spill went near the {prize.label}.")
            world.say(f"In the end, {hero.id} felt proud, and the room shone with kindness.")
            world.facts.update(hero=hero, parent=parent, prize=prize, activity=activity, gear=gear_def, resolved=True)
        else:
            pass
    else:
        world.say(f"The little action stayed tidy, and no one needed a rescue at all.")
        world.facts.update(hero=hero, parent=parent, prize=prize, activity=activity, gear=None, resolved=False)

    return world


