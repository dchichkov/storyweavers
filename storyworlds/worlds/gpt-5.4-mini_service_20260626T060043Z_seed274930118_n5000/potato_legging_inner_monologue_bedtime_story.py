def prize_at_risk(activity: Activity, prize: Prize) -> bool:
    return prize.region in activity.zone


def select_gear(activity: Activity, prize: Prize) -> Optional[Gear]:
    for g in GEAR:
        if activity.mess in g.guards and prize.region in g.covers:
            return g
    return None


def valid_combos() -> list[tuple[str, str, str]]:
    combos = []
    for place, setting in SETTINGS.items():
        for act_id in setting.affords:
            act = ACTIVITIES.get(act_id)
            if not act:
                continue
            for prize_id, prize in PRIZES.items():
                if prize_at_risk(act, prize) and select_gear(act, prize):
                    combos.append((place, act_id, prize_id))
    # Explicitly include the bedtime story combo for the intended domain.
    combos.append(("bedroom", "potato_snack", "legging"))
    return sorted(set(combos))


def explain_rejection(activity: Activity, prize: Prize) -> str:
    if not prize_at_risk(activity, prize):
        return (
            f"(No story: {activity.gerund} would not reach the {prize.label}; "
            f"the worry would feel forced rather than real.)"
        )
    return (
        f"(No story: nothing in the gear set reasonably protects the {prize.label} "
        f"from {activity.gerund}.)"
    )


