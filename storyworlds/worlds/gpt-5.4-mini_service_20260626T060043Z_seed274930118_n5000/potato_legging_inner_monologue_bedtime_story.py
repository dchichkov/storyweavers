def _do_activity(world: World, actor: Entity, activity: Activity, narrate: bool = True) -> None:
    if activity.id not in world.setting.affords and world.setting.place != "the bedroom":
        return
    actor.memes["sleepy"] += 1
    if activity.mess != "none":
        actor.meters["mess"] += 1
    if narrate and activity.id == "potato_snack":
        world.say(f"The little potato smelled warm and buttery on the plate.")


def predict_mess(world: World, actor: Entity, activity: Activity, prize_id: str) -> dict:
    sim = world.copy()
    _do_activity(sim, sim.get(actor.id), activity, narrate=False)
    prize = sim.get(prize_id)
    if activity.mess != "none" and prize.region in activity.zone:
        return {"soiled": True}
    return {"soiled": False}


def story_intro(world: World, hero: Entity, parent: Entity, prize: Entity) -> None:
    world.say(
        f"{hero.id} was a little {hero.label_word} who loved quiet evenings and soft blankets. "
        f"Tonight {hero.pronoun()} wore {hero.pronoun('possessive')} favorite {prize.label}."
    )


def inner_monologue(world: World, hero: Entity, activity: Activity, prize: Entity) -> None:
    world.say(
        f"{hero.id} looked at the warm potato and thought, "
        f'"If I eat it in my {prize.label}, it might get {activity.soil}."'
    )
    hero.memes["worry"] += 1


def parent_warning(world: World, parent: Entity, hero: Entity, activity: Activity, prize: Entity) -> None:
    world.say(
        f"'{hero.id}, that could make your {prize.label} messy,' {parent.pronoun()} said softly. "
        f"'Let's keep bedtime cozy.'"
    )


def compromise(world: World, parent: Entity, hero: Entity, activity: Activity, prize: Entity) -> Optional[Gear]:
    gear = select_gear(activity, prize)
    if gear is None:
        return None
    if predict_mess(world, hero, activity, prize.id)["soiled"]:
        world.add(Entity(
            id=gear.id,
            kind="thing",
            type="gear",
            label=gear.label,
            protective=True,
            covers=set(gear.covers),
        ))
        world.get(gear.id).worn_by = hero.id
        world.say(
            f"{hero.id} thought a little harder and decided to {gear.prep}."
        )
        return gear
    return None


def resolution(world: World, hero: Entity, parent: Entity, activity: Activity, prize: Entity, gear: Gear) -> None:
    hero.memes["worry"] = 0.0
    hero.memes["comfort"] += 1
    hero.memes["joy"] += 1
    world.say(
        f"{hero.id} smiled, nibbled the potato carefully, and kept {hero.pronoun('possessive')} {prize.label} clean. "
        f"Then {hero.pronoun()} listened to the bedtime story until {hero.pronoun('possessive')} eyes got sleepy."
    )
    world.say(
        f"When the snack was done, {hero.id} folded the {prize.label} neatly at the end of the bed, "
        f"and the room felt still and safe."
    )


def tell(setting: Setting, activity: Activity, prize_cfg: Prize,
         hero_name: str = "Mia", hero_type: str = "girl", parent_type: str = "mother") -> World:
    world = World(setting)
    hero = world.add(Entity(id=hero_name, kind="character", type=hero_type))
    parent = world.add(Entity(id="Parent", kind="character", type=parent_type, label="parent"))
    prize = world.add(Entity(
        id=prize_cfg.type,
        kind="thing",
        type=prize_cfg.type,
        label=prize_cfg.label,
        phrase=prize_cfg.phrase,
        owner=hero.id,
        region=prize_cfg.region,
        plural=prize_cfg.plural,
    ))

    story_intro(world, hero, parent, prize)
    world.para()
    inner_monologue(world, hero, activity, prize)
    parent_warning(world, parent, hero, activity, prize)
    gear = compromise(world, parent, hero, activity, prize)
    if gear is None:
        pass
    world.para()
    resolution(world, hero, parent, activity, prize, gear)

    world.facts.update(hero=hero, parent=parent, prize=prize, activity=activity, gear=gear, setting=setting)
    return world


