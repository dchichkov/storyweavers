def predict(world: World, hero: Entity, curiosity: Curiosity) -> dict:
    sim = world.copy()
    sim.get(hero.id).memes["curiosity"] = 1.0
    return {"curiosity_spread": True}


def _spark_curiosity(world: World, hero: Entity, curiosity: Curiosity) -> None:
    hero.memes["curiosity"] = hero.memes.get("curiosity", 0.0) + 1.0
    world.say(f"{hero.id} noticed {curiosity.about}, and that tiny wonder started to grow.")


def _metastasize(world: World, hero: Entity, curiosity: Curiosity) -> None:
    if hero.memes.get("curiosity", 0.0) < THRESHOLD:
        return
    if ("metastasize", hero.id) in world.fired:
        return
    world.fired.add(("metastasize", hero.id))
    hero.memes["curiosity"] += 1.0
    world.say(
        f"Before long, the curiosity metastasized into more questions, as if one little question had invited its friends."
    )


def _reveal(world: World, hero: Entity, parent: Entity, prize: Entity, curiosity: Curiosity, gear: Gear) -> None:
    hero.memes["joy"] = hero.memes.get("joy", 0.0) + 1.0
    hero.memes["love"] = hero.memes.get("love", 0.0) + 1.0
    world.say(
        f"They {gear.tail}, and together they found {prize.phrase}."
    )
    world.say(
        f"Inside was {curiosity.reveal}, something {parent.pronoun('possessive')} heart had kept safe for a long time."
    )
    world.say(
        f"{hero.id} smiled, and the room felt warmer, as if the answer itself had given everyone a hug."
    )


def tell(setting: Setting, curiosity: Curiosity, prize_cfg: Prize,
         hero_name: str = "Nia", hero_type: str = "girl",
         hero_traits: Optional[list[str]] = None, parent_type: str = "mother") -> World:
    world = World(setting)

    hero = world.add(Entity(
        id=hero_name, kind="character", type=hero_type,
        traits=["little"] + (hero_traits or ["curious", "gentle"]),
    ))
    parent = world.add(Entity(id="Parent", kind="character", type=parent_type, label="the parent"))
    prize = world.add(Entity(
        id="prize", type=prize_cfg.type, label=prize_cfg.label, phrase=prize_cfg.phrase,
        owner=hero.id, caretaker=parent.id, fragile=prize_cfg.fragile,
    ))

    world.say(
        f"{hero.id} was a little {next((t for t in hero.traits if t != 'little'), 'curious')} {hero.type} who loved to notice small things."
    )
    world.say(
        f"One day, {hero.id} saw {curiosity.about}, and {hero.pronoun('possessive')} curiosity woke up at once."
    )
    world.say(
        f"{hero.id} wanted to {curiosity.verb}, but {hero.pronoun('possessive')} {parent.id.lower()} smiled and said to be careful with {prize.phrase}."
    )

    world.para()
    world.say(
        f"At {setting.place}, {hero.id} began to {curiosity.poke}."
    )
    _spark_curiosity(world, hero, curiosity)
    _metastasize(world, hero, curiosity)
    world.say(
        f"{hero.id} did not rush; {hero.pronoun('subject').capitalize()} kept asking gentle questions and {curiosity.turn}."
    )

    gear = select_gear(curiosity, prize)
    if gear is None:
        gear = next(iter(globals().get("GEARS", globals().get("GEAR", []))))
    world.para()
    world.say(
        f"{hero.pronoun('possessive').capitalize()} {parent.id.lower()} brought {gear.label} and said, \"Let's see together.\""
    )
    world.say(f"They {gear.prep}.")
    _reveal(world, hero, parent, prize, curiosity, gear)

    world.facts.update(
        hero=hero,
        parent=parent,
        prize=prize,
        curiosity=curiosity,
        gear=gear,
        setting=setting,
    )
    return world


