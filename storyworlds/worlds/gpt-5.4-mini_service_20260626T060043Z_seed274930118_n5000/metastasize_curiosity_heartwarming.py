def generation_prompts(world: World) -> list[str]:
    f = world.facts
    hero, parent, curiosity, prize = f["hero"], f["parent"], f["curiosity"], f["prize"]
    return [
        f'Write a short heartwarming story for a young child about {hero.id}, {curiosity.about}, and {prize.phrase}.',
        f"Tell a gentle story where {hero.id} follows curiosity at {world.setting.place} and ends up sharing a warm discovery with {parent.pronoun('object')}.",
        f'Write a simple story that uses the word "metastasize" in a child-friendly way and ends with kindness.',
    ]


def story_qa(world: World) -> list[QAItem]:
    f = world.facts
    hero, parent, curiosity, prize = f["hero"], f["parent"], f["curiosity"], f["prize"]
    trait = next((t for t in hero.traits if t != "little"), hero.type)
    place = world.setting.place
    gear = _safe_fact(world, f, "gear")
    return [
        QAItem(
            question=f"Who was the story about?",
            answer=f"It was about {hero.id}, a little {trait} {hero.type}, and {parent.pronoun('object')} helping with a gentle discovery.",
        ),
        QAItem(
            question=f"What made {hero.id} so interested at {place}?",
            answer=f"{hero.id} noticed {curiosity.about}, and that made {hero.pronoun('possessive')} curiosity grow into a bigger search for answers.",
        ),
        QAItem(
            question=f"What did {hero.id} and {parent.pronoun('subject')} find together?",
            answer=f"They found {prize.phrase} after using {gear.label} to look carefully.",
        ),
        QAItem(
            question=f"How did the story end?",
            answer=f"It ended with a warm discovery, a smiling child, and a room that felt kinder because they had looked together.",
        ),
    ]


def world_knowledge_qa(world: World) -> list[QAItem]:
    tags = set(world.facts["curiosity"].tags)
    out: list[QAItem] = []
    for tag in ["curiosity", "mystery", "surprise", "treasure"]:
        if tag in tags:
            out.extend(QAItem(question=q, answer=a) for q, a in KNOWLEDGE[tag])
    return out


def format_qa(sample: StorySample) -> str:
    lines = ["== (1) Generation prompts -- asks that would produce this story =="]
    for i, p in enumerate(sample.prompts, 1):
        lines.append(f"{i}. {p}")
    lines.append("")
    lines.append("== (2) Story questions -- answerable from the story text ==")
    for item in sample.story_qa:
        lines.append(f"Q: {item.question}")
        lines.append(f"A: {item.answer}")
    lines.append("")
    lines.append("== (3) World-knowledge questions -- child level, no story needed ==")
    for item in sample.world_qa:
        lines.append(f"Q: {item.question}")
        lines.append(f"A: {item.answer}")
    return "\n".join(lines)


