def prize_at_risk(activity: Activity, prize: Prize) -> bool:
    return prize.region in activity.zone or prize.label == "blanket"


def select_gear(activity: Activity, prize: Prize) -> Optional[Gear]:
    for gear in GEAR:
        if prize.region in gear.covers and "cold" in gear.guards:
            return gear
    return None


def valid_combos() -> list[tuple[str, str, str]]:
    out = []
    for place, setting in SETTINGS.items():
        for act_id in setting.affords:
            act = _safe_lookup(ACTIVITIES, act_id)
            for prize_id, prize in PRIZES.items():
                if prize_at_risk(act, prize) and select_gear(act, prize):
                    out.append((place, act_id, prize_id))
    return out


def setting_line(setting: Setting, activity: Activity) -> str:
    if setting.indoors:
        return f"Outside, the Atlantic wind tapped the walls, but inside {setting.place} the room stayed warm and dim."
    return f"The Atlantic air was crisp, and {setting.place} looked quiet beneath the night."


def predict(world: World, actor: Entity, activity: Activity, prize_id: str) -> dict:
    sim = world.copy()
    sim.zone = set(activity.zone)
    a = sim.get(actor.id)
    a.memes["restless"] = a.memes.get("restless", 0.0) + 1
    a.meters["cold"] = a.meters.get("cold", 0.0) + 1
    prize = sim.get(prize_id)
    if prize.region in activity.zone and not sim.covered(a, prize.region):
        prize.meters["cold"] = prize.meters.get("cold", 0.0) + 1
    return {"cold": prize.meters.get("cold", 0.0) >= THRESHOLD}


def tell(setting: Setting, activity: Activity, prize_cfg: Prize, hero_name: str, hero_type: str, parent_type: str, trait: str) -> World:
    world = World(setting)
    world.weather = activity.weather

    child = world.add(Entity(
        id=hero_name,
        kind="character",
        type=hero_type,
        meters={"sleepiness": 0.0, "cold": 0.0},
        memes={"curiosity": 0.0, "conflict": 0.0, "insight": 0.0, "joy": 0.0},
    ))
    parent = world.add(Entity(
        id="Parent",
        kind="character",
        type=parent_type,
        label="the parent",
        meters={"tiredness": 0.0},
        memes={"care": 0.0},
    ))
    prize = world.add(Entity(
        id="prize",
        type=prize_cfg.type,
        label=prize_cfg.label,
        phrase=prize_cfg.phrase,
        owner=child.id,
        caretaker=parent.id,
        plural=prize_cfg.plural,
    ))

    world.say(f"{child.id} was a {trait} little {hero_type} who loved quiet nights and listening for city sounds.")
    world.say(f"{child.id} especially loved the Atlantic, because the sea seemed to hum like a lullaby.")
    world.say(f"That evening, {child.id}'s {parent.label} had given {child.pronoun('object')} {prize.phrase}.")
    world.say(f"{child.id} liked {prize.label} so much that {child.pronoun('subject')} wore it to feel ready for bed.")

    world.para()
    world.say(setting_line(setting, activity))
    world.say(f"Then a faraway rumble told them a plow was clearing the road.")
    world.say(f"{child.id} wanted to {activity.verb}, even though bedtime was getting late.")
    pred = predict(world, child, activity, prize.id)
    if pred["cold"]:
        world.say(f'"If you go out now, you may get cold and lose sleep," {parent.pronoun("subject")} said in a careful voice.')
    else:
        world.say(f'"It is late," {parent.pronoun("subject")} said. "A bedtime watch is safer than a midnight dash."')
    child.memes["conflict"] += 1
    world.say(f"{child.id} frowned and tried to {activity.rush}, because the plow sounded exciting.")
    world.say(f"Then {parent.pronoun('subject')} held up a hand and said, " +
              f'"Listen first. The plow is helping the street, and the street will still be there in the morning."')
    world.say(f"That cautionary dialogue made {child.id} pause.")

    world.para()
    gear = select_gear(activity, prize)
    if gear is not None:
        world.say(f'{parent.pronoun("subject").capitalize()} suggested, "How about we {gear.prep} and watch from the window?"')
        child.memes["curiosity"] += 1
        child.memes["conflict"] = 0.0
        child.memes["insight"] += 1
        child.memes["joy"] += 1
        gear_ent = world.add(Entity(
            id=gear.id,
            type="gear",
            label=gear.label,
            owner=child.id,
            worn_by=child.id,
            plural=gear.plural,
        ))
        gear_ent.covers = set(gear.covers)  # type: ignore[attr-defined]
        world.say(f"{child.id}'s eyes widened. {child.pronoun('subject').capitalize()} understood at once: the plow was not a toy to chase, but a helper that kept the town safe.")
        world.say(f"So {child.id} put on {gear.label}, stood by the window, and watched the bright blade of the plow move through the Atlantic night.")
        world.say(f"At last {child.id} yawned, smiled at {parent.pronoun('object')}, and went to bed feeling warm, calm, and clever.")
    else:
        child.memes["conflict"] += 1
        world.say(f"{child.id} stayed near the window without a better plan, and the moment felt unfinished.")

    world.facts.update(
        child=child,
        parent=parent,
        prize=prize,
        activity=activity,
        gear=gear,
        resolved=gear is not None,
        conflict=child.memes.get("conflict", 0.0) > 0,
        setting=setting,
    )
    return world


