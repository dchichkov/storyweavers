class World:
    def __init__(self, setting: Setting) -> None:
        self.setting = setting
        self.entities: dict[str, Entity] = {}
        self.paragraphs: list[list[str]] = [[]]
        self.fired: set[tuple] = set()
        self.zone: set[str] = set()
        self.facts: dict = {}

    def add(self, ent: Entity) -> Entity:
        self.entities[ent.id] = ent
        return ent

    def get(self, eid: str) -> Entity:
        if eid not in self.entities:
            label = str(eid).replace("_", " ")
            self.entities[eid] = Entity(str(eid), label=label)
        return self.entities[eid]

    def characters(self) -> list[Entity]:
        return [e for e in self.entities.values() if e.kind == "character"]

    def say(self, text: str) -> None:
        if text:
            self.paragraphs[-1].append(text)

    def para(self) -> None:
        if self.paragraphs[-1]:
            self.paragraphs.append([])

    def render(self) -> str:
        return "\n\n".join(" ".join(p) for p in self.paragraphs if p)


def _r_steal(world: World) -> list[str]:
    out: list[str] = []
    for actor in world.characters():
        if actor.memes.get("grabs_remote", 0.0) < THRESHOLD:
            continue
        sig = ("steal", actor.id)
        if sig in world.fired:
            continue
        world.fired.add(sig)
        remote = next((e for e in world.entities.values() if e.type == "remote"), None)
        if remote and remote.owner and remote.owner != actor.id:
            remote.owner = actor.id
            actor.memes["pride"] = actor.memes.get("pride", 0.0) + 1
            out.append(f"{actor.id} ended up holding the remote tight.")
    return out


def _r_hurt_feelings(world: World) -> list[str]:
    out: list[str] = []
    for actor in world.characters():
        if actor.memes.get("ignored", 0.0) < THRESHOLD:
            continue
        sig = ("hurt", actor.id)
        if sig in world.fired:
            continue
        world.fired.add(sig)
        actor.memes["sad"] = actor.memes.get("sad", 0.0) + 1
        out.append(f"{actor.id}'s feelings hurt a little.")
    return out


CAUSAL_RULES = [
    ("steal", _r_steal),
    ("hurt", _r_hurt_feelings),
]


def propagate(world: World, narrate: bool = True) -> list[str]:
    produced: list[str] = []
    changed = True
    for _ in range(len(globals().get("CAUSAL_RULES", [])) + 4):
        changed = False
        for _, rule in CAUSAL_RULES:
            sents = rule(world)
            if sents:
                changed = True
                produced.extend(sents)
    if narrate:
        for s in produced:
            world.say(s)
    return produced


def prize_at_risk(activity: Activity, prize: Prize) -> bool:
    return prize.region in activity.zone


def select_gear(activity: Activity, prize: Prize) -> Optional[Gear]:
    for gear in GEAR:
        if activity.mess in gear.guards and prize.region in gear.covers:
            return gear
    return None


def predict(world: World, actor: Entity, activity: Activity, prize_id: str) -> dict:
    sim = World(world.setting)
    sim.entities = {k: Entity(**vars(v)) for k, v in world.entities.items()}
    sim.fired = set(world.fired)
    sim.zone = set(world.zone)
    sim.facts = dict(world.facts)
    sim.get(actor.id).memes = dict(world.get(actor.id).memes)
    perform_activity(sim, sim.get(actor.id), activity, narrate=False)
    prize = sim.entities.get(prize_id)
    return {
        "soiled": bool(prize and prize.meters.get("dirty", 0.0) >= THRESHOLD),
        "remote_lost": prize.owner is not None and prize.owner != actor.id if prize else False,
    }


def perform_activity(world: World, actor: Entity, activity: Activity, narrate: bool = True) -> None:
    if activity.id not in world.setting.affords:
        pass
    world.zone = set(activity.zone)
    actor.meters[activity.mess] = actor.meters.get(activity.mess, 0.0) + 1
    actor.memes["want"] = actor.memes.get("want", 0.0) + 1
    propagate(world, narrate=narrate)


def introduce(world: World, hero: Entity) -> None:
    trait = "little " + next((t for t in hero.memes.get("traits", ["gentle"]) if t != "little"), "gentle")
    world.say(f"{hero.id} was a {trait} {hero.type} who loved quiet nights and warm blankets.")


def setup_love(world: World, hero: Entity, prize: Entity, activity: Activity) -> None:
    world.say(f"{hero.id} loved the {prize.label} more than anything at bedtime.")
    world.say(f"{hero.id} also loved to {activity.verb} when the house was sleepy and soft.")


def take_remote(world: World, hero: Entity, prize: Entity) -> None:
    prize.owner = hero.id
    hero.memes["grabs_remote"] = hero.memes.get("grabs_remote", 0.0) + 1
    world.say(f"One evening, {hero.id} grabbed the {prize.label} first and kept it close.")


def sibling_wants_turn(world: World, other: Entity, prize: Entity) -> None:
    other.memes["ignored"] = other.memes.get("ignored", 0.0) + 1
    world.say(f"{other.id} reached out too, because {other.pronoun()} wanted a turn with the {prize.label}.")


def revenge_plan(world: World, child: Entity, other: Entity, activity: Activity) -> None:
    child.memes["revenge"] = child.memes.get("revenge", 0.0) + 1
    world.say(
        f"Feeling cross, {child.id} whispered a tiny revenge plan: "
        f"maybe {child.pronoun()} would not share at all."
    )
    world.say(f"{child.id} tried to {activity.rush}, just to make the other one wait.")


def parent_warns(world: World, parent: Entity, hero: Entity, prize: Entity, activity: Activity) -> bool:
    pred = predict(world, hero, activity, prize.id)
    if not pred["soiled"]:
        return False
    world.say(
        f'"If you keep the {prize.label} all to yourself, bedtime will get fussy," '
        f"{parent.label_word} said. \"Sharing will feel better.\""
    )
    return True


def apology_and_share(world: World, hero: Entity, other: Entity, prize: Entity, activity: Activity) -> None:
    hero.memes["revenge"] = max(0.0, hero.memes.get("revenge", 0.0) - 1)
    hero.memes["reconciliation"] = hero.memes.get("reconciliation", 0.0) + 1
    other.memes["reconciliation"] = other.memes.get("reconciliation", 0.0) + 1
    prize.owner = None
    world.say(f"{hero.id} slowed down and said sorry.")
    world.say(
        f"Then {hero.id} and {other.id} agreed to share the {prize.label}, "
        f"one calm turn at a time."
    )
    world.say(
        f"Soon the room felt soft again, and both children were {activity.gerund}, "
        f"smiling under the blanket-light."
    )


def tell(setting: Setting, activity: Activity, prize_cfg: Prize,
         hero_name: str = "Mila", hero_type: str = "girl",
         other_name: str = "Noah", other_type: str = "boy",
         parent_type: str = "mother", trait: str = "sleepy") -> World:
    world = World(setting)
    hero = world.add(Entity(id=hero_name, kind="character", type=hero_type, memes={"traits": [trait, "little"]}))
    other = world.add(Entity(id=other_name, kind="character", type=other_type, memes={"traits": ["patient", "little"]}))
    parent = world.add(Entity(id="Parent", kind="character", type=parent_type, label="the parent"))
    prize = world.add(Entity(id="remote", type=prize_cfg.type, label=prize_cfg.label, phrase=prize_cfg.phrase, owner=hero.id, caretaker=parent.id, region=prize_cfg.region, plural=prize_cfg.plural))

    introduce(world, hero)
    setup_love(world, hero, prize, activity)
    world.para()
    take_remote(world, hero, prize)
    sibling_wants_turn(world, other, prize)
    parent_warns(world, parent, hero, prize, activity)
    revenge_plan(world, hero, other, activity)
    world.para()
    gear = select_gear(activity, prize)
    if gear is None:
        gear = next(iter(globals().get("GEARS", globals().get("GEAR", []))))
    world.say(f"{parent.label_word} brought out {gear.label} and reminded them that sharing could be gentle.")
    apology_and_share(world, hero, other, prize, activity)

    world.facts.update(hero=hero, other=other, parent=parent, prize=prize, activity=activity, gear=gear, setting=setting)
    return world


