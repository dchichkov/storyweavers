def story_qa(world: World) -> list[QAItem]:
    f = world.facts
    hero, other, parent, prize, activity = f["hero"], f["other"], f["parent"], f["prize"], f["activity"]
    return [
        QAItem(
            question=f"Why did {hero.id} want the {prize.label} first?",
            answer=f"{hero.id} wanted the {prize.label} because {hero.pronoun()} loved to {activity.verb} and did not want bedtime to end yet.",
        ),
        QAItem(
            question=f"What made {other.id} feel left out?",
            answer=f"{other.id} felt left out when {hero.id} grabbed the {prize.label} and would not share a turn.",
        ),
        QAItem(
            question=f"How did the story end between {hero.id} and {other.id}?",
            answer=f"They made up, shared the {prize.label}, and ended the bedtime story feeling calm again.",
        ),
        QAItem(
            question=f"What helped turn the revenge idea into reconciliation?",
            answer=f"The parent reminded them about sharing, and then {hero.id} chose to apologize and take turns instead of staying angry.",
        ),
    ]


def world_knowledge_qa(world: World) -> list[QAItem]:
    out: list[QAItem] = []
    for tag in ["remote", "sharing", "reconciliation", "timer"]:
        if tag in {"remote", "sharing", "reconciliation", "timer"}:
            out.extend(QAItem(question=q, answer=a) for q, a in KNOWLEDGE.get(tag, []))
    return out


def format_qa(sample: StorySample) -> str:
    lines = ["== (1) Generation prompts -- asks that would produce this story =="]
    for i, p in enumerate(sample.prompts, 1):
        lines.append(f"{i}. {p}")
    lines.append("")
    lines.append("== (2) Story questions -- answerable from the story text ==")
    for item in sample.story_qa:
        lines.append(f"Q: {item.question}")
        lines.append(f"A: {item.answer}")
    lines.append("")
    lines.append("== (3) World-knowledge questions -- child level, no story needed ==")
    for item in sample.world_qa:
        lines.append(f"Q: {item.question}")
        lines.append(f"A: {item.answer}")
    return "\n".join(lines)


def dump_trace(world: World) -> str:
    lines = ["--- world model state ---"]
    for e in list(world.entities.values()):
        meters = {k: v for k, v in e.meters.items() if v}
        memes = {k: v for k, v in e.memes.items() if v and k != "traits"}
        bits = []
        if meters:
            bits.append(f"meters={meters}")
        if memes:
            bits.append(f"memes={memes}")
        lines.append(f"  {e.id:8} ({e.type:7}) {' '.join(bits)}")
    return "\n".join(lines)


