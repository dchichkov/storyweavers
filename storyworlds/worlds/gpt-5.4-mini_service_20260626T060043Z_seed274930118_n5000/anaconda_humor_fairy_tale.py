def _r_tangle(world: World) -> list[str]:
    out: list[str] = []
    for actor in world.characters():
        if actor.meters.get("stretch", 0.0) < HUMOR_THRESHOLD:
            continue
        for item in list(world.entities.values()):
            if item.kind != "thing" or item.owner != actor.id:
                continue
            if item.region not in world.zone:
                continue
            sig = ("tangle", actor.id, item.id)
            if sig in world.fired:
                continue
            world.fired.add(sig)
            item.meters["tangled"] = item.meters.get("tangled", 0.0) + 1
            out.append(f"{actor.id}'s {item.label} got tangled in the small place.")
    return out


def _r_laugh(world: World) -> list[str]:
    out: list[str] = []
    for actor in world.characters():
        if actor.memes.get("humor", 0.0) < HUMOR_THRESHOLD:
            continue
        sig = ("laugh", actor.id)
        if sig in world.fired:
            continue
        world.fired.add(sig)
        actor.memes["joy"] = actor.memes.get("joy", 0.0) + 1
        out.append(f"Everyone laughed a little, because {actor.id} knew a merry trick.")
    return out


CAUSAL_RULES: list[Rule] = [
    Rule("tangle", "physical", _r_tangle),
    Rule("laugh", "social", _r_laugh),
]


def propagate(world: World, narrate: bool = True) -> list[str]:
    produced: list[str] = []
    changed = True
    for _ in range(len(globals().get("CAUSAL_RULES", [])) + 4):
        changed = False
        for rule in CAUSAL_RULES:
            sents = rule.apply(world)
            if sents:
                changed = True
                produced.extend(sents)
    if narrate:
        for s in produced:
            world.say(s)
    return produced


def prize_at_risk(activity: Activity, prize: Prize) -> bool:
    return prize.region in activity.zone


def select_gear(activity: Activity, prize: Prize) -> Optional[Gear]:
    for gear in GEAR:
        if activity.mess in gear.guards and prize.region in gear.covers:
            return gear
    return None


def predict_mess(world: World, actor: Entity, activity: Activity, prize_id: str) -> dict:
    sim = world.copy()
    _do_activity(sim, sim.get(actor.id), activity, narrate=False)
    prize = sim.entities.get(prize_id)
    return {
        "tangled": bool(prize and prize.meters.get("tangled", 0.0) >= HUMOR_THRESHOLD),
        "joy": sum(e.memes.get("joy", 0.0) for e in sim.characters()),
    }


def _do_activity(world: World, actor: Entity, activity: Activity, narrate: bool = True) -> None:
    world.zone = set(activity.zone)
    actor.meters[activity.mess] = actor.meters.get(activity.mess, 0.0) + 1
    actor.meters["stretch"] = actor.meters.get("stretch", 0.0) + 1
    actor.memes["humor"] = actor.memes.get("humor", 0.0) + 1
    propagate(world, narrate=narrate)


