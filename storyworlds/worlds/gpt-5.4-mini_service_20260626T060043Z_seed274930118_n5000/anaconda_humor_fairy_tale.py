def offer(world: World, parent: Entity, hero: Entity, activity: Activity, prize: Entity) -> Optional[Gear]:
    gear_def = select_gear(activity, prize)
    if gear_def is None:
        return None
    gear = world.add(Entity(
        id=gear_def.id,
        type="thing",
        label=gear_def.label,
        owner=hero.id,
        protective=True,
        plural=gear_def.plural,
    ))
    gear.worn_by = hero.id
    if predict_mess(world, hero, activity, prize.id)["tangled"]:
        gear.worn_by = None
        del world.entities[gear.id]
        return None
    world.say(
        f"The {parent.type} smiled. 'How about we {gear_def.prep} and then you can {activity.verb}?'"
    )
    return gear_def


