def tell(
    setting: Setting,
    activity: Activity,
    prize_cfg: Prize,
    hero_name: str = "Nia",
    hero_type: str = "anaconda",
    hero_traits: Optional[list[str]] = None,
    parent_type: str = "queen",
) -> World:
    world = World(setting)

    hero = world.add(Entity(
        id=hero_name,
        kind="character",
        type=hero_type,
        traits=["young"] + (hero_traits or ["kind", "funny"]),
    ))
    parent = world.add(Entity(id="Parent", kind="character", type=parent_type, label=f"the {parent_type}"))
    prize = world.add(Entity(
        id="prize",
        type=prize_cfg.type,
        label=prize_cfg.label,
        phrase=prize_cfg.phrase,
        owner=hero.id,
        caretaker=parent.id,
        region=prize_cfg.region,
        plural=prize_cfg.plural,
    ))

    intro(world, hero)
    loves_place(world, hero, activity)
    promise(world, parent, hero, prize)

    world.para()
    wants(world, hero, activity)
    warn(world, parent, hero, activity, prize)
    stretch(world, hero, activity)
    joke(world, hero)

    world.para()
    gear_def = offer(world, parent, hero, activity, prize)
    if gear_def:
        accept(world, parent, hero, activity, prize, gear_def)

    world.facts.update(
        hero=hero,
        parent=parent,
        prize=prize,
        activity=activity,
        setting=setting,
        gear=gear_def,
        resolved=gear_def is not None,
    )
    return world


