class World:
    def __init__(self, place: Place) -> None:
        self.place = place
        self.entities: dict[str, Entity] = {}
        self.history: list[list[str]] = [[]]
        self.zone: set[str] = set()
        self.weather: str = ""
        self.fired: set[tuple] = set()
        self.facts: dict = {}

    def add(self, e: Entity) -> Entity:
        self.entities[e.id] = e
        return e

    def get(self, eid: str) -> Entity:
        if eid not in self.entities:
            label = str(eid).replace("_", " ")
            self.entities[eid] = Entity(str(eid), label=label)
        return self.entities[eid]

    def characters(self) -> list[Entity]:
        return [e for e in self.entities.values() if e.kind == "character"]

    def say(self, text: str) -> None:
        if text:
            self.history[-1].append(text)

    def para(self) -> None:
        if self.history[-1]:
            self.history.append([])

    def render(self) -> str:
        return "\n\n".join(" ".join(p) for p in self.history if p)

    def copy(self) -> "World":
        import copy as _copy
        w = World(self.place)
        w.entities = _copy.deepcopy(self.entities)
        w.zone = set(self.zone)
        w.weather = self.weather
        w.fired = set(self.fired)
        w.history = [[]]
        return w


def _r_spill(world: World) -> list[str]:
    out: list[str] = []
    for actor in world.characters():
        if actor.meters.get("splash", 0.0) < THRESHOLD:
            continue
        for item in list(world.entities.values()):
            if item.worn_by != actor.id:
                continue
            if item.region not in world.zone:
                continue
            sig = ("spill", actor.id, item.id)
            if sig in world.fired:
                continue
            world.fired.add(sig)
            item.meters["messy"] = item.meters.get("messy", 0.0) + 1
            out.append(f"{actor.pronoun('possessive').capitalize()} {item.label} got splashed.")
    return out


def _r_worry(world: World) -> list[str]:
    out: list[str] = []
    for item in list(world.entities.values()):
        if item.meters.get("messy", 0.0) < THRESHOLD or not item.caretaker:
            continue
        sig = ("worry", item.id)
        if sig in world.fired:
            continue
        world.fired.add(sig)
        carer = world.get(item.caretaker)
        carer.memes["worry"] = carer.memes.get("worry", 0.0) + 1
        out.append(f"That would trouble {carer.label}.")
    return out


def _r_friend_help(world: World) -> list[str]:
    out: list[str] = []
    for actor in world.characters():
        if actor.memes.get("need_help", 0.0) < THRESHOLD:
            continue
        helper_id = world.facts.get("helper")
        if not helper_id:
            continue
        helper = world.get(helper_id)
        sig = ("help", actor.id, helper.id)
        if sig in world.fired:
            continue
        world.fired.add(sig)
        actor.memes["hope"] = actor.memes.get("hope", 0.0) + 1
        helper.memes["friendship"] = helper.memes.get("friendship", 0.0) + 1
        out.append("__help__")
    return out


RULES = [_r_spill, _r_worry, _r_friend_help]


def propagate(world: World, narrate: bool = True) -> list[str]:
    produced: list[str] = []
    changed = True
    for _ in range(len(globals().get("CAUSAL_RULES", [])) + 4):
        changed = False
        for rule in RULES:
            sents = rule(world)
            if sents:
                changed = True
                produced.extend(s for s in sents if s != "__help__")
    if narrate:
        for s in produced:
            world.say(s)
    return produced


def prize_at_risk(activity: Activity, prize: Prize) -> bool:
    return prize.region in activity.zone


def select_gear(activity: Activity, prize: Prize) -> Optional[Gear]:
    for gear in GEAR:
        if activity.mess in gear.guards and prize.region in gear.covers:
            return gear
    return None


def predict(world: World, actor: Entity, activity: Activity, prize_id: str) -> dict:
    sim = world.copy()
    do_activity(sim, sim.get(actor.id), activity, narrate=False)
    prize = sim.entities[prize_id]
    return {"soiled": prize.meters.get("messy", 0.0) >= THRESHOLD}


def setting_detail(place: Place, activity: Activity) -> str:
    if place.indoors:
        return f"The ship's hold was dim, and the lanterns swayed gently."
    if activity.weather == "stormy":
        return f"The sea heaved below, and the deck shone with spray."
    return f"The deck was bright, but the water still tugged at the hull."


