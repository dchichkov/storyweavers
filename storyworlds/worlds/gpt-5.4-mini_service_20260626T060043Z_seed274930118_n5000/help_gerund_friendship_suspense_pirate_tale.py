def warn(world: World, parent: Entity, hero: Entity, activity: Activity, prize: Entity) -> bool:
    pred = predict(world, hero, activity, prize.id)
    if not pred["soiled"]:
        return False
    world.facts["warning"] = True
    world.say(
        f'"If you {activity.verb}, your {prize.label} may get ruined," '
        f"{hero.pronoun('possessive')} {parent.label} said."
    )
    return True


def defies(world: World, hero: Entity, activity: Activity) -> None:
    hero.memes["defiance"] = hero.memes.get("defiance", 0.0) + 1
    world.say(f"{hero.id} still tried to {activity.rush}, even with the waves watching.")


def friend_steps_in(world: World, helper: Entity, hero: Entity, activity: Activity) -> None:
    hero.memes["need_help"] = hero.memes.get("need_help", 0.0) + 1
    helper.memes["friendship"] = helper.memes.get("friendship", 0.0) + 1
    propagate(world, narrate=False)
    world.say(
        f"Then {helper.id} dashed over and said, "
        f'"I can help you {activity.help_gerund} first."'
    )


def compromise(world: World, parent: Entity, hero: Entity, activity: Activity, prize: Entity) -> Optional[Gear]:
    gear_def = select_gear(activity, prize)
    if gear_def is None:
        return None
    gear = world.add(Entity(
        id=gear_def.id,
        type="gear",
        label=gear_def.label,
        owner=hero.id,
        caretaker=parent.id,
        plural=gear_def.plural,
    ))
    gear.worn_by = hero.id
    if predict(world, hero, activity, prize.id)["soiled"]:
        del world.entities[gear.id]
        return None
    world.say(
        f"{hero.pronoun('possessive').capitalize()} {parent.label} smiled and said, "
        f'"How about we {gear_def.prep}?"'
    )
    return gear_def


def accept(world: World, parent: Entity, hero: Entity, activity: Activity, prize: Entity, gear_def: Gear, helper: Entity) -> None:
    hero.memes["joy"] = hero.memes.get("joy", 0.0) + 1
    hero.memes["love"] = hero.memes.get("love", 0.0) + 1
    hero.memes["need_help"] = 0.0
    world.say(
        f"{hero.id}'s face lit up. {hero.pronoun().capitalize()} nodded, and {helper.id} grinned beside {hero.pronoun('object')}."
    )
    world.say(
        f"They {gear_def.tail}. Soon {hero.id} was {activity.gerund}, "
        f"{hero.pronoun('possessive')} {prize.label} stayed clean, and the ship felt safe again."
    )


def do_activity(world: World, actor: Entity, activity: Activity, narrate: bool = True) -> None:
    if activity.id not in world.place.affords:
        return
    world.zone = set(activity.zone)
    actor.meters["splash"] = actor.meters.get("splash", 0.0) + 1
    actor.memes["joy"] = actor.memes.get("joy", 0.0) + 1
    propagate(world, narrate=narrate)


def tell(place: Place, activity: Activity, prize_cfg: Prize,
         hero_name: str = "Mina", hero_type: str = "girl",
         parent_type: str = "captain", helper_name: str = "Pip",
         helper_type: str = "boy") -> World:
    world = World(place)
    world.weather = activity.weather

    hero = world.add(Entity(id=hero_name, kind="character", type=hero_type))
    parent = world.add(Entity(id="Parent", kind="character", type=parent_type, label="captain"))
    helper = world.add(Entity(id=helper_name, kind="character", type=helper_type, label="matey"))
    prize = world.add(Entity(
        id="prize",
        type=prize_cfg.label,
        label=prize_cfg.label,
        phrase=prize_cfg.phrase,
        owner=hero.id,
        caretaker=parent.id,
        region=prize_cfg.region,
        plural=prize_cfg.plural,
    ))

    world.facts["helper"] = helper.id

    introduce(world, hero)
    loves_activity(world, hero, activity)
    buys(world, parent, hero, prize)
    loves_prize(world, hero, prize)

    world.para()
    arrive(world, hero, parent, activity)
    wants(world, hero, activity)
    warn(world, parent, hero, activity, prize)
    defies(world, hero, activity)
    friend_steps_in(world, helper, hero, activity)

    world.para()
    gear_def = compromise(world, parent, hero, activity, prize)
    if gear_def:
        accept(world, parent, hero, activity, prize, gear_def, helper)

    world.facts.update(hero=hero, parent=parent, helper=helper, prize=prize, activity=activity, gear=gear_def)
    return world


