def _r_dust_damage(world: World) -> list[str]:
    out = []
    for actor in world.characters():
        if actor.meters.get("careful", 0.0) < THRESHOLD:
            continue
        for item in list(world.entities.values()):
            if item.owner != actor.id:
                continue
            if item.protective or item.region not in world.zone:
                continue
            if world.covered(actor, item.region):
                continue
            sig = ("dust_damage", actor.id, item.id)
            if sig in world.fired:
                continue
            world.fired.add(sig)
            item.meters["dusty"] = item.meters.get("dusty", 0.0) + 1
            item.meters["damaged"] = item.meters.get("damaged", 0.0) + 1
            out.append(f"{actor.pronoun('possessive').capitalize()} {item.label} got dusty.")
    return out


def _r_seeds_found(world: World) -> list[str]:
    if world.facts.get("opened") and not world.facts.get("seeds_found"):
        world.facts["seeds_found"] = True
        child = _safe_fact(world, world.facts, "child")
        gp = _safe_fact(world, world.facts, "grandparent")
        child.memes["hope"] = child.memes.get("hope", 0.0) + 1
        gp.memes["tenderness"] = gp.memes.get("tenderness", 0.0) + 1
        return ["Inside, they found a tiny seed packet tucked behind the note."]
    return []


def _r_sprout(world: World) -> list[str]:
    if world.facts.get("planted") and not world.facts.get("sprout"):
        world.facts["sprout"] = True
        child = _safe_fact(world, world.facts, "child")
        gp = _safe_fact(world, world.facts, "grandparent")
        child.memes["joy"] = child.memes.get("joy", 0.0) + 1
        gp.memes["joy"] = gp.memes.get("joy", 0.0) + 1
        return ["A few days later, a green sprout peeked from the soil."]
    return []


CAUSAL_RULES = [Rule("dust_damage", _r_dust_damage), Rule("seeds", _r_seeds_found), Rule("sprout", _r_sprout)]


def propagate(world: World, narrate: bool = True) -> list[str]:
    out: list[str] = []
    changed = True
    for _ in range(len(globals().get("CAUSAL_RULES", [])) + 4):
        changed = False
        for rule in CAUSAL_RULES:
            sents = rule.apply(world)
            if sents:
                changed = True
                out.extend(sents)
    if narrate:
        for s in out:
            world.say(s)
    return out


def select_gear(activity: Activity, prize: Prize) -> Optional[Gear]:
    for gear in GEAR:
        if activity.mess in gear.guards and prize.region in gear.covers:
            return gear
    return None


def prize_at_risk(activity: Activity, prize: Prize) -> bool:
    return prize.region in activity.zone


def predict_mess(world: World, actor: Entity, activity: Activity, prize_id: str) -> dict:
    sim = world.copy()
    _do_activity(sim, sim.get(actor.id), activity, narrate=False)
    prize = sim.entities.get(prize_id)
    return {"soiled": bool(prize and prize.meters.get("damaged", 0.0) >= THRESHOLD)}


def _do_activity(world: World, actor: Entity, activity: Activity, narrate: bool = True) -> None:
    if activity.id not in world.setting.affords:
        return
    world.zone = set(activity.zone)
    actor.meters[activity.mess] = actor.meters.get(activity.mess, 0.0) + 1
    actor.meters["careful"] = actor.meters.get("careful", 0.0) + 1
    propagate(world, narrate=narrate)


def introduce(world: World, child: Entity) -> None:
    world.say(f"{child.id} was a little {child.type} who loved old things and quiet corners.")


def setup(world: World, child: Entity, gp: Entity, prize: Entity) -> None:
    world.say(f"One afternoon, {child.id} and {gp.pronoun('possessive')} {gp.type} went up to the attic.")
    world.say(f"There, they found {prize.phrase}, and it looked almost morbid in the dusty light.")


def want_open(world: World, child: Entity, prize: Entity) -> None:
    child.memes["curiosity"] = child.memes.get("curiosity", 0.0) + 1
    world.say(f"{child.id} wanted to open {prize.it()} right away, but the paper inside looked fragile.")


def warn(world: World, gp: Entity, child: Entity, activity: Activity, prize: Entity) -> bool:
    pred = predict_mess(world, child, activity, prize.id)
    if not pred["soiled"]:
        return False
    world.say(f'"Be gentle," {gp.pronoun("subject")} said. "If you rush, you could ruin the note."')
    return True


