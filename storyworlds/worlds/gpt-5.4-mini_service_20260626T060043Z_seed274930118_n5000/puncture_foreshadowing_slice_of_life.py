def tell(setting: Setting, activity: Activity, prize_cfg: Prize, name: str, gender: str, parent: str) -> World:
    world = World(setting)
    child = world.add(Entity(id=name, kind="character", type=gender))
    grownup = world.add(Entity(id=parent, kind="character", type="mother" if parent == "mother" else "father"))
    prize = world.add(Entity(
        id="prize",
        type=prize_cfg.type,
        label=prize_cfg.label,
        phrase=prize_cfg.phrase,
        owner=child.id,
    ))

    introduce(world, child)
    enjoy_day(world, child, activity)
    bring_prize(world, child, prize)
    world.para()
    foreshadow(world, child, activity)
    head_out(world, child, activity)
    world.para()
    puncture_turn(world, child, prize)
    fix_with_tool(world, child, grownup, activity, prize)
    world.facts.update(
        child=child,
        parent=grownup,
        prize=prize,
        activity=activity,
        setting=setting,
    )
    return world


