def fix_with_tool(world: World, child: Entity, parent: Entity, activity: Activity, prize: Entity) -> None:
    tool = select_tool(activity, prize)
    if tool is None:
        _fallback_pool = globals().get("TOOLS") or globals().get("TOOLES") or []
        if hasattr(_fallback_pool, "values"):
            _fallback_pool = list(_fallback_pool.values())
        tool = next(iter(_fallback_pool), None)
        if tool is None:
            raise StoryError
    world.add(Entity(id=tool.id, type="tool", label=tool.label))
    world.say(
        f"{parent.id} smiled and said, \"Good thing we noticed it early.\""
    )
    world.say(
        f"They used {tool.label} to patch the tire, and soon the wheel held air again."
    )
    world.say(
        f"After that, {child.id} could keep {activity.gerund}, and the little nail was just a story to tell."
    )


