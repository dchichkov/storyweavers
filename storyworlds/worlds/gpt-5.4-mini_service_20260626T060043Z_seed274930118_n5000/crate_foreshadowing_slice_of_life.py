class World:
    def __init__(self, setting: Setting) -> None:
        self.setting = setting
        self.entities: dict[str, Entity] = {}
        self.paragraphs: list[list[str]] = [[]]
        self.zone: set[str] = set()
        self.fired: set[tuple] = set()
        self.facts: dict = {}

    def add(self, ent: Entity) -> Entity:
        self.entities[ent.id] = ent
        return ent

    def get(self, eid: str) -> Entity:
        if eid not in self.entities:
            label = str(eid).replace("_", " ")
            self.entities[eid] = Entity(str(eid), label=label)
        return self.entities[eid]

    def characters(self) -> list[Entity]:
        return [e for e in self.entities.values() if e.kind == "character"]

    def say(self, text: str) -> None:
        if text:
            self.paragraphs[-1].append(text)

    def para(self) -> None:
        if self.paragraphs[-1]:
            self.paragraphs.append([])

    def render(self) -> str:
        return "\n\n".join(" ".join(p) for p in self.paragraphs if p)

    def copy(self) -> "World":
        import copy

        w = World(self.setting)
        w.entities = copy.deepcopy(self.entities)
        w.zone = set(self.zone)
        w.fired = set(self.fired)
        w.paragraphs = [[]]
        return w


def _boost(d: dict[str, float], key: str, amt: float = 1.0) -> None:
    d[key] = d.get(key, 0.0) + amt


def _do_activity(world: World, actor: Entity, activity: Activity, narrate: bool = True) -> None:
    if activity.id not in world.setting.affords:
        pass
    world.zone = set(activity.zone)
    _boost(actor.meters, activity.mess, 1.0)
    _boost(actor.memes, "joy", 1.0)
    if narrate:
        world.say(f"{actor.id} gently did the {activity.gerund} part of the day.")


def prize_at_risk(activity: Activity, prize: Prize) -> bool:
    return prize.region in activity.zone


def select_gear(activity: Activity, prize: Prize) -> Optional[Gear]:
    for gear in GEAR:
        if activity.mess in gear.guards and prize.region in gear.covers:
            return gear
    return None


def predict_mess(world: World, actor: Entity, activity: Activity, prize_id: str) -> dict:
    sim = world.copy()
    _do_activity(sim, sim.get(actor.id), activity, narrate=False)
    prize = sim.entities[prize_id]
    return {"soiled": prize.meters.get("dirty", 0.0) >= THRESHOLD}


def introduce(world: World, child: Entity) -> None:
    world.say(f"{child.id} was a little {child.type} who noticed the small things in a room.")


def foreshadow(world: World, activity: Activity, crate: Entity, prize: Entity) -> None:
    world.say(
        f"On the counter sat {crate.phrase}, and one bright {prize.label} peeked out like a tiny promise."
    )
    world.say(
        f"{activity.keyword.capitalize()} already seemed to be waiting in the room, even before anyone moved anything."
    )


def wants(world: World, child: Entity, activity: Activity, crate: Entity) -> None:
    _boost(child.memes, "desire", 1.0)
    world.say(f"{child.id} wanted to {activity.verb} right away because the {crate.label} looked ready to help.")


def warn(world: World, parent: Entity, child: Entity, activity: Activity, prize: Entity) -> bool:
    pred = predict_mess(world, child, activity, prize.id)
    if not pred["soiled"]:
        return False
    world.say(
        f"'{prize.phrase.capitalize()} could get messy if we hurry,' {parent.label_word} said. "
        f"'Let's make a better plan first.'"
    )
    _boost(child.memes, "pause", 1.0)
    return True


def reach_for(world: World, child: Entity, activity: Activity) -> None:
    _boost(child.memes, "impatience", 1.0)
    world.say(f"{child.id} reached for the {activity.keyword} plan anyway, then slowed down when {child.pronoun()} noticed the wobble.")


def offer_hand(world: World, parent: Entity, child: Entity) -> None:
    _boost(child.memes, "comfort", 1.0)
    world.say(f"Then {parent.label_word} held out a hand, and the room felt calmer again.")


def compromise(world: World, parent: Entity, child: Entity, activity: Activity, prize: Entity) -> Optional[Gear]:
    gear_def = select_gear(activity, prize)
    if gear_def is None:
        return None
    gear = world.add(Entity(
        id=gear_def.id,
        type="gear",
        label=gear_def.label,
        owner=child.id,
        caretaker=parent.id,
        plural=gear_def.plural,
    ))
    gear.worn_by = child.id
    if predict_mess(world, child, activity, prize.id)["soiled"]:
        del world.entities[gear.id]
        return None
    world.say(
        f"{parent.label_word} smiled and said, 'How about we {gear_def.prep} first, then {activity.verb} together?'"
    )
    return gear


def accept(world: World, child: Entity, parent: Entity, activity: Activity, prize: Entity, gear_def: Gear) -> None:
    _boost(child.memes, "joy", 1.0)
    _boost(child.memes, "love", 1.0)
    child.memes["worry"] = 0.0
    world.say(f"{child.id} beamed, hugged {child.pronoun('possessive')} {parent.label_word}, and said yes right away.")
    world.say(
        f"They {gear_def.tail}. Soon {child.id} was {activity.gerund}, and the {prize.label} stayed neat beside the window."
    )


def tell(setting: Setting, activity: Activity, prize_cfg: Prize, hero_name: str, hero_type: str, parent_type: str) -> World:
    world = World(setting)
    child = world.add(Entity(id=hero_name, kind="character", type=hero_type))
    parent = world.add(Entity(id="Parent", kind="character", type=parent_type, label="the parent"))
    crate = world.add(Entity(
        id="crate",
        type="crate",
        label="crate",
        phrase="a small wooden crate of fresh produce",
        owner=parent.id,
    ))
    prize = world.add(Entity(
        id="prize",
        type=prize_cfg.type,
        label=prize_cfg.label,
        phrase=prize_cfg.phrase,
        owner=child.id,
        caretaker=parent.id,
        plural=prize_cfg.plural,
    ))

    introduce(world, child)
    foreshadow(world, activity, crate, prize)
    world.para()
    world.say(f"{child.id} and {child.pronoun('possessive')} {parent.label_word} were in {world.setting.place}.")
    wants(world, child, activity, crate)
    warn(world, parent, child, activity, prize)
    reach_for(world, child, activity)
    offer_hand(world, parent, child)
    world.para()
    gear_def = compromise(world, parent, child, activity, prize)
    if gear_def:
        accept(world, child, parent, activity, prize, gear_def)

    world.facts.update(
        hero=child,
        parent=parent,
        crate=crate,
        prize=prize,
        activity=activity,
        setting=setting,
        gear=gear_def,
        resolved=gear_def is not None,
    )
    return world


