def tell(setting: Setting, action: Action, prize_cfg: Prize, hero_name: str,
         hero_gender: str, parent_type: str, trait: str) -> World:
    world = World(setting)
    hero = world.add(Entity(id=hero_name, kind="character", type=hero_gender, meters={}, memes={}))
    parent = world.add(Entity(id="Parent", kind="character", type=parent_type))
    prize = world.add(Entity(id="Prize", type=prize_cfg.type, label=prize_cfg.label,
                             phrase=prize_cfg.phrase, owner=hero.id, caretaker=parent.id,
                             plural=prize_cfg.plural))
    world.facts.update(hero=hero, parent=parent, prize=prize, action=action, prize_cfg=prize_cfg,
                       trait=trait, setting=setting)

    hero.memes["love"] = 1
    world.say(
        f"{hero.id} was a {trait} little pirate who loved {action.gerund} and watching the tide."
    )
    world.say(
        f"One day, {hero.id}'s {parent_type} brought {hero.pronoun('object')} {prize.phrase}."
    )
    world.say(
        f"{hero.id} liked {prize.label} very much and kept {prize.it()} close as the crew sailed by the wigwam shore."
    )

    # conflict
    world.say(
        f"At {setting.place}, {hero.id} wanted to {action.verb}, but the sea wind made everyone wary."
    )
    if action.id == "media":
        world.say(
            f"{hero.id} reached for the comic pages anyway, and the splash of salt made the others frown."
        )
        hero.memes["defiance"] = 1
        hero.memes["worry"] = 1

    # kindness turn
    gear = select_gear(action, prize)
    if gear is None:
        gear = next(iter(globals().get("GEARS", globals().get("GEAR", []))))
    world.say(
        f"Then {hero.id} did a contrary thing: {hero.pronoun()} stopped, noticed {parent.pronoun('object')}, and offered a kind smile."
    )
    hero.memes["kindness"] = 1
    hero.memes["conflict"] = 0
    world.say(
        f"{parent.pronoun().capitalize()} smiled back and said, \"How about we {gear.prep} and do it together?\""
    )
    world.say(
        f"{hero.id} nodded, and soon they {gear.tail}. The {prize.label} stayed dry, and the little pirate {action.gerund} with a happy heart."
    )

    world.facts["gear"] = gear
    return world


