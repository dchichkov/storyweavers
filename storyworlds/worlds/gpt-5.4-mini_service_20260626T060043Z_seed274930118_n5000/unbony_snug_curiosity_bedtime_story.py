def _comfort_does(world: World, child: Entity, comfort: Comfort) -> None:
    if comfort.id == "pillow":
        child.meters["rest"] += 1
    else:
        child.meters["snug"] += 1
    child.memes["safe"] += 1


def tell(setting: Setting, activity: Activity, comfort: Comfort, name: str, gender: str, parent_type: str) -> World:
    world = World(setting)
    child = world.add(Entity(id=name, kind="character", type=gender))
    parent = world.add(Entity(id="Parent", kind="character", type=parent_type, label=f"the {parent_type}"))
    bed = world.add(Entity(id="bed", type="bed", label="bed"))

    child.memes["curiosity"] += 1
    world.say(f"{child.id} was a little {child.type} who had a curious mind and a tired yawn.")
    world.say(f"At {setting.place}, {child.id} liked bedtime, but {child.pronoun('possessive')} thoughts kept asking for one more look and one more whisper.")
    world.say(f"{child.pronoun().capitalize()} wanted to {activity.verb}, yet {activity.restlessness} kept scratching at {child.pronoun('possessive')} elbows and knees.")

    world.para()
    world.say(f"The room was quiet and dim, and {setting.place} held {child.id} close like a hush.")
    world.say(f"But when {child.id} lay down, the blanket slipped away, and the pillow felt flat.")
    child.meters["rest"] += 0.5
    child.memes["worry"] += 1
    world.say(f"That made {child.id} feel a little prickly and not quite {activity.calm}.")

    world.para()
    world.say(f"{parent_type.capitalize()} came in softly and saw the tiny wriggles under the sheet.")
    world.say(f'"Let us make you more {comfort.effect}," {parent_type} said, and {comfort.action.format(child=child.id)}.')
    _comfort_does(world, child, comfort)
    child.memes["calm"] += 1
    child.memes["worry"] = 0
    world.say(f"The bed grew {comfort.effect}, the room grew warmer, and {child.id} stopped listening to every little creak.")

    world.para()
    world.say(f"At last, {child.id} curled up in a {comfort.effect} little heap and fell asleep with a smile.")
    world.say(f"The {bed.label} stayed still, the blanket stayed tucked, and the night felt soft enough to hold a dream.")

    world.facts.update(child=child, parent=parent, activity=activity, comfort=comfort, setting=setting)
    return world


def generate(params: StoryParams) -> StorySample:
    world = tell(_safe_lookup(SETTINGS, params.setting), _safe_lookup(ACTIVITIES, params.activity), _safe_lookup(COMFORTS, params.comfort),
                 params.name, params.gender, params.parent)
    return StorySample(
        params=params,
        story=world.render(),
        prompts=generation_prompts(world),
        story_qa=story_qa(world),
        world_qa=world_knowledge_qa(world),
        world=world,
    )


