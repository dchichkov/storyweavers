def asp_facts() -> str:
    import asp
    lines = []
    for sid, s in SETTINGS.items():
        lines.append(asp.fact("setting", sid))
        for a in sorted(s.affords):
            lines.append(asp.fact("affords", sid, a))
    for aid, a in ACTIVITIES.items():
        lines.append(asp.fact("activity", aid))
        for t in sorted(a.tags):
            lines.append(asp.fact("tagged", aid, t))
    for cid, c in COMFORTS.items():
        lines.append(asp.fact("comfort", cid))
        for t in sorted(c.tags):
            lines.append(asp.fact("tagged", cid, t))
    return "\n".join(lines)


ASP_RULES = r"""
compatible(S, A, C) :- affords(S, sleep), activity(A), comfort(C).
has_snug_fix(C) :- comfort(C), tagged(C, snug).
valid_story(S, A, C) :- compatible(S, A, C), has_snug_fix(C).
#show valid_story/3.
"""


def asp_program(show: str) -> str:
    return f"{asp_facts()}\n{ASP_RULES}\n{show}\n"


def valid_combos() -> list[tuple[str, str, str]]:
    return [(s, a, c) for s in SETTINGS for a in ACTIVITIES for c in COMFORTS]


def asp_valid_combos() -> list[tuple]:
    import asp
    model = asp.one_model(asp_program("#show valid_story/3."))
    return sorted(set(asp.atoms(model, "valid_story")))


def asp_verify() -> int:
    py = set(valid_combos())
    cl = set(asp_valid_combos())
    if py == cl:
        print(f"OK: clingo gate matches valid_combos() ({len(py)} combos).")
        return 0
    print("MISMATCH between clingo and valid_combos():")
    if py - cl:
        print("  only in python:", sorted(py - cl))
    if cl - py:
        print("  only in clingo:", sorted(cl - py))
    return 1


