def _r_shadow(world: World) -> list[str]:
    out: list[str] = []
    for actor in world.characters():
        if actor.meters["dark"] < THRESHOLD:
            continue
        for item in world.worn_items(actor):
            if item.protective:
                continue
            sig = ("shadow", item.id)
            if sig in world.fired:
                continue
            world.fired.add(sig)
            item.meters["dark"] += 1
            out.append(f"{actor.id}'s {item.label} looked dim in the shade.")
    return out


def _r_safe(world: World) -> list[str]:
    out: list[str] = []
    for actor in world.characters():
        if actor.memes["joy"] < THRESHOLD:
            continue
        sig = ("safe", actor.id)
        if sig in world.fired:
            continue
        world.fired.add(sig)
        actor.meters["safe"] += 1
        out.append(f"The little plan felt safe and bright.")
    return out


CAUSAL_RULES = [
    Rule("shadow", "physical", _r_shadow),
    Rule("safe", "social", _r_safe),
]


def propagate(world: World, narrate: bool = True) -> list[str]:
    produced: list[str] = []
    changed = True
    for _ in range(len(globals().get("CAUSAL_RULES", [])) + 4):
        changed = False
        for rule in CAUSAL_RULES:
            sents = rule.apply(world)
            if sents:
                changed = True
                produced.extend(sents)
    if narrate:
        for s in produced:
            world.say(s)
    return produced


def prize_at_risk(activity: Activity, prize: Prize) -> bool:
    return prize.region in activity.zone


def select_gear(activity: Activity, prize: Prize) -> Optional[Gear]:
    for gear in GEAR:
        if activity.mess in gear.guards and prize.region in gear.covers:
            return gear
    return None


def tell(setting: Setting, activity: Activity, prize_cfg: Prize,
         hero_name: str = "Mila", hero_type: str = "girl",
         hero_traits: Optional[list[str]] = None, parent_type: str = "mother") -> World:
    world = World(setting)

    hero = world.add(Entity(
        id=hero_name, kind="character", type=hero_type,
        traits=["little"] + (hero_traits or ["bright", "curious"]),
    ))
    parent = world.add(Entity(id="Parent", kind="character", type=parent_type, label="mom"))
    prize = world.add(Entity(
        id="prize", type=prize_cfg.type, label=prize_cfg.label,
        phrase=prize_cfg.phrase, owner=hero.id, caretaker=parent.id,
        region=prize_cfg.region, plural=prize_cfg.plural,
    ))

    hero.memes["love"] += 1
    world.say(f"{hero.id} was a little {hero.traits[1]} {hero.type} who loved rhyme and shine.")
    world.say(f"{hero.id} liked to make words chime, time after time.")
    world.say(f"{hero.id}'s {parent.label} bought {hero.pronoun('object')} {prize.phrase}.")
    prize.worn_by = hero.id
    world.say(f"{hero.id} loved {prize.phrase} and wore {prize.label} all day long.")

    world.para()
    world.say(f"One dusk, {hero.id} and {hero.pronoun('possessive')} {parent.label} went to {setting.place}.")
    world.say(f"There was a dark little nook there, and the shadows felt thick as a book.")
    world.say(f"{hero.id} wanted to {activity.verb}, but {hero.pronoun('possessive')} {parent.label} grew wary and quick.")

    if prize_at_risk(activity, prize):
        hero.memes["worry"] += 1
        world.say(f'"If we rush in, your {prize.label} will get {activity.soil}," {parent.label} said with a sigh.')
    else:
        pass

    world.say(f"{hero.id} took a breath and began to rhyme, soft as a chime.")
    world.say(f"{hero.id} tried to {activity.rush}, but {hero.pronoun('possessive')} {parent.label} held up a light.")
    hero.meters["dark"] += 1
    propagate(world, narrate=False)

    gear_def = select_gear(activity, prize)
    if gear_def is None:
        pass

    gear = world.add(Entity(
        id=gear_def.id, type="gear", label=gear_def.label,
        owner=hero.id, caretaker=parent.id, protective=True,
        covers=set(gear_def.covers), plural=gear_def.plural,
    ))
    gear.worn_by = hero.id

    world.say(f'"How about we {gear_def.prep}," {parent.label} said, "and do it the bright, safe way?"')
    hero.memes["joy"] += 1
    hero.memes["love"] += 1
    hero.memes["worry"] = 0.0
    world.say(f"{hero.id}'s face lit up. {hero.id} hugged {hero.pronoun('possessive')} {parent.label} tight.")
    world.say(f'"Yes, yes!" {hero.id} said. "We can rhyme and shine!"')

    world.para()
    world.say(f"They {gear_def.tail}. Soon the lantern glow could penetrate the dark nook.")
    world.say(f"{hero.id} was {activity.gerund}, and the little light slipped in like a kite on a string.")
    world.say(f"The shadows grew thinner, the corner grew clear, and the day felt merry and near.")

    world.facts.update(
        hero=hero,
        parent=parent,
        prize=prize,
        prize_cfg=prize_cfg,
        activity=activity,
        setting=setting,
        gear=gear_def,
        conflict=True,
        resolved=True,
    )
    return world


