class World:
    def __init__(self, setting: Setting):
        self.setting = setting
        self.entities: dict[str, Entity] = {}
        self.fired: set[tuple] = set()
        self.paragraphs: list[list[str]] = [[]]
        self.zone: set[str] = set()
        self.weather: str = ""
        self.facts: dict = {}

    def add(self, ent: Entity) -> Entity:
        self.entities[ent.id] = ent
        return ent

    def get(self, eid: str) -> Entity:
        if eid not in self.entities:
            label = str(eid).replace("_", " ")
            self.entities[eid] = Entity(str(eid), label=label)
        return self.entities[eid]

    def characters(self) -> list[Entity]:
        return [e for e in self.entities.values() if e.kind == "character"]

    def worn_items(self, actor: Entity) -> list[Entity]:
        return [e for e in self.entities.values() if e.worn_by == actor.id]

    def covered(self, actor: Entity, region: str) -> bool:
        return any(g.protective and region in g.covers for g in self.worn_items(actor))

    def say(self, text: str) -> None:
        if text:
            self.paragraphs[-1].append(text)

    def para(self) -> None:
        if self.paragraphs[-1]:
            self.paragraphs.append([])

    def render(self) -> str:
        return "\n\n".join(" ".join(p) for p in self.paragraphs if p)


def _r_cold(world: World) -> list[str]:
    out = []
    for actor in world.characters():
        if actor.meters["cold"] < THRESHOLD:
            continue
        for item in world.worn_items(actor):
            if item.protective or item.region not in world.zone:
                continue
            if world.covered(actor, item.region):
                continue
            sig = ("cold", item.id)
            if sig in world.fired:
                continue
            world.fired.add(sig)
            item.meters["cold"] += 1
            item.meters["whole"] -= 1
            out.append(f"The chill slipped through and nipped at {actor.id}'s {item.label}.")
    return out


def _r_tremor(world: World) -> list[str]:
    out = []
    for actor in world.characters():
        if actor.memes["fear"] < THRESHOLD:
            continue
        sig = ("tremor", actor.id)
        if sig in world.fired:
            continue
        world.fired.add(sig)
        actor.meters["tremor"] += 1
        out.append(f"{actor.id}'s hands trembled in the dark.")
    return out


RULES = [_r_cold, _r_tremor]


def propagate(world: World, narrate: bool = True) -> list[str]:
    produced: list[str] = []
    changed = True
    for _ in range(len(globals().get("CAUSAL_RULES", [])) + 4):
        changed = False
        for rule in RULES:
            sents = rule(world)
            if sents:
                changed = True
                produced.extend(sents)
    if narrate:
        for s in produced:
            world.say(s)
    return produced


def prize_at_risk(activity: Activity, prize: Prize) -> bool:
    return prize.region in activity.zone


def select_gear(activity: Activity, prize: Prize) -> Optional[Gear]:
    for gear in GEAR:
        if activity.mess in gear.guards and prize.region in gear.covers:
            return gear
    return None


def predict_mess(world: World, actor: Entity, activity: Activity, prize_id: str) -> dict:
    sim = world.copy()
    _do_activity(sim, sim.get(actor.id), activity, narrate=False)
    prize = sim.entities.get(prize_id)
    return {"soiled": bool(prize and prize.meters["cold"] >= THRESHOLD)}


def activity_detail(activity: Activity) -> str:
    return {
        "knocks": "The house answered with one soft knock after another, all the way to ninety.",
        "attic": "Up above, the attic stair breathed out a thin, chilly draft.",
        "whisper": "The whisper in the hallway sounded like someone turning a page slowly.",
    }.get(activity.id, "The old place felt full of listening shadows.")


def setting_detail(setting: Setting, activity: Activity) -> str:
    if setting.indoor:
        return f"{setting.place.capitalize()} held its breath, and {activity_detail(activity)}"
    return f"{setting.place.capitalize()} looked pale in the dark, and {activity_detail(activity)}"


def _do_activity(world: World, actor: Entity, activity: Activity, narrate: bool = True) -> None:
    if activity.id not in world.setting.affords:
        pass
    world.zone = set(activity.zone)
    actor.meters[activity.mess] += 1
    actor.memes["curiosity"] += 1
    propagate(world, narrate=narrate)


def tell(setting: Setting, activity: Activity, prize_cfg: Prize, hero_name: str, hero_type: str,
         hero_traits: Optional[list[str]] = None, guardian_type: str = "mother") -> World:
    world = World(setting)
    world.weather = "" if setting.indoor else activity.weather

    hero = world.add(Entity(
        id=hero_name, kind="character", type=hero_type,
        traits=[],
    ))
    guardian = world.add(Entity(id="Guardian", kind="character", type=guardian_type, label="the guardian"))
    ghost = world.add(Entity(id="Ghost", kind="character", type="thing", label="the ghost"))
    prize = world.add(Entity(
        id="prize", type=prize_cfg.type, label=prize_cfg.label,
        phrase=prize_cfg.phrase, owner=hero.id, caretaker=guardian.id,
        region=prize_cfg.region, plural=prize_cfg.plural,
    ))

    hero.traits = ["little"] + (hero_traits or ["careful"])
    prize.worn_by = hero.id

    hero.memes["love"] += 1
    guardian.memes["care"] += 1
    world.say(f"{hero.id} was a little {hero.type} who loved {prize.phrase}.")
    world.say(f"{hero.id}'s {guardian.label_word if hasattr(guardian, 'label_word') else guardian.type} had given {hero.id} {prize.phrase} to wear.")
    world.say(f"Then the house went quiet, as if it was waiting for {hero.id} to hear something.")

    world.para()
    world.say(setting_detail(setting, activity))
    world.say(f"{hero.id} wanted to {activity.verb}, but {hero.pronoun('possessive')} guardian lifted a warning hand.")
    world.say(f'"Don’t answer the ninety knocks," {hero.pronoun("possessive")} guardian said. "The cold in that hall can spoil what you love."')
    hero.memes["fear"] += 1
    hero.memes["courage"] += 1
    world.say(f"But the warning only made {hero.id} more curious.")
    world.say(f"{hero.pronoun().capitalize()} tried to {activity.rush}, and the dark seemed to lean closer.")

    _do_activity(world, hero, activity, narrate=True)

    world.para()
    ghost.memes["sadness"] += 1
    world.say(f"At the end of the hallway, {hero.id} found the ghost.")
    world.say(f"It was not wearing chains or a torn sheet. It held a tiny key and looked lonely, not cruel.")
    world.say(f"The ghost explained that the ninety knocks came from an old clock it had been trying to finish for years.")
    world.say(f"The clock had stopped before it could ring for the child it once watched over.")
    hero.memes["fear"] = max(0.0, hero.memes["fear"] - 1)
    hero.memes["trust"] += 1
    world.say(f"{hero.id} understood the twist: the haunting was really a waiting room for a promise.")
    world.say(f"{hero.id} and {hero.pronoun('possessive')} guardian helped wind the clock, while the ghost stood very still and hoped.")

    if activity.id == "knocks":
        world.say("The clock gave one breath, then another, until the whole house sounded warm instead of strange.")
    else:
        world.say("The chill eased, and the hallway stopped feeling so sharp.")

    world.para()
    gear_def = None
    if prize_at_risk(activity, prize):
        gear_def = select_gear(activity, prize)
    if gear_def:
        gear = world.add(Entity(
            id=gear_def.id, type="gear", label=gear_def.label,
            owner=hero.id, caretaker=guardian.id, protective=True,
            covers=set(gear_def.covers), plural=gear_def.plural,
        ))
        gear.worn_by = hero.id
        hero.memes["relief"] += 1
        world.say(f"Before the last step, {hero.id}'s guardian put on {gear_def.label}.")
        world.say(f"With {gear_def.label}, {hero.id} could stay in the hall without ruining {hero.pronoun('possessive')} {prize.label}.")
    hero.memes["courage"] += 1
    hero.memes["trust"] += 1
    world.say(f"In the end, {hero.id} was no longer afraid of the ghost.")
    world.say(f"{hero.id} left the old place with {prize.phrase} safe, the clock ticking softly, and the ghost no longer lonely.")

    world.facts.update(
        hero=hero,
        guardian=guardian,
        ghost=ghost,
        prize=prize,
        prize_cfg=prize_cfg,
        activity=activity,
        setting=setting,
        gear=gear_def,
        resolved=True,
    )
    return world


def _entity_copy(self: World) -> World:
    clone = World(self.setting)
    clone.entities = {k: Entity(**{
        "id": e.id, "kind": e.kind, "type": e.type, "label": e.label, "phrase": e.phrase,
        "owner": e.owner, "caretaker": e.caretaker, "worn_by": e.worn_by, "region": e.region,
        "protective": e.protective, "covers": set(e.covers), "plural": e.plural,
        "meters": dict(e.meters), "memes": dict(e.memes),
    }) for k, e in self.entities.items()}
    clone.fired = set(self.fired)
    clone.zone = set(self.zone)
    clone.weather = self.weather
    return clone


