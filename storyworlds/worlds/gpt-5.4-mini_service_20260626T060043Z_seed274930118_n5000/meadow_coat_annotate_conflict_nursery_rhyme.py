def _r_soil(world: World) -> list[str]:
    out: list[str] = []
    for actor in world.characters():
        if actor.meters["mess"] < THRESHOLD:
            continue
        for item in world.worn_items(actor):
            if item.protective or item.region not in world.zone:
                continue
            if world.covered(actor, item.region):
                continue
            sig = ("soil", actor.id, item.id)
            if sig in world.fired:
                continue
            world.fired.add(sig)
            item.meters["dirty"] += 1
            out.append(f"{actor.pronoun('possessive').capitalize()} {item.label} got dirty.")
    return out


def _r_conflict(world: World) -> list[str]:
    for actor in world.characters():
        if actor.memes["worry"] >= THRESHOLD and actor.memes["desire"] >= THRESHOLD:
            sig = ("conflict", actor.id)
            if sig in world.fired:
                continue
            world.fired.add(sig)
            actor.memes["conflict"] += 1
            return ["__conflict__"]
    return []


RULES = [Rule("soil", _r_soil), Rule("conflict", _r_conflict)]


def propagate(world: World, narrate: bool = True) -> list[str]:
    out: list[str] = []
    changed = True
    for _ in range(len(globals().get("CAUSAL_RULES", [])) + 4):
        changed = False
        for rule in RULES:
            sents = rule.apply(world)
            if sents:
                changed = True
                out.extend(s for s in sents if s != "__conflict__")
    if narrate:
        for s in out:
            world.say(s)
    return out


def prize_at_risk(activity: Activity, prize: Prize) -> bool:
    return prize.region in activity.zone


def select_gear(activity: Activity, prize: Prize) -> Optional[Gear]:
    for gear in GEAR:
        if activity.mess in gear.guards and prize.region in gear.covers:
            return gear
    return None


def predict_mess(world: World, actor: Entity, activity: Activity, prize_id: str) -> dict:
    sim = world.copy()
    _do_activity(sim, sim.get(actor.id), activity, narrate=False)
    prize = sim.entities[prize_id]
    return {
        "soiled": bool(prize.meters["dirty"] >= THRESHOLD),
        "conflict": sum(e.memes["conflict"] for e in sim.characters()) >= THRESHOLD,
    }


def _do_activity(world: World, actor: Entity, activity: Activity, narrate: bool = True) -> None:
    if activity.id not in world.setting.affords:
        pass
    world.zone = set(activity.zone)
    actor.meters["mess"] += 1
    actor.memes["joy"] += 1
    propagate(world, narrate=narrate)


def introduce(world: World, hero: Entity) -> None:
    world.say(f"Little {hero.id} went skip-skip-skip through {world.setting.place}.")


def loves_activity(world: World, hero: Entity, activity: Activity) -> None:
    world.say(f"{hero.pronoun().capitalize()} loved to {activity.gerund} and sing a soft little tune.")


def loves_prize(world: World, hero: Entity, prize: Entity) -> None:
    hero.memes["love"] += 1
    prize.worn_by = hero.id
    world.say(f"{hero.id} wore {hero.pronoun('possessive')} {prize.label} and hugged {(getattr(prize, 'it')() if callable(getattr(prize, 'it', None)) else getattr(prize, 'it', 'it'))} close.")


def arrive(world: World, hero: Entity, parent: Entity, activity: Activity) -> None:
    world.say(f"One bright day, {hero.id} and {hero.pronoun('possessive')} {parent.label} came to {world.setting.place}.")
    world.say("The grass was green, and the air was still and sweet.")


def wants(world: World, hero: Entity, activity: Activity) -> None:
    hero.memes["desire"] += 1
    world.say(f"{hero.id} wanted to {activity.verb} at once.")


def warn(world: World, parent: Entity, hero: Entity, activity: Activity, prize: Entity) -> bool:
    pred = predict_mess(world, hero, activity, prize.id)
    if not pred["soiled"]:
        return False
    hero.memes["worry"] += 1
    world.facts["predicted_soil"] = activity.soil
    world.say(f'"Mind the {prize.label}," {parent.label} said. "It will get {activity.soil} out here."')
    return True


def conflict(world: World, hero: Entity, activity: Activity) -> None:
    if hero.memes["worry"] >= THRESHOLD:
        world.say(f"{hero.id} frowned and tried to {activity.rush}.")
        hero.memes["conflict"] += 1


def offer(world: World, parent: Entity, hero: Entity, activity: Activity, prize: Entity) -> Optional[Gear]:
    gear = select_gear(activity, prize)
    if gear is None:
        return None
    if predict_mess(world, hero, activity, prize.id)["soiled"]:
        return None
    g = world.add(Entity(
        id=gear.id, type="gear", label=gear.label,
        protective=True, covers=set(gear.covers), plural=gear.plural,
        owner=hero.id, caretaker=parent.id,
    ))
    g.worn_by = hero.id
    world.say(f'{parent.label} smiled and said, "Let us {gear.prep} first."')
    return gear


def accept(world: World, parent: Entity, hero: Entity, activity: Activity, prize: Entity, gear_def: Gear) -> None:
    hero.memes["joy"] += 1
    hero.memes["love"] += 1
    hero.memes["conflict"] = 0.0
    world.say(f"{hero.id} clapped and gave {parent.label} a hug.")
    world.say(f"Then they {gear_def.tail}, and {hero.id} went on to {activity.verb} while {prize.label} stayed clean.")


def tell(setting: Setting, activity: Activity, prize_cfg: Prize, hero_name: str = "Milly", hero_type: str = "girl", parent_type: str = "mother") -> World:
    world = World(setting)
    world.weather = activity.weather
    hero = world.add(Entity(id=hero_name, kind="character", type=hero_type))
    parent = world.add(Entity(id="Parent", kind="character", type=parent_type, label="the parent"))
    prize = world.add(Entity(
        id="prize", type=prize_cfg.type, label=prize_cfg.label, phrase=prize_cfg.phrase,
        owner=hero.id, caretaker=parent.id, region=prize_cfg.region, plural=prize_cfg.plural,
    ))

    introduce(world, hero)
    loves_activity(world, hero, activity)
    loves_prize(world, hero, prize)

    world.para()
    arrive(world, hero, parent, activity)
    wants(world, hero, activity)
    warn(world, parent, hero, activity, prize)
    conflict(world, hero, activity)

    world.para()
    gear_def = offer(world, parent, hero, activity, prize)
    if gear_def:
        accept(world, parent, hero, activity, prize, gear_def)

    world.facts.update(hero=hero, parent=parent, prize=prize, prize_cfg=prize_cfg, activity=activity, gear=gear_def)
    return world


