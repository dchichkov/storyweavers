def explain_rejection(activity: Activity, prize: Prize) -> str:
    if not prize_at_risk(activity, prize):
        return "(No story: that prize would not be at risk in this activity.)"
    if select_gear(activity, prize) is None:
        return "(No story: there is no fitting gear to make this conflict resolve cleanly.)"
    return "(No story: the requested options do not make a reasonable tale.)"


