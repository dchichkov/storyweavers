def predict_mess(world: World, actor: Entity, activity: Activity, prize_id: str) -> dict:
    sim = world.copy()
    _do_activity(sim, sim.get(actor.id), activity, narrate=False)
    prize = sim.entities.get(prize_id)
    return {
        "soiled": bool(prize and prize.meters.get("dirty", 0.0) >= THRESHOLD),
        "worry": sim.get(actor.id).memes.get("worry", 0.0),
    }


def setting_detail(setting: Setting, activity: Activity) -> str:
    if setting.indoor:
        return f"Inside, the room waited quiet as a mouse, though the day outside still had a big voice."
    if activity.weather == "windy":
        return f"The wind came galloping over {setting.place}, rattling every loose board like a bucket of buttons."
    if activity.weather == "dusk":
        return f"The sky over {setting.place} turned the color of warm corn bread."
    return f"{setting.place.capitalize()} stood open and wide, ready for trouble or fun."


def introduce(world: World, hero: Entity) -> None:
    trait = next((t for t in hero.traits if t != "little"), "cheerful")
    world.say(f"{hero.id} was a little {trait} {hero.type} with ears open for any sign the day wanted to give.")
    world.say(f"People said {hero.id} could hear a hinge think and a cloud sigh before anybody else did.")


