def arrive(world: World, hero: Entity, parent: Entity, activity: Activity) -> None:
    day = {"windy": "One windy day, ", "dusk": "One dusky evening, "}.get(world.weather, "One day, ")
    go = "went to" if not world.setting.indoor else "were in"
    world.say(f"{day}{hero.id} and {hero.pronoun('possessive')} {parent.label} {go} {world.setting.place}.")
    world.say(setting_detail(world.setting, activity))


def foreshadow(world: World, hero: Entity, activity: Activity) -> None:
    hero.memes["foreshadow"] = hero.memes.get("foreshadow", 0.0) + 1
    clues = {
        "bridge": "The first clue was a bridge rail that gave one long, nervous creak before anybody touched it.",
        "windmill": "The second clue was the windmill's blade tapping slow, then fast, then slow again like a warning drum.",
        "barn": "The third clue was a flock of sparrows lifting off all at once, as if the sky had cleared its throat.",
        "river": "The river itself kept whispering against the bank in a way that sounded like it was trying to say, not yet.",
    }
    world.say(clues.get(world.setting.place.split()[-1], "A small sign of trouble winked at the edge of the day."))
    world.say(f"That was the sort of sign that told {hero.id} a big change was brewing.")
    propagate(world, narrate=False)


def wants(world: World, hero: Entity, parent: Entity, activity: Activity) -> None:
    hero.memes["desire"] = hero.memes.get("desire", 0.0) + 1
    world.say(f"{hero.id} wanted to {activity.verb} right away, because the day looked daring and the air looked full of stories.")


def warn(world: World, parent: Entity, hero: Entity, activity: Activity, prize: Entity) -> bool:
    pred = predict_mess(world, hero, activity, prize.id)
    if not pred["soiled"]:
        return False
    hero.memes["worry"] = hero.memes.get("worry", 0.0) + 1
    world.say(f'"If you go to {activity.verb}, your {prize.label} will get {activity.soil}," {parent.label} said.')
    world.say(f'"I saw the signs early, and early signs are the best kind," {parent.label} added.')
    return True


def defies(world: World, hero: Entity, activity: Activity) -> None:
    hero.memes["defiance"] = hero.memes.get("defiance", 0.0) + 1
    world.say(f"{hero.id} pretended not to hear the warning and tried to {activity.rush}.")


def grab_conflict(world: World, hero: Entity, parent: Entity) -> None:
    hero.memes["grabbed_by"] = hero.memes.get("grabbed_by", 0.0) + 1
    hero.memes["conflict"] = hero.memes.get("conflict", 0.0) + 1
    world.say(f"Then {parent.label} caught {hero.pronoun('possessive')} hand, and the air crackled with a little storm of feelings.")


def pout(world: World, hero: Entity) -> None:
    if hero.memes.get("conflict", 0.0) >= THRESHOLD:
        world.say(f"{hero.id} pouted so hard it could have bent a sunflower.")
        world.say(f'"But I wanted to go now!" {hero.pronoun()} said.')


def compromise(world: World, parent: Entity, hero: Entity, activity: Activity, prize: Entity) -> Optional[Gear]:
    gear_def = select_gear(activity, prize)
    if gear_def is None:
        return None
    if predict_mess(world, hero, activity, prize.id)["soiled"]:
        gear = world.add(Entity(id=gear_def.id, type="gear", label=gear_def.label, protective=True, worn_by=hero.id))
        gear.worn_by = hero.id
        gear.owner = hero.id
        world.say(f"{parent.label} looked once at the signs, once at {prize.label}, and said, {gear_def.prep}.")
        return gear_def
    return None


def accept(world: World, parent: Entity, hero: Entity, activity: Activity, prize: Entity, gear_def: Gear) -> None:
    hero.memes["joy"] = hero.memes.get("joy", 0.0) + 1
    hero.memes["love"] = hero.memes.get("love", 0.0) + 1
    hero.memes["conflict"] = 0.0
    world.say(f"{hero.id}'s face opened up like a barn door in spring.")
    world.say(f'"All right," {hero.pronoun()} said. "Let’s do it the safe way."')
    world.say(f"Together they {gear_def.tail}, and soon {hero.id} was {activity.gerund}, while {prize.label} stayed clean and the warning had turned into wisdom.")
    world.say(f"The last image was simple and proud: {parent.label} smiling beside {hero.id}, with the wind going on about its business and getting nowhere with the story.")


def _do_activity(world: World, actor: Entity, activity: Activity, narrate: bool = True) -> None:
    if activity.id not in world.setting.affords:
        return
    world.zone = set(activity.zone)
    actor.meters[activity.mess] = actor.meters.get(activity.mess, 0.0) + 1
    actor.memes["joy"] = actor.memes.get("joy", 0.0) + 1
    propagate(world, narrate=narrate)


def tell(setting: Setting, activity: Activity, prize_cfg: Prize, hero_name: str = "Nora", hero_type: str = "girl", hero_traits: Optional[list[str]] = None, parent_type: str = "mother") -> World:
    world = World(setting)
    world.weather = "" if setting.indoor else activity.weather

    hero = world.add(Entity(id=hero_name, kind="character", type=hero_type, traits=["little"] + (hero_traits or ["curious", "stubborn"])))
    parent = world.add(Entity(id="Parent", kind="character", type=parent_type, label="the parent"))
    prize = world.add(Entity(id="prize", type=prize_cfg.type, label=prize_cfg.label, phrase=prize_cfg.phrase, owner=hero.id, caretaker=parent.id, region=prize_cfg.region, plural=prize_cfg.plural))

    introduce(world, hero)
    loves_activity(world, hero, activity)
    buys(world, parent, hero, prize)
    loves_prize(world, hero, prize)

    world.para()
    arrive(world, hero, parent, activity)
    foreshadow(world, hero, activity)
    wants(world, hero, parent, activity)
    warn(world, parent, hero, activity, prize)
    defies(world, hero, activity)
    grab_conflict(world, hero, parent)

    world.para()
    pout(world, hero)
    gear_def = compromise(world, parent, hero, activity, prize)
    if gear_def:
        accept(world, parent, hero, activity, prize, gear_def)

    world.facts.update(hero=hero, parent=parent, prize=prize, prize_cfg=prize_cfg, activity=activity, setting=setting, gear=gear_def, conflict=hero.memes.get("conflict", 0.0) >= THRESHOLD, resolved=gear_def is not None)
    return world


