def _do_activity(world: World, actor: Entity, activity: Activity, narrate: bool = True) -> None:
    world.zone = set(activity.zone)
    actor.memes["hope"] += 1
    actor.memes["suspense"] += 1
    if world.setting.outdoors:
        actor.meters[WET] += 1
    if narrate:
        world.say(f"{actor.id} wanted to {activity.verb}.")


def predict_mess(world: World, actor: Entity, activity: Activity, prize_id: str) -> dict:
    sim = world.copy()
    _do_activity(sim, sim.get(actor.id), activity, narrate=False)
    prize = sim.get(prize_id)
    return {
        "wet": prize.meters[WET] >= THRESHOLD,
        "dirty": prize.meters[DIRTY] >= THRESHOLD,
    }


def introduce(world: World, hero: Entity) -> None:
    trait = next((t for t in [hero.type] if t), "child")
    world.say(f"{hero.id} was a little {trait} who loved making small spaces feel safe.")


def want_story(world: World, hero: Entity, activity: Activity, prize: Entity) -> None:
    hero.memes["hope"] += 1
    world.say(
        f"{hero.id} loved the idea of {activity.gerund}, especially because {hero.pronoun('possessive')} "
        f"{prize.label} could become the softest place in the whole house."
    )


def warn(world: World, parent: Entity, hero: Entity, activity: Activity, prize: Entity) -> bool:
    pred = predict_mess(world, hero, activity, prize.id)
    if not pred["wet"]:
        return False
    parent.memes["worry"] += 1
    world.facts["predicted_wet"] = True
    world.say(
        f'"If we rush it outside, {activity.risk}," {parent.pronoun("subject").capitalize()} said. '
        f'"Let us think first."'
    )
    return True


def suspense_pause(world: World, hero: Entity) -> None:
    hero.memes["suspense"] += 1
    world.say(
        f"{hero.id} peered at the clouds, then at the mattress, and held still for a second."
    )


def search_under(world: World, hero: Entity, kitten: Entity) -> None:
    hero.memes["suspense"] += 1
    kitten.meters[SAFE] += 1
    kitten.memes["worry"] += 1
    world.say(
        f"When {hero.id} lifted the corner, there was a tiny kitten tucked under the mattress."
    )
    world.say(
        f"It was shivering, soft as a puffball, and looking up with round worried eyes."
    )


def protect(world: World, parent: Entity, hero: Entity, gear: Gear, activity: Activity, prize: Entity) -> None:
    hero.memes["relief"] += 1
    parent.memes["relief"] += 1
    world.say(
        f"Then {parent.id} smiled and said they should {gear.prep} before anyone moved the mattress."
    )
    world.say(
        f"They {gear.tail}, and the old mattress stayed dry enough to use for a cozy nest."
    )


def resolve(world: World, hero: Entity, kitten: Entity, prize: Entity) -> None:
    hero.memes["tenderness"] += 1
    hero.memes["joy"] += 1
    kitten.meters[WARMTH] += 1
    kitten.meters[SAFE] += 1
    world.say(
        f"{hero.id} made a little bed for the kitten beside the mattress, and the kitten curled into a warm ball."
    )
    world.say(
        f"By bedtime, the mattress was clean, dry, and perfect for a sleepy story, "
        f"and {hero.id} had a new friend to cuddle nearby."
    )


