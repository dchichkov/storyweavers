def tell(setting: Setting, activity: Activity, prize_cfg: Prize, hero_name: str,
         hero_type: str, parent_type: str = "mother", trait: str = "gentle") -> World:
    world = World(setting)
    world.weather = activity.weather if setting.outdoors else ""

    hero = world.add(Entity(id=hero_name, kind="character", type=hero_type))
    parent = world.add(Entity(id="Parent", kind="character", type=parent_type))
    prize = world.add(Entity(
        id="mattress",
        type="mattress",
        label="mattress",
        phrase=prize_cfg.phrase,
        owner=hero.id,
        caretaker=parent.id,
    ))
    kitten = world.add(Entity(
        id="Kitten",
        kind="animal",
        type="kitten",
        label="kitten",
        phrase="a tiny shivering kitten",
    ))

    introduce(world, hero)
    want_story(world, hero, activity, prize)

    world.para()
    world.say(f"Late in the afternoon, {hero.id} and {hero.pronoun('possessive')} {parent.pronoun('subject')} went to {setting.place}.")
    world.say(f"The sky looked gray, and {activity.keyword} was exactly the thing that could make the mattress tricky.")
    warn(world, parent, hero, activity, prize)
    suspense_pause(world, hero)
    search_under(world, hero, kitten)

    world.para()
    gear = select_gear(activity, prize)
    if gear is None:
        gear = next(iter(globals().get("GEARS", globals().get("GEAR", []))))
    protect(world, parent, hero, gear, activity, prize)
    resolve(world, hero, kitten, prize)

    world.facts.update(
        hero=hero,
        parent=parent,
        prize=prize,
        kitten=kitten,
        activity=activity,
        gear=gear,
        setting=setting,
        trait=trait,
        resolved=True,
    )
    return world


