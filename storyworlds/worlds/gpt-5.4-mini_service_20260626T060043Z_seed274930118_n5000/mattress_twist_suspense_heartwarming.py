def generation_prompts(world: World) -> list[str]:
    f = world.facts
    hero = _safe_fact(world, f, "hero")
    act = _safe_fact(world, f, "activity")
    return [
        f'Write a warm story for a young child about a {hero.type} named {hero.id}, a mattress, and a surprising discovery.',
        f'Write a suspenseful but heartwarming story in which {hero.id} wants to {act.verb} and the family must protect the mattress.',
        f'Write a gentle bedtime story that includes the word "mattress" and ends with a comforting twist.',
    ]


def story_qa(world: World) -> list[QAItem]:
    f = world.facts
    hero = _safe_fact(world, f, "hero")
    parent = _safe_fact(world, f, "parent")
    prize = _safe_fact(world, f, "prize")
    kitten = _safe_fact(world, f, "kitten")
    act = _safe_fact(world, f, "activity")
    gear = _safe_fact(world, f, "gear")

    return [
        QAItem(
            question=f"What did {hero.id} want to do with the {prize.label}?",
            answer=f"{hero.id} wanted to {act.verb}, so the mattress could become a cozy reading nest.",
        ),
        QAItem(
            question=f"Why did {parent.pronoun('subject').capitalize()} worry about the mattress?",
            answer=f"{parent.pronoun('subject').capitalize()} worried because {act.risk}, especially with the rainy sky and the mattress outside.",
        ),
        QAItem(
            question="What was the twist in the story?",
            answer=f"When they looked under the mattress, they found a tiny kitten hiding there.",
        ),
        QAItem(
            question=f"How did {gear.label} help?",
            answer=f"It kept the mattress dry enough to stay cozy while they made the nest.",
        ),
        QAItem(
            question=f"How did the story end for the kitten?",
            answer=f"The kitten got warm, safe, and happy beside the mattress, where {hero.id} could keep it company.",
        ),
    ]


