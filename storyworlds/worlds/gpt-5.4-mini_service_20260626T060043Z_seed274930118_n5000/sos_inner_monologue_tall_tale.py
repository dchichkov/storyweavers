class World:
    def __init__(self, setting: Setting) -> None:
        self.setting = setting
        self.entities: dict[str, Entity] = {}
        self.fired: set[tuple] = set()
        self.paragraphs: list[list[str]] = [[]]
        self.facts: dict = {}

    def add(self, ent: Entity) -> Entity:
        self.entities[ent.id] = ent
        return ent

    def get(self, eid: str) -> Entity:
        if eid not in self.entities:
            label = str(eid).replace("_", " ")
            self.entities[eid] = Entity(str(eid), label=label)
        return self.entities[eid]

    def characters(self) -> list[Entity]:
        return [e for e in self.entities.values() if e.kind == "character"]

    def say(self, text: str) -> None:
        if text:
            self.paragraphs[-1].append(text)

    def para(self) -> None:
        if self.paragraphs[-1]:
            self.paragraphs.append([])

    def render(self) -> str:
        return "\n\n".join(" ".join(p) for p in self.paragraphs if p)

    def copy(self) -> "World":
        import copy as _copy

        clone = World(self.setting)
        clone.entities = _copy.deepcopy(self.entities)
        clone.fired = set(self.fired)
        clone.paragraphs = [[]]
        clone.facts = dict(self.facts)
        return clone

    def worn_or_carried_items(self, actor: Entity) -> list[Entity]:
        out = []
        for e in self.entities.values():
            if e.worn_by == actor.id or e.carried_by == actor.id:
                out.append(e)
        return out

    def covered(self, actor: Entity, region: str) -> bool:
        return any(item.protective and region in item.covers for item in self.worn_or_carried_items(actor))


def _add_meter(ent: Entity, key: str, amount: float = 1.0) -> None:
    ent.meters[key] = ent.meters.get(key, 0.0) + amount


def _add_meme(ent: Entity, key: str, amount: float = 1.0) -> None:
    ent.memes[key] = ent.memes.get(key, 0.0) + amount


def _meter(ent: Entity, key: str) -> float:
    return ent.meters.get(key, 0.0)


def _meme(ent: Entity, key: str) -> float:
    return ent.memes.get(key, 0.0)


def _speak(world: World, speaker: Entity, text: str) -> None:
    world.say(f'{speaker.id} said, "{text}"')


def _setup(world: World, hero: Entity, adult: Entity, prize: Entity) -> None:
    trait = next((t for t in hero.traits if t != "little"), "brave")
    world.say(
        f"{hero.id} was a little {trait} {hero.type} who loved the biggest skies and the loudest winds."
    )
    world.say(
        f"{hero.id} treasured {hero.pronoun('possessive')} {prize.label} because it could climb the air like a silver fish."
    )
    world.say(
        f"{adult.id} kept an eye on {hero.id} whenever the weather got wild as a wagon chase."
    )


def _inner_monologue(world: World, hero: Entity, text: str) -> None:
    _add_meme(hero, "thinking", 1.0)
    world.say(f'In {hero.pronoun("possessive")} head, {hero.id} thought, "{text}"')


def _arrive(world: World, hero: Entity) -> None:
    world.say(
        f"One day, at {world.setting.place}, the wind came barreling along like a herd of invisible buffalo."
    )
    world.say(
        f"The sky was so wide it looked stitched from blue cloth, and the clouds were piled up like cream on a pie."
    )
    world.say(f"{hero.id} wanted to send up an SOS with the {world.facts['prize'].label}.")
    _add_meme(hero, "desire", 1.0)


def _warn(world: World, adult: Entity, hero: Entity, prize: Entity, activity: Activity) -> bool:
    if _meter(prize, "torn") >= THRESHOLD:
        return False
    if world.setting.wind == "gentle":
        return False
    _add_meme(adult, "worry", 1.0)
    world.facts["predicted_torn"] = activity.soil
    _speak(
        world,
        adult,
        f"That wind could tear your {prize.label}, and then we'd have one sorry signal on our hands.",
    )
    return True


def _wish_and_think(world: World, hero: Entity, activity: Activity) -> None:
    world.say(
        f"{hero.id} stared at the sky and wanted to {activity.verb}, but the idea came with a thorny feeling."
    )
    _inner_monologue(
        world,
        hero,
        f"If I throw my {world.facts['prize'].label} up there bare, that wind may gnaw it to ribbons.",
    )
    _inner_monologue(
        world,
        hero,
        "There must be a smarter way to shout SOS at the clouds without ruining my favorite thing.",
    )


def _offer_gear(world: World, adult: Entity, hero: Entity, prize: Entity, gear: Gear, activity: Activity) -> Optional[Gear]:
    if activity.mess not in gear.guards or prize.region not in gear.covers:
        return None
    g = world.add(
        Entity(
            id=gear.id,
            type="gear",
            label=gear.label,
            protective=True,
            covers=set(gear.covers),
            plural=gear.plural,
            owner=hero.id,
            caretaker=adult.id,
            carried_by=hero.id,
        )
    )
    world.facts["gear"] = gear
    _speak(
        world,
        adult,
        f"How about we use {gear.prep} and let that SOS fly the safe way?",
    )
    return g


def _resolve(world: World, hero: Entity, adult: Entity, prize: Entity, gear: Gear, activity: Activity) -> None:
    _add_meme(hero, "joy", 1.0)
    _add_meme(hero, "relief", 1.0)
    _add_meme(adult, "relief", 1.0)
    _speak(world, hero, "That'll do it! The sky can have my message, but not my kite.")
    world.say(
        f"{hero.id} and {adult.id} patched the {prize.label} first, then sent it up on the wind."
    )
    world.say(
        f"The {gear.label} held firm, and the child spelled SOS against the blue as neat as a fence line."
    )
    world.say(
        f"By the end, the {prize.label} was still whole, and the great windy sky carried the message far and wide."
    )


def _do_activity(world: World, hero: Entity, activity: Activity, prize: Entity) -> None:
    _add_meter(hero, activity.mess, 1.0)
    if not world.covered(hero, prize.region):
        _add_meter(prize, activity.mess, 1.0)
        _add_meter(prize, "torn", 1.0)
    if _meter(prize, "torn") >= THRESHOLD:
        _add_meme(hero, "alarm", 1.0)


def tell(setting: Setting, activity: Activity, prize_cfg: Prize, hero_name: str, hero_type: str, parent_type: str, trait: str) -> World:
    world = World(setting)
    hero = world.add(
        Entity(
            id=hero_name,
            kind="character",
            type=hero_type,
            traits=["little", trait],
            meters={},
            memes={},
        )
    )
    adult = world.add(
        Entity(
            id=parent_type.capitalize(),
            kind="character",
            type=parent_type,
            label=parent_type,
            meters={},
            memes={},
        )
    )
    prize = world.add(
        Entity(
            id="prize",
            type=prize_cfg.type,
            label=prize_cfg.label,
            phrase=prize_cfg.phrase,
            owner=hero.id,
            caretaker=adult.id,
            region=prize_cfg.region,
            plural=prize_cfg.plural,
            carried_by=hero.id,
            meters={},
            memes={},
        )
    )
    world.facts.update(hero=hero, adult=adult, prize=prize, activity=activity, setting=setting)

    _setup(world, hero, adult, prize)
    world.para()
    _arrive(world, hero)
    _wish_and_think(world, hero, activity)
    warned = _warn(world, adult, hero, prize, activity)
    if warned:
        _add_meme(hero, "defiance", 1.0)
        world.say(
            f"{hero.id} bit {hero.pronoun('possessive')} lip, because the wish to help was bigger than a barn roof in a storm."
        )
        _inner_monologue(world, hero, "I don't want to be stubborn; I want to be smart.")
    gear = _offer_gear(world, adult, hero, prize, PATCH_KIT, activity)
    world.para()
    if gear is not None:
        _resolve(world, hero, adult, prize, PATCH_KIT, activity)
    else:
        world.say(
            f"The wind kept huffing, and the two of them had to wait for a safer day."
        )
    world.facts["resolved"] = gear is not None
    return world


def prize_at_risk(activity: Activity, prize: Prize) -> bool:
    return prize.region in activity.zone


def select_gear(activity: Activity, prize: Prize) -> Optional[Gear]:
    for gear in PATCH_KIT_LIST:
        if activity.mess in gear.guards and prize.region in gear.covers:
            return gear
    return None


