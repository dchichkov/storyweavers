def tell(setting: Setting, activity: Activity, prize_cfg: Prize,
         hero_name: str, hero_type: str, parent_type: str, trait: str) -> World:
    world = World(setting)
    hero = world.add(Entity(id=hero_name, kind="character", type=hero_type))
    parent = world.add(Entity(id="Parent", kind="character", type=parent_type, label="the parent"))
    prize = world.add(Entity(
        id="prize",
        type=prize_cfg.type,
        label=prize_cfg.label,
        phrase=prize_cfg.phrase,
        owner=hero.id,
        caretaker=parent.id,
        region=prize_cfg.region,
        plural=prize_cfg.plural,
    ))
    prize.worn_by = hero.id

    hero.memes["care"] += 1
    hero.memes["joy"] += 1

    # Act I
    world.say(f"Once upon a time, in {setting.place}, lived little {hero_name}, a {trait} {hero.type}.")
    world.say(f"{hero.pronoun().capitalize()} loved the shine of {prize.phrase}, and {prize.label} sparkled like a tiny treasure.")
    world.say(f"Whenever {hero_name} twirled, {prize.label} gleamed, and the little toes looked proud as a princess's crown.")

    # Act II
    world.para()
    world.say(f"One day, {hero_name} longed to {activity.verb} in {setting.place}.")
    world.say(f"The air went still. {activity.sound}! {activity.sound}! The path seemed to grin at {hero_name}.")
    world.say(f"But {parent.label} lifted a finger and warned, \"If you go now, {prize.label} may end up {activity.soil}.\"")
    world.say(f"That warning made the moment feel like a candle in a cave: small, bright, and full of suspense.")

    hero.memes["fear"] += 1
    hero.memes["suspense"] += 1
    world.say(f"{hero_name} wanted to rush ahead, but the muddy lane looked ready to bite, and the puddles whispered, \"Shh...\"")

    # Act III
    world.para()
    gear = select_gear(activity, prize)
    if gear is None:
        _fallback_pool = globals().get("GEARS") or globals().get("GEARES") or []
        if hasattr(_fallback_pool, "values"):
            _fallback_pool = list(_fallback_pool.values())
        gear = next(iter(_fallback_pool), None)
        if gear is None:
            raise StoryError
    gear_ent = world.add(Entity(
        id=gear.id,
        type="gear",
        label=gear.label,
        owner=hero.id,
        caretaker=parent.id,
        protective=True,
        covers=set(gear.covers),
        plural=gear.plural,
    ))
    gear_ent.worn_by = hero.id

    if predict_mess(world, hero, activity, prize.id)["soiled"]:
        pass

    world.say(f"Then the parent smiled and said, \"{gear.prep.capitalize()} and the worry will fall away.\"")
    world.say(f"So {hero_name} did as told: {gear.prep}.")
    world.say(f"{gear.tail}, {activity.sound}, and off they went.")
    world.say(f"At last {hero_name} could {activity.gerund}, while {prize.label} stayed clean and neat.")
    world.say(f"And so the little tale ended in a merry rhyme: \"{activity.rhyme}.\"")

    hero.memes["joy"] += 2
    hero.memes["relief"] += 2
    hero.memes["pride"] += 1
    hero.memes["fear"] = 0.0

    world.facts.update(
        hero=hero,
        parent=parent,
        prize=prize,
        prize_cfg=prize_cfg,
        activity=activity,
        setting=setting,
        gear=gear,
    )
    return world


