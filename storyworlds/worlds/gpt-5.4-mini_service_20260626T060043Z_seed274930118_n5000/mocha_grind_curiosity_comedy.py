def prize_at_risk(activity: Activity, prize: Prize) -> bool:
    return prize.region in activity.zone


def select_gear(activity: Activity, prize: Prize) -> Optional[Gear]:
    for gear in GEAR:
        if activity.mess in gear.guards and prize.region in gear.covers:
            return gear
    return None


def valid_combos() -> list[tuple[str, str, str]]:
    combos = []
    for place, setting in SETTINGS.items():
        for act_id in setting.affords:
            act = _safe_lookup(ACTIVITIES, act_id)
            for prize_id, prize in PRIZES.items():
                if prize_at_risk(act, prize) and select_gear(act, prize):
                    combos.append((place, act_id, prize_id))
    return combos


def _do_activity(world: World, actor: Entity, activity: Activity, narrate: bool = True) -> None:
    world.zone = set(activity.zone)
    actor.meters[activity.mess] = actor.meters.get(activity.mess, 0.0) + 1
    actor.memes["curiosity"] = actor.memes.get("curiosity", 0.0) + 1
    if narrate:
        world.say(f"{actor.id} tried to {activity.verb}, and the room filled with {activity.spill}.")


def predict_mess(world: World, actor: Entity, activity: Activity, prize_id: str) -> dict:
    sim = world.copy()
    _do_activity(sim, sim.get(actor.id), activity, narrate=False)
    prize = sim.entities.get(prize_id)
    return {"soiled": bool(prize and prize.meters.get(activity.mess, 0.0) >= THRESHOLD)}


def tell(setting: Setting, activity: Activity, prize_cfg: Prize,
         hero_name: str, hero_type: str, parent_type: str, trait: str) -> World:
    world = World(setting)
    hero = world.add(Entity(id=hero_name, kind="character", type=hero_type, meters={}, memes={}))
    parent = world.add(Entity(id="Parent", kind="character", type=parent_type, label="the parent", meters={}, memes={}))
    prize = world.add(Entity(
        id="prize",
        type=prize_cfg.type,
        label=prize_cfg.label,
        phrase=prize_cfg.phrase,
        caretaker=parent.id,
        owner=hero.id,
        worn_by=hero.id if prize_cfg.region else None,
    ))

    world.say(f"{hero.id} was a little {trait} {hero.type} who loved surprises and smells.")
    world.say(f"{hero.pronoun().capitalize()} especially loved the rich smell of {activity.keyword} at {setting.place}.")
    world.say(f"That day, {hero.id} wore {hero.pronoun('possessive')} {prize.label} and wanted to {activity.verb}.")

    world.para()
    world.say(f"In {setting.place}, {hero.id} leaned toward the counter and asked, \"Can I try the grinder?\"")
    world.say(f"{hero.pronoun('possessive').capitalize()} {parent_type} gave a careful warning: \"Go slowly, or the {activity.keyword} will go puff.\"")
    world.say(f"But curiosity twinkled in {hero.id}'s eyes.")

    world.para()
    _do_activity(world, hero, activity, narrate=False)
    if predict_mess(world, hero, activity, prize.id)["soiled"]:
        world.say(f"{hero.id} gave it one tiny extra spin, and {activity.spill} flew onto the apron.")
        world.say(f"That made {hero.pronoun('possessive')} {parent_type} laugh, because the kitchen looked like a cocoa snowstorm.")

    gear = select_gear(activity, prize)
    if gear:
        gear_ent = world.add(Entity(id=gear.id, type="gear", label=gear.label, worn_by=hero.id, plural=gear.plural))
        world.say(f"{hero.pronoun('possessive').capitalize()} {parent_type} smiled and said, \"How about we {gear.prep} and try again?\"")
        hero.memes["surprise"] = hero.memes.get("surprise", 0.0) + 1
        hero.memes["laughter"] = hero.memes.get("laughter", 0.0) + 1
        world.say(f"{hero.id} laughed, nodded, and {gear.tail}.")
        world.say(f"This time, the grinder stayed neat, the {activity.keyword} smelled wonderful, and {hero.id} ended with a mug of cozy mocha.")
        world.facts.update(hero=hero, parent=parent, prize=prize, activity=activity, setting=setting, gear=gear_ent, resolved=True)
    else:
        world.say(f"They wiped the counter together and still made the drink, but the day stayed a little messy.")
        world.facts.update(hero=hero, parent=parent, prize=prize, activity=activity, setting=setting, gear=None, resolved=False)

    return world


