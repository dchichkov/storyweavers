def _r_dampen(world: World) -> list[str]:
    out: list[str] = []
    for actor in world.characters():
        if actor.meters.get("flutter", 0.0) < THRESHOLD:
            continue
        for item in world.worn_items(actor):
            if item.protective or item.region not in world.zone:
                continue
            if world.covered(actor, item.region):
                continue
            sig = ("dampen", item.id)
            if sig in world.fired:
                continue
            world.fired.add(sig)
            item.meters["damp"] = item.meters.get("damp", 0.0) + 1
            out.append(f"{actor.id}'s {item.label} grew damp in the pond mist.")
    return out


def _r_worry(world: World) -> list[str]:
    out: list[str] = []
    for item in list(world.entities.values()):
        if item.meters.get("damp", 0.0) < THRESHOLD or not item.caretaker:
            continue
        sig = ("worry", item.id)
        if sig in world.fired:
            continue
        world.fired.add(sig)
        carer = world.get(item.caretaker)
        carer.memes["worry"] = carer.memes.get("worry", 0.0) + 1
        out.append(f"That would trouble {carer.label}.")
    return out


def _r_laugh(world: World) -> list[str]:
    out: list[str] = []
    for actor in world.characters():
        if actor.memes.get("humor", 0.0) < THRESHOLD or actor.memes.get("surprise", 0.0) < THRESHOLD:
            continue
        sig = ("laugh", actor.id)
        if sig in world.fired:
            continue
        world.fired.add(sig)
        actor.memes["joy"] = actor.memes.get("joy", 0.0) + 1
        out.append(f"{actor.id} laughed like a bell over water.")
    return out


CAUSAL_RULES = [
    Rule("dampen", _r_dampen),
    Rule("worry", _r_worry),
    Rule("laugh", _r_laugh),
]


def propagate(world: World, narrate: bool = True) -> list[str]:
    produced: list[str] = []
    changed = True
    for _ in range(len(globals().get("CAUSAL_RULES", [])) + 4):
        changed = False
        for rule in CAUSAL_RULES:
            sents = rule.apply(world)
            if sents:
                changed = True
                produced.extend(sents)
    if narrate:
        for s in produced:
            world.say(s)
    return produced


def predict_mess(world: World, actor: Entity, activity: Activity, prize_id: str) -> dict:
    sim = World(world.setting)
    import copy
    sim.entities = copy.deepcopy(world.entities)
    sim.zone = set(activity.zone)
    hero = sim.get(actor.id)
    hero.meters["flutter"] = hero.meters.get("flutter", 0.0) + 1
    propagate(sim, narrate=False)
    prize = sim.get(prize_id)
    return {"damp": prize.meters.get("damp", 0.0) >= THRESHOLD}


