def resolve_params(args: argparse.Namespace, rng: random.Random) -> StoryParams:
    if getattr(args, "activity", None) and getattr(args, "prize", None):
        act, pr = _safe_lookup(ACTIVITIES, getattr(args, "activity", None)), _safe_lookup(PRIZES, getattr(args, "prize", None))
        if not select_gear(act, pr):
            return _fallback_storyparams(args, rng, StoryParams, globals())
    combos = [c for c in valid_combos()
              if (getattr(args, "place", None) is None or c[0] == getattr(args, "place", None))
              and (getattr(args, "activity", None) is None or c[1] == getattr(args, "activity", None))
              and (getattr(args, "prize", None) is None or c[2] == getattr(args, "prize", None))]
    if not combos:
        return _fallback_storyparams(args, rng, StoryParams, globals())
    place, activity, prize = (list(rng.choice(combos)) + [None, None, None])[:3]
    return StoryParams(
        place=place,
        activity=activity,
        prize=prize,
        name=getattr(args, "name", None) or "Norm",
        helper=getattr(args, "helper", None) or rng.choice(HELPERS),
        trait=getattr(args, "trait", None) or rng.choice(TRACES),
    )


def tell(setting: Setting, activity: Activity, prize_cfg: Prize, helper: str, trait: str) -> World:
    world = World(setting)
    world.weather = activity.weather
    hero = world.add(Entity(id="Norm", kind="character", type="dragonfly", label="Norm"))
    friend = world.add(Entity(id=helper, kind="character", type="frog", label=helper))
    prize = world.add(Entity(
        id="pajama", type="pajama", label="pajama", phrase=prize_cfg.phrase,
        owner=hero.id, caretaker=friend.id, region=prize_cfg.region
    ))

    hero.memes["joy"] = 1
    world.say(
        f"Norm was a {trait} dragonfly who lived by the pond, where the old norm was simple: share what you can."
    )
    world.say(
        f"He loved his {prize.phrase}, because it made bedtime feel like a fairy tale and the reeds looked like a palace."
    )
    world.para()
    world.say(
        f"One evening at {world.setting.place}, Norm wanted to {activity.verb}, and {activity_delight(activity)}."
    )
    world.say(
        f"But the pond had a sneaky way of sending surprise mist and playful ripples across the water."
    )
    world.say(
        f"Norm fluttered closer, and his {prize.label} began to look soft and damp."
    )
    world.say(
        f"That made his heart feel small, because he did not want to spoil the pajama he loved."
    )
    predict = predict_mess(world, hero, activity, prize.id)
    if predict["damp"]:
        world.say(
            f"{friend} saw the worry and told a joke about a lily pad trying to wear a crown."
        )
        hero.memes["humor"] = hero.memes.get("humor", 0.0) + 1
        hero.memes["surprise"] = hero.memes.get("surprise", 0.0) + 1
        gear = select_gear(activity, prize)
        if gear:
            world.say(
                f"Then {friend} surprised Norm with {gear.label}, and said he could share it if he wanted to keep the pajama dry."
            )
            gear_ent = world.add(Entity(
                id=gear.id, type="gear", label=gear.label, protective=True,
                covers=set(gear.covers), owner=hero.id, caretaker=friend.id
            ))
            gear_ent.worn_by = hero.id
            world.say(
                f"Norm smiled at the kindness. He shared the leaf cloak with {friend}, and they both peered out at the moon like two little castle guards."
            )
            hero.meters["flutter"] = 1
            propagate(world, narrate=True)
            world.para()
            world.say(
                f"At last, Norm {activity.gerund}, while the pajama stayed dry and the two friends laughed softly beside the pond."
            )
    world.facts.update(hero=hero, friend=friend, prize=prize, activity=activity, setting=setting)
    return world


