"""
storyworlds/worlds/fold_bad_ending_bedtime_story.py
====================================================

A small bedtime-story world about folding, tired feelings, and a gentle
bad ending.

Premise:
- A child gets sleepy at bedtime and wants to fold one last soft thing.
- A parent worries the folding will make the room messier or make the child
  miss the bedtime routine.
- The child ignores the warning, folds badly, and the night ends with a
  small loss instead of a fix.

This world is intentionally tiny and constraint-checked. It models a few
objects, a bedtime setting, and one causal arc:
- desire -> warning -> stubborn choice -> bad ending

The ending is sad, but not scary. The "bad ending" means the child does not
get the tidy, cozy resolution they wanted.
"""

