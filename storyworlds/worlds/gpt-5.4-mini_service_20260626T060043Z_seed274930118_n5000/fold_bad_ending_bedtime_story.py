def _do_activity(world: World, actor: Entity, activity: Activity) -> None:
    actor.memes[activity.mess] = actor.memes.get(activity.mess, 0.0) + 1
    actor.memes["stubborn"] = actor.memes.get("stubborn", 0.0) + 1
    for item in list(world.entities.values()):
        if item.kind == "thing" and item.owner == actor.id and item.region == "bed":
            item.meters[activity.mess] = item.meters.get(activity.mess, 0.0) + 1
            item.meters["dirty"] = item.meters.get("dirty", 0.0) + 1


def predict_loss(world: World, actor: Entity, activity: Activity, prize_id: str) -> bool:
    sim = world.copy()
    _do_activity(sim, sim.get(actor.id), activity)
    prize = sim.get(prize_id)
    return prize.meters.get("dirty", 0.0) >= THRESHOLD


def tell(setting: Setting, activity: Activity, prize_cfg: Prize, hero_name: str,
         hero_gender: str, hero_traits: list[str], parent_type: str) -> World:
    world = World(setting)
    hero = world.add(Entity(
        id=hero_name,
        kind="character",
        type=hero_gender,
        traits=["little"] + hero_traits,
    ))
    parent = world.add(Entity(
        id="Parent",
        kind="character",
        type=parent_type,
        label="the parent",
    ))
    prize = world.add(Entity(
        id="prize",
        type=prize_cfg.type,
        label=prize_cfg.label,
        phrase=prize_cfg.phrase,
        owner=hero.id,
        caretaker=parent.id,
        region=prize_cfg.region,
        plural=prize_cfg.plural,
        meters={"dirty": 0.0},
        memes={"warmth": 1.0},
    ))

    world.say(f"{hero.id} was a little {hero_traits[0]} {hero.type} who loved bedtime.")
    world.say(f"{hero.id} liked the soft quiet of {world.setting.place} and the way the room felt after the lamp was turned low.")
    world.say(f"One night, {hero.id}'s {parent.label} gave {hero.pronoun('object')} {prize.phrase}.")
    world.say(f"{hero.id} loved {prize.it()} and held {prize.it()} close while the moon watched through the window.")

    world.para()
    world.say(f"At {world.setting.place}, {hero.id} wanted to {activity.verb} before sleep.")
    world.say(f"{hero.pronoun('possessive').capitalize()} {parent.label} warned, \"Be careful. If you keep {activity.gerund}, {prize.label} may get {activity.soil}.\"")

    world.para()
    if predict_loss(world, hero, activity, prize.id):
        hero.memes["desire"] = hero.memes.get("desire", 0.0) + 1
        hero.memes["defiance"] = hero.memes.get("defiance", 0.0) + 1
        world.say(f"But {hero.id} still reached for {prize.label} and tried to {activity.verb} anyway.")
        world.say(f"{hero.id} hurried too fast, and the careful fold came out crooked.")
        _do_activity(world, hero, activity)
        prize.meters["dirty"] = prize.meters.get("dirty", 0.0) + 1
        world.say(f"The {prize.label} ended up {activity.soil}, and the room felt less cozy than before.")
        world.say(f"{hero.id}'s {parent.label} sighed softly and took the {prize.label} away to wash it in the morning.")
        world.para()
        world.say(f"So {hero.id} climbed into bed without the neat little comfort {hero.id} wanted, and the blanket stayed in a lumpy pile at the edge of the chair.")
    else:
        # Should never happen for valid combos, but keep a fallback.
        world.say(f"{hero.id} stopped and left the {prize.label} alone.")
        world.say(f"The night stayed quiet, but nothing changed.")

    world.facts.update(hero=hero, parent=parent, prize=prize, activity=activity, setting=setting)
    return world


