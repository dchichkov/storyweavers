def reasonableness_gate(activity: Activity, prize: Prize) -> bool:
    return True if activity.id == "golf" and prize.region == "hands" else False


def select_fix(activity: Activity, prize: Prize) -> Optional[Fix]:
    for fix in FIXES:
        if activity.mess in fix.guards and prize.region in fix.covers:
            return fix
    return None


def predict_spook(world: World, actor: Entity, activity: Activity, prize_id: str) -> dict:
    sim = world.copy()
    _do_activity(sim, sim.get(actor.id), activity, narrate=False)
    prize = sim.get(prize_id)
    return {"spooked": bool(prize.memes.get("spooked", 0) >= THRESHOLD)}


def _do_activity(world: World, actor: Entity, activity: Activity, narrate: bool = True) -> None:
    actor.meters[activity.mess] = actor.meters.get(activity.mess, 0) + 1
    actor.memes["joy"] = actor.memes.get("joy", 0) + 1
    if actor.meters[activity.mess] >= THRESHOLD:
        for ent in list(world.entities.values()):
            if ent.owner == actor.id:
                ent.meters[activity.mess] = ent.meters.get(activity.mess, 0) + 1
                ent.memes["spooked"] = ent.memes.get("spooked", 0) + 1
    if narrate:
        world.say(f"{actor.id} {activity.gerund} made the air feel cold and still.")


def rhyme_line(word1: str, word2: str) -> str:
    return f"“{word1} and {word2}, a twilight tune,” the breeze seemed to sing under the moon."


def build_spooky_foreshadowing(activity: Activity) -> str:
    return (
        f"The flags were thin as thread, and the cups looked like little mouths. "
        f"A soft whistle sighed across the green, and {activity.keyword} seemed to wait in the dark."
    )


def introduce(world: World, hero: Entity) -> None:
    trait = next((t for t in hero.memes.get("traits", []) if t != "little"), "curious")
    world.say(f"{hero.id} was a little {trait} {hero.type} who loved quiet evenings and stories that shivered.")


def set_scene(world: World, activity: Activity, prize: Entity) -> None:
    world.say(
        f"At {world.setting.place}, the grass looked silver under the clouds, and "
        f"{prize.phrase} gleamed near the tee."
    )
    world.say(build_spooky_foreshadowing(activity))
    world.say(rhyme_line("golf", "dolph?"))


def wants(world: World, hero: Entity, activity: Activity) -> None:
    hero.memes["want"] = hero.memes.get("want", 0) + 1
    world.say(f"{hero.id} wanted to {activity.verb}, even though the wind kept whispering, “Don't begin.”")


def warning(world: World, parent: Entity, hero: Entity, activity: Activity, prize: Entity) -> bool:
    pred = predict_spook(world, hero, activity, prize.id)
    if not pred["spooked"]:
        return False
    world.say(
        f"“If you {activity.verb}, your {prize.label} will get {activity.soil},” "
        f"{parent.pronoun('possessive')} {parent.type} said. “Let's not wander where the shadows yawn.”"
    )
    return True


def foreshadow(world: World, hero: Entity) -> None:
    hero.memes["unease"] = hero.memes.get("unease", 0) + 1
    world.say(f"A little chill slid up {hero.pronoun('possessive')} arms, and {hero.id} paused.")
    world.say(f"The nearest flag gave one tiny flutter, as if it knew what came next.")


def turn_toward_truth(world: World, parent: Entity, hero: Entity, activity: Activity) -> None:
    hero.memes["defiance"] = hero.memes.get("defiance", 0) + 1
    world.say(f"{hero.id} still took a step forward, but {hero.pronoun('possessive')} {parent.type} held out a calm hand.")
    world.say(f"“We can choose the safer way,” {parent.pronoun('subject')} said, “and keep the night kind.”")


def compromise(world: World, parent: Entity, hero: Entity, activity: Activity, prize: Entity) -> Optional[Fix]:
    fix = select_fix(activity, prize)
    if fix is None:
        return None
    world.say(f"{parent.pronoun('possessive').capitalize()} {parent.type} suggested {fix.label}: {fix.prep}.")
    return fix


def accept(world: World, parent: Entity, hero: Entity, activity: Activity, prize: Entity, fix: Fix) -> None:
    hero.memes["joy"] = hero.memes.get("joy", 0) + 1
    hero.memes["fear"] = 0.0
    world.say(f"{hero.id} nodded, and the dark did not seem so big anymore.")
    world.say(
        f"They {fix.tail}, and soon {hero.id} was {activity.gerund}, while "
        f"{prize.phrase} stayed dry and bright."
    )
    world.say(
        f"The course went quiet again, but now it was a friendly quiet, "
        f"and the moon made the cup look like a small smiling mouth."
    )


def tell(setting: Setting, activity: Activity, prize_cfg: Prize, hero_name: str, hero_type: str,
         hero_traits: Optional[list[str]] = None, parent_type: str = "mother") -> World:
    world = World(setting)
    hero = world.add(Entity(
        id=hero_name, kind="character", type=hero_type,
        memes={"traits": ["little"] + (hero_traits or ["curious"])},
    ))
    parent = world.add(Entity(id="Parent", kind="character", type=parent_type, label="parent"))
    prize = world.add(Entity(id="prize", type=prize_cfg.type, label=prize_cfg.label,
                             phrase=prize_cfg.phrase, owner=hero.id, caretaker=parent.id,
                             region=prize_cfg.region, plural=prize_cfg.plural))
    introduce(world, hero)
    world.para()
    set_scene(world, activity, prize)
    wants(world, hero, activity)
    warning(world, parent, hero, activity, prize)
    foreshadow(world, hero)
    turn_toward_truth(world, parent, hero, activity)
    world.para()
    fix = compromise(world, parent, hero, activity, prize)
    if fix:
        accept(world, parent, hero, activity, prize, fix)
    world.facts.update(hero=hero, parent=parent, prize=prize, activity=activity, fix=fix)
    return world


