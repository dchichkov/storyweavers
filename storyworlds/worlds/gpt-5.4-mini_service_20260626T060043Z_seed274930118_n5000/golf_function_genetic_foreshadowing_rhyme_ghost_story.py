def asp_facts() -> str:
    import asp
    lines = []
    for sid, setting in SETTINGS.items():
        lines.append(asp.fact("setting", sid))
        for act in sorted(setting.affords):
            lines.append(asp.fact("affords", sid, act))
    for aid, act in ACTIVITIES.items():
        lines.append(asp.fact("activity", aid))
        lines.append(asp.fact("mess_of", aid, act.mess))
        for tag in sorted(act.tags):
            lines.append(asp.fact("tags", aid, tag))
    for pid, prize in PRIZES.items():
        lines.append(asp.fact("prize", pid))
        lines.append(asp.fact("worn_on", pid, prize.region))
    for fx in FIXES:
        lines.append(asp.fact("fix", fx.id))
        for g in sorted(fx.guards):
            lines.append(asp.fact("guards", fx.id, g))
        for c in sorted(fx.covers):
            lines.append(asp.fact("covers", fx.id, c))
    return "\n".join(lines)


ASP_RULES = r"""
prize_at_risk(A,P) :- mess_of(A,M), guards(F,M), prize(P), worn_on(P,R), covers(F,R).
has_fix(A,P) :- prize_at_risk(A,P), fix(F), guards(F,M), mess_of(A,M), covers(F,R), worn_on(P,R).
valid(Place,A,P) :- setting(Place), affords(Place,A), prize(P), has_fix(A,P).
"""


def asp_program(show: str) -> str:
    return f"{asp_facts()}\n{ASP_RULES}\n{show}\n"


def valid_combos() -> list[tuple[str, str, str]]:
    combos = []
    for place, setting in SETTINGS.items():
        for act_id in setting.affords:
            act = _safe_lookup(ACTIVITIES, act_id)
            for prize_id, prize in PRIZES.items():
                if reasonableness_gate(act, prize) and select_fix(act, prize):
                    combos.append((place, act_id, prize_id))
    return combos


def asp_valid_combos() -> list[tuple]:
    import asp
    model = asp.one_model(asp_program("#show valid/3."))
    return sorted(set(asp.atoms(model, "valid")))


def asp_verify() -> int:
    a, b = set(asp_valid_combos()), set(valid_combos())
    if a == b:
        print(f"OK: clingo gate matches valid_combos() ({len(a)} combos).")
        return 0
    print("MISMATCH:")
    print("only in asp:", sorted(a - b))
    print("only in python:", sorted(b - a))
    return 1


