def tell(setting: Setting, activity: Activity, prize_cfg: Prize, name: str, gender: str,
         trait: str, parent_type: str) -> World:
    world = World(setting)
    hero = world.add(Entity(
        id=name,
        kind="character",
        type=gender,
        meters={"wet": 0.0},
        memes={"inferior": 0.0, "joy": 0.0, "love": 0.0, "conflict": 0.0},
    ))
    parent = world.add(Entity(
        id="Parent",
        kind="character",
        type=parent_type,
        label="the good parent",
        memes={"love": 0.0},
    ))
    prize = world.add(Entity(
        id="prize",
        type=prize_cfg.type,
        label=prize_cfg.label,
        phrase=prize_cfg.phrase,
        owner=hero.id,
        caretaker=parent.id,
        region=prize_cfg.region,
        plural=prize_cfg.plural,
    ))

    # Act I
    world.say(
        f"{hero.id} was a {trait} little {gender} who lived by {setting.place}."
    )
    world.say(
        f"{hero.pronoun().capitalize()} loved the day the bells rang for a baptize, "
        f"but {hero.pronoun('possessive')} {prize.label} made {hero.pronoun('object')} feel inferior beside the shining robes."
    )
    world.say(
        f"Still, {hero.id} wore {hero.pronoun('possessive')} {prize.label} as if it were a crown."
    )

    # Act II
    world.para()
    world.say(
        f"On the morning of the baptize, {hero.id} and {hero.pronoun('possessive')} {parent.label_word if hasattr(parent, 'label_word') else 'parent'} went to {setting.place}."
    )
    world.say(
        f"{hero.id} wanted to {activity.verb}, but the water would surely make {hero.pronoun('possessive')} {prize.label} {activity.soil}."
    )
    if predict_mess(world, hero, activity, prize.id):
        hero.memes["inferior"] += 1
        world.say(
            f'“You will get your {prize.label} {activity.soil}, and then I will have to clean it,” said the good parent.'
        )

    # Flashback beat
    world.para()
    hero.memes["inferior"] += 1
    world.say(
        f"{hero.id} looked down. Then a flashback came to mind: once, on a colder day, {hero.id} had been the smallest one in the room, and nobody had noticed."
    )
    world.say(
        f"A kind old woman had then placed a ribbon at {hero.pronoun('possessive')} throat and whispered that small hands can still do holy work."
    )

    # Act III
    gear_def = select_gear(activity, prize)
    if gear_def is None:
        pass
    gear = world.add(Entity(
        id=gear_def.id,
        type="gear",
        label=gear_def.label,
        protective=True,
        covers=set(gear_def.covers),
        worn_by=hero.id,
    ))
    world.para()
    world.say(
        f"{hero.pronoun('possessive').capitalize()} {parent.id.lower()} smiled and said, “Then we will {gear_def.prep}.”"
    )
    world.say(
        f"{hero.id} brightened at once. {hero.pronoun().capitalize()} put on {gear.label}, and the two of them went on with the baptize."
    )
    _do_activity(world, hero, activity, narrate=False)
    prize.meters["wet"] = 0.0
    hero.memes["inferior"] = 0.0
    hero.memes["joy"] += 1
    hero.memes["love"] += 1

    world.say(
        f"In the end, {hero.id} was {activity.gerund}, {prize.label} stayed dry, and the bells sounded like gold over the chapel yard."
    )

    world.facts.update(
        hero=hero,
        parent=parent,
        prize=prize,
        activity=activity,
        setting=setting,
        gear=gear_def,
    )
    return world


