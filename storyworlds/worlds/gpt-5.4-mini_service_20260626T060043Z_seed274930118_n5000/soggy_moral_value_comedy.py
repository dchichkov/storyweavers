class World:
    def __init__(self, setting: Setting) -> None:
        self.setting = setting
        self.entities: dict[str, Entity] = {}
        self.paragraphs: list[list[str]] = [[]]
        self.fired: set[tuple] = set()
        self.zone: set[str] = set()
        self.facts: dict = {}

    def add(self, ent: Entity) -> Entity:
        self.entities[ent.id] = ent
        return ent

    def get(self, eid: str) -> Entity:
        if eid not in self.entities:
            label = str(eid).replace("_", " ")
            self.entities[eid] = Entity(str(eid), label=label)
        return self.entities[eid]

    def characters(self) -> list[Entity]:
        return [e for e in self.entities.values() if e.kind == "character"]

    def say(self, text: str) -> None:
        if text:
            self.paragraphs[-1].append(text)

    def para(self) -> None:
        if self.paragraphs[-1]:
            self.paragraphs.append([])

    def render(self) -> str:
        return "\n\n".join(" ".join(p) for p in self.paragraphs if p)


def _msg(world: World, text: str) -> None:
    world.say(text)


def propagate(world: World, narrate: bool = True) -> list[str]:
    produced: list[str] = []
    changed = True
    for _ in range(len(globals().get("CAUSAL_RULES", [])) + 4):
        changed = False
        for actor in world.characters():
            if actor.meters.get("soggy", 0.0) < THRESHOLD:
                continue
            for item in list(world.entities.values()):
                if item.owner != actor.id:
                    continue
                if item.meters.get("dirty", 0.0) >= THRESHOLD:
                    continue
                if item.region not in world.zone:
                    continue
                sig = ("soak", actor.id, item.id)
                if sig in world.fired:
                    continue
                world.fired.add(sig)
                item.meters["dirty"] = item.meters.get("dirty", 0.0) + 1
                item.meters["soggy"] = item.meters.get("soggy", 0.0) + 1
                produced.append(f"{actor.pronoun('possessive').capitalize()} {item.noun()} got soggy.")
                changed = True
    if narrate:
        for s in produced:
            world.say(s)
    return produced


def setup_story(world: World, hero: Entity, parent: Entity, snack: Entity, setting: Setting, activity: Activity) -> None:
    _msg(world, f"{hero.id} was a little {hero.type} who loved funny adventures and loud snack breaks.")
    _msg(world, f"{hero.pronoun().capitalize()} loved to {activity.gerund}; {activity.mood}.")
    _msg(world, f"One day, {hero.id} and {hero.pronoun('possessive')} {parent.noun()} went to {setting.place}.")
    _msg(world, f"{hero.id} carried {hero.pronoun('possessive')} {snack.phrase} like it was a treasure.")
    hero.memes["fondness"] = hero.memes.get("fondness", 0.0) + 1


def conflict_story(world: World, hero: Entity, parent: Entity, snack: Entity, activity: Activity) -> None:
    world.para()
    world.zone = set(activity.zone)
    _msg(world, f"Then {hero.id} tried to {activity.verb}, and the day went from sunny to silly in one splash.")
    hero.meters["soggy"] = hero.meters.get("soggy", 0.0) + 1
    propagate(world, narrate=True)
    if snack.meters.get("dirty", 0.0) >= THRESHOLD:
        hero.memes["worry"] = hero.memes.get("worry", 0.0) + 1
        _msg(world, f"{hero.id} blinked at {hero.pronoun('possessive')} {snack.label} and tried a very tiny lie.")
        _msg(world, f'"Nope," {hero.id} said, "nothing happened."')
        _msg(world, f"But the snack was still soggy, and the lie felt bigger than the puddle.")


def turn_story(world: World, hero: Entity, parent: Entity, snack: Entity, fix: MoralFix) -> None:
    world.para()
    hero.memes["guilt"] = hero.memes.get("guilt", 0.0) + 1
    hero.memes["honesty"] = hero.memes.get("honesty", 0.0) + 1
    _msg(world, f"At last, {hero.id} sighed and told the truth.")
    _msg(world, f'"I got it wet," {hero.id} admitted. "I was embarrassed."')
    _msg(world, f"{parent.id} did not make a giant face about it. {parent.id} just snorted a laugh.")
    _msg(world, f'"Thank you for telling me," {parent.id} said. "{fix.offer}"')
    _msg(world, f"Together they followed {fix.result}, and the problem stopped feeling scary.")


def ending_story(world: World, hero: Entity, parent: Entity, snack: Entity) -> None:
    world.para()
    hero.memes["relief"] = hero.memes.get("relief", 0.0) + 1
    _msg(world, f"{hero.id} got a fresh snack and a sheepish grin.")
    _msg(world, f"The soggy one was set aside, the truth was kept, and everyone laughed at the dripping mess.")
    _msg(world, f"By the end, {hero.id} was still a little wet, but {hero.pronoun('possessive')} heart felt dry, light, and brave.")


