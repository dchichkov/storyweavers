    pred = predict(world, hero, activity, prize.id)
    if not pred["soiled"]:
        return False
    person.memes["worry"] += 1
    world.facts["predicted_soil"] = activity.soil
    world.say(
        f'"Careful," {person.pronoun("subject")} said. "If you try to {activity.verb}, that yuck might leave {activity.soil} on your {prize.label}."'
    )
    return True


