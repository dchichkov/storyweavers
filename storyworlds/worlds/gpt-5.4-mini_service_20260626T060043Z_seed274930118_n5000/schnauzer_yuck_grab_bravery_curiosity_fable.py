def generation_prompts(world: World) -> list[str]:
    f = world.facts
    return [
        f'Write a tiny fable about a schnauzer named {f["hero"].id} who sees yuck near {f["place"]}.',
        f"Tell a gentle story where {f['hero'].id} wants to {f['activity'].verb} but learns to be brave and curious first.",
        f'Write a child-friendly fable that includes the words "schnauzer", "yuck", and "grab".',
    ]


def story_qa(world: World) -> list[QAItem]:
    f = world.facts
    hero = _safe_fact(world, f, "hero")
    person = _safe_fact(world, f, "person")
    prize = _safe_fact(world, f, "prize")
    act = _safe_fact(world, f, "activity")
    qa = [
        QAItem(
            question=f"Who is the story about?",
            answer=f"It is about {hero.id}, a little schnauzer, and {person.label_word}, who stays close and helps.",
        ),
        QAItem(
            question=f"What did {hero.id} want to do near {world.setting.place}?",
            answer=f"{hero.id} wanted to {act.verb} because the yuck made the pup curious.",
        ),
        QAItem(
            question=f"Why did {person.label_word} warn {hero.id} about the yuck?",
            answer=f"{person.label_word} warned {hero.id} because the yuck could stain the clean {prize.label} and make a mess on small paws.",
        ),
    ]
    if f.get("resolved"):
        qa.append(
            QAItem(
                question=f"How did {hero.id} show bravery in the end?",
                answer=f"{hero.id} showed bravery by waiting, looking closely, and trusting {person.label_word} to help with the stick.",
            )
        )
    return qa


def world_knowledge_qa(world: World) -> list[QAItem]:
    tags = set(world.facts["activity"].tags)
    out = []
    for tag in globals().get("KNOWLEDGE_ORDER", sorted(globals().get("KNOWLEDGE", []))):
        if tag in tags:
            out.extend(QAItem(question=q, answer=a) for q, a in KNOWLEDGE[tag])
    return out


def format_qa(sample: StorySample) -> str:
    lines = ["== (1) Generation prompts =="]
    for i, p in enumerate(sample.prompts, 1):
        lines.append(f"{i}. {p}")
    lines.append("")
    lines.append("== (2) Story questions ==")
    for item in sample.story_qa:
        lines.append(f"Q: {item.question}")
        lines.append(f"A: {item.answer}")
    lines.append("")
    lines.append("== (3) World knowledge questions ==")
    for item in sample.world_qa:
        lines.append(f"Q: {item.question}")
        lines.append(f"A: {item.answer}")
    return "\n".join(lines)


def dump_trace(world: World) -> str:
    lines = ["--- world model state ---"]
    for e in list(world.entities.values()):
        meters = {k: v for k, v in e.meters.items() if v}
        memes = {k: v for k, v in e.memes.items() if v}
        bits = []
        if meters:
            bits.append(f"meters={dict(meters)}")
        if memes:
            bits.append(f"memes={dict(memes)}")
        if e.region:
            bits.append(f"region={e.region}")
        lines.append(f"  {e.id:10} ({e.type:7}) {' '.join(bits)}")
    lines.append(f"  fired rules: {sorted(set(n for n, *_ in world.fired))}")
    return "\n".join(lines)


