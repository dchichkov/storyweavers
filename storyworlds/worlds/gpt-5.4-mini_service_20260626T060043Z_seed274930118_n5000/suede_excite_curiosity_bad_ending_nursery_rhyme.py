def tell(setting: Setting, activity: Activity, prize_cfg: Prize,
         hero_name: str, hero_type: str, parent_type: str, trait: str) -> World:
    world = World(setting)
    hero = world.add(Entity(
        id=hero_name,
        kind="character",
        type=hero_type,
        traits=["little", trait, "curious"],
    ))
    parent = world.add(Entity(
        id="Parent",
        kind="character",
        type=parent_type,
        label="the parent",
    ))
    prize = world.add(Entity(
        id="prize",
        type=prize_cfg.type,
        label=prize_cfg.label,
        phrase=prize_cfg.phrase,
        owner=hero.id,
        caretaker=parent.id,
        region=prize_cfg.region,
        plural=prize_cfg.plural,
    ))
    prize.worn_by = hero.id

    world.say(nursery_opening(hero, parent, prize, setting))
    world.say(
        f"One rainy morn, {hero.id} and {hero.pronoun('possessive')} {parent.label} "
        f"stood by {setting.place}. {hero.id} was excited to {activity.verb}, "
        f"for the puddles made a merry, splishy sound."
    )

    world.para()
    hero.memes["excitement"] += 1
    hero.memes["curiosity"] += 1
    parent.memes["worry"] += 1
    world.say(
        f'"Not that way," said {hero.pronoun("possessive")} {parent.label}, '
        f'"for {prize.label} is suede, and suede likes to stay quite dry."'
    )
    world.say(
        f"But curiosity did tick and curiosity did tug, and {hero.id} crept a step "
        f'and then another, to look and to look and to look.'
    )

    world.para()
    hero.memes["curiosity"] += 1
    hero.meters[activity.mess] += 1
    prize.meters[activity.mess] += 1
    prize.meters["dirty"] += 1
    hero.memes["bad_ending"] += 1
    parent.memes["worry"] += 2

    world.say(
        f"Then {hero.id} ran to the wet little path and tried to {activity.verb}. "
        f"The ground gave a splash, and {hero.pronoun('possessive')} {prize.label} "
        f"got {activity.soil}."
    )
    world.say(
        f"{hero.id} stopped at once, but the stain stayed there. "
        f"{hero.pronoun().capitalize()} looked sad, and the day went still."
    )

    world.para()
    world.say(
        f"{hero.pronoun('possessive').capitalize()} {parent.label} lifted the {prize.label} "
        f"by the heel and sighed a soft little sigh. "
        f'"Curiosity is fine," said {parent.pronoun().capitalize()}, '
        f'"but some things cannot be unspilled."'
    )
    world.say(
        f"So {hero.id} went home in quiet socks, and the suede shoes stayed on the mat, "
        f"brown with rain and no longer bright."
    )

    world.facts = {
        "hero": hero,
        "parent": parent,
        "prize": prize,
        "activity": activity,
        "setting": setting,
        "bad_ending": True,
    }
    return world


