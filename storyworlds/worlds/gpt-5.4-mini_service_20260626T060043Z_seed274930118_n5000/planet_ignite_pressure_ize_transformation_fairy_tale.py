def compromise(world: World, parent: Entity, hero: Entity, activity: Activity, prize: Entity) -> Optional[Gear]:
    gear_def = select_gear(activity, prize)
    if gear_def is None:
        return None
    gear = world.add(Entity(
        id=gear_def.id, type="gear", label=gear_def.label, owner=hero.id,
        caretaker=parent.id, protective=True, covers=set(gear_def.covers), plural=gear_def.plural
    ))
    gear.worn_by = hero.id
    if predict_mess(world, hero, activity, prize.id)["soiled"]:
        del world.entities[gear.id]
        return None
    if activity.id == "ignite":
        world.say(f"At last, {hero.pronoun('possessive').capitalize()} {parent.label} smiled and said, \"How about we {gear_def.prep}?\"")
    elif activity.id == "pressureize":
        world.say(f"At last, {hero.pronoun('possessive').capitalize()} {parent.label} smiled and said, \"How about we {gear_def.prep}?\"")
    else:
        world.say(f"At last, {hero.pronoun('possessive').capitalize()} {parent.label} smiled and said, \"How about we {gear_def.prep}?\"")
    return gear_def


def accept(world: World, parent: Entity, hero: Entity, activity: Activity, prize: Entity, gear_def: Gear) -> None:
    hero.memes["joy"] += 1
    hero.memes["love"] += 1
    hero.memes["conflict"] = 0.0
    world.say(f"{hero.id} hugged {hero.pronoun('possessive')} {parent.label}. \"Yes, please,\" {hero.pronoun()} said.")
    if activity.id == "pressureize":
        ending = "Soon the little moon dough was pressed into a stronger shell."
    elif activity.id == "ignite":
        ending = "Soon the tiny planet charm was glowing softly instead of smoking."
    else:
        ending = "Soon the spell turned the quiet little thing into something new and bright."
    world.say(
        f"They {gear_def.tail}. {ending} In the end, {hero.id} was {activity.gerund}, "
        f"{prize.label} stayed safe, and the night looked like a storybook sky."
    )


