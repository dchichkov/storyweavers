def story_qa(world: World) -> list[QAItem]:
    f = world.facts
    hero, parent, prize, act = f["hero"], f["parent"], f["prize"], f["activity"]
    qa = [
        QAItem(
            question=f"Who is the fairy tale about?",
            answer=f"It is about {hero.id}, a little {hero.type}, and {parent.label}, who helps keep {prize.label} safe.",
        ),
        QAItem(
            question=f"What did {hero.id} want to do?",
            answer=f"{hero.id} wanted to {act.verb}, but that could have hurt {prize.label}.",
        ),
        QAItem(
            question=f"Why did {parent.label} worry?",
            answer=f"{parent.label} worried because {prize.label} could get {act.soil} if the spell went ahead without help.",
        ),
    ]
    if f.get("resolved"):
        gear = _safe_fact(world, f, "gear")
        qa.append(QAItem(
            question="How did they make the plan safer?",
            answer=f"They used {gear.label} first, so {hero.id} could still take part without ruining {prize.label}.",
        ))
    if f.get("conflict"):
        qa.append(QAItem(
            question=f"How did {hero.id} feel before the compromise?",
            answer=f"{hero.id} felt upset and stubborn for a moment, because the wish to {act.verb} was very strong.",
        ))
