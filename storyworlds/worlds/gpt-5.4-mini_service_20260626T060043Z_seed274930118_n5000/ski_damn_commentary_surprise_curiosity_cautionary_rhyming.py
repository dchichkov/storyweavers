def prize_at_risk(activity: Activity, prize: Prize) -> bool:
    return prize.region in activity.zone


def select_gear(activity: Activity, prize: Prize) -> Optional[Gear]:
    for gear in GEAR:
        if activity.mess in gear.guards and prize.region in gear.covers:
            return gear
    return None


def valid_combos() -> list[tuple[str, str, str]]:
    combos = []
    for place, setting in SETTINGS.items():
        for act_id in setting.affords:
            act = _safe_lookup(ACTIVITIES, act_id)
            for prize_id, prize in PRIZES.items():
                if prize_at_risk(act, prize) and select_gear(act, prize):
                    combos.append((place, act_id, prize_id))
    return combos


def _do_activity(world: World, actor: Entity, activity: Activity, narrate: bool = True) -> None:
    world.zone = set(activity.zone)
    actor.meters[activity.mess] = actor.meters.get(activity.mess, 0.0) + 1
    actor.memes["joy"] = actor.memes.get("joy", 0.0) + 1
    if narrate:
        world.say(f"{actor.id} went to {world.setting.place} and {activity.gerund} began.")
    for item in world.worn_items(actor):
        if item.plural and item.id == "ski_boots":
            continue


def predict_mess(world: World, actor: Entity, activity: Activity, prize_id: str) -> dict:
    sim = World(world.setting)
    sim.entities = {k: asdict_to_entity(v) for k, v in world.entities.items()}
    sim.zone = set(world.zone)
    _do_activity(sim, sim.get(actor.id), activity, narrate=False)
    prize = sim.entities.get(prize_id)
    return {"soiled": False if prize is None else True, "commentary": "careful"}


def asdict_to_entity(src: Entity) -> Entity:
    return Entity(**{k: getattr(src, k) for k in src.__dataclass_fields__.keys()})


def setting_detail(setting: Setting) -> str:
    return f"{setting.place.capitalize()} glittered white, with a wind that could nip and bite."


def introduce(world: World, hero: Entity) -> None:
    world.say(f"{hero.id} was a little {hero.type} who loved a bright winter day.")


def loves_activity(world: World, hero: Entity, activity: Activity) -> None:
    hero.memes["curiosity"] = hero.memes.get("curiosity", 0.0) + 1
    world.say(f"{hero.id} felt curiosity spark; {activity.keyword} was the word of the day.")


def prize_line(world: World, hero: Entity, prize: Entity) -> None:
    hero.memes["love"] = hero.memes.get("love", 0.0) + 1
    prize.worn_by = hero.id
    world.say(f"{hero.id} wore {hero.pronoun('possessive')} {prize.label} with pride and cheer.")


def commentary(world: World, parent: Entity, hero: Entity, activity: Activity, prize: Entity) -> None:
    world.facts["commentary"] = True
    world.say(
        f'"A little cautionary commentary," {parent.id} said, '
        f'"{prize.label} may get cold and damp if you ski too near the sheer."'
    )


def surprise_turn(world: World, hero: Entity, parent: Entity) -> None:
    hero.memes["surprise"] = hero.memes.get("surprise", 0.0) + 1
    world.say(f"Then came surprise: a laughing friend rolled over a shiny lost ski pole near.")


def compromise(world: World, parent: Entity, hero: Entity, activity: Activity, prize: Entity) -> Optional[Gear]:
    gear = select_gear(activity, prize)
    if gear is None:
        return None
    g = world.add(Entity(
        id=gear.id,
        kind="thing",
        type="gear",
        label=gear.label,
        owner=hero.id,
        worn_by=hero.id,
        plural=gear.plural,
    ))
    g.meters["protection"] = 1.0
    world.say(f'{parent.id} smiled, and said, "Let\'s {gear.prep}, then ski with care and cheer."')
    return gear


def resolve(world: World, hero: Entity, parent: Entity, activity: Activity, prize: Entity, gear: Gear) -> None:
    hero.memes["cautionary"] = hero.memes.get("cautionary", 0.0) + 1
    world.say(
        f"So {hero.id} {activity.gerund}, safe and bright, while {prize.label} stayed snug and near; "
        f"with a wink and a grin, the day went on, and the snow felt soft as a feather, dear."
    )


def tell(setting: Setting, activity: Activity, prize_cfg: Prize,
         hero_name: str = "Luna", hero_type: str = "girl",
         hero_traits: Optional[list[str]] = None, parent_type: str = "mother") -> World:
    world = World(setting)
    hero = world.add(Entity(id=hero_name, kind="character", type=hero_type, meters={}, memes={}))
    parent = world.add(Entity(id="Parent", kind="character", type=parent_type, label="parent"))
    prize = world.add(Entity(
        id="prize",
        kind="thing",
        type=prize_cfg.type,
        label=prize_cfg.label,
        phrase=prize_cfg.phrase,
        owner=hero.id,
        caretaker=parent.id,
        plural=prize_cfg.plural,
    ))

    introduce(world, hero)
    loves_activity(world, hero, activity)
    prize_line(world, hero, prize)
    world.para()
    world.say(setting_detail(setting))
    commentary(world, parent, hero, activity, prize)
    hero.memes["desire"] = hero.memes.get("desire", 0.0) + 1
    world.say(f"{hero.id} wanted to {activity.verb}, though {hero.pronoun('possessive')} feet did quiver.")
    surprise_turn(world, hero, parent)
    gear = compromise(world, parent, hero, activity, prize)
    world.para()
    if gear:
        resolve(world, hero, parent, activity, prize, gear)
        world.facts["resolved"] = True
        world.facts["gear"] = gear
    world.facts.update(hero=hero, parent=parent, prize=prize, activity=activity, setting=setting)
    return world


