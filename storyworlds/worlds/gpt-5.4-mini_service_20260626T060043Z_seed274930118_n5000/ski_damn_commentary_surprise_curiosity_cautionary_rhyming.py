def generation_prompts(world: World) -> list[str]:
    f = world.facts
    hero, parent, act, prize = f["hero"], f["parent"], f["activity"], f["prize"]
    return [
        f'Write a rhyming story about a child named {hero.id} who wants to {act.verb}.',
        f'Write a cautionary story with surprise and curiosity that mentions a {prize.label}.',
        f'Tell a short ski story with a parent\'s commentary and a happy ending in rhyme.',
    ]


def story_qa(world: World) -> list[QAItem]:
    f = world.facts
    hero, parent, prize, act = f["hero"], f["parent"], f["prize"], f["activity"]
    qa = [
        QAItem(
            question=f"What did {hero.id} want to do at {world.setting.place}?",
            answer=f"{hero.id} wanted to {act.verb}, because the ski hill felt exciting and bright.",
        ),
        QAItem(
            question=f"Why did {parent.id} give cautionary commentary about {prize.label}?",
            answer=f"{parent.id} worried that {prize.label} might get cold and damp during the snowy ride.",
        ),
        QAItem(
            question=f"What feeling rose when the surprise happened?",
            answer=f"Surprise rose when the lost ski pole appeared, and curiosity kept the day lively.",
        ),
    ]
    if f.get("resolved"):
        qa.append(
            QAItem(
                question=f"How did the story end for {hero.id}?",
                answer=f"{hero.id} skied safely with help, and the {prize.label} stayed snug and clean.",
            )
        )
    return qa


WORLD_KNOWLEDGE = {
    "ski": ("What is skiing?", "Skiing is moving over snow on long narrow boards called skis."),
    "snow": ("What is snow?", "Snow is frozen water that falls as soft white flakes."),
    "surprise": ("What is surprise?", "Surprise is the feeling you get when something unexpected happens."),
    "curiosity": ("What is curiosity?", "Curiosity is the wish to learn more and ask questions."),
    "cautionary": ("What does cautionary mean?", "Cautionary means giving a warning to help keep someone safe."),
    "commentary": ("What is commentary?", "Commentary is spoken or written talk that explains what is happening."),
}


def world_knowledge_qa(world: World) -> list[QAItem]:
    tags = set(world.facts["activity"].tags)
    out: list[QAItem] = []
    for key in ["ski", "snow", "surprise", "curiosity", "cautionary", "commentary"]:
        if key in tags or key == "commentary":
            q, a = WORLD_KNOWLEDGE[key]
            out.append(QAItem(question=q, answer=a))
    return out


def format_qa(sample: StorySample) -> str:
    lines = []
    lines.append("== Generation prompts ==")
    for i, p in enumerate(sample.prompts, 1):
        lines.append(f"{i}. {p}")
    lines.append("")
    lines.append("== Story Q&A ==")
    for item in sample.story_qa:
        lines.append(f"Q: {item.question}")
        lines.append(f"A: {item.answer}")
    lines.append("")
    lines.append("== World Q&A ==")
    for item in sample.world_qa:
        lines.append(f"Q: {item.question}")
        lines.append(f"A: {item.answer}")
    return "\n".join(lines)


