ASP_RULES = r"""
place(P) :- setting(P).
activity(A) :- activity(A).
prize(X) :- prize(X).

valid(Place, Act, Prize) :- affords(Place, Act), splashes(Act, R), worn_on(Prize, R).

bad_fix(Act, Fix) :- activity(Act), fix(Fix), bad_use(Fix, Act).
"""


def asp_facts() -> str:
    import asp
    lines = []
    for pid, s in SETTINGS.items():
        lines.append(asp.fact("setting", pid))
        for a in sorted(s.affords):
            lines.append(asp.fact("affords", pid, a))
    for aid, a in ACTIVITIES.items():
        lines.append(asp.fact("activity", aid))
        for r in sorted(a.zone):
            lines.append(asp.fact("splashes", aid, r))
    for prid, p in PRIZES.items():
        lines.append(asp.fact("prize", prid))
        lines.append(asp.fact("worn_on", prid, p.region))
    for fid, fx in FIXES.items():
        lines.append(asp.fact("fix", fid))
    return "\n".join(lines)


def asp_program(show: str) -> str:
    return f"{asp_facts()}\n{ASP_RULES}\n{show}\n"


def asp_valid_combos() -> list[tuple]:
    import asp
    model = asp.one_model(asp_program("#show valid/3."))
    return sorted(set(asp.atoms(model, "valid")))


def asp_verify() -> int:
    python_set = set(valid_combos())
    asp_set = set(asp_valid_combos())
    if python_set == asp_set:
        print(f"OK: ASP matches Python ({len(python_set)} combos).")
        return 0
    print("MISMATCH between ASP and Python")
    print("only in Python:", sorted(python_set - asp_set))
    print("only in ASP:", sorted(asp_set - python_set))
    return 1


