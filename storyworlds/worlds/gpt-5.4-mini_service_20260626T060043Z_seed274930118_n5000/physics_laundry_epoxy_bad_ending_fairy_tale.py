def _do_activity(world: World, child: Entity, activity: Activity) -> None:
    child.memes["curiosity"] = child.memes.get("curiosity", 0) + 1
    child.meters[activity.mess] = child.meters.get(activity.mess, 0) + 1
    child.meters["physics"] = child.meters.get("physics", 0) + 1


def tell(setting: Setting, activity: Activity, prize_cfg: Prize, hero_name: str, gender: str, parent_type: str) -> World:
    world = World(setting)
    hero = world.add(Entity(id=hero_name, kind="character", type=gender))
    parent = world.add(Entity(id="Parent", kind="character", type=parent_type, label=parent_type))
    prize = world.add(Entity(
        id="prize",
        type=prize_cfg.id,
        label=prize_cfg.label,
        phrase=prize_cfg.phrase,
        owner=hero.id,
        caretaker=parent.id,
        worn_by=hero.id,
    ))
    fix = world.add(Entity(id="epoxy", type="thing", label="epoxy", phrase="a jar of sticky epoxy"))

    world.say(f"Once in a little cottage, {hero.id} wore {prize.phrase} and loved learning about {activity.keyword}.")
    world.say(f"{hero.id} thought {activity.gerund} would be a grand fairy-tale trick, because {activity.physics} made the world seem magical.")

    world.para()
    world.say(f"One day, {hero.id} and the {parent.label} went to {setting.place}.")
    world.say(f"{hero.id} wanted to {activity.verb}, but the project left {activity.spill} and {activity.mess} marks on {prize.label}.")
    _do_activity(world, hero, activity)
    prize.meters[activity.mess] = prize.meters.get(activity.mess, 0) + 1
    prize.meters["dirty"] = prize.meters.get("dirty", 0) + 1

    world.para()
    world.say(f"The {parent.label} sighed, for the {prize.label} had become dirty and would need a wash.")
    world.say(f"Then they found {fix.phrase}, and {hero.id} said it could {fix.use}.")

    world.para()
    world.say(f"But the little spell went wrong.")
    world.say(f"Instead of helping, the epoxy was used to {fix.bad_use}, and the basket jammed shut.")
    prize.meters["stuck"] = prize.meters.get("stuck", 0) + 1
    world.facts["bad_ending"] = True

    world.para()
    world.say(f"The {parent.label} could not open it, the laundry stayed trapped, and the sticky jar would not come off the floor.")
    world.say(f"By dusk, {hero.id} had learned that clever ideas can fail when they are used in the wrong way.")
    world.say(f"So the cloak stayed stained, the wash stayed undone, and the cottage ended the day in a sad, sticky mess.")

    world.facts.update(
        hero=hero,
        parent=parent,
        prize=prize,
        activity=activity,
        setting=setting,
        fix=fix,
    )
    return world


