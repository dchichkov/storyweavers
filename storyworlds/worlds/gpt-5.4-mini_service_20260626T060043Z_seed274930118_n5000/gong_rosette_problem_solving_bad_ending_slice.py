def warn(world: World, parent: Entity, child: Entity, activity: Activity, prize: Entity) -> None:
    pred = predict_mess(world, child, activity, prize.id)
    if pred["damaged"]:
        world.facts["warning"] = True
        world.say(f'"If you hit the gong too hard, your {prize.label_word} might get bent," {parent.label_word} said.')


def attempt_fix(world: World, parent: Entity, child: Entity, activity: Activity, prize: Entity) -> Optional[Gear]:
    gear = select_gear(activity, prize)
    if gear is None:
        return None
    g = world.add(Entity(
        id=gear.id,
        type="gear",
        label=gear.label,
        protective=True,
        covers=set(gear.covers),
        owner=child.id,
    ))
    g.worn_by = child.id
    world.say(f"{child.pronoun('possessive').capitalize()} {parent.label_word} found a careful plan: {gear.prep}.")
    world.say(f"They hoped it would keep the {prize.label_word} safe.")
    return gear


def outcome_bad(world: World, child: Entity, parent: Entity, activity: Activity, prize: Entity, gear: Optional[Gear]) -> None:
    world.say(f"But the gong still made a deep, jumpy sound.")
    _do_activity(world, child, activity, narrate=True)
    if prize.meters["dusty"] >= THRESHOLD or prize.meters["bent"] >= THRESHOLD:
        world.say(f"The {prize.label_word} ended up dusty and bent anyway.")
    child.memes["disappointment"] += 1
    world.say(f"{child.id}'s smile slipped away, and the room went quiet again.")
    if gear:
        world.say(f"The {gear.label} stayed on, but it was not enough to fix the problem.")
    world.say(f"At the end, the gong was still, and the rosette sat crooked on {child.pronoun('possessive')} chest.")


