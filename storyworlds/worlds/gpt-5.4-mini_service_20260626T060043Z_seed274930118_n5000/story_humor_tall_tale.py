class World:
    def __init__(self, setting: Setting) -> None:
        self.setting = setting
        self.entities: dict[str, Entity] = {}
        self.fired: set[tuple] = set()
        self.paragraphs: list[list[str]] = [[]]
        self.facts: dict = {}

    def add(self, ent: Entity) -> Entity:
        self.entities[ent.id] = ent
        return ent

    def get(self, eid: str) -> Entity:
        if eid not in self.entities:
            label = str(eid).replace("_", " ")
            self.entities[eid] = Entity(str(eid), label=label)
        return self.entities[eid]

    def characters(self) -> list[Entity]:
        return [e for e in self.entities.values() if e.kind == "character"]

    def worn_items(self, actor: Entity) -> list[Entity]:
        return [e for e in self.entities.values() if e.worn_by == actor.id]

    def covered(self, actor: Entity, region: str) -> bool:
        return any(g.protective and region in g.covers for g in self.worn_items(actor))

    def say(self, text: str) -> None:
        if text:
            self.paragraphs[-1].append(text)

    def para(self) -> None:
        if self.paragraphs[-1]:
            self.paragraphs.append([])

    def render(self) -> str:
        return "\n\n".join(" ".join(p) for p in self.paragraphs if p)


def _r_blow_hat(world: World) -> list[str]:
    out: list[str] = []
    for actor in world.characters():
        if actor.memes["laugh"] < THRESHOLD and actor.meters["wind"] < THRESHOLD:
            continue
        for item in world.worn_items(actor):
            if item.protective:
                continue
            if item.region != "head":
                continue
            if world.covered(actor, "head"):
                continue
            sig = ("blow", item.id)
            if sig in world.fired:
                continue
            world.fired.add(sig)
            item.meters["lost"] += 1
            out.append(f"{actor.pronoun('possessive').capitalize()} {item.label} nearly blew away.")
    return out


CAUSAL_RULES = [_r_blow_hat]


def propagate(world: World, narrate: bool = True) -> list[str]:
    produced: list[str] = []
    changed = True
    for _ in range(len(globals().get("CAUSAL_RULES", [])) + 4):
        changed = False
        for rule in CAUSAL_RULES:
            sents = rule(world)
            if sents:
                changed = True
                produced.extend(sents)
    if narrate:
        for s in produced:
            world.say(s)
    return produced


def prize_at_risk(activity: Activity, prize: Prize) -> bool:
    return prize.region in activity.zone


def select_gear(activity: Activity, prize: Prize) -> Optional[Gear]:
    for gear in GEAR:
        if activity.mess in gear.guards and prize.region in gear.covers:
            return gear
    return None


def predict(world: World, actor: Entity, activity: Activity, prize_id: str) -> dict:
    sim = World(world.setting)
    import copy
    sim.entities = copy.deepcopy(world.entities)
    sim.fired = set(world.fired)
    sim.facts = dict(world.facts)
    sim.get(actor.id).meters["wind"] += 1
    sim.get(actor.id).memes["laugh"] += 1
    prize = sim.get(prize_id)
    if prize.region == "head" and not sim.covered(sim.get(actor.id), "head"):
        prize.meters["lost"] += 1
    return {"lost": prize.meters["lost"] >= THRESHOLD}


def introduce(world: World, hero: Entity) -> None:
    world.say(f"{hero.id} was a little {hero.type} who loved a good story and a louder one.")
    world.say(f"{hero.pronoun().capitalize()} could stretch a tale like taffy and still grin at the end of it.")


def love_hat(world: World, hero: Entity, hat: Entity) -> None:
    hero.memes["love"] += 1
    hat.worn_by = hero.id
    world.say(
        f"{hero.id} loved {hero.pronoun('possessive')} {hat.label} because it had a ribbon "
        f"that fluttered like a flag on a parade horse."
    )


def arrive(world: World, hero: Entity, parent: Entity, activity: Activity) -> None:
    place = world.setting.place
    if world.setting.indoors:
        world.say(f"One day, {hero.id} and {hero.pronoun('possessive')} {parent.label} were inside {place}.")
    else:
        world.say(f"One day, {hero.id} and {hero.pronoun('possessive')} {parent.label} went to {place}.")
    world.say("The stage was small, but the grin on it could have fed a whole barn full of chickens.")


def begins(world: World, hero: Entity, activity: Activity) -> None:
    hero.memes["pride"] += 1
    hero.meters["wind"] += 1
    world.say(f"{hero.id} wanted to {activity.verb} right away, and {hero.pronoun('possessive')} voice got bigger with every word.")
    world.say(f"{hero.pronoun().capitalize()} started {activity.gerund}, and the tale grew taller than the porch post.")


def warn(world: World, parent: Entity, hero: Entity, activity: Activity, prize: Entity) -> bool:
    pred = predict(world, hero, activity, prize.id)
    if not pred["lost"]:
        return False
    parent.memes["worry"] += 1
    world.facts["predicted_lost"] = True
    world.say(
        f'"Careful," {parent.id} said. "If that story gets any taller, {hero.pronoun("possessive")} {prize.label} may blow away."'
    )
    return True


def overdo(world: World, hero: Entity, activity: Activity) -> None:
    hero.memes["laugh"] += 1
    hero.meters["wind"] += 1
    world.say(f"{hero.id} tried to {activity.rush}, and even the fence seemed to lean in to listen.")
    world.say("The crowd laughed, and the laugh made a little wind of its own.")


def slip(world: World, hero: Entity, prize: Entity) -> None:
    if prize.meters["lost"] >= THRESHOLD:
        world.say(f"Then {hero.id}'s {prize.label} gave a wobble and nearly skipped off like a bright little moon.")
    propagate(world, narrate=True)


def compromise(world: World, parent: Entity, hero: Entity, activity: Activity, prize: Entity) -> Optional[Gear]:
    gear_def = select_gear(activity, prize)
    if gear_def is None:
        return None
    if predict(world, hero, activity, prize.id)["lost"]:
        gear = world.add(Entity(
            id=gear_def.id,
            type="gear",
            label=gear_def.label,
            owner=hero.id,
            caretaker=parent.id,
            protective=True,
            covers=set(gear_def.covers),
            plural=gear_def.plural,
        ))
        gear.worn_by = hero.id
        world.say(
            f"{parent.id} laughed and said, "
            f'"How about we {gear_def.prep}?"'
        )
        return gear_def
    return None


def finish(world: World, parent: Entity, hero: Entity, activity: Activity, prize: Entity, gear_def: Gear) -> None:
    hero.memes["relief"] += 1
    hero.memes["delight"] += 1
    world.say(
        f"{hero.id} nodded so fast the ribbon on {hero.pronoun('possessive')} {prize.label} bounced like a jump rope."
    )
    world.say(
        f"They {gear_def.tail}. Then {hero.id} kept {activity.gerund}, "
        f"{prize.label} stayed snug, and the whole crowd laughed for the right reason."
    )


def tell(setting: Setting, activity: Activity, prize_cfg: Prize,
         hero_name: str = "Ruby", hero_type: str = "girl",
         parent_type: str = "mother", trait: str = "bold") -> World:
    world = World(setting)
    hero = world.add(Entity(id=hero_name, kind="character", type=hero_type))
    parent = world.add(Entity(id="Parent", kind="character", type=parent_type, label="mom"))
    prize = world.add(Entity(
        id="hat",
        type=prize_cfg.type,
        label=prize_cfg.label,
        phrase=prize_cfg.phrase,
        owner=hero.id,
        caretaker=parent.id,
        region=prize_cfg.region,
        plural=prize_cfg.plural,
    ))

    intro = f"{hero.id} was a {trait} little {hero.type} with a bigger grin than a wagon wheel."
    world.say(intro)
    introduce(world, hero)
    love_hat(world, hero, prize)

    world.para()
    arrive(world, hero, parent, activity)
    begins(world, hero, activity)
    warn(world, parent, hero, activity, prize)
    overdo(world, hero, activity)
    slip(world, hero, prize)

    world.para()
    gear_def = compromise(world, parent, hero, activity, prize)
    if gear_def:
        finish(world, parent, hero, activity, prize, gear_def)

    world.facts.update(
        hero=hero,
        parent=parent,
        prize=prize,
        activity=activity,
        gear=gear_def,
        setting=setting,
        resolved=gear_def is not None,
    )
    return world


