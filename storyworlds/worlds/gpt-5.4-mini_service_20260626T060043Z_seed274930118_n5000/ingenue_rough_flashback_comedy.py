    prize = world.add(Entity(id="prize", type=prize_cfg.id, label=prize_cfg.label, phrase=prize_cfg.phrase, owner=hero.id, caretaker=parent.id))
    prize.worn_by = hero.id

    world.say(f"{hero.id} was a {trait} {hero.type} who loved rehearsals and comic mishaps.")
    world.say(f"{hero.id} wore {hero.pronoun('possessive')} {prize.label} like it was the star of the show.")
    world.para()
    world.say(f"One day at {setting.place}, {hero.id} wanted to {activity.verb}.")
    world.say(f"That sounded easy, until the rough work began.")
    world.say(f"{hero.pronoun().capitalize()} glanced at the props and laughed nervously.")
    world.para()
    world.say(flashback_reason(world, hero))
    world.say(f"That was why {hero.id} had tucked away a backup idea for exactly this kind of day.")
    world.para()
    world.say(f"{parent.label if parent.label else 'The parent'} pointed at the problem and smiled.")
    gear = select_gear(activity, prize)
    if gear is None:
        gear = next(iter(globals().get("GEARS", globals().get("GEAR", []))))
    world.say(f'"How about we {gear.prep}?" said the parent.')
    world.say(f"{hero.id} grinned, because the plan was rough but clever.")
    _do_activity(world, hero, activity)
    if activity.id == "props":
        world.say(f"The props were rough, but the gloves made the job feel like a joke with handles.")
    elif activity.id == "paint":
        world.say(f"The paint splashed everywhere except on the apron, which was almost suspiciously well-behaved.")
    elif activity.id == "mud":
        world.say(f"The mud squelched, but the boots kept the feet dry and the punchline on track.")
    world.say(f"In the end, {hero.id} was {activity.gerund}, {prize.label} stayed clean, and everyone laughed at the lucky timing.")
    world.facts = {
        "hero": hero,
        "parent": parent,
        "prize": prize,
        "activity": activity,
        "setting": setting,
        "gear": gear,
        "flashback": True,
    }
    return world


