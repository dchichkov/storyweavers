def resolve_params(args: argparse.Namespace, rng: random.Random) -> StoryParams:
    combos = [c for c in valid_combos()
              if (getattr(args, "place", None) is None or c[0] == getattr(args, "place", None))
              and (getattr(args, "problem", None) is None or c[1] == getattr(args, "problem", None))
              and (getattr(args, "friend", None) is None or c[2] == getattr(args, "friend", None))]
    if not combos:
        return _fallback_storyparams(args, rng, StoryParams, globals())
    place, problem, friend = rng.choice(list(combos))
    gender = getattr(args, "gender", None) or rng.choice(["girl", "boy"])
    name = getattr(args, "name", None) or rng.choice(GIRL_NAMES if gender == "girl" else BOY_NAMES)
    return StoryParams(place=place, problem=problem, friend=friend, name=name, gender=gender)


def _narrate_setting(world: World, hero: Entity, friend: Entity, problem: Problem) -> None:
    world.say(
        f"{hero.id} and {friend.id} went to {world.setting.place} to {problem.gerund}."
    )
    world.say(
        f"The place felt {world.setting.clue_style}, and the first thing {hero.id} noticed was the silence."
    )


def _foreshadow(world: World, problem: Problem) -> None:
    world.say(
        f"Before long, {problem.trail} made {_safe_fact((globals().get("world") or locals().get("world") or locals().get("mw") or locals().get("w")), world.facts, "hero").id} pause and look twice."
    )
    world.say(
        f"It was a small hint, but it meant the answer was hiding nearby."
    )


def _problem_turn(world: World, hero: Entity, friend: Entity, problem: Problem) -> None:
    hero.memes["worry"] = hero.memes.get("worry", 0.0) + 1
    friend.memes["support"] = friend.memes.get("support", 0.0) + 1
    world.say(
        f"{hero.id} looked around again, but the missing thing was still gone."
    )
    world.say(
        f"Then {friend.id} said, \"{_safe_lookup(FRIENDS, _safe_fact((globals().get("world") or locals().get("world") or locals().get("mw") or locals().get("w")), world.facts, "friend_id")).help_line}.\""
    )
    world.say(
        f"That helped {hero.id} comprehend the clue instead of just staring at it."
    )


def _solve(world: World, hero: Entity, friend: Entity, problem: Problem) -> None:
    hero.memes["relief"] = hero.memes.get("relief", 0.0) + 1
    friend.memes["joy"] = friend.memes.get("joy", 0.0) + 1
    world.say(
        f"Together they looked low, looked high, and followed the clue to the right spot."
    )
    world.say(
        f"At last, {problem.reveal}, and {hero.id} smiled because the mystery made sense."
    )
    world.say(
        f"{friend.id} laughed, and the two friends left {world.setting.place} knowing teamwork had solved it."
    )


def tell(setting: Setting, problem: Problem, friend_def: Friend, hero_name: str, hero_gender: str) -> World:
    world = World(setting)
    hero = world.add(Entity(id=hero_name, kind="character", type=hero_gender, traits=["curious"]))
    friend = world.add(Entity(id=friend_def.id, kind="character", type="child", label=friend_def.label))
    mystery = world.add(Entity(id=problem.id, kind="thing", type="object", label=problem.keyword, hidden=True))

    world.facts.update(hero=hero, friend=friend, friend_id=friend_def.id, problem=problem, mystery=mystery)
    world.say(f"{hero.id} was a {hero_gender} who liked to look for clues and comprehend puzzles.")
    world.say(f"{hero.id} had a good friend named {friend.id}, and together they were very good at problem solving.")
