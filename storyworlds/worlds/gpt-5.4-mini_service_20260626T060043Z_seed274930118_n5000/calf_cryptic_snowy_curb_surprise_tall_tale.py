def _r_soak(world: World) -> list[str]:
    out: list[str] = []
    for actor in world.characters():
        for mess in ("wet", "snowy"):
            if actor.meters.get(mess, 0.0) < THRESHOLD:
                continue
            for item in world.worn_items(actor):
                if item.protective or item.region not in world.zone:
                    continue
                if world.covered(actor, item.region):
                    continue
                sig = ("soak", item.id, mess)
                if sig in world.fired:
                    continue
                world.fired.add(sig)
                item.meters[mess] = item.meters.get(mess, 0.0) + 1
                item.meters["dirty"] = item.meters.get("dirty", 0.0) + 1
                out.append(f"{actor.id}'s {item.label} got wet and snowy.")
    return out


def _r_worry(world: World) -> list[str]:
    out: list[str] = []
    for item in list(world.entities.values()):
        if item.meters.get("dirty", 0.0) < THRESHOLD or not item.caretaker:
            continue
        sig = ("worry", item.id)
        if sig in world.fired:
            continue
        world.fired.add(sig)
        carer = world.get(item.caretaker)
        carer.memes["worry"] = carer.memes.get("worry", 0.0) + 1
        out.append(f"That would worry {carer.id}.")
    return out


CAUSAL_RULES: list[Rule] = [Rule("soak", "physical", _r_soak), Rule("worry", "social", _r_worry)]


def propagate(world: World, narrate: bool = True) -> list[str]:
    produced: list[str] = []
    changed = True
    for _ in range(len(globals().get("CAUSAL_RULES", [])) + 4):
        changed = False
        for rule in CAUSAL_RULES:
            sents = rule.apply(world)
            if sents:
                changed = True
                produced.extend(sents)
    if narrate:
        for s in produced:
            world.say(s)
    return produced


def prize_at_risk(activity: Activity, prize: Prize) -> bool:
    return prize.region in activity.zone


def select_gear(activity: Activity, prize: Prize) -> Optional[Gear]:
    for gear in GEAR:
        if activity.mess in gear.guards and prize.region in gear.covers:
            return gear
    return None


def predict_mess(world: World, actor: Entity, activity: Activity, prize_id: str) -> dict:
    sim = world.copy()
    _do_activity(sim, sim.get(actor.id), activity, narrate=False)
    prize = sim.entities.get(prize_id)
    return {"soiled": bool(prize and prize.meters.get("dirty", 0.0) >= THRESHOLD)}


def _do_activity(world: World, actor: Entity, activity: Activity, narrate: bool = True) -> None:
    if activity.id not in world.setting.affords:
        return
    world.zone = set(activity.zone)
    actor.meters[activity.mess] = actor.meters.get(activity.mess, 0.0) + 1
    actor.memes["joy"] = actor.memes.get("joy", 0.0) + 1
    propagate(world, narrate=narrate)


def tell(world_setting: Setting, activity: Activity, prize_cfg: Prize,
         hero_name: str = "Mabel", parent_name: str = "Nora") -> World:
    world = World(world_setting)
    calf = world.add(Entity(id=hero_name, kind="character", type="calf"))
    guardian = world.add(Entity(id=parent_name, kind="character", type="cow", label="the old cow"))
    prize = world.add(Entity(
        id="prize",
        type=prize_cfg.type,
        label=prize_cfg.label,
        phrase=prize_cfg.phrase,
        owner=calf.id,
        caretaker=guardian.id,
        region=prize_cfg.region,
        plural=prize_cfg.plural,
    ))
    clue = world.add(Entity(
        id="clue",
        type="thing",
        label="cryptic sign",
        phrase="a cryptic sign",
    ))

    world.say(f"{calf.id} was a little calf with a big nose for mysteries.")
    world.say(f"{calf.id} loved {activity.gerund}, especially when the world looked {world.setting.place}.")
    world.say(f"One morning, {guardian.id} had given {calf.id} {prize.phrase}, and {calf.id} loved it dearly.")
    world.para()

    world.say(f"At {world.setting.place}, {calf.id} found {clue.phrase} half-buried in the snow.")
    world.say(f"It pointed toward the curb and seemed to whisper, \"Follow me if you want a surprise.\"")
    world.say(f"{calf.id} wanted to {activity.verb}, but {guardian.id} frowned at the icy curb and the snowdrift below it.")
    if predict_mess(world, calf, activity, prize.id)["soiled"]:
        world.say(f"\"If you {activity.verb}, your {prize.label} will get {activity.soil},\" {guardian.id} said.")
    world.say(f"{calf.id} tried to {activity.rush}, because the clue looked too crooked to ignore.")
    world.para()

    calf.memes["defiance"] = calf.memes.get("defiance", 0.0) + 1
    calf.memes["grabbed_by"] = calf.memes.get("grabbed_by", 0.0) + 1
    world.say(f"Then {guardian.id} hooked a gentle hoof around {calf.id} and said, \"Not without a safer way.\"")

    gear_def = select_gear(activity, prize)
    if gear_def is not None:
        gear = world.add(Entity(
            id=gear_def.id,
            type="gear",
            label=gear_def.label,
            owner=calf.id,
            caretaker=guardian.id,
            protective=True,
            covers=set(gear_def.covers),
            plural=gear_def.plural,
        ))
        gear.worn_by = calf.id
        if predict_mess(world, calf, activity, prize.id)["soiled"]:
            gear.worn_by = None
            del world.entities[gear.id]
            gear_def = None
        else:
            world.say(f"{guardian.id} smiled. \"How about we {gear_def.prep} and then look?\"")
            world.say(f"{calf.id}'s ears perked up, and the grumpy feeling melted like ice in a warm pail.")
            world.say(f"They {gear_def.tail}, and the cryptic sign led them right to the curb's hidden nook.")
            world.say(f"There, tucked under a snowy hump, was a surprise: a tiny bright bell and a bowl of sweet hay.")
            world.say(f"{calf.id} was {activity.gerund}, {prize.id if False else 'still'} keeping {prize.label} clean, while {guardian.id} laughed at the marvelous find.")
    if gear_def is None:
        world.say(f"{guardian.id} pulled {calf.id} back and together they studied the clue until they found the surprise by hand.")
