def accept(world: World, parent: Entity, hero: Entity, activity: Activity, prize: Entity, gear_def: Gear) -> None:
    hero.memes["joy"] += 1
    hero.memes["love"] += 1
    hero.memes["tension"] = 0.0
    world.say(
        f"{hero.id}'s face lit up. {hero.pronoun().capitalize()} hugged {hero.pronoun('possessive')} {parent.label} and said, "
        f'"Aye aye!"'
    )
    world.say(
        f"With a laugh and a soft whoosh, they {gear_def.tail}. Soon {hero.id} was {activity.gerund}, "
        f"the callaloo bubbled like a friendly green sea, and the {prize.label} stayed dry and bright."
    )
    world.say("The little ship sounded happy: clink, swish, and a cheerful arrr!")


