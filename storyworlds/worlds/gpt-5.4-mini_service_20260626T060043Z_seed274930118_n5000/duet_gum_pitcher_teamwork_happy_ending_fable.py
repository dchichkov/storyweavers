def prize_at_risk(activity: Activity, prize: Prize) -> bool:
    return prize.region in activity.zone


def select_gear(activity: Activity, prize: Prize) -> Optional[Gear]:
    for gear in GEAR:
        if activity.mess in gear.guards and prize.region in gear.covers:
            return gear
    return None


def valid_combos() -> list[tuple[str, str, str]]:
    combos = []
    for place, setting in SETTINGS.items():
        for act_id in setting.affords:
            act = _safe_lookup(ACTIVITIES, act_id)
            for prize_id, prize in PRIZES.items():
                if prize_at_risk(act, prize) and select_gear(act, prize):
                    combos.append((place, act_id, prize_id))
    return combos


def explain_rejection(activity: Activity, prize: Prize) -> str:
    if not prize_at_risk(activity, prize):
        return "(No story: the pitcher is not actually in the duet's splash zone, so there is no honest problem to solve.)"
    return "(No story: no gear in this world can both cover the pitcher and handle the sticky gum.)"


def explain_gender(prize_id: str, gender: str) -> str:
    ok = " / ".join(sorted(_safe_lookup(PRIZES, prize_id).genders))
    return f"(No story: this world prefers a {ok} lead for that object, not {gender}.)"


def _do_activity(world: World, actor: Entity, activity: Activity, narrate: bool = True) -> None:
    world.zone = set(activity.zone)
    actor.meters[activity.mess] = actor.m(activity.mess) + 1
    actor.memes["joy"] = actor.e("joy") + 1
    if narrate:
        world.say(f"{actor.id} tried to {activity.verb}.")


def predict_mess(world: World, actor: Entity, activity: Activity, prize_id: str) -> dict:
    sim = world.copy()
    _do_activity(sim, sim.get(actor.id), activity, narrate=False)
    prize = sim.get(prize_id)
    return {"soiled": prize.m(activity.mess) >= THRESHOLD}


def propagate(world: World, narrate: bool = True) -> list[str]:
    out: list[str] = []
    for actor in world.characters():
        if actor.m("sticky") >= THRESHOLD:
            for item in list(world.entities.values()):
                if item.worn_by == actor.id and not item.protective and item.region in world.zone:
                    sig = ("sticky", actor.id, item.id)
                    if sig in world.fired:
                        continue
                    world.fired.add(sig)
                    item.meters["sticky"] = item.m("sticky") + 1
                    out.append(f"{actor.id}'s {item.label} got sticky with gum.")
    if narrate:
        for s in out:
            world.say(s)
    return out


