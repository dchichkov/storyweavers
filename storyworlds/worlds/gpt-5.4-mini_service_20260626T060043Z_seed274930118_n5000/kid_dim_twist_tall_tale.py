class World:
    def __init__(self, setting: Setting) -> None:
        self.setting = setting
        self.entities: dict[str, Entity] = {}
        self.fired: set[tuple] = set()
        self.paragraphs: list[list[str]] = [[]]
        self.zone: set[str] = set()
        self.weather: str = ""
        self.facts: dict = {}

    def add(self, ent: Entity) -> Entity:
        self.entities[ent.id] = ent
        return ent

    def get(self, eid: str) -> Entity:
        if eid not in self.entities:
            label = str(eid).replace("_", " ")
            self.entities[eid] = Entity(str(eid), label=label)
        return self.entities[eid]

    def characters(self) -> list[Entity]:
        return [e for e in self.entities.values() if e.kind == "character"]

    def worn_items(self, actor: Entity) -> list[Entity]:
        return [e for e in self.entities.values() if e.worn_by == actor.id]

    def covered(self, actor: Entity, region: str) -> bool:
        return any(g.protective and region in g.covers for g in self.worn_items(actor))

    def say(self, text: str) -> None:
        if text:
            self.paragraphs[-1].append(text)

    def para(self) -> None:
        if self.paragraphs[-1]:
            self.paragraphs.append([])

    def render(self) -> str:
        return "\n\n".join(" ".join(p) for p in self.paragraphs if p)

    def copy(self) -> "World":
        clone = World(self.setting)
        clone.entities = copy.deepcopy(self.entities)
        clone.fired = set(self.fired)
        clone.zone = set(self.zone)
        clone.weather = self.weather
        clone.paragraphs = [[]]
        return clone


def _r_soil(world: World) -> list[str]:
    out: list[str] = []
    for actor in world.characters():
        for mess in ["dusty", "tangled", "windy"]:
            if actor.meters[mess] < THRESHOLD:
                continue
            for item in world.worn_items(actor):
                if item.protective or item.region not in world.zone:
                    continue
                if world.covered(actor, item.region):
                    continue
                sig = ("soil", actor.id, item.id, mess)
                if sig in world.fired:
                    continue
                world.fired.add(sig)
                item.meters[mess] += 1
                out.append(f"{actor.id}'s {item.label} got {mess}.")
    return out


def _r_worry(world: World) -> list[str]:
    out: list[str] = []
    for item in list(world.entities.values()):
        if item.meters["dusty"] < THRESHOLD and item.meters["tangled"] < THRESHOLD:
            continue
        if not item.caretaker:
            continue
        sig = ("worry", item.id)
        if sig in world.fired:
            continue
        world.fired.add(sig)
        carer = world.get(item.caretaker)
        carer.memes["worry"] += 1
        out.append(f"That would mean extra work for {carer.label_word}.")
    return out


def _r_defiance(world: World) -> list[str]:
    for actor in world.characters():
        if actor.memes["defiance"] < THRESHOLD:
            continue
        sig = ("tension", actor.id)
        if sig in world.fired:
            continue
        world.fired.add(sig)
        actor.memes["awe"] += 0.5
        return ["__tension__"]
    return []


CAUSAL_RULES = [
    _r_soil,
    _r_worry,
    _r_defiance,
]


def propagate(world: World, narrate: bool = True) -> list[str]:
    produced: list[str] = []
    changed = True
    for _ in range(len(globals().get("CAUSAL_RULES", [])) + 4):
        changed = False
        for rule in CAUSAL_RULES:
            sents = rule(world)
            if sents:
                changed = True
                produced.extend(s for s in sents if s != "__tension__")
    if narrate:
        for s in produced:
            world.say(s)
    return produced


def tall_tale_intro(hero: Entity) -> str:
    return f"{hero.id} was a little {next((t for t in hero.traits if t != 'little'), 'bright-eyed')} {hero.type} in a kid-dim world where a fence post could feel as tall as a lighthouse."


def setting_detail(setting: Setting, activity: Activity) -> str:
    if setting.indoor:
        return f"Inside {setting.place}, the air felt snug, but the twisting room still had plenty of room for a tall tale."
    if activity.weather == "windy":
        return f"At {setting.place}, the wind came in long, curly breaths and shook the grasses like a giant's whiskers."
    if activity.weather == "dusty":
        return f"{setting.place.capitalize()} was dry as an old biscuit, with dust waiting on every breeze."
    return f"{setting.place.capitalize()} looked wide and bright, ready for a small hero with a big idea."


def predict(world: World, actor: Entity, activity: Activity, prize_id: str) -> dict:
    sim = world.copy()
    _do_activity(sim, sim.get(actor.id), activity, narrate=False)
    prize = sim.entities.get(prize_id)
    return {
        "soiled": bool(prize and (prize.meters[activity.mess] >= THRESHOLD)),
        "worry": sum(e.memes["worry"] for e in sim.characters()),
    }


def _do_activity(world: World, actor: Entity, activity: Activity, narrate: bool = True) -> None:
    if activity.id not in world.setting.affords:
        return
    world.zone = set(activity.zone)
    actor.meters[activity.mess] += 1
    actor.memes["joy"] += 1
    propagate(world, narrate=narrate)


def select_gear(activity: Activity, prize: Prize) -> Optional[Gear]:
    for gear in GEAR:
        if activity.mess in gear.guards and prize.region in gear.covers:
            return gear
    return None


def prize_at_risk(activity: Activity, prize: Prize) -> bool:
    return prize.region in activity.zone


def tell(world_setting: Setting, activity: Activity, prize_cfg: Prize, hero_name: str, hero_type: str,
         parent_type: str, trait: str) -> World:
    world = World(world_setting)
    world.weather = "" if world_setting.indoor else activity.weather

    hero = world.add(Entity(id=hero_name, kind="character", type=hero_type, traits=["little", trait, "stubborn"]))
    parent = world.add(Entity(id="Parent", kind="character", type=parent_type, label="the parent"))
    prize = world.add(Entity(
        id="prize",
        type=prize_cfg.type,
        label=prize_cfg.label,
        phrase=prize_cfg.phrase,
        owner=hero.id,
        caretaker=parent.id,
        region=prize_cfg.region,
        plural=prize_cfg.plural,
    ))

    hero.memes["love"] += 1
    hero.memes["awe"] += 1
    world.say(tall_tale_intro(hero))
    world.say(f"{hero.pronoun().capitalize()} loved to {activity.verb} and {activity.gerund} when the day felt made for mischief.")
    world.say(f"One week, {hero.label_word if hero.kind == 'character' else hero.id}'s {parent.label_word} bought {hero.pronoun('object')} {prize.phrase}.")
    prize.worn_by = hero.id
    world.say(f"{hero.id} loved {prize.it()} and wore {prize.it()} like it had been handed down by the moon itself.")

    world.para()
    world.say(setting_detail(world_setting, activity))
    world.say(f"{hero.id} wanted to {activity.verb}, but {hero.pronoun('possessive')} {parent.label_word} raised a careful eyebrow.")
    pred = predict(world, hero, activity, prize.id)
    if not prize_at_risk(activity, prize):
        pass
    world.facts["predicted_soil"] = activity.soil
    world.facts["predicted_worry"] = pred["worry"]
    world.say(f'"You\'ll get your {prize.label} {activity.soil}," {hero.pronoun("possessive")} {parent.label_word} warned.')
    hero.memes["defiance"] += 1
    world.say(f"{hero.id} heard that and still tried to {activity.rush}, because the wish to play was bigger than a wagon wheel.")
    _do_activity(world, hero, activity, narrate=True)

    world.para()
    gear_def = select_gear(activity, prize)
    if gear_def is None:
        pass
    gear = world.add(Entity(
        id=gear_def.id,
        type="gear",
        label=gear_def.label,
        owner=hero.id,
        caretaker=parent.id,
        protective=True,
        covers=set(gear_def.covers),
        plural=gear_def.plural,
    ))
    gear.worn_by = hero.id
    if predict(world, hero, activity, prize.id)["soiled"]:
        gear.worn_by = None
        del world.entities[gear.id]
        pass
    world.say(f"{hero.pronoun('possessive').capitalize()} {parent.label_word} smiled and said, \"How about we {gear_def.prep} and try again?\"")
    hero.memes["joy"] += 1
    hero.memes["love"] += 1
    hero.memes["defiance"] = 0.0
    world.say(f"{hero.id}'s face lit up like sunrise on a tin roof. {hero.id} hugged {hero.pronoun('possessive')} {parent.label_word} and said, \"Yes, let's do it!\"")
    world.say(f"They {gear_def.tail}. Soon {hero.id} was {activity.gerund}, {hero.pronoun('possessive')} {prize.label} stayed clean, and the whole day seemed to tip its hat.")
    world.facts.update(hero=hero, parent=parent, prize=prize, prize_cfg=prize_cfg, activity=activity, setting=world_setting, gear=gear, resolved=True, conflict=True)
    return world


