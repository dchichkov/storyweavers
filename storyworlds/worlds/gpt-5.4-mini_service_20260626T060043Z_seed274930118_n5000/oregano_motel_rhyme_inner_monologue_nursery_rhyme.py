def rhyme_line(a: str, b: str) -> str:
    return f"{a} and {b} in a tidy little whirl."


def choose_pair(rng: random.Random, options: list[str]) -> tuple[str, str]:
    a = rng.choice(options)
    b = rng.choice([x for x in options if x != a])
    return a, b


