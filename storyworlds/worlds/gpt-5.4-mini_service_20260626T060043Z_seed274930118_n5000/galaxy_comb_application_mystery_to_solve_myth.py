def _r_fear(world: World) -> list[str]:
    out: list[str] = []
    hero = world.facts.get("hero")
    mystery = world.facts.get("mystery")
    if not hero or not mystery:
        return out
    if hero.memes.get("dread", 0.0) >= THRESHOLD and ("fear", hero.id) not in world.fired:
        world.fired.add(("fear", hero.id))
        out.append(f"{hero.id} felt the old fear of the unknown stir under the stars.")
    return out


def _r_clue(world: World) -> list[str]:
    out: list[str] = []
    hero = world.facts.get("hero")
    mystery = world.facts.get("mystery")
    tool = world.facts.get("tool")
    if not hero or not mystery or not tool:
        return out
    if tool.carried_by == hero.id and hero.meters.get("seek", 0.0) >= THRESHOLD:
        sig = ("clue", mystery.id)
        if sig not in world.fired:
            world.fired.add(sig)
            world.clue_revealed = True
            out.append(
                f"The comb parted the bright tangle, and a hidden sign shone in the galaxy."
            )
    return out


def _r_resolution(world: World) -> list[str]:
    out: list[str] = []
    hero = world.facts.get("hero")
    mystery = world.facts.get("mystery")
    if not hero or not mystery:
        return out
    if world.clue_revealed and ("resolve", hero.id) not in world.fired:
        world.fired.add(("resolve", hero.id))
        hero.memes["peace"] = hero.memes.get("peace", 0.0) + 1
        hero.memes["dread"] = 0.0
        out.append(f"At last, the mystery had an answer, and the night grew gentle again.")
    return out


RULES = [Rule("fear", _r_fear), Rule("clue", _r_clue), Rule("resolution", _r_resolution)]


def propagate(world: World, narrate: bool = True) -> list[str]:
    out: list[str] = []
    changed = True
    for _ in range(len(globals().get("CAUSAL_RULES", [])) + 4):
        changed = False
        for rule in RULES:
            sents = rule.apply(world)
            if sents:
                changed = True
                out.extend(sents)
    if narrate:
        for s in out:
            world.say(s)
    return out


