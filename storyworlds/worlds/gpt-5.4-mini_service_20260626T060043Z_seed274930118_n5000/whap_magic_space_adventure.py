def prize_at_risk(action: Action, prize: Prize) -> bool:
    return prize.region in action.zone


def select_gear(action: Action, prize: Prize) -> Optional[Gear]:
    for gear in GEAR:
        if action.mess in gear.guards and prize.region in gear.covers:
            return gear
    return None


def valid_combos() -> list[tuple[str, str, str]]:
    combos = []
    for place, setting in SETTINGS.items():
        for act_id in setting.affords:
            action = _safe_lookup(ACTIONS, act_id)
            for prize_id, prize in PRIZES.items():
                if prize_at_risk(action, prize) and select_gear(action, prize):
                    combos.append((place, act_id, prize_id))
    return combos


def build_story(world: World, params: StoryParams) -> World:
    hero = world.add(Entity(id=params.name, kind="character", type=params.gender))
    parent = world.add(Entity(id="parent", kind="character", type=params.parent, label=f"the {params.parent}"))
    prize = world.add(Entity(
        id="prize",
        type=_safe_lookup(PRIZES, params.prize).type,
        label=_safe_lookup(PRIZES, params.prize).label,
        phrase=_safe_lookup(PRIZES, params.prize).phrase,
        owner=hero.id,
        caretaker=parent.id,
        region=_safe_lookup(PRIZES, params.prize).region,
        plural=_safe_lookup(PRIZES, params.prize).plural,
    ))

    action = _safe_lookup(ACTIONS, params.action)
    gear = select_gear(action, prize)

    world.say(f"{hero.id} was a little {params.trait} {params.gender} who loved {action.gerund} in space.")
    world.say(f"{hero.id}'s {params.parent} bought {hero.pronoun('object')} {prize.phrase}.")
    world.say(f"{hero.id} loved {prize.it()} and wore {prize.it()} everywhere.")

    world.para()
    world.say(f"One day, at {world.setting.place}, {hero.id} wanted to {action.verb}.")
    world.say(f"That could ruin {hero.pronoun('possessive')} {prize.label}, so {hero.pronoun('possessive')} {params.parent} warned, \"If you do that, your {prize.label} will get {action.soil}.\"")
    hero.memes["worry"] += 1
    hero.memes["tension"] += 1
    hero.memes["wonder"] += 1
    world.say(f"{hero.id} tried to {action.rush}, and it went {('whap' if action.id == 'wand' else 'thump')} against the shiny station wall.")
    hero.meters[action.mess] += 1
    world.zone = set(action.zone)

    if gear is None:
        pass

    world.para()
    world.say(f"Then {params.parent} smiled and said, \"How about we {gear.prep}?\"")
    hero.memes["joy"] += 1
    hero.memes["tension"] = 0.0
    world.say(f"{hero.id} nodded, and they {gear.tail}.")
    world.say(f"After that, {hero.id} was {action.gerund}, {prize.label} stayed clean, and the ship lights sparkled like tiny stars.")

    world.facts.update(hero=hero, parent=parent, prize=prize, action=action, gear=gear, setting=world.setting)
    return world


