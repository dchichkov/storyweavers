def can_story(activity: Activity, segment: Segment) -> bool:
    return "segment" in activity.tags and segment.region == "hands"


ASP_RULES = r"""
eligible(A,S) :- activity(A), segment(S), tagged(A,segment), hand_segment(S).
"""

def asp_facts() -> str:
    import asp
    lines: list[str] = []
    for pid, setting in SETTINGS.items():
        lines.append(asp.fact("place", pid))
        if setting.indoors:
            lines.append(asp.fact("indoors", pid))
        for a in sorted(setting.affords):
            lines.append(asp.fact("affords", pid, a))
    for aid, act in ACTIVITIES.items():
        lines.append(asp.fact("activity", aid))
        for t in sorted(act.tags):
            lines.append(asp.fact("tagged", aid, t))
    for sid, seg in SEGMENTS.items():
        lines.append(asp.fact("segment", sid))
        if seg.region == "hands":
            lines.append(asp.fact("hand_segment", sid))
    return "\n".join(lines)


def asp_program(show: str) -> str:
    return f"{asp_facts()}\n{ASP_RULES}\n{show}\n"


def asp_valid() -> list[tuple]:
    import asp
    model = asp.one_model(asp_program("#show eligible/2."))
    return sorted(set(asp.atoms(model, "eligible")))


