def asp_verify() -> int:
    import asp
    py = set(valid_combos())
    clingo_set = set(asp_valid())
    # only compare activity/segment support; place is implicit by affordance in this tiny world
    py_pairs = {(a, s) for _, a, s in py}
    if py_pairs == clingo_set:
        print(f"OK: clingo gate matches valid_combos() ({len(clingo_set)} pairs).")
        return 0
    print("MISMATCH between clingo and valid_combos():")
    if clingo_set - py_pairs:
        print("  only in clingo:", sorted(clingo_set - py_pairs))
    if py_pairs - clingo_set:
        print("  only in python:", sorted(py_pairs - clingo_set))
    return 1


