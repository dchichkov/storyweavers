def valid_combos() -> list[tuple[str, str, str]]:
    out = []
    for place, setting in SETTINGS.items():
        for act in setting.affords:
            for seg in SEGMENTS:
                if can_story(_safe_lookup(ACTIVITIES, act), _safe_lookup(SEGMENTS, seg)):
                    out.append((place, act, seg))
    return out


def resolve_params(args: argparse.Namespace, rng: random.Random) -> StoryParams:
    combos = [c for c in valid_combos()
              if (getattr(args, "place", None) is None or c[0] == getattr(args, "place", None))
              and (getattr(args, "activity", None) is None or c[1] == getattr(args, "activity", None))
              and (getattr(args, "segment", None) is None or c[2] == getattr(args, "segment", None))]
    if not combos:
        return _fallback_storyparams(args, rng, StoryParams, globals())
    place, activity, segment = rng.choice(list(combos))
    gender = getattr(args, "gender", None) or rng.choice(["girl", "boy"])
    name = getattr(args, "name", None) or rng.choice(GIRL_NAMES if gender == "girl" else BOY_NAMES)
    helper = getattr(args, "helper", None) or rng.choice(list(HELPERS))
    return StoryParams(place=place, activity=activity, segment=segment, name=name, gender=gender, helper=helper)


def _build_world(params: StoryParams) -> World:
    setting = _safe_lookup(SETTINGS, params.place)
    activity = _safe_lookup(ACTIVITIES, params.activity)
    segment = _safe_lookup(SEGMENTS, params.segment)
    helper = _safe_lookup(HELPERS, params.helper)
    world = World(setting)

    child = world.add(Entity(id=params.name, kind="character", type=params.gender))
    adult = world.add(Entity(id=helper.id, kind="character", type=helper.type))
    prop = world.add(Entity(id="project", type="project", label="paper project", phrase=segment.phrase))
    prop.caretaker = adult.id
    world.facts.update(child=child, adult=adult, prop=prop, activity=activity, segment=segment, helper=helper)
    return world


def generate(params: StoryParams) -> StorySample:
    world = _build_world(params)
    child: Entity = _safe_fact(world, world.facts, "child")
    adult: Entity = _safe_fact(world, world.facts, "adult")
    activity: Activity = _safe_fact(world, world.facts, "activity")
    segment: Segment = _safe_fact(world, world.facts, "segment")
    helper: Helper = _safe_fact(world, world.facts, "helper")

    child.memes["enthusiasm"] = 1.0
    world.say(f"{child.id} was a little {params.gender} who loved {activity.gerund}.")
    world.say(f"{child.id} and {helper.phrase} were making {segment.phrase} into a long, happy chain.")
    world.say(f"Each new segment made {child.id} even more excited.")

    world.para()
    world.say(f"At {world.setting.place}, {child.id} wanted to {activity.verb} right away.")
