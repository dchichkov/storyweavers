def _r_swell_ruin(world: World) -> list[str]:
    out: list[str] = []
    for actor in world.characters():
        for mess in ("swell",):
            if actor.meters.get(mess, 0.0) < THRESHOLD:
                continue
            for item in world.worn_items(actor):
                if item.plural:
                    continue
                if item.meters.get("protected", 0.0) >= THRESHOLD:
                    continue
                sig = ("ruin", actor.id, item.id)
                if sig in world.fired:
                    continue
                world.fired.add(sig)
                item.meters["ruined"] = 1.0
                item.meters["swollen"] = 1.0
                out.append(f"{actor.pronoun('possessive').capitalize()} {item.label} looked puffy and wrong.")
    return out


def _r_nerve(world: World) -> list[str]:
    out: list[str] = []
    for actor in world.characters():
        if actor.memes.get("suspense", 0.0) < THRESHOLD:
            continue
        sig = ("nerve", actor.id)
        if sig in world.fired:
            continue
        world.fired.add(sig)
        actor.memes["worry"] = actor.memes.get("worry", 0.0) + 1
        out.append("The wait felt extra long.")
    return out


CAUSAL_RULES = [Rule("swell_ruin", "physical", _r_swell_ruin), Rule("nerve", "social", _r_nerve)]


def propagate(world: World, narrate: bool = True) -> list[str]:
    produced: list[str] = []
    changed = True
    for _ in range(len(globals().get("CAUSAL_RULES", [])) + 4):
        changed = False
        for rule in CAUSAL_RULES:
            sents = rule.apply(world)
            if sents:
                changed = True
                produced.extend(sents)
    if narrate:
        for s in produced:
            world.say(s)
    return produced


def prize_at_risk(activity: Activity, prize: Prize) -> bool:
    return prize.region in activity.zone


def select_gear(activity: Activity, prize: Prize) -> Optional[Gear]:
    for gear in GEAR:
        if activity.mess in gear.guards and prize.region in gear.covers:
            return gear
    return None


def predict_mess(world: World, actor: Entity, activity: Activity, prize_id: str) -> dict:
    sim = world.copy()
    _do_activity(sim, sim.get(actor.id), activity, narrate=False)
    prize = sim.entities.get(prize_id)
    return {"ruined": bool(prize and prize.meters.get("ruined", 0.0) >= THRESHOLD)}


def _do_activity(world: World, actor: Entity, activity: Activity, narrate: bool = True) -> None:
    if activity.id not in world.setting.affords:
        return
    world.zone = set(activity.zone)
    actor.meters[activity.mess] = actor.meters.get(activity.mess, 0.0) + 1
    actor.memes["suspense"] = actor.memes.get("suspense", 0.0) + 1
    propagate(world, narrate=narrate)


def rhyme(a: str, b: str) -> bool:
    def tail(s: str) -> str:
        s = re.sub(r"[^a-z]+", "", s.lower())
        return s[-2:] if len(s) >= 2 else s
    return tail(a) == tail(b)


def tell(setting: Setting, activity: Activity, prize_cfg: Prize,
         hero_name: str = "Mina", hero_type: str = "girl",
         parent_type: str = "mother") -> World:
    world = World(setting)
    world.weather = activity.weather if not setting.indoor else ""

    hero = world.add(Entity(id=hero_name, kind="character", type=hero_type))
    parent = world.add(Entity(id="Parent", kind="character", type=parent_type, label="the parent"))
    prize = world.add(Entity(
        id="prize", type=prize_cfg.type, label=prize_cfg.label, phrase=prize_cfg.phrase,
        owner=hero.id, caretaker=parent.id, region=prize_cfg.region, plural=prize_cfg.plural,
    ))

    hero.memes["hope"] = 1
    world.say(f"{hero.id} had a dream with a ding and a chime,")
    world.say(f"to promote a small booth in the market at rhyme-time.")

    world.para()
    world.say(f"{hero.id} loved {activity.gerund}, with a tap and a beat,")
    world.say(f"and wore {hero.pronoun('possessive')} {prize.label} like a treat.")
    world.say(f"The flyer had one special word in its line: melanin, bright and kind.")

    world.para()
    world.say(f"At {setting.place}, the crowd formed a ring,")
    world.say(f"when {hero.id} said, \"Listen!\" and let the bell sing.")
    hero.memes["desire"] = 1

    if predict_mess(world, hero, activity, prize.id)["ruined"]:
        world.say(f"Then came a hush, then a swish, then a swell in the air,")
        world.say(f"and {hero.pronoun('possessive')} {prize.label} started puffing with care.")

    _do_activity(world, hero, activity, narrate=True)
    world.say(f"Sound effects went \"pop-pop!\" and \"whirr-whirr!\" with flair,")
    world.say(f"but the swelling made {hero.pronoun('possessive')} {prize.label} seem sad and spare.")

    gear_def = None
    if prize_at_risk(activity, prize):
        gear_def = select_gear(activity, prize)
        if gear_def is None:
            pass
        gear = world.add(Entity(
            id=gear_def.id, type="gear", label=gear_def.label, owner=hero.id,
            caretaker=parent.id, plural=gear_def.plural,
        ))
        gear.worn_by = hero.id
        gear.meters["protected"] = 1.0
        world.say(f"{parent.pronoun('possessive').capitalize()} eyes shone with a plan,")
        world.say(f"\"{gear_def.prep}, and we can still do the stand!\"")
        world.say(f"The helper gear fit like a cozy tune,")
        world.say(f"and kept the trouble from growing too soon.")

        prize.meters["ruined"] = 0.0
        prize.meters["swollen"] = 0.0
        hero.memes["suspense"] = 0.0
        hero.memes["joy"] = 1.0
        world.say(f"Then came a soft \"zip-zip,\" then a bright little cheer,")
        world.say(f"as {hero.id} showed the booth and the guests drew near.")
        world.say(f"The poster stayed tidy, the ending stayed sweet,")
        world.say(f"and the rhyming old sidewalk tapped to the beat.")
    else:
        world.say("The night turned quite quiet, and the plan felt wrong.")
        world.say("No fix could be found, so the show was not long.")

    world.facts.update(
        hero=hero,
        parent=parent,
        prize=prize,
        prize_cfg=prize_cfg,
        activity=activity,
        setting=setting,
        gear=gear_def,
        resolved=gear_def is not None,
        conflict=True,
    )
    return world


