def _r_blur(world: World) -> list[str]:
    out: list[str] = []
    for actor in world.characters():
        if actor.meters.get("blurry", 0.0) < THRESHOLD:
            continue
        for item in world.worn_items(actor):
            if item.protective or item.region not in world.zone:
                continue
            if world.covered(actor, item.region):
                continue
            sig = ("blur", item.id)
            if sig in world.fired:
                continue
            world.fired.add(sig)
            item.meters["blurry"] = item.meters.get("blurry", 0.0) + 1
            out.append(f"{item.label.capitalize()} went too blurry to read.")
    return out


def _r_worry(world: World) -> list[str]:
    out: list[str] = []
    hero = world.facts.get("hero")
    if hero and hero.memes.get("worry", 0.0) >= THRESHOLD and ("worry", hero.id) not in world.fired:
        world.fired.add(("worry", hero.id))
        out.append("__worry__")
    return out


def propagate(world: World, narrate: bool = True) -> list[str]:
    produced: list[str] = []
    changed = True
    for _ in range(len(globals().get("CAUSAL_RULES", [])) + 4):
        changed = False
        for rule in (_r_blur, _r_worry):
            sents = rule(world)
            if sents:
                changed = True
                produced.extend(s for s in sents if s != "__worry__")
    if narrate:
        for s in produced:
            world.say(s)
    return produced


def predict_mess(world: World, actor: Entity, activity: Activity, prize_id: str) -> dict:
    sim = world.copy()
    _do_activity(sim, sim.get(actor.id), activity, narrate=False)
    prize = sim.entities.get(prize_id)
    return {"soiled": bool(prize and prize.meters.get("blurry", 0.0) >= THRESHOLD)}


def _do_activity(world: World, actor: Entity, activity: Activity, narrate: bool = True) -> None:
    if activity.id not in world.setting.affords:
        return
    world.zone = set(activity.zone)
    actor.meters["blurry"] = actor.meters.get("blurry", 0.0) + 1
    actor.memes["curiosity"] = actor.memes.get("curiosity", 0.0) + 1
    propagate(world, narrate=narrate)


