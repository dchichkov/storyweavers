def _do_activity(world: World, actor: Entity, activity: Activity, narrate: bool = True) -> None:
    if activity.id not in world.setting.affords:
        return
    world.zone = set(activity.zone)
    actor.meters[activity.mess] = actor.meters.get(activity.mess, 0.0) + 1.0
    actor.memes["joy"] = actor.memes.get("joy", 0.0) + 1.0
    if narrate:
        world.say(f"{actor.id} loved {activity.gerund} in {world.setting.place}.")


def _propagate(world: World, narrate: bool = True) -> None:
    out: list[str] = []
    for actor in world.characters():
        if actor.meters.get("wet", 0.0) < 1.0 and actor.meters.get("muddy", 0.0) < 1.0:
            continue
        for item in world.worn_items(actor):
            if item.protective or item.region not in world.zone:
                continue
            if world.covered(actor, item.region):
                continue
            sig = ("mess", actor.id, item.id, tuple(sorted(world.zone)))
            if sig in world.fired:
                continue
            world.fired.add(sig)
            kind = "wet" if actor.meters.get("wet", 0.0) >= 1.0 else "muddy"
            item.meters[kind] = item.meters.get(kind, 0.0) + 1.0
            item.meters["dirty"] = item.meters.get("dirty", 0.0) + 1.0
            out.append(f"{actor.pronoun('possessive').capitalize()} {item.label} got {kind}.")
    if narrate:
        for s in out:
            world.say(s)


def predict_mess(world: World, actor: Entity, activity: Activity, prize_id: str) -> dict[str, object]:
    sim = world.copy()
    _do_activity(sim, sim.get(actor.id), activity, narrate=False)
    _propagate(sim, narrate=False)
    prize = sim.entities[prize_id]
    return {"soiled": prize.meters.get("dirty", 0.0) >= 1.0}


def prize_at_risk(activity: Activity, prize: Prize) -> bool:
    return prize.region in activity.zone


def select_gear(activity: Activity, prize: Prize) -> Optional[Gear]:
    for gear in GEAR:
        if activity.mess in gear.guards and prize.region in gear.covers:
            return gear
    return None


def tell(setting: Setting, activity: Activity, prize_cfg: Prize, hero_name: str, hero_type: str, parent_type: str, trait: str) -> World:
    world = World(setting)
    hero = world.add(Entity(id=hero_name, kind="character", type=hero_type, meters={}, memes={}, label=hero_name))
    parent = world.add(Entity(id="Parent", kind="character", type=parent_type, label=f"the {parent_type}"))
    prize = world.add(Entity(id="prize", type=prize_cfg.type, label=prize_cfg.label, phrase=prize_cfg.phrase, owner=hero.id, caretaker=parent.id, region=prize_cfg.region, plural=prize_cfg.plural))

    world.say(f"{hero.id} was a little {trait} {hero.type} who adored the spa.")
    world.say(f"{hero.pronoun().capitalize()} loved {activity.gerund}, because the whole place felt like a grand palace of soap and steam.")
    world.say(f"One day, {parent.label} bought {hero.pronoun('object')} {prize.phrase}.")
    prize.worn_by = hero.id
    world.say(f"{hero.id} wore {prize.it()} like treasure.")

    world.para()
    world.say(f"At {world.setting.place}, {activity.surprise}!")
    world.say(f"{hero.id} wanted to {activity.verb}, but {parent.label} noticed the {prize.label} could get {activity.soil}.")
    if predict_mess(world, hero, activity, prize.id)["soiled"]:
        world.say(f'"If you do that, your {prize.label} will get {activity.soil}," {parent.label} said.')
    world.say(f"{hero.id} tried to {activity.rush}, and the whole room gave a startled little hop.")
    _do_activity(world, hero, activity)
    _propagate(world)

    gear_def = None
    if prize_at_risk(activity, prize):
        gear_def = select_gear(activity, prize)
        if gear_def and not predict_mess(world, hero, activity, prize.id)["soiled"]:
            pass

    world.para()
    if gear_def:
        gear = world.add(Entity(id=gear_def.id, type="gear", label=gear_def.label, owner=hero.id, caretaker=parent.id, protective=True, covers=set(gear_def.covers), plural=gear_def.plural))
        gear.worn_by = hero.id
        world.say(f"Then came the twist: {parent.label} smiled and said, 'Let's {gear_def.prep}.'")
        world.say(f"They {gear_def.tail}, and {activity.twist}.")
        hero.memes["joy"] = hero.memes.get("joy", 0.0) + 1.0
        world.say(f"{hero.id} laughed so hard the steam seemed to tiptoe.")
        world.say(f"By the end, {hero.id} was still {activity.gerund}, and the {prize.label} stayed clean.")
    else:
        world.say(f"Then came the twist: there was no perfect fix, so they changed the plan.")
        world.say(f"They chose a safer game, and the spa turned as quiet as a whispering teacup.")

    world.facts.update(hero=hero, parent=parent, prize=prize, activity=activity, gear=gear_def, setting=setting)
    return world


