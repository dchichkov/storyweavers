def problem(world: World, parent: Entity, hero: Entity, act: Activity) -> None:
    hero.memes["alert"] = hero.memes.get("alert", 0.0) + 1
    world.say(f"Then {hero.pronoun('possessive')} {parent.type} looked around for a safer plan.")
    world.say(f"\"We can still make the video,\" {parent.pronoun('subject')} said. \"We only need a steadier stand.\"")


def compromise(world: World, parent: Entity, hero: Entity, act: Activity, prize: Entity) -> Optional[Gear]:
    gear_def = select_gear(act, prize)
    if gear_def is None:
        return None
    gear = world.add(Entity(
        id=gear_def.id,
        type="gear",
        label=gear_def.label,
        owner=hero.id,
        caretaker=parent.id,
        protective=True,
        covers=set(gear_def.covers),
        plural=gear_def.plural,
    ))
    gear.worn_by = hero.id
    if predict_problem(world, hero, act, prize.id)["scuffed"]:
        gear.worn_by = None
        del world.entities[gear.id]
        return None
    hero.memes["calm"] = hero.memes.get("calm", 0.0) + 1
    world.say(f"They found {gear_def.label} to hold the phone nice and firm.")
    world.say(f"\"Use this first,\" {parent.pronoun('subject')} said, \"and your turns will stay safe and sure.\"")
    return gear_def


