def compromise(world: World, parent: Entity, hero: Entity, activity: Activity, prize: Entity) -> Optional[Gear]:
    gear_def = select_gear(activity, prize)
    if gear_def is None:
        return None
    gear = world.add(Entity(id=gear_def.id, kind="gear", type="gear", label=gear_def.label, owner=hero.id, worn_by=hero.id, plural=gear_def.plural))
    if predict_smell(world, hero, activity, prize.id)["mess"]:
        del world.entities[gear.id]
        return None
    world.say(f'{parent.label} smiled. "How about we {gear_def.prep} first, then we can {activity.verb}?"')
    return gear_def


