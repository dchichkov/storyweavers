def warn(world: World, parent: Entity, hero: Entity, activity: Activity, prize: Entity) -> bool:
    pred = predict_smell(world, hero, activity, prize.id)
    if not pred["mess"]:
        return False
    world.facts["warning"] = activity.surprise
    world.say(f'"If you do that, your {prize.label} will get {activity.surprise}," {parent.label} warned.')
    return True


