THRESHOLD = 1.0


def _r_soil(world: World) -> list[str]:
    out = []
    for actor in world.characters():
        if actor.meters.get("smell", 0.0) < THRESHOLD:
            continue
        for item in world.worn_items(actor):
            if item.kind == "gear":
                continue
            if item.id == "pastrami" or item.owner != actor.id:
                continue
            sig = ("soil", item.id)
            if sig in world.fired:
                continue
            world.fired.add(sig)
            item.meters["smell"] = item.meters.get("smell", 0.0) + 1
            item.meters["mess"] = item.meters.get("mess", 0.0) + 1
            out.append(f"{item.label.capitalize()} picked up the pastrami smell.")
    return out


def _r_surprise(world: World) -> list[str]:
    out = []
    for actor in world.characters():
        if actor.memes.get("surprised", 0.0) < THRESHOLD:
            continue
        sig = ("surprise", actor.id)
        if sig in world.fired:
            continue
        world.fired.add(sig)
        actor.memes["delight"] = actor.memes.get("delight", 0.0) + 1
        out.append(f"{actor.id} blinked at the sudden surprise.")
    return out


CAUSAL_RULES = [Rule("soil", _r_soil), Rule("surprise", _r_surprise)]


def propagate(world: World, narrate: bool = True) -> list[str]:
    produced = []
    changed = True
    for _ in range(len(globals().get("CAUSAL_RULES", [])) + 4):
        changed = False
        for rule in CAUSAL_RULES:
            sents = rule.apply(world)
            if sents:
                produced.extend(sents)
                changed = True
    if narrate:
        for s in produced:
            world.say(s)
    return produced


def select_gear(activity: Activity, prize: Prize) -> Optional[Gear]:
    for gear in GEAR:
        if activity.mess in gear.guards and prize.region in gear.covers:
            return gear
    return None


def prize_at_risk(activity: Activity, prize: Prize) -> bool:
    return prize.region in activity.zone


def predict_smell(world: World, actor: Entity, activity: Activity, prize_id: str) -> dict:
    sim = world.copy()
    _do_activity(sim, sim.get(actor.id), activity, narrate=False)
    prize = sim.get(prize_id)
    return {"mess": prize.meters.get("mess", 0.0) >= THRESHOLD}


def _do_activity(world: World, actor: Entity, activity: Activity, narrate: bool = True) -> None:
    world.zone = set(activity.zone)
    actor.meters["smell"] = actor.meters.get("smell", 0.0) + 1
    actor.memes["glee"] = actor.memes.get("glee", 0.0) + 1
    propagate(world, narrate=narrate)


