def _r_soak(world: World) -> list[str]:
    out: list[str] = []
    for actor in world.characters():
        for item in world.worn_items(actor):
            if item.protective:
                continue
            if item.region not in world.zone:
                continue
            if actor.meters["messy"] < THRESHOLD:
                continue
            sig = ("soak", actor.id, item.id)
            if sig in world.fired:
                continue
            world.fired.add(sig)
            item.meters["dirty"] += 1
            out.append(f"{actor.id}'s {item.label} got messy.")
    return out


def _r_workload(world: World) -> list[str]:
    out: list[str] = []
    for item in list(world.entities.values()):
        if item.meters["dirty"] < THRESHOLD or not item.caretaker:
            continue
        sig = ("work", item.id)
        if sig in world.fired:
            continue
        world.fired.add(sig)
        carer = world.get(item.caretaker)
        carer.memes["worry"] += 1
        out.append(f"That would mean extra cleaning for {carer.label}.")
    return out


CAUSAL_RULES = [Rule("soak", _r_soak), Rule("workload", _r_workload)]


def propagate(world: World, narrate: bool = True) -> list[str]:
    produced: list[str] = []
    changed = True
    for _ in range(len(globals().get("CAUSAL_RULES", [])) + 4):
        changed = False
        for rule in CAUSAL_RULES:
            sents = rule.apply(world)
            if sents:
                changed = True
                produced.extend(sents)
    if narrate:
        for s in produced:
            world.say(s)
    return produced


def prize_at_risk(activity: Activity, prize: Prize) -> bool:
    return prize.region in activity.zone


def select_gear(activity: Activity, prize: Prize) -> Optional[Gear]:
    for gear in GEAR:
        if prize.region in gear.covers and activity.mess in gear.guards:
            return gear
    return None


def predict_mess(world: World, actor: Entity, activity: Activity, prize_id: str) -> dict:
    sim = world.copy()
    _do_activity(sim, sim.get(actor.id), activity, narrate=False)
    prize = sim.entities[prize_id]
    return {
        "soiled": prize.meters["dirty"] >= THRESHOLD,
        "worry": sum(e.memes["worry"] for e in sim.characters()),
    }


def _do_activity(world: World, actor: Entity, activity: Activity, narrate: bool = True) -> None:
    if activity.id not in world.setting.affords:
        pass
    world.zone = set(activity.zone)
    actor.meters["messy"] += 1
    actor.memes["joy"] += 1
    propagate(world, narrate=narrate)


def funny_detail(activity: Activity) -> str:
    return {
        "pie": "The pie tin wobbled like it had a secret joke.",
        "soap": "The bubbles kept trying to wear tiny hats.",
        "water": "The water sparkled like it was giggling.",
        "chalk": "The chalk dust made silly little clouds on the floor.",
    }.get(activity.id, "The whole thing looked ready to make someone laugh.")


def setting_detail(setting: Setting, activity: Activity) -> str:
    if setting.indoor:
        return f"The {setting.place} was warm and echoey, so even a tiny sneeze sounded funny."
    if activity.weather == "rainy":
        return f"Outside, the air was fresh, and {setting.place} gleamed after the rain."
    return f"{setting.place.capitalize()} waited quietly, as if it knew a joke was coming."


