def warn(world: World, parent: Entity, hero: Entity, activity: Activity, prize: Entity) -> bool:
    pred = predict_mess(world, hero, activity, prize.id)
    if not pred["soiled"]:
        return False
    world.facts["predicted_soil"] = activity.soil
    world.facts["predicted_worry"] = pred["worry"]
    world.say(
        f'"If you {activity.verb}, your {prize.label} will get {activity.soil}," {parent.label} said. "And then the laundry basket will laugh at us."'
    )
    return True


def flashback(world: World, hero: Entity, activity: Activity) -> None:
    hero.memes["embarrassment"] += 1
    world.say(
        f"{hero.id} had a flashback to an older day when {hero.pronoun('possessive')} socks, a spoon, and one heroic banana had all ended up in the wrong places."
    )
    world.say(
        f"{hero.id} remembered how funny it looked at first, and how not-funny it was when everyone had to scrub."
    )


