def explain_rejection(activity: Activity, prize: Prize) -> str:
    noun = prize.label if prize.plural else f"a {prize.label}"
    if not prize_at_risk(activity, prize):
        return f"(No story: {activity.gerund} does not threaten {noun}, so there is no real worry to resolve.)"
    return f"(No story: nothing in the gear catalog honestly protects {noun} from {activity.gerund}.)"


