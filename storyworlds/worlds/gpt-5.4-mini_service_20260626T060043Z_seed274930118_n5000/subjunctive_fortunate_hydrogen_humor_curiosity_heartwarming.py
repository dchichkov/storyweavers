def story_qa(world: World) -> list[QAItem]:
    f = world.facts
    hero, parent, prize, act = f["hero"], f["parent"], f["prize"], f["activity"]
    pw = parent.label_word
    pos = hero.pronoun("possessive")
    qa: list[QAItem] = [
        QAItem(
            question=f"What did {hero.id} want to do at {world.setting.place} with {pos} {prize.label}?",
            answer=f"{hero.id} wanted to {act.verb}. {pos.capitalize()} {pw} was worried that the {prize.label} might get {act.soil}.",
        ),
        QAItem(
            question=f"Why was {pw} careful about the {prize.label}?",
            answer=f"{pw.capitalize()} was careful because the hydrogen demo made foam that could splash onto the {prize.label} and make it messy.",
        ),
        QAItem(
            question=f"What helped {hero.id} keep the fun without ruining {pos} {prize.label}?",
            answer=f"A bright apron helped, because it covered the shirt and kept the foam from getting on it.",
        ),
    ]
    if f.get("resolved"):
        gear = f["gear"]
        qa.append(QAItem(
            question=f"How did {hero.id} feel when {pw} offered {gear.label}?",
            answer=f"{hero.id} felt happy and a little relieved. The plan was safe, and it still let {hero.id} enjoy the bubbles.",
        ))
    return qa


def world_knowledge_qa(world: World) -> list[QAItem]:
    f = world.facts
    tags = set(f["activity"].tags)
    if f.get("gear"):
        tags.add(f["gear"].id)
    out: list[QAItem] = []
    for tag in KNOWLEDGE_ORDER:
        if tag in tags:
            out.extend(QAItem(question=q, answer=a) for q, a in KNOWLEDGE[tag])
    return out


def format_qa(sample: StorySample) -> str:
    lines = ["== (1) Generation prompts -- asks that would produce this story =="]
    for i, p in enumerate(sample.prompts, 1):
        lines.append(f"{i}. {p}")
    lines.append("")
    lines.append("== (2) Story questions -- answerable from the story text ==")
    for item in sample.story_qa:
        lines.append(f"Q: {item.question}")
        lines.append(f"A: {item.answer}")
    lines.append("")
    lines.append("== (3) World-knowledge questions -- child level, no story needed ==")
    for item in sample.world_qa:
        lines.append(f"Q: {item.question}")
        lines.append(f"A: {item.answer}")
    return "\n".join(lines)


def dump_trace(world: World) -> str:
    lines = ["--- world model state ---"]
    for e in world.entities.values():
        meters = {k: v for k, v in e.meters.items() if v}
        memes = {k: v for k, v in e.memes.items() if v}
        bits = []
        if meters:
            bits.append(f"meters={dict(meters)}")
        if memes:
            bits.append(f"memes={dict(memes)}")
        if e.protective:
            bits.append(f"covers={sorted(e.covers)}")
        elif e.region:
            bits.append(f"region={e.region}")
        lines.append(f"  {e.id:8} ({e.type:7}) {' '.join(bits)}")
    lines.append(f"  fired rules: {sorted(set(n for n, *_ in world.fired))}")
    return "\n".join(lines)


