def _r_scratch(world: World) -> list[str]:
    out: list[str] = []
    for actor in world.characters():
        if actor.meters.get("dust", 0.0) < THRESHOLD:
            continue
        for item in world.worn_items(actor):
            if item.protective or item.region not in world.zone:
                continue
            if world.covered(actor, item.region):
                continue
            sig = ("scratch", item.id)
            if sig in world.fired:
                continue
            world.fired.add(sig)
            item.meters["scratch"] = item.meters.get("scratch", 0.0) + 1
            item.meters["dirty"] = item.meters.get("dirty", 0.0) + 1
            out.append(f"{actor.id}'s {item.label} got scratched by the dust.")
    return out


def _r_alarm(world: World) -> list[str]:
    if world.facts.get("alarm_used") and ("alarm",) not in world.fired:
        world.fired.add(("alarm",))
        return ["A warning beep echoed through the ship."]
    return []


CAUSAL_RULES = [
    Rule("scratch", "physical", _r_scratch),
    Rule("alarm", "suspense", _r_alarm),
]


def propagate(world: World, narrate: bool = True) -> list[str]:
    produced: list[str] = []
    changed = True
    for _ in range(len(globals().get("CAUSAL_RULES", [])) + 4):
        changed = False
        for rule in CAUSAL_RULES:
            sents = rule.apply(world)
            if sents:
                changed = True
                produced.extend(sents)
    if narrate:
        for s in produced:
            world.say(s)
    return produced


