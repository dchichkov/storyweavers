def tell(setting: Setting, activity: Activity, prize_cfg: Prize, hero_name: str, hero_type: str,
         parent_type: str, trait: str) -> World:
    world = World(setting)
    hero = world.add(Entity(id=hero_name, kind="character", type=hero_type, traits=["little", trait, "stubborn"]))
    parent = world.add(Entity(id="Captain", kind="character", type=parent_type, label="the captain"))
    prize = world.add(Entity(
        id="prize", type=prize_cfg.type, label=prize_cfg.label, phrase=prize_cfg.phrase,
        owner=hero.id, caretaker=parent.id, region=prize_cfg.region, plural=prize_cfg.plural
    ))

    world.say(f"Caption: {hero.id} was a little {trait} space explorer aboard the station.")
    world.say(f"At the start, {hero.id} loved {activity.gerund}.")
    world.say(f"{hero.id}'s {prize.label} sparkled on {hero.pronoun('possessive')} suit like a tiny moon.")
    world.para()
    world.say(setting_detail(setting))
    world.say(activity_detail(activity))
    world.say(f"{hero.id} wanted to {activity.verb}, but {parent.label_word} raised a hand.")
    if predict_risk(world, hero, activity, prize.id)["scratched"]:
        world.say(f'"If you go now, your {prize.label} will get {activity.soil}," {parent.label_word} said.')
    world.say(f"{hero.id} still tried to {activity.rush}.")
    hero.memes["defiance"] = hero.memes.get("defiance", 0.0) + 1
    hero.memes["conflict"] = hero.memes.get("conflict", 0.0) + 1
    world.facts["alarm_used"] = True
    propagate(world, narrate=True)
    world.say(f"The ship got very quiet for a second, and that made the warning feel even bigger.")
    world.para()

    gear = select_gear(activity, prize)
    if not gear:
        pass
    g = world.add(Entity(id=gear.id, type="gear", label=gear.label, protective=True, covers=set(gear.covers)))
    g.worn_by = hero.id
    world.say(f"Then {parent.label_word} said, 'Let's solve it safely.'")
    world.say(f"{parent.label_word.capitalize()} helped {hero.id} {gear.prep}.")
    hero.memes["trust"] = hero.memes.get("trust", 0.0) + 1
    hero.memes["conflict"] = 0.0
    hero.memes["joy"] = hero.memes.get("joy", 0.0) + 1
    world.say(f"{hero.id} nodded, and the two of them {gear.tail}.")
    _do_activity(world, hero, activity, narrate=True)
    world.say(f"In the end, {hero.id} was still {activity.gerund}, and {prize.label} stayed clean and shiny.")
    world.facts.update(hero=hero, parent=parent, prize=prize, activity=activity, gear=gear, setting=setting)
    return world


