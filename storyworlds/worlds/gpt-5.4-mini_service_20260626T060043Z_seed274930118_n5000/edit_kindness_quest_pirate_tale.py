def resolve_params(args: argparse.Namespace, rng: random.Random) -> StoryParams:
    combos = [c for c in valid_combos()
              if (getattr(args, "setting", None) is None or c[0] == getattr(args, "setting", None))
              and (getattr(args, "quest", None) is None or c[1] == getattr(args, "quest", None))
              and (getattr(args, "prize", None) is None or c[2] == getattr(args, "prize", None))]
    if not combos:
        return _fallback_storyparams(args, rng, StoryParams, globals())
    setting, quest, prize = rng.choice(list(combos))
    kind = getattr(args, "kind", None) or rng.choice(CREW_TYPES)
    name = getattr(args, "name", None) or rng.choice(KIND_NAMES)
    trait = getattr(args, "trait", None) or rng.choice(CREW_TRAITS)
    return StoryParams(setting=setting, quest=quest, prize=prize, name=name, kind=kind, trait=trait)


def _base_story(world: World, hero: Entity, mate: Entity, quest: Quest, prize: Prize) -> None:
    world.say(
        f"{hero.id} was a little {hero.type} pirate who loved {quest.gerund} along {world.setting.place}."
    )
    world.say(
        f"{hero.id} and {mate.label} had a map to {prize.phrase}, and the crew called the trip a {quest.keyword} quest."
    )


def _conflict_story(world: World, hero: Entity, mate: Entity, quest: Quest, prize: Prize) -> None:
    world.para()
    world.say(
        f"One gray tide morning, {hero.id} wanted to {quest.rush}, but {mate.label} pointed at a smudged mark."
    )
    world.say(
        f'"If we hurry now," {mate.label} said, "we may reach {quest.danger} instead of the cove we want."'
    )
    hero.memes["impatient"] = hero.memes.get("impatient", 0) + 1
    hero.memes["worry"] = hero.memes.get("worry", 0) + 1
    world.say(
        f"{hero.id} frowned, because the old map looked tricky and {hero.pronoun('possessive')} {prize.label} could be lost to the sea."
    )


def _resolution_story(world: World, hero: Entity, mate: Entity, quest: Quest, prize: Prize) -> None:
    world.para()
    tool = _safe_fact(world, world.facts, "tool")
    world.say(
        f"{mate.label} chose kindness over hurry and said, \"Let's make a small edit first.\""
    )
    world.say(f"They {tool.prep}, and the line on the map became clear.")
    hero.memes["kindness"] = hero.memes.get("kindness", 0) + 1
    hero.memes["pride"] = hero.memes.get("pride", 0) + 1
    world.say(
        f"The crew followed the safer path, found a stranded boat, and used {TOOLS['rope'].label} to help."
    )
    world.say(
        f"At last they reached the cove, where the treasure was not only {prize.phrase} but also a shared supper and a repaired sail."
    )


def tell(params: StoryParams) -> World:
    world = World(_safe_lookup(SETTINGS, params.setting))
    hero = world.add(Entity(id=params.name, kind="character", type=params.kind, meters={}, memes={}))
    mate = world.add(Entity(id="Mate", kind="character", type="pirate", label="the old mate", meters={}, memes={}))
    prize = world.add(Entity(id="Prize", type=_safe_lookup(PRIZES, params.prize).type, label=_safe_lookup(PRIZES, params.prize).label, phrase=_safe_lookup(PRIZES, params.prize).phrase))
    quest = _safe_lookup(QUESTS, params.quest)
    tool = TOOLS["ink"] if params.quest == "edit" else TOOLS["lantern"]
    if params.quest == "kindness":
        tool = TOOLS["rope"]

    world.facts.update(hero=hero, mate=mate, prize=prize, quest=quest, tool=tool)
    _base_story(world, hero, mate, quest, prize)
    _conflict_story(world, hero, mate, quest, prize)
    _resolution_story(world, hero, mate, quest, prize)
    return world


