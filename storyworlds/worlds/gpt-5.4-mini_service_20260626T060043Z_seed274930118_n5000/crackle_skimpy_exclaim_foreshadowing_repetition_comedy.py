class World:
    def __init__(self, setting: Setting) -> None:
        self.setting = setting
        self.entities: dict[str, Entity] = {}
        self.paragraphs: list[list[str]] = [[]]
        self.fired: set[tuple] = set()
        self.facts: dict = {}
        self.zone: set[str] = set()

    def add(self, ent: Entity) -> Entity:
        self.entities[ent.id] = ent
        return ent

    def get(self, eid: str) -> Entity:
        if eid not in self.entities:
            label = str(eid).replace("_", " ")
            self.entities[eid] = Entity(str(eid), label=label)
        return self.entities[eid]

    def characters(self) -> list[Entity]:
        return [e for e in self.entities.values() if e.kind == "character"]

    def worn_items(self, actor: Entity) -> list[Entity]:
        return [e for e in self.entities.values() if e.worn_by == actor.id]

    def covered(self, actor: Entity, region: str) -> bool:
        return any(g.protective and region in g.covers for g in self.worn_items(actor))

    def say(self, text: str) -> None:
        if text:
            self.paragraphs[-1].append(text)

    def para(self) -> None:
        if self.paragraphs[-1]:
            self.paragraphs.append([])

    def render(self) -> str:
        return "\n\n".join(" ".join(p) for p in self.paragraphs if p)

    def copy(self) -> "World":
        clone = World(self.setting)
        clone.entities = copy.deepcopy(self.entities)
        clone.paragraphs = [[]]
        clone.fired = set(self.fired)
        clone.zone = set(self.zone)
        clone.facts = copy.deepcopy(self.facts)
        return clone


def _r_warmth(world: World) -> list[str]:
    out: list[str] = []
    for actor in world.characters():
        if actor.meters["heat"] < THRESHOLD:
            continue
        for item in world.worn_items(actor):
            if item.protective or item.region not in world.zone:
                continue
            if world.covered(actor, item.region):
                continue
            sig = ("warmth", actor.id, item.id)
            if sig in world.fired:
                continue
            world.fired.add(sig)
            item.meters["soot"] += 1
            item.meters["heat"] += 1
            out.append(f"{actor.pronoun('possessive').capitalize()} {item.label} got singed by the crackle.")
    return out


def _r_alarm(world: World) -> list[str]:
    out: list[str] = []
    for actor in world.characters():
        if actor.meters["heat"] < THRESHOLD:
            continue
        if actor.memes["worry"] < THRESHOLD:
            continue
        sig = ("alarm", actor.id)
        if sig in world.fired:
            continue
        world.fired.add(sig)
        actor.memes["alarm"] += 1
        out.append("__alarm__")
    return out


CAUSAL_RULES = [
    _r_warmth,
    _r_alarm,
]


def propagate(world: World, narrate: bool = True) -> list[str]:
    produced: list[str] = []
    changed = True
    for _ in range(len(globals().get("CAUSAL_RULES", [])) + 4):
        changed = False
        for rule in CAUSAL_RULES:
            sents = rule(world)
            if sents:
                changed = True
                for s in sents:
                    if s != "__alarm__":
                        produced.append(s)
    if narrate:
        for s in produced:
            world.say(s)
    return produced


def prize_at_risk(activity: Activity, prize: Prize) -> bool:
    return prize.region in activity.zone


def select_gear(activity: Activity, prize: Prize) -> Optional[Gear]:
    for gear in GEAR:
        if activity.mess in gear.guards and prize.region in gear.covers:
            return gear
    return None


def predict(world: World, actor: Entity, activity: Activity, prize_id: str) -> dict:
    sim = world.copy()
    _do_activity(sim, sim.get(actor.id), activity, narrate=False)
    prize = sim.entities.get(prize_id)
    return {"soiled": bool(prize and prize.meters["soot"] >= THRESHOLD)}


def _do_activity(world: World, actor: Entity, activity: Activity, narrate: bool = True) -> None:
    if activity.id not in world.setting.affords:
        return
    world.zone = set(activity.zone)
    actor.meters["heat"] += 1
    actor.memes["joy"] += 1
    world.say(f"The {activity.sound} seemed to follow {actor.id} everywhere.") if narrate else None
    propagate(world, narrate=narrate)


def introduce(world: World, hero: Entity) -> None:
    trait = next((t for t in hero.traits if t != "little"), "silly")
    world.say(f"{hero.id} was a little {trait} {hero.type} who loved making a scene.")


def loves_sound(world: World, hero: Entity, activity: Activity) -> None:
    hero.memes["mischief"] += 1
    world.say(
        f"{hero.id} loved the {activity.sound}; "
        f"it made {hero.pronoun('subject')} want to grin and exclaim, \"Again!\""
    )


def buys(world: World, parent: Entity, hero: Entity, prize: Entity) -> None:
    world.say(f"One day, {hero.id}'s {parent.label} bought {hero.pronoun('object')} {prize.phrase}.")


def loves_prize(world: World, hero: Entity, prize: Entity) -> None:
    hero.memes["confidence"] += 1
    prize.worn_by = hero.id
    world.say(f"{hero.id} loved {hero.pronoun('possessive')} {prize.label} and wore {prize.it()} proudly.")


def arrive(world: World, hero: Entity, parent: Entity, activity: Activity) -> None:
    world.say(f"One evening, {hero.id} and {hero.pronoun('possessive')} {parent.label} went to {world.setting.place}.")
    world.say(f"Even before anything began, the air already hinted at a {activity.sound}.")


def wants(world: World, hero: Entity, activity: Activity) -> None:
    hero.memes["worry"] += 0.5
    world.say(f"{hero.id} wanted to {activity.verb} right away.")


def foreshadow(world: World, parent: Entity, hero: Entity, activity: Activity, prize: Entity) -> bool:
    pred = predict(world, hero, activity, prize.id)
    if not pred["soiled"]:
        return False
    world.facts["predicted_soil"] = activity.soil
    world.say(
        f'"That {activity.sound} is already looking cheeky," {parent.label} said. '
        f'"Your {prize.label} is being a little too skimpy for this."'
    )
    world.say(f"{hero.id} looked down. The {prize.label} did, in fact, look skimpy.")
    return True


def repeat_refrain(world: World, hero: Entity, activity: Activity, prize: Entity) -> None:
    world.say(f"The {activity.sound} went crackle, crackle, crackle.")
    world.say(f"{hero.id} glanced at the skimpy {prize.label} and tried not to laugh.")
    world.say(f"The crackle said trouble, and the skimpy {prize.label} said trouble too.")


def exclaim_conflict(world: World, hero: Entity, activity: Activity) -> None:
    hero.memes["embarrassment"] += 1
    world.say(f"\"I can handle it!\" {hero.id} exclaimed, and then immediately hopped back.")
    world.say(f"{hero.id} tried to {activity.rush}, but the heat made {hero.id} wobble like a jellybean.")


def offer_fix(world: World, parent: Entity, hero: Entity, activity: Activity, prize: Entity) -> Optional[Gear]:
    gear_def = select_gear(activity, prize)
    if gear_def is None:
        return None
    if predict(world, hero, activity, prize.id)["soiled"]:
        return None
    gear = world.add(Entity(
        id=gear_def.id,
        type="gear",
        label=gear_def.label,
        owner=hero.id,
        caretaker=parent.id,
        protective=True,
        covers=set(gear_def.covers),
        plural=gear_def.plural,
    ))
    gear.worn_by = hero.id
    world.say(
        f'{parent.id} held up {gear_def.label} and said, '
        f"\"How about we {gear_def.prep}?\""
    )
    return gear_def


def accept(world: World, parent: Entity, hero: Entity, activity: Activity, prize: Entity, gear_def: Gear) -> None:
    hero.memes["relief"] += 1
    hero.memes["joy"] += 1
    hero.memes["embarrassment"] = 0
    world.say(f"{hero.id} snorted, then grinned. \"Okay, that's funnier than my plan,\" {hero.id} admitted.")
    world.say(
        f"They {gear_def.tail}. After that, the {activity.sound} still went crackle, crackle, crackle, "
        f"but {hero.pronoun('possessive')} {prize.label} stayed safe and {parent.label} laughed so hard {parent.pronoun('subject')} had to hold {parent.pronoun('possessive')} sides."
    )


def tell(setting: Setting, activity: Activity, prize_cfg: Prize, hero_name: str, hero_type: str,
         parent_type: str, traits: Optional[list[str]] = None) -> World:
    world = World(setting)
    hero = world.add(Entity(id=hero_name, kind="character", type=hero_type, traits=["little"] + (traits or ["silly"])))
    parent = world.add(Entity(id="Parent", kind="character", type=parent_type, label="parent"))
    prize = world.add(Entity(
        id="Prize", type=prize_cfg.type, label=prize_cfg.label, phrase=prize_cfg.phrase,
        owner=hero.id, caretaker=parent.id, region=prize_cfg.region, plural=prize_cfg.plural,
    ))

    introduce(world, hero)
    loves_sound(world, hero, activity)
    buys(world, parent, hero, prize)
    loves_prize(world, hero, prize)

    world.para()
    arrive(world, hero, parent, activity)
    wants(world, hero, activity)
    foreshadow(world, parent, hero, activity, prize)
    repeat_refrain(world, hero, activity, prize)
    exclaim_conflict(world, hero, activity)

    world.para()
    gear_def = offer_fix(world, parent, hero, activity, prize)
    if gear_def:
        accept(world, parent, hero, activity, prize, gear_def)

    world.facts.update(
        hero=hero,
        parent=parent,
        prize=prize,
        prize_cfg=prize_cfg,
        activity=activity,
        setting=setting,
        gear=gear_def,
        resolved=gear_def is not None,
    )
    return world


