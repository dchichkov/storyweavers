class World:
    def __init__(self, setting: Setting) -> None:
        self.setting = setting
        self.entities: dict[str, Entity] = {}
        self.fired: set[tuple] = set()
        self.paragraphs: list[list[str]] = [[]]
        self.zone: set[str] = set()
        self.facts: dict = {}

    def add(self, ent: Entity) -> Entity:
        self.entities[ent.id] = ent
        return ent

    def get(self, eid: str) -> Entity:
        if eid not in self.entities:
            label = str(eid).replace("_", " ")
            self.entities[eid] = Entity(str(eid), label=label)
        return self.entities[eid]

    def chars(self) -> list[Entity]:
        return [e for e in self.entities.values() if e.kind == "character"]

    def worn_items(self, actor: Entity) -> list[Entity]:
        return [e for e in self.entities.values() if e.worn_by == actor.id]

    def covered(self, actor: Entity, region: str) -> bool:
        return any(g.protective and region in g.covers for g in self.worn_items(actor))

    def say(self, text: str) -> None:
        if text:
            self.paragraphs[-1].append(text)

    def para(self) -> None:
        if self.paragraphs[-1]:
            self.paragraphs.append([])

    def render(self) -> str:
        return "\n\n".join(" ".join(p) for p in self.paragraphs if p)


def _r_wet(world: World) -> list[str]:
    out: list[str] = []
    for actor in world.chars():
        if actor.meters["wet"] < THRESHOLD:
            continue
        for item in world.worn_items(actor):
            if item.protective or item.region not in world.zone or world.covered(actor, item.region):
                continue
            sig = ("wet", item.id)
            if sig in world.fired:
                continue
            world.fired.add(sig)
            item.meters["wet"] += 1
            out.append(f"{actor.id}'s {item.label} grew damp in the storm water.")
    return out


def _r_lose_tissue(world: World) -> list[str]:
    out: list[str] = []
    tissue = world.entities.get("tissue")
    if tissue and tissue.meters["wet"] >= THRESHOLD and tissue.meters["lost"] < THRESHOLD:
        sig = ("lost", tissue.id)
        if sig not in world.fired:
            world.fired.add(sig)
            tissue.meters["lost"] += 1
            out.append("The tissue nearly slipped away into the dark drain.")
    return out


def _r_teamwork(world: World) -> list[str]:
    out: list[str] = []
    if world.facts.get("helping") and world.facts.get("saved") and ("teamwork", "done") not in world.fired:
        world.fired.add(("teamwork", "done"))
        for e in world.chars():
            e.memes["teamwork"] += 1
        out.append("Together, they held fast and made a small safe circle.")
    return out


CAUSAL_RULES = [_r_wet, _r_lose_tissue, _r_teamwork]


def propagate(world: World, narrate: bool = True) -> list[str]:
    produced: list[str] = []
    changed = True
    for _ in range(len(globals().get("CAUSAL_RULES", [])) + 4):
        changed = False
        for rule in CAUSAL_RULES:
            sents = rule(world)
            if sents:
                changed = True
                produced.extend(sents)
    if narrate:
        for s in produced:
            world.say(s)
    return produced


def prize_at_risk(activity: Activity, prize: Prize) -> bool:
    return prize.region in activity.zone


def select_gear(activity: Activity, prize: Prize) -> Optional[Gear]:
    for gear in GEAR:
        if activity.mess in gear.guards and prize.region in gear.covers:
            return gear
    return None


def story_intro(world: World, ferris: Entity, helper: Entity, activity: Activity, prize: Entity) -> None:
    world.say(
        f"Long ago, in the little kingdom beneath the road, a brave {ferris.label} named Ferris "
        f"loved the bright whirr of the {activity.keyword} wheel."
    )
    world.say(
        f"By Ferris's side was {helper.label}, a kind friend who carried a tissue and a cuff "
        f"that shone like a moonbeam."
    )
    world.say(
        f"Ferris loved {activity.gerund}, and the {prize.label} was the best treasure in the storm drain."
    )


def story_middle(world: World, ferris: Entity, helper: Entity, activity: Activity, prize: Entity) -> None:
    world.para()
    world.say(
        f"One stormy evening, Ferris went to {world.setting.place}, where rain sang down the pipes."
    )
    world.say(
        f"Ferris wanted to {activity.verb}, but the rushing water could make the {prize.label} {activity.soil}."
    )
    world.say(
        f"Ferris's friend saw the danger and lifted a gentle hand. "
        f"\"Let's think together,\" {helper.pronoun('subject')} said."
    )
    ferris.memes["worry"] += 1
    helper.memes["hope"] += 1
    world.facts["helping"] = True


def story_turn(world: World, ferris: Entity, helper: Entity, activity: Activity, prize: Entity) -> Optional[Gear]:
    gear_def = select_gear(activity, prize)
    if gear_def is None:
        pass
    gear = world.add(Entity(
        id=gear_def.id,
        type="gear",
        label=gear_def.label,
        protective=True,
        covers=set(gear_def.covers),
        plural=gear_def.plural,
    ))
    gear.worn_by = ferris.id
    world.say(
        f'Then {helper.label} smiled and said, "{gear_def.prep}."'
    )
    world.say(
        f"Ferris nodded, and soon the cuff was tucked safe inside the {gear.label} while the tissue stayed dry in a pocket."
    )
    world.facts["saved"] = True
    return gear


def story_end(world: World, ferris: Entity, helper: Entity, activity: Activity, prize: Entity, gear: Gear) -> None:
    ferris.meters["wet"] += 1
    propagate(world, narrate=True)
    ferris.memes["joy"] += 1
    helper.memes["joy"] += 1
    world.para()
    world.say(
        f"At last, Ferris {activity.gerund}, and the {prize.label} stayed safe."
    )
    world.say(
        f"The tissue fluttered like a tiny banner, the cuff glowed softly, and the two friends laughed as the storm water rushed past."
    )


