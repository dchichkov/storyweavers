def story_qa(world: World) -> list[QAItem]:
    f = world.facts
    hero = _safe_fact(world, f, "hero")
    prize = _safe_fact(world, f, "prize")
    act = _safe_fact(world, f, "activity")
    parent = _safe_fact(world, f, "parent")
    qa = [
        QAItem(
            question=f"Who is the story about?",
            answer=f"The story is about {hero.id}, a jolly little {hero.type}, and {parent.pronoun('subject')} keeps the ceramic prize safe.",
        ),
        QAItem(
            question=f"What made {hero.id} worry in the middle of the story?",
            answer=f"{hero.id} worried that {act.gerund} would make {hero.pronoun('possessive')} tummy gassy and shake the {prize.label}.",
        ),
        QAItem(
            question=f"What prize did the child need to protect?",
            answer=f"The child needed to protect {prize.phrase}, which was a fragile ceramic treasure.",
        ),
    ]
    if f.get("resolved"):
        qa.append(
            QAItem(
                question="How did the story end safely?",
                answer=f"They used {f['gear'].label} as a soft nest, so the {prize.label} stayed safe and still by the end.",
            )
        )
    return qa


def world_knowledge_qa(world: World) -> list[QAItem]:
    tags = set(world.facts["activity"].tags)
    tags.update(world.facts["prize"].tags)
    out: list[QAItem] = []
    for tag in globals().get("KNOWLEDGE_ORDER", sorted(globals().get("KNOWLEDGE", []))):
        if tag in tags:
            out.extend(QAItem(question=q, answer=a) for q, a in KNOWLEDGE[tag])
    return out


def format_qa(sample: StorySample) -> str:
    lines = ["== (1) Generation prompts -- asks that would produce this story =="]
    for i, p in enumerate(sample.prompts, 1):
        lines.append(f"{i}. {p}")
    lines.append("")
    lines.append("== (2) Story questions -- answerable from the story text ==")
    for item in sample.story_qa:
        lines.append(f"Q: {item.question}")
        lines.append(f"A: {item.answer}")
    lines.append("")
    lines.append("== (3) World-knowledge questions -- child level, no story needed ==")
    for item in sample.world_qa:
        lines.append(f"Q: {item.question}")
        lines.append(f"A: {item.answer}")
    return "\n".join(lines)


def dump_trace(world: World) -> str:
    lines = ["--- world model state ---"]
    for e in list(world.entities.values()):
        meters = {k: v for k, v in e.meters.items() if v}
        memes = {k: v for k, v in e.memes.items() if v}
        bits = []
        if meters:
            bits.append(f"meters={dict(meters)}")
        if memes:
            bits.append(f"memes={dict(memes)}")
        lines.append(f"  {e.id:12} ({e.type:7}) {' '.join(bits)}")
    lines.append(f"  fired rules: {sorted(n for n, *_ in world.fired)}")
    return "\n".join(lines)


def explain_rejection(activity: Activity, prize: Prize) -> str:
    if not prize_at_risk(activity, prize):
        return f"(No story: {activity.gerund} would not reasonably disturb a {prize.label}.)"
    if not select_gear(activity, prize):
        return f"(No story: there is no safe soft fix for a {prize.label} and {activity.gerund}.)"
    return "(No story: invalid combination.)"


