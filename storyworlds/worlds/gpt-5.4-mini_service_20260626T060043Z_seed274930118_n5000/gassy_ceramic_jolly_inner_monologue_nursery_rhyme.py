def _r_soil(world: World) -> list[str]:
    out: list[str] = []
    for actor in world.characters():
        if actor.meters.get("gassy", 0.0) < THRESHOLD:
            continue
        prize = world.entities.get("ceramic_prize")
        if not prize:
            continue
        if prize.meters.get("safe", 0.0) >= THRESHOLD:
            continue
        sig = ("soil", prize.id)
        if sig in world.fired:
            continue
        world.fired.add(sig)
        prize.meters["wobbled"] = prize.meters.get("wobbled", 0.0) + 1
        out.append(f"The little shelf gave a wobble and the ceramic prize trembled.")
    return out


def _r_break(world: World) -> list[str]:
    prize = world.entities.get("ceramic_prize")
    if not prize:
        return []
    if prize.meters.get("wobbled", 0.0) < THRESHOLD:
        return []
    if prize.meters.get("cushioned", 0.0) >= THRESHOLD:
        return []
    sig = ("break", prize.id)
    if sig in world.fired:
        return []
    world.fired.add(sig)
    prize.meters["safe"] = 0.0
    return ["__break__"]


CAUSAL_RULES = [Rule("soil", _r_soil), Rule("break", _r_break)]


def propagate(world: World, narrate: bool = True) -> list[str]:
    produced: list[str] = []
    changed = True
    for _ in range(len(globals().get("CAUSAL_RULES", [])) + 4):
        changed = False
        for rule in CAUSAL_RULES:
            sents = rule.apply(world)
            if sents:
                changed = True
                produced.extend(s for s in sents if s != "__break__")
    if narrate:
        for s in produced:
            world.say(s)
    return produced


def place_line(setting: Setting) -> str:
    return f"In {setting.place}, all was calm as a kitten's paw."


def joy_line(hero: Entity) -> str:
    return f"{hero.pronoun().capitalize()} was jolly and bright, with a hop in {hero.pronoun('possessive')} step."


def inner_monologue(hero: Entity, activity: Activity, prize: Entity) -> str:
    return (
        f"Inside {hero.pronoun('possessive')} own small mind, {hero.pronoun()} thought, "
        f'"Oh my, oh my, this {activity.keyword or activity.id} may make me gassy, '
        f'and the little {prize.label} may quiver like a leaf."'
    )


def predict(world: World, hero: Entity, activity: Activity) -> dict:
    sim = world.copy()
    do_activity(sim, sim.get(hero.id), activity, narrate=False)
    prize = sim.get("ceramic_prize")
    return {
        "wobbled": prize.meters.get("wobbled", 0.0) >= THRESHOLD,
        "safe": prize.meters.get("safe", 0.0) >= THRESHOLD,
    }


def select_gear(activity: Activity, prize: Prize) -> Optional[Gear]:
    for gear in GEARS:
        if activity.mess in gear.guards and prize.fragile:
            return gear
    return None


def prize_at_risk(activity: Activity, prize: Prize) -> bool:
    return activity.mess in RISK_KINDS and prize.fragile


def do_activity(world: World, actor: Entity, activity: Activity, narrate: bool = True) -> None:
    actor.meters[activity.mess] = actor.meters.get(activity.mess, 0.0) + 1
    actor.memes["worry"] = actor.memes.get("worry", 0.0) + 1
    propagate(world, narrate=narrate)


def tell(setting: Setting, activity: Activity, prize_cfg: Prize,
         hero_name: str = "Milo", hero_type: str = "boy",
         parent_type: str = "mother") -> World:
    world = World(setting)
    hero = world.add(Entity(id=hero_name, kind="character", type=hero_type))
    parent = world.add(Entity(id="Parent", kind="character", type=parent_type))
    prize = world.add(Entity(
        id="ceramic_prize",
        type=prize_cfg.type,
        label=prize_cfg.label,
        phrase=prize_cfg.phrase,
        caretaker=parent.id,
    ))
    prize.meters["safe"] = 1.0

    world.say(place_line(setting))
    world.say(joy_line(hero))
    world.say(f"{hero.id} loved {activity.gerund} in the cozy little room.")
    world.say(f"There sat {prize.phrase}, neat and fine, a tiny treasure in a line.")
    world.para()

    world.say(f"One day {hero.id} wanted to {activity.verb}, and {hero.pronoun('possessive')} tummy made a little thrum.")
    world.say(inner_monologue(hero, activity, prize))
    world.say(f"{hero.pronoun().capitalize()} took a breath and looked around, while the room stayed soft and warm.")
    if prize_at_risk(activity, prize):
        pred = predict(world, hero, activity)
        world.facts["predicted_wobble"] = pred["wobbled"]
        world.facts["predicted_safe"] = pred["safe"]
    world.para()

    world.say(f"{hero.id} almost rushed ahead, but then {hero.pronoun()} heard the gentle warning in {hero.pronoun('possessive')} own head.")
    world.say(f'"If I bounce and burp, the little {prize.label} may tip," {hero.pronoun()} thought. "I must be kind instead."')
    do_activity(world, hero, activity)
    world.say(f"So {hero.id} slowed right down and chose the safer way, with tiny steps and a careful sway.")

    gear_def = select_gear(activity, prize_cfg)
    if gear_def:
        gear = world.add(Entity(id=gear_def.id, type="gear", label=gear_def.label))
        gear.meters["cushioned"] = 1.0
        prize.meters["cushioned"] = 1.0
        prize.meters["safe"] = 1.0
        world.say(f"{parent.pronoun().capitalize()} brought {gear_def.label}, and placed it neat and low.")
        world.say(f"They used it as a soft small nest, and kept the ceramic prize from harm and woe.")
        world.say(f"{hero.id} smiled a jolly smile, for the trouble passed in nursery-rhyme style.")
    else:
        world.say(f"{parent.pronoun().capitalize()} held the ceramic prize with care until the gassy moment had gone by.")

