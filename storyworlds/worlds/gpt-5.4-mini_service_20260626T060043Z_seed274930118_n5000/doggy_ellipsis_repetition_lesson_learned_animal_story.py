def _r_soil(world: World) -> list[str]:
    out: list[str] = []
    for actor in world.characters():
        if actor.meters["mess"] < THRESHOLD:
            continue
        for item in world.worn_items(actor):
            if item.protective:
                continue
            if item.region not in {"neck"}:
                continue
            if world.covered(actor, item.region):
                continue
            sig = ("soil", actor.id, item.id)
            if sig in world.fired:
                continue
            world.fired.add(sig)
            item.meters["dirty"] += 1
            out.append(f"{actor.id}'s {item.label} got dirty.")
    return out


def _r_lesson(world: World) -> list[str]:
    out: list[str] = []
    dog = next((e for e in world.characters() if e.type == "doggy"), None)
    if not dog:
        return out
    if dog.memes["worry"] >= THRESHOLD and dog.memes["joy"] >= THRESHOLD:
        sig = ("lesson", dog.id)
        if sig not in world.fired:
            world.fired.add(sig)
            dog.memes["lesson"] += 1
            out.append("lesson")
    return out


CAUSAL_RULES = [Rule("soil", _r_soil), Rule("lesson", _r_lesson)]


def propagate(world: World, narrate: bool = True) -> list[str]:
    produced: list[str] = []
    changed = True
    for _ in range(len(globals().get("CAUSAL_RULES", [])) + 4):
        changed = False
        for rule in CAUSAL_RULES:
            sents = rule.apply(world)
            if sents:
                changed = True
                produced.extend(s for s in sents if s != "lesson")
    if narrate:
        for s in produced:
            world.say(s)
    return produced


def prize_at_risk(activity: Activity, prize: Prize) -> bool:
    return prize.region == "neck" and activity.mess != "none"


def select_gear(activity: Activity, prize: Prize) -> Optional[Gear]:
    for gear in GEAR:
        if activity.mess in gear.guards and prize.region in gear.covers:
            return gear
    return None


