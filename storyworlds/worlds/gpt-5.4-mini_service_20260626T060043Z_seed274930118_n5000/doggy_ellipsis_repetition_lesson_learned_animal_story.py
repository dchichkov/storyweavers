def intro(world: World, dog: Entity) -> None:
    world.say(
        f"{dog.id} was a little {dog.type} who loved repeating the same game, "
        f"again and again."
    )
    world.say(
        f"Every time he got excited, he wagged his tail and said, "
        f'"Again... again... again!"'
    )


def want_story(world: World, dog: Entity, activity: Activity) -> None:
    dog.memes["desire"] += 1
    world.say(
        f"{dog.id} wanted to {activity.verb}, and he kept hopping toward the fun "
        f"with a happy little bounce."
    )


def warn(world: World, parent: Entity, dog: Entity, activity: Activity, prize: Entity) -> bool:
    pred = predict_mess(world, dog, activity, prize.id)
    if not pred["soiled"]:
        return False
    parent.memes["worry"] += 1
    world.say(
        f'"Not with your {prize.label} on, {dog.id}..." {parent.id} said gently. '
        f'"That would make it messy."'
    )
    return True


def repeat_try(world: World, dog: Entity, activity: Activity) -> None:
    dog.memes["impulse"] += 1
    world.say(
        f"{dog.id} tried it once, then wanted to do it again, and again, "
        f"and again."
    )
    world.say(
        f"Each try made the game feel bigger, but it also made the warning feel bigger too..."
    )


def turn(world: World, parent: Entity, dog: Entity, activity: Activity) -> None:
    dog.memes["worry"] += 1
    world.say(
        f"{parent.id} touched {dog.id}'s shoulder and said, "
        f'"Slow paws, little one. A good game can wait for the right gear."'
    )


def compromise(world: World, parent: Entity, dog: Entity, activity: Activity, prize: Entity) -> Optional[Gear]:
    gear_def = select_gear(activity, prize)
    if gear_def is None:
        return None
    gear = world.add(
        Entity(
            id=gear_def.id,
            type="gear",
            label=gear_def.label,
            owner=dog.id,
            caretaker=parent.id,
            protective=True,
            covers=set(gear_def.covers),
            plural=gear_def.plural,
        )
    )
    gear.worn_by = dog.id
    if predict_mess(world, dog, activity, prize.id)["soiled"]:
        gear.worn_by = None
        del world.entities[gear.id]
        return None
    world.say(
        f'{parent.id} smiled and said, "How about we {gear_def.prep}?"'
    )
    return gear_def


def finish(world: World, dog: Entity, parent: Entity, activity: Activity, prize: Entity, gear_def: Gear) -> None:
    dog.memes["joy"] += 1
    dog.memes["love"] += 1
    dog.memes["lesson"] += 1
    dog.memes["worry"] = 0
    world.say(
        f"{dog.id} nodded, sat down, and waited for the {gear_def.label}."
    )
    world.say(
        f"Then he went back out and {activity.gerund}, "
        f"with {prize.label} staying clean."
    )
    world.say(
        f"This time, {dog.id} did not rush ahead over and over. He had learned that "
        f"repeating the same mistake was not the best game."
    )


def tell(setting: Setting, activity: Activity, prize_cfg: Prize, name: str, trait: str, parent_type: str) -> World:
    world = World(setting)
    world.weather = activity.weather

    dog = world.add(Entity(id=name, kind="character", type="doggy", traits=["little", trait]))
    parent = world.add(Entity(id="Parent", kind="character", type=parent_type, label="parent"))
    prize = world.add(
        Entity(
            id="prize",
            type=prize_cfg.type,
            label=prize_cfg.label,
            phrase=prize_cfg.phrase,
            owner=dog.id,
            caretaker=parent.id,
            region=prize_cfg.region,
            plural=prize_cfg.plural,
        )
    )

    intro(world, dog)
    want_story(world, dog, activity)
    world.para()
    world.say(f"One day, {dog.id} and {parent.id} went to {setting.place}.")
    world.say(f"The air was {activity.weather} and {setting.place} felt ready for play.")
    warn(world, parent, dog, activity, prize)
    repeat_try(world, dog, activity)
    turn(world, parent, dog, activity)
    world.para()
    gear_def = compromise(world, parent, dog, activity, prize)
    if gear_def:
        finish(world, dog, parent, activity, prize, gear_def)

    world.facts.update(
        dog=dog,
        parent=parent,
        prize=prize,
        activity=activity,
        setting=setting,
        gear=gear_def,
        resolved=gear_def is not None,
    )
    return world


