def predict_rescue(world: World, hero: Entity, activity: Activity, goal: Goal) -> dict:
    sim = world.copy()
    sim.get(hero.id).memes["worry"] += 1
    sim.get(hero.id).meters["bravery"] += 1
    sim.facts["tool"] = select_tool(activity, goal)
    return {
        "can_reach": select_tool(activity, goal) is not None,
        "goal_safe": True,
    }


def tell(setting: Setting, activity: Activity, goal: Goal,
         hero_name: str, hero_type: str, parent_type: str, trait: str) -> World:
    world = World(setting)
    hero = world.add(Entity(id=hero_name, kind="character", type=hero_type, meters={}, memes={}))
    parent = world.add(Entity(id="Parent", kind="character", type=parent_type, label="parent", meters={}, memes={}))
    ball = world.add(Entity(id=goal.label, kind="thing", type=goal.kind, label=goal.label, owner=hero.id,
                            meters={"stuck": 0.0, "soot": 0.0}, memes={}))

    world.facts.update(hero=hero, parent=parent, goal=ball, activity=activity, trait=trait)

    world.say(
        f"In {setting.place}, {hero_name} was {trait} and spry, "
        f"and loved to run with a laugh and a sigh."
    )
    world.say(
        f"{hero.pronoun('possessive').capitalize()} heart liked {activity.gerund}, "
        f"for the game felt bright like a kite in the sky."
    )
    world.say(
        f"Then a bounce went wrong, with a little ding-dong, "
        f"and {ball.label} went bump to the chimney so high."
    )

    world.para()
    hero.memes["worry"] += 1
    world.say(
        f"{hero_name} looked up and felt small for a spell; "
        f"the ball was now stuck, and it did not fit well."
    )
    world.say(
        f"{parent.pronoun('subject').capitalize()} saw the dark soot and the steep, smoky height, "
        f"and said, \"Let's be careful and brave, if we might.\""
    )

    tool = select_tool(activity, goal)
    if tool is None:
        pass

    world.para()
    hero.memes["bravery"] += 1
    hero.memes["worry"] = 0.0
    world.say(
        f"{hero_name} took a deep breath, then stood up straight; "
        f"{hero.pronoun('subject').capitalize()} chose to be brave, though the shadows looked great."
    )
    world.say(
        f"They fetched {tool.phrase}, with a steady, kind sway, "
        f"and reached for the ball in a careful new way."
    )
    ball.meters["stuck"] = 0.0
    ball.meters["soot"] += 1
    hero.memes["joy"] += 1
    parent.memes["pride"] += 1

    world.para()
    world.say(
        f"Out came the {goal.label}, not lost in the gloom; "
        f"it rolled to the grass with a soft little zoom."
    )
    world.say(
        f"{hero_name} smiled at {parent.pronoun('object')} and gave {parent.pronoun('object')} a hug; "
        f"the brave little rescue felt warm and snug."
    )
    world.say(
        f"Then rugby could start with a cheer and a play, "
        f"and the chimney stood quiet at the end of the day."
    )

    world.facts["tool"] = tool
    world.facts["resolved"] = True
    return world


