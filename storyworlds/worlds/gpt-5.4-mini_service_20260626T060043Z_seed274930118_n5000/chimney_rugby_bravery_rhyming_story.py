ASP_RULES = r"""
% A goal is risky if the activity can send it into the chimney.
risk(A, G) :- activity(A), goal(G), can_fall_into_chimney(A, G).

% A tool is a compatible fix when it helps the activity and provides the needed
% reach or height for the chimney rescue.
fix(A, G, T) :- risk(A, G), tool(T), helps(T, A), covers(T, C), needed(G, C).

valid_story(S, A, G) :- setting(S), affords(S, A), risk(A, G), has_fix(A, G).
"""


def asp_facts() -> str:
    import asp
    lines: list[str] = []
    for sid, s in SETTINGS.items():
        lines.append(asp.fact("setting", sid))
        for a in sorted(s.affords):
            lines.append(asp.fact("affords", sid, a))
    for aid, a in ACTIVITIES.items():
        lines.append(asp.fact("activity", aid))
        lines.append(asp.fact("can_fall_into_chimney", aid, "ball"))
        lines.append(asp.fact("needed", "ball", "reach"))
        lines.append(asp.fact("needed", "ball", "height"))
    for gid, g in GOALS.items():
        lines.append(asp.fact("goal", gid))
        lines.append(asp.fact("goal_at", gid, g.place))
        lines.append(asp.fact("worn_or_found", gid, g.place))
    for t in TOOLS:
        lines.append(asp.fact("tool", t.id))
        for a in sorted(t.helps):
            lines.append(asp.fact("helps", t.id, a))
        for c in sorted(t.covers):
            lines.append(asp.fact("covers", t.id, c))
    return "\n".join(lines)


def asp_program(show: str) -> str:
    return f"{asp_facts()}\n{ASP_RULES}\n{show}\n"


def asp_valid() -> list[tuple]:
    import asp
    model = asp.one_model(asp_program("#show valid_story/3."))
    return sorted(set(asp.atoms(model, "valid_story")))


