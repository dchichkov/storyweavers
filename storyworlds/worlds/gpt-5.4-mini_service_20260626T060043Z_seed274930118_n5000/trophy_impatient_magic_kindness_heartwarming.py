def resolve_params(args: argparse.Namespace, rng: random.Random) -> StoryParams:
    combos = [c for c in valid_combos()
              if (getattr(args, "place", None) is None or c[0] == getattr(args, "place", None))
              and (getattr(args, "activity", None) is None or c[1] == getattr(args, "activity", None))
              and (getattr(args, "prize", None) is None or c[2] == getattr(args, "prize", None))]
    if not combos:
        return _fallback_storyparams(args, rng, StoryParams, globals())
    place, activity, prize = (list(rng.choice(combos)) + [None, None, None])[:3]
    gender = getattr(args, "gender", None) or rng.choice(["girl", "boy"])
    name = getattr(args, "name", None) or rng.choice(GIRL_NAMES if gender == "girl" else BOY_NAMES)
    helper = getattr(args, "helper", None) or rng.choice(HELPERS)
    trait = getattr(args, "trait", None) or rng.choice(TRAITS)
    return StoryParams(place=place, activity=activity, prize=prize, name=name, gender=gender, helper=helper, trait=trait)


def _do_activity(world: World, hero: Entity, activity: Activity, narrate: bool = True) -> None:
    hero.memes["impatient"] = hero.memes.get("impatient", 0.0) + 1
    hero.meters["restless"] = hero.meters.get("restless", 0.0) + 1
    propagate(world, narrate=narrate)


def predict_tension(world: World, hero: Entity, activity: Activity) -> bool:
    sim = world.copy()
    _do_activity(sim, sim.get(hero.id), activity, narrate=False)
    return sim.get(hero.id).memes.get("stuck", 0.0) >= THRESHOLD


def tell(setting: Setting, activity: Activity, prize_cfg: Prize, hero_name: str, hero_type: str, helper_type: str, trait: str) -> World:
    world = World(setting)
    hero = world.add(Entity(id=hero_name, kind="character", type=hero_type, traits=["little", trait, "impatient"]))
    helper = world.add(Entity(id="Helper", kind="character", type=helper_type, label=f"the {helper_type}"))
    prize = world.add(Entity(id="prize", type=prize_cfg.type, label=prize_cfg.label, phrase=prize_cfg.phrase, owner=helper.id))
    charm = world.add(Entity(id="charm", type="charm", label=CHARMS["sparkle"].label, phrase=CHARMS["sparkle"].label))
    world.facts.update(hero=hero, helper=helper, prize=prize, activity=activity, charm=charm)

    world.say(f"{hero.id} was a little {trait} {hero.type} who wanted {prize.label} more than anything.")
    world.say(f"{hero.id} loved {activity.gerund}, but waiting made {hero.pronoun('object')} feel impatient.")
    world.say(f"One day, {hero.id} and {hero.pronoun('possessive')} {helper_type} went to {setting.place}.")
    world.para()
    world.say(f"{hero.id} saw {prize.phrase} on a small table and wanted to {activity.verb} right away.")
    if predict_tension(world, hero, activity):
        hero.memes["impatient"] += 1
        world.say(f'"I want it now!" {hero.id} blurted, because {hero.pronoun("possessive")} feet could hardly stay still.')
        world.say(f"{hero.pronoun().capitalize()} tried to {activity.rush}, but {hero.pronoun('possessive')} {helper_type} held up a gentle hand.")
        world.say(f'"Let’s do one kind thing first," {helper.id} said.')
    world.para()
    hero.memes["kindness"] = 1.0
    helper.memes["kindness"] = 1.0
    charm.memes["magic"] = 1.0
    propagate(world, narrate=True)
    world.say(f"{helper.id} showed {hero.id} {CHARMS['sparkle'].prep}.")
    world.say(f"The little charm glowed softly, and {hero.id} remembered that kind hearts shine better than hurried hands.")
    hero.memes["impatient"] = 0.0
    hero.memes["kindness"] = 2.0
    world.say(f"{hero.id} took a breath, helped {helper.id} tidy the stage, and waited with a calmer smile.")
    world.say(f"At last, {hero.id} got to {activity.verb}, and the {prize.label} felt like a prize for patience, too.")
    world.say(f"{hero.id} beamed beside {helper.id}, and the room felt warm with kindness.")
    return world


