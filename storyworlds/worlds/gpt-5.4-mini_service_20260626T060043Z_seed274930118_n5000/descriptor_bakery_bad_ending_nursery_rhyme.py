def resolve_params(args: argparse.Namespace, rng: random.Random) -> StoryParams:
    combos = [c for c in valid_combos()
              if (getattr(args, "place", None) is None or c[0] == getattr(args, "place", None))
              and (getattr(args, "activity", None) is None or c[1] == getattr(args, "activity", None))
              and (getattr(args, "prize", None) is None or c[2] == getattr(args, "prize", None))]
    if not combos:
        return _fallback_storyparams(args, rng, StoryParams, globals())
    place, activity, prize = (list(rng.choice(combos)) + [None, None, None])[:3]
    gender = getattr(args, "gender", None) or rng.choice(["girl", "boy"])
    name = getattr(args, "name", None) or rng.choice(NAMES)
    parent = getattr(args, "parent", None) or rng.choice(["mother", "father"])
    trait = rng.choice(TRAITS)
    return StoryParams(place=place, activity=activity, prize=prize, name=name, gender=gender, parent=parent, trait=trait)


def _do_activity(world: World, actor: Entity, activity: Activity) -> None:
    if activity.id not in world.setting.affords:
        return
    world.zone = set(activity.zone)
    actor.meters[activity.mess] += 1
    actor.memes["joy"] += 1
    for item in world.worn_items(actor):
        if item.region in world.zone and not item.protective and not world.covered(actor, item.region):
            item.meters[activity.mess] += 1
            item.meters["dirty"] += 1


def select_gear(activity: Activity, prize: Prize) -> Optional[Gear]:
    for g in GEAR:
        if activity.mess in g.guards and prize.region in g.covers:
            return g
    return None


def tell(setting: Setting, activity: Activity, prize_cfg: Prize, hero_name: str, hero_type: str, parent_type: str, trait: str) -> World:
    world = BakeryWorld(setting)
    child = world.add(Entity(id=hero_name, kind="character", type=hero_type))
    parent = world.add(Entity(id="Parent", kind="character", type=parent_type, label="the grown-up"))
    prize = world.add(Entity(
        id="prize",
        type=prize_cfg.label,
        label=prize_cfg.label,
        phrase=prize_cfg.phrase,
        owner=child.id,
        caretaker=parent.id,
        region=prize_cfg.region,
    ))

    child.memes["joy"] += 1
    world.say(f"In the bakery bright, with ovens aglow, {child.id} was a {trait} little {hero_type} who loved to help and go, go, go.")
    world.say(f"{child.id} liked the word {activity.keyword}; it sat on a sign and twinkled like snow.")
    world.say(f"{parent.id} brought {prize.phrase}, and {child.id} wore it with pride, to the sugar-sweet counter by the floury side.")

    world.para()
    world.say(f"One morning in the bakery warm, {child.id} and {child.pronoun('possessive')} {parent.label} went near the oven's hum.")
    world.say(f"{child.id} wanted to {activity.verb}, but {child.pronoun('possessive')} {parent.label} said, 'Not so fast, little chum.'")
    world.say(f'"You may get {prize.label} {activity.soil}," said the grown-up, "and the tray may tumble and come undone."')
    child.memes["worry"] += 1
    child.memes["stubborn"] += 1

    gear = select_gear(activity, prize)
    if gear:
        world.say(f"The grown-up held up {gear.label} and said, 'We can {gear.prep} if you wish to play.'")
        world.say(f"But {child.id} shook {child.pronoun('possessive')} head and darted away.")
    world.para()

    _do_activity(world, child, activity)
    child.meters["messy"] += 1
    prize.meters[activity.mess] += 1
    prize.meters["dirty"] += 1
    child.meters["damage"] += 1
    child.memes["sad"] += 1

    world.say(f"Then {child.id} dashed and the tray tipped with a clink and a clatter.")
    world.say(f"The buns went bump, and the frosting went flop, and the pretty {prize.label} was no longer fit for the table.")
    world.say(f"The bakery fell quiet; the sweet little smell turned dull in the air.")
    world.say(f"{child.id} stood still with sticky hands and a tear in the eye, for the lovely treat was gone and could not be made whole again.")

    world.facts.update(hero=child, parent=parent, prize=prize, activity=activity, setting=setting, gear=gear)
    return world


def generation_prompts(world: World) -> list[str]:
    f = world.facts
    hero, parent, act, prize = f["hero"], f["parent"], f["activity"], f["prize"]
    return [
        f'Write a nursery-rhyme style story set in a bakery about a child named {hero.id} who wants to {act.verb}.',
        f"Tell a gentle but sad story where {hero.id}'s {parent.label} worries about {prize.phrase} and the child does not listen.",
        f'Write a short story that includes the word "{act.keyword}" and ends with the bakery in a sad silence.',
    ]


