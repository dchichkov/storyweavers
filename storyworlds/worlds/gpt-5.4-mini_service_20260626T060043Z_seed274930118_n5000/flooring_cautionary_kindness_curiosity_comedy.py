def tell(setting: Setting, activity: Activity, prize_cfg: Prize,
         hero_name: str, hero_type: str, parent_type: str, trait: str) -> World:
    world = World(setting)
    hero = world.add(Entity(id=hero_name, kind="character", type=hero_type))
    parent = world.add(Entity(id="parent", kind="character", type=parent_type, label="the grown-up"))
    prize = world.add(Entity(
        id="prize", type=prize_cfg.type, label=prize_cfg.label, phrase=prize_cfg.phrase,
        owner=hero.id, caretaker=parent.id, plural=prize_cfg.plural
    ))

    world.say(f"{hero.id} was a {trait} little {hero.type} who noticed every shiny thing in {setting.place}.")
    world.say(f"{hero.id} loved {activity.gerund}, and {hero.pronoun('possessive')} eyes went straight to the floor.")
    world.say(f"{parent.label} had just bought {hero.pronoun('object')} {prize.phrase}.")

    world.para()
    world.say(f"One day in {setting.place}, the floor looked almost like a mirror.")
    world.say(f"{hero.id} wanted to {activity.verb}, but {parent.label} lifted a hand and warned, “Careful—this floor is still wet.”")

    hero.memes["curiosity"] = 1.0
    hero.memes["cautionary"] = 1.0
    world.say(f"{hero.id} giggled anyway and tried to {activity.rush}.")
    if is_at_risk(activity, prize):
        hero.meters[activity.mess] = 1.0
        if prize.region in activity.zone and not world.covered(hero, prize.region):
            prize.meters[activity.mess] = 1.0
            world.say(f"The {prize.label} got {activity.soil}, and even the floor seemed to look offended.")
            parent.memes["concern"] = 1.0
            world.say(f"That would mean more cleanup for {parent.label}.")

    world.para()
    gear = select_gear(activity, prize)
    if gear:
        gear_ent = world.add(Entity(
            id=gear.id, type="gear", label=gear.label, owner=hero.id, caretaker=parent.id,
            plural=gear.plural
        ))
        gear_ent.worn_by = hero.id
        world.say(f"Then a kind helper laughed, pointed at the mess, and said, “How about we {gear.prep}?”")
        world.say(f"{hero.id} smiled, because {gear.label} made the floor feel less like a joke at {hero.pronoun('possessive')} socks’ expense.")
        world.say(f"They {gear.tail}, wiped the wet spot, and soon the floor was safe again.")
        world.say(f"{hero.id} tried the move one more time, this time {activity.gerund}, while {prize.label} stayed clean enough to brag.")
        hero.memes["kindness"] = 1.0
        hero.memes["joy"] = 1.0
        parent.memes["relief"] = 1.0

    world.facts.update(
        hero=hero, parent=parent, prize=prize, activity=activity, setting=setting,
        gear=gear if gear else None
    )
    return world


def generation_prompts(world: World) -> list[str]:
    f = world.facts
    hero, parent, act, prize = f["hero"], f["parent"], f["activity"], f["prize"]
    return [
        f'Write a funny story for a young child about {hero.id} and a {act.keyword} floor.',
        f"Tell a cautionary but kind story where {hero.id} wants to {act.verb} and {parent.label} worries about {prize.phrase}.",
        f"Write a short comedy about flooring, a warning, and a helpful fix that keeps everyone smiling.",
    ]


def story_qa(world: World) -> list[QAItem]:
    f = world.facts
    hero, parent, prize, act = f["hero"], f["parent"], f["prize"], f["activity"]
    qa = [
        QAItem(
            question=f"Who wanted to {act.verb} in the story?",
            answer=f"{hero.id} wanted to {act.verb} because the shiny floor looked funny and inviting."
        ),
        QAItem(
            question=f"Why did {parent.label} warn {hero.id} about the floor?",
            answer=f"{parent.label} warned {hero.id} because the floor was still wet and could make {hero.id} slip."
        ),
        QAItem(
            question=f"What happened to {prize.label} before the helper fixed the problem?",
            answer=f"The {prize.label} got {act.soil} when {hero.id} rushed onto the floor too quickly."
        ),
    ]
    if f.get("gear"):
        gear = _safe_fact(world, f, "gear")
        qa.append(QAItem(
            question=f"How did {gear.label} help in the end?",
            answer=f"{gear.label} helped by making the floor safer so {hero.id} could keep playing without slipping."
        ))
    return qa


def world_knowledge_qa(world: World) -> list[QAItem]:
    tags = {world.facts["activity"].keyword, world.facts["activity"].mess, "flooring"}
    if world.facts.get("gear"):
        tags.add(world.facts["gear"].id)
        tags.add("shoes" if "shoe" in world.facts["gear"].label else "sign")
    out: list[QAItem] = []
    for tag in globals().get("KNOWLEDGE_ORDER", sorted(globals().get("KNOWLEDGE", []))):
        if tag in tags:
            out.extend(QAItem(question=q, answer=a) for q, a in KNOWLEDGE[tag])
    return out


def format_qa(sample: StorySample) -> str:
    parts = ["== (1) Generation prompts ==", *[f"{i+1}. {p}" for i, p in enumerate(sample.prompts)], ""]
    parts.append("== (2) Story questions ==")
    for item in sample.story_qa:
        parts.append(f"Q: {item.question}")
        parts.append(f"A: {item.answer}")
    parts.append("")
    parts.append("== (3) World knowledge ==")
    for item in sample.world_qa:
        parts.append(f"Q: {item.question}")
        parts.append(f"A: {item.answer}")
    return "\n".join(parts)


def dump_trace(world: World) -> str:
    lines = ["--- world model state ---"]
    for e in list(world.entities.values()):
        meters = {k: v for k, v in e.meters.items() if v}
        memes = {k: v for k, v in e.memes.items() if v}
        lines.append(f"  {e.id:8} ({e.type:8}) meters={meters} memes={memes}")
    return "\n".join(lines)


