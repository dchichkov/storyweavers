class World:
    def __init__(self, setting: Setting) -> None:
        self.setting = setting
        self.entities: dict[str, Entity] = {}
        self.paragraphs: list[list[str]] = [[]]
        self.fired: set[tuple] = set()
        self.zone: set[str] = set()
        self.facts: dict = {}

    def add(self, ent: Entity) -> Entity:
        self.entities[ent.id] = ent
        return ent

    def get(self, eid: str) -> Entity:
        if eid not in self.entities:
            label = str(eid).replace("_", " ")
            self.entities[eid] = Entity(str(eid), label=label)
        return self.entities[eid]

    def characters(self) -> list[Entity]:
        return [e for e in self.entities.values() if e.kind == "character"]

    def worn_items(self, actor: Entity) -> list[Entity]:
        return [e for e in self.entities.values() if e.worn_by == actor.id]

    def covered(self, actor: Entity, region: str) -> bool:
        return any(g.protective and region in g.covers for g in self.worn_items(actor))

    def say(self, text: str) -> None:
        if text:
            self.paragraphs[-1].append(text)

    def para(self) -> None:
        if self.paragraphs[-1]:
            self.paragraphs.append([])

    def render(self) -> str:
        return "\n\n".join(" ".join(p) for p in self.paragraphs if p)

    def copy(self) -> "World":
        import copy as _copy

        clone = World(self.setting)
        clone.entities = _copy.deepcopy(self.entities)
        clone.paragraphs = [[]]
        clone.fired = set(self.fired)
        clone.zone = set(self.zone)
        clone.facts = dict(self.facts)
        return clone


def _soak(world: World) -> list[str]:
    out: list[str] = []
    for actor in world.characters():
        if actor.meters.get("sauce", 0.0) < THRESHOLD:
            continue
        for item in world.worn_items(actor):
            if item.protective or item.region not in world.zone:
                continue
            if world.covered(actor, item.region):
                continue
            sig = ("soak", actor.id, item.id)
            if sig in world.fired:
                continue
            world.fired.add(sig)
            item.meters["sauce"] = item.meters.get("sauce", 0.0) + 1
            item.meters["dirty"] = item.meters.get("dirty", 0.0) + 1
            out.append(f"{actor.pronoun('possessive').capitalize()} {item.label} got sauce on it.")
    return out


CAUSAL_RULES = [_soak]


def propagate(world: World, narrate: bool = True) -> list[str]:
    produced: list[str] = []
    changed = True
    for _ in range(len(globals().get("CAUSAL_RULES", [])) + 4):
        changed = False
        for rule in CAUSAL_RULES:
            sents = rule(world)
            if sents:
                changed = True
                produced.extend(sents)
    if narrate:
        for s in produced:
            world.say(s)
    return produced


def predict_mess(world: World, actor: Entity, activity: Activity, garment_id: str) -> dict:
    sim = world.copy()
    _do_activity(sim, sim.get(actor.id), activity, narrate=False)
    garment = sim.get(garment_id)
    return {
        "soiled": garment.meters.get("dirty", 0.0) >= THRESHOLD,
    }


def _do_activity(world: World, actor: Entity, activity: Activity, narrate: bool = True) -> None:
    if activity.id not in world.setting.affords:
        pass
    world.zone = set(activity.zone)
    actor.meters["sauce"] = actor.meters.get("sauce", 0.0) + 1
    actor.memes["joy"] = actor.memes.get("joy", 0.0) + 1
    propagate(world, narrate=narrate)


def setting_detail(setting: Setting) -> str:
    if setting.indoor:
        return f"Inside {setting.place}, the room smelled warm and quiet."
    return f"{setting.place.capitalize()} felt calm, like an ordinary place for a small adventure."


def introduce(world: World, hero: Entity) -> None:
    world.say(f"{hero.id} was a little {hero.type} who liked ordinary days with small surprises.")


def love_garment(world: World, hero: Entity, garment: Entity) -> None:
    hero.memes["love"] = hero.memes.get("love", 0.0) + 1
    garment.worn_by = hero.id
    world.say(f"{hero.id} loved {hero.pronoun('possessive')} {garment.label} and wore {garment.it()} happily.")


def wants(world: World, hero: Entity, activity: Activity) -> None:
    hero.memes["desire"] = hero.memes.get("desire", 0.0) + 1
    world.say(f"{hero.id} wanted to {activity.verb}, even though the day was moving gently along.")


def warn(world: World, parent: Entity, hero: Entity, activity: Activity, garment: Entity) -> bool:
    pred = predict_mess(world, hero, activity, garment.id)
    if not pred["soiled"]:
        return False
    world.facts["predicted_soil"] = activity.soil
    world.say(f"“You’ll get your {garment.label} {activity.soil},” {parent.pronoun('possessive')} {parent.type} said.")
    return True


def defies(world: World, hero: Entity, activity: Activity) -> None:
    hero.memes["defiance"] = hero.memes.get("defiance", 0.0) + 1
    world.say(f"{hero.id} hesitated, then tried to {activity.rush}.")


def offer_magic(world: World, parent: Entity, hero: Entity, activity: Activity, garment: Entity) -> Optional[Magic]:
    for magic in MAGIC:
        if activity.mess in magic.guards and garment.region in magic.covers:
            helper = world.add(Entity(
                id=magic.id,
                type="magic",
                label=magic.label,
                phrase=magic.phrase,
                owner=hero.id,
                protective=True,
                covers=set(magic.covers),
            ))
            helper.worn_by = hero.id
            if predict_mess(world, hero, activity, garment.id)["soiled"]:
                helper.worn_by = None
                del world.entities[helper.id]
                continue
            world.say(f"{parent.id} smiled and said they could {magic.prep}.")
            return magic
    return None


def accept(world: World, hero: Entity, parent: Entity, activity: Activity, garment: Entity, magic: Magic) -> None:
    hero.memes["joy"] = hero.memes.get("joy", 0.0) + 1
    hero.memes["conflict"] = 0.0
    world.say(f"{hero.id} grinned, and {hero.pronoun()} went along with the plan.")
    world.say(
        f"Then they {magic.tail}. Soon {hero.id} was {activity.gerund}, "
        f"and {hero.pronoun('possessive')} {garment.label} stayed clean."
    )


def tell(setting: Setting, activity: Activity, garment_cfg: Garment, hero_name: str, hero_type: str, parent_type: str) -> World:
    world = World(setting)
    hero = world.add(Entity(id=hero_name, kind="character", type=hero_type))
    parent = world.add(Entity(id="Parent", kind="character", type=parent_type, label="parent"))
    garment = world.add(Entity(
        id=garment_cfg.id,
        type=garment_cfg.id,
        label=garment_cfg.label,
        phrase=garment_cfg.phrase,
        owner=hero.id,
        caretaker=parent.id,
        region=garment_cfg.region,
        plural=garment_cfg.plural,
    ))

    introduce(world, hero)
    world.say(setting_detail(setting))
    love_garment(world, hero, garment)
    wants(world, hero, activity)

    world.para()
    warn(world, parent, hero, activity, garment)
    defies(world, hero, activity)

    world.para()
    magic = offer_magic(world, parent, hero, activity, garment)
    if magic is None:
        pass
    accept(world, hero, parent, activity, garment, magic)

    world.facts.update(hero=hero, parent=parent, garment=garment, activity=activity, magic=magic, setting=setting)
    return world


