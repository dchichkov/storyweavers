def tell(setting: Setting, activity: Activity, prize_cfg: Prize, hero_name: str,
         parent_type: str, trait: str) -> World:
    world = World(setting)
    hero = world.add(Entity(id=hero_name, kind="character", type="boy", memes={"worry": 0.0, "joy": 0.0, "humor": 0.0}))
    parent = world.add(Entity(id="Parent", kind="character", type=parent_type, label=f"the {parent_type}"))
    prize = world.add(Entity(
        id="prize",
        type=prize_cfg.type,
        label=prize_cfg.label,
        phrase=prize_cfg.phrase,
        owner=hero.id,
        caretaker=parent.id,
        region=prize_cfg.region,
        plural=prize_cfg.plural,
    ))
    tool = select_tool(activity, prize)
    if tool is None:
        pass
    tool_ent = world.add(Entity(id=tool.id, type="tool", label=tool.label, owner=parent.id))

    # Act 1
    world.say(
        f"Victor was a {trait} little boy who liked to laugh at puddles and songs "
        f"at {setting.place}."
    )
    world.say(
        f"He loved {activity.gerund}, and he wore {prize.phrase} as bright as a "
        f"bean in a boat."
    )

    # Act 2
    world.para()
    world.say(
        f"One day at {setting.place}, Victor wanted to {activity.verb}, but the water "
        f"kept jumping up to say hello."
    )
    hero.memes["worry"] += 1
    parent.memes["worry"] += 1
    world.say(
        f"The {parent_type} frowned and said, \"Oh dear, your {prize.label} may get all "
        f"wet and queer.\""
    )
    world.say(
        f"Victor said, \"I can still help!\" and he made a funny face to keep from "
        f"feeling glum."
    )
    hero.memes["humor"] += 1

    # Act 3
    world.para()
    world.say(
        f"Then the {parent_type} smiled and offered {tool.label}: \"We can {tool.prep} "
        f"and sing a little rhyme.\""
    )
    hero.memes["joy"] += 1
    hero.memes["worry"] = 0.0
    parent.memes["worry"] = 0.0
    world.say(
        f"They {tool.tail}, and Victor kept the boat from going belly-up with "
        f"a splash and a laugh."
    )
    world.say(
        f"In the end, Victor was still {activity.gerund}, {prize.label} stayed dry, "
        f"and the little dock sounded like giggles."
    )

    world.facts.update(
        hero=hero,
        parent=parent,
        prize=prize,
        activity=activity,
        setting=setting,
        tool=tool_ent,
        trait=trait,
        resolved=True,
    )
    return world


