def prize_at_risk(activity: Activity, prize: Prize) -> bool:
    return prize.region in {"head", "torso", "feet"} and activity.id == "bail"


def select_tool(activity: Activity, prize: Prize) -> Optional[Tool]:
    for tool in TOOLS.values():
        if activity.id in tool.helps:
            return tool
    return None


def explain_rejection(activity: Activity, prize: Prize) -> str:
    return (
        f"(No story: this little tale only works when {activity.verb} can truly "
        f"make the prize worry-worthy. Try another prize or activity.)"
    )


