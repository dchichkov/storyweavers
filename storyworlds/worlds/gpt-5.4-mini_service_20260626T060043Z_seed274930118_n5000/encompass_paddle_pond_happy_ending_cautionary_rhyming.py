def warn(world: World, parent: Entity, hero: Entity, activity: Activity, prize: Entity) -> bool:
    pred = predict_mess(world, hero, activity, prize.id)
    if not pred["soiled"]:
        return False
    world.facts["warned"] = True
    world.say(
        f'"If you {activity.verb}, your {prize.label} may get {activity.soil}," '
        f"{parent.pronoun('possessive')} parent said with a care-filled tone."
    )
    return True


def caution(world: World, hero: Entity, activity: Activity) -> None:
    hero.memes["caution"] += 1
    world.say(f"{hero.id} paused at first, though {hero.pronoun('subject')} still wanted the fun and the cheer.")
    world.say(f"{hero.pronoun().capitalize()} reached for the little paddle and gave it a peer.")


def offer_compromise(world: World, parent: Entity, hero: Entity, activity: Activity, prize: Entity) -> Optional[Gear]:
    gear_def = select_gear(activity, prize)
    if gear_def is None:
        return None
    gear = world.add(Entity(
        id=gear_def.id,
        type="gear",
        label=gear_def.label,
        protective=True,
        covers=set(gear_def.covers),
        owner=hero.id,
    ))
    gear.worn_by = hero.id
    if predict_mess(world, hero, activity, prize.id)["soiled"]:
        gear.worn_by = None
        del world.entities[gear.id]
        return None
    world.say(
        f'"How about we {gear_def.prep}?" {parent.pronoun("possessive")} parent said with a grin.'
    )
    return gear_def


