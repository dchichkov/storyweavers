def prize_at_risk(activity: Activity, prize: Prize) -> bool:
    return prize.region in {"head", "hands", "chest"} and activity.id in {"tether_walk", "star_watch"}


def select_gear(activity: Activity, prize: Prize) -> Optional[Gear]:
    for gear in GEAR:
        if prize.region in gear.protects:
            return gear
    return None


def valid_combos() -> list[tuple[str, str, str]]:
    out = []
    for place in PLACES:
        for act_id, act in ACTIVITIES.items():
            for prize_id, pr in PRIZES.items():
                if place in PLACES and act_id in _safe_lookup(PLACES, place).afford and prize_at_risk(act, pr) and select_gear(act, pr):
                    out.append((place, act_id, prize_id))
    return out


