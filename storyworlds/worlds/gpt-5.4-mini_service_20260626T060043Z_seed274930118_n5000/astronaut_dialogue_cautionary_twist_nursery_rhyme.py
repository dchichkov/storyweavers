ASP_RULES = r"""
prize_at_risk(A,P) :- activity(A), prize(P), worn_on(P,R), risky_region(R), active(A).
has_fix(A,P) :- prize_at_risk(A,P), gear(G), protects(G,R), worn_on(P,R), active(A).
valid(Place,A,P) :- place(Place), affords(Place,A), prize_at_risk(A,P), has_fix(A,P).
"""


def asp_facts() -> str:
    import asp
    lines = []
    for pid, place in PLACES.items():
        lines.append(asp.fact("place", pid))
        for a in sorted(place.afford):
            lines.append(asp.fact("affords", pid, a))
    for aid in ACTIVITIES:
        lines.append(asp.fact("activity", aid))
        lines.append(asp.fact("active", aid))
    for rid, pr in PRIZES.items():
        lines.append(asp.fact("prize", rid))
        lines.append(asp.fact("worn_on", rid, pr.region))
    for region in {"head", "hands", "chest"}:
        lines.append(asp.fact("risky_region", region))
    for gear in GEAR:
        lines.append(asp.fact("gear", gear.id))
        for reg in sorted(gear.protects):
            lines.append(asp.fact("protects", gear.id, reg))
    return "\n".join(lines)


def asp_program(show: str) -> str:
    return f"{asp_facts()}\n{ASP_RULES}\n{show}\n"


def asp_valid_combos() -> list[tuple]:
    import asp
    model = asp.one_model(asp_program("#show valid/3."))
    return sorted(set(asp.atoms(model, "valid")))


def asp_verify() -> int:
    py = set(valid_combos())
    cl = set(asp_valid_combos())
    if py == cl:
        print(f"OK: clingo gate matches valid_combos() ({len(py)} combos).")
        return 0
    print("MISMATCH:")
    print("only python:", sorted(py - cl))
    print("only clingo:", sorted(cl - py))
    return 1


