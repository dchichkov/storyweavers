def build_story(params: StoryParams) -> StorySample:
    world = World(_safe_lookup(PLACES, params.place))
    hero = world.add(Entity(id=params.name, kind="character", type="boy"))
    captain = world.add(Entity(id="captain", kind="character", type="captain", label="the captain"))
    prize = world.add(Entity(
        id=params.prize,
        type=_safe_lookup(PRIZES, params.prize).type,
        label=_safe_lookup(PRIZES, params.prize).label,
        phrase=_safe_lookup(PRIZES, params.prize).phrase,
        caretaker=captain.id,
        owner=hero.id,
        plural=_safe_lookup(PRIZES, params.prize).plural,
    ))

    act = _safe_lookup(ACTIVITIES, params.activity)
    gear = select_gear(act, _safe_lookup(PRIZES, params.prize))

    hero.meters["hope"] = 1
    hero.memes["wonder"] = 1
    world.say(f"{hero.id} was a small astronaut with a moon-bright grin.")
    world.say(f"{hero.id} loved {act.gerund} beneath {world.place.sky} skies.")
    world.say(f"One day, {hero.id}'s {prize.label} shone like a song, and {hero.id} wore {prize.it()} with pride.")

    world.say(f"Then {hero.id} and {hero.pronoun('possessive')} captain went to {world.place.name}.")
    world.say(f'"{act.caution}," said {captain.label or "the captain"}.')
    world.say(f'"{act.verb}?" asked {hero.id}. "I can be careful!"')

    hero.memes["desire"] += 1
    hero.memes["doubt"] += 1
    world.say(f"{hero.id} took one tiny step and looked at the glittering line.")
    world.say(f"Still, {hero.id} tried to {act.rush}, and the moon air went hush-hush hush.")

    # cautionary tension
    if act.id == "tether_walk":
        hero.meters["drift"] += 1
        hero.memes["worry"] += 1
        world.say(f"\"The tether keeps you near,\" said the captain. \"A loose step can send you far.\"")
    else:
        hero.meters["glare"] += 1
        world.say(f"\"A bright thing can trick your hands,\" said the captain. \"Watch before you leap.\"")

    # twist
    hero.memes["surprise"] = 1
    world.say(f"Then came a twist in the starry mist: {act.twist}.")
    world.say(f"{hero.id} blinked. \"That isn't a star!\" {hero.id} said. \"It helps us find home.\"")

    if gear:
        world.say(f'"{gear.prep}," said the captain, and they smiled in the silver light.')
        world.say(f"{gear.ending}.")
        hero.meters["safe"] = 1
        hero.memes["joy"] += 1
    world.say(f"So {hero.id} sang a soft little tune, and back they went, safe and snug, beneath the moon.")
    world.facts.update(hero=hero, captain=captain, prize=prize, activity=act, gear=gear, place=world.place.name)
    return StorySample(
        params=params,
        story=world.render(),
        prompts=generation_prompts(world),
        story_qa=story_qa(world),
        world_qa=world_knowledge_qa(world),
        world=world,
    )


def generation_prompts(world: World) -> list[str]:
    f = world.facts
    hero = _safe_fact(world, f, "hero")
    act = _safe_fact(world, f, "activity")
    return [
        f"Write a short nursery-rhyme story about an astronaut named {hero.id} who wants to {act.verb}.",
        f"Tell a gentle space story with dialogue, a caution, and a twist about {hero.id} and a bright moon path.",
        f"Make a child-friendly rhyme where an astronaut listens to a warning and learns that a shining thing is not what it seemed.",
    ]


def story_qa(world: World) -> list[QAItem]:
    f = world.facts
    hero = _safe_fact(world, f, "hero")
    act = _safe_fact(world, f, "activity")
    prize = _safe_fact(world, f, "prize")
    qa = [
        QAItem(
            question=f"Who is the story about?",
            answer=f"The story is about {hero.id}, a small astronaut who loved {act.gerund}.",
        ),
        QAItem(
            question=f"What warning did the captain give before {hero.id} tried to {act.verb}?",
            answer=f"The captain said, \"{act.caution}\" so {hero.id} would stay safe.",
        ),
        QAItem(
            question=f"What was the twist in the story?",
            answer=f"The twist was that the shining thing was not a star at all; it was a rescue beacon that could guide them home.",
        ),
        QAItem(
            question=f"How did the story end for {hero.id} and the {prize.label}?",
            answer=f"They returned safely, and {hero.id} kept {prize.it()} bright and clean while singing a soft space tune.",
        ),
    ]
    return qa


def world_knowledge_qa(world: World) -> list[QAItem]:
    return [
        QAItem(
            question="What is an astronaut?",
            answer="An astronaut is a person who travels in space and helps explore the moon, stars, and stations far above Earth.",
        ),
        QAItem(
            question="Why do astronauts use a tether?",
            answer="A tether helps keep an astronaut from floating away when they step outside in space.",
        ),
        QAItem(
            question="What is a rescue beacon?",
            answer="A rescue beacon is a light or signal that helps people find the right way and stay safe.",
        ),
    ]


def format_qa(sample: StorySample) -> str:
    lines = ["== prompts =="]
    lines.extend(sample.prompts)
    lines.append("")
    lines.append("== story qa ==")
    for item in sample.story_qa:
        lines.append(f"Q: {item.question}")
        lines.append(f"A: {item.answer}")
    lines.append("")
    lines.append("== world qa ==")
    for item in sample.world_qa:
        lines.append(f"Q: {item.question}")
        lines.append(f"A: {item.answer}")
    return "\n".join(lines)


def dump_trace(world: World) -> str:
    parts = ["--- trace ---"]
    for e in list(world.entities.values()):
        parts.append(f"{e.id}: type={e.type} meters={e.meters} memes={e.memes}")
    return "\n".join(parts)


