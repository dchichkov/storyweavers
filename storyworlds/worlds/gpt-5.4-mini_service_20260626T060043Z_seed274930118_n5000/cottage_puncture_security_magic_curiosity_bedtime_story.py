def _r_puncture(world: World) -> list[str]:
    out: list[str] = []
    for actor in world.characters():
        if actor.meters.get("poke", 0.0) < COZY_THRESHOLD:
            continue
        for item in list(world.entities.values()):
            if item.owner != actor.id:
                continue
            if item.protective:
                continue
            sig = ("puncture", actor.id, item.id)
            if sig in world.fired:
                continue
            world.fired.add(sig)
            item.meters["puncture"] = item.meters.get("puncture", 0.0) + 1
            item.meters["security"] = max(0.0, item.meters.get("security", 1.0) - 1)
            out.append(f"{actor.pronoun('possessive').capitalize()} {item.label} got a tiny puncture.")
    return out


def _r_worry(world: World) -> list[str]:
    out: list[str] = []
    for item in list(world.entities.values()):
        if item.meters.get("puncture", 0.0) < COZY_THRESHOLD:
            continue
        if not item.caretaker:
            continue
        sig = ("worry", item.id)
        if sig in world.fired:
            continue
        world.fired.add(sig)
        carer = world.get(item.caretaker)
        carer.memes["worry"] = carer.memes.get("worry", 0.0) + 1
        out.append(f"That made {carer.label} worry about the cottage's security.")
    return out


CAUSAL_RULES = [Rule("puncture", _r_puncture), Rule("worry", _r_worry)]


def propagate(world: World, narrate: bool = True) -> list[str]:
    produced: list[str] = []
    changed = True
    for _ in range(len(globals().get("CAUSAL_RULES", [])) + 4):
        changed = False
        for rule in CAUSAL_RULES:
            sents = rule.apply(world)
            if sents:
                changed = True
                produced.extend(sents)
    if narrate:
        for s in produced:
            world.say(s)
    return produced


def tell(world_setting: Setting, activity: Activity, prize_cfg: Prize,
         hero_name: str = "Nia", hero_type: str = "girl",
         parent_type: str = "mother") -> World:
    world = World(world_setting)
    child = world.add(Entity(
        id=hero_name, kind="character", type=hero_type, label=hero_name,
        meters={"poke": 0.0}, memes={"curiosity": 1.0, "sleepiness": 0.0, "joy": 0.0},
    ))
    parent = world.add(Entity(
        id="Parent", kind="character", type=parent_type, label="Mom",
        memes={"calm": 1.0, "worry": 0.0},
    ))
    prize = world.add(Entity(
        id="lantern", type=prize_cfg.type, label=prize_cfg.label, phrase=prize_cfg.phrase,
        owner=child.id, caretaker=parent.id, region=prize_cfg.region, plural=prize_cfg.plural,
        meters={"puncture": 0.0, "security": 1.0},
    ))

    world.say(f"In the warm cottage, {child.id} was a little curious {hero_type} who loved bedtime shadows and moonbeams.")
    world.say(f"By the window, {child.id} noticed {child.pronoun('possessive')} {prize.label} and wished to {activity.verb}.")

    world.para()
    world.say(f"{child.id} wanted to {activity.verb}, but {parent.label} gently lifted a hand.")
    world.say(f'"That could {activity.soil}," {parent.pronoun("subject")} whispered, "and I want our cottage to stay safe and snug."')

    child.meters["poke"] += 1
    child.memes["curiosity"] += 1
    propagate(world, narrate=True)

    world.para()
    world.say(f"{child.id} paused, then looked at the little hole with round, sleepy eyes.")
    world.say(f'"Can magic help?" {child.pronoun("subject")} asked.')
    patch = world.add(Entity(
        id="PatchCharm", type="charm", label="a tiny moon patch", protective=True,
        covers={"torso"}, meters={"security": 1.0},
    ))
    patch.worn_by = child.id
    prize.meters["puncture"] = 0.0
    prize.meters["security"] = 2.0
    child.memes["joy"] += 1
    child.memes["curiosity"] += 0.5
    parent.memes["calm"] += 1
    world.say(f"With a little magic, they placed {patch.label} over the puncture, and the lantern shone again.")
    world.say(f"Now the cottage felt secure, the moonlight stayed soft, and {child.id}'s curiosity turned into a cozy smile.")

    world.facts.update(
        hero=child,
        parent=parent,
        prize=prize,
        activity=activity,
        setting=world_setting,
        patch=patch,
        resolved=True,
        puncture=True,
    )
    return world


