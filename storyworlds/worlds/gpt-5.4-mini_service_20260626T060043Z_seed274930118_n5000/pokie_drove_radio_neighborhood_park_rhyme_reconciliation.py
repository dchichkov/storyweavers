def rhyme_line(activity: Activity) -> str:
    return f"{activity.rhyme} kept the day in tune."


def predict_break(world: World, actor: Entity, activity: Activity, prize_id: str) -> bool:
    sim = world.copy()
    _do_activity(sim, sim.get(actor.id), activity, narrate=False)
    prize = sim.get(prize_id)
    return bool(prize.meters.get("shaky", 0.0) >= THRESHOLD)


def _do_activity(world: World, actor: Entity, activity: Activity, narrate: bool = True) -> None:
    actor.meters[activity.mess] = actor.meters.get(activity.mess, 0.0) + 1.0
    if narrate:
        world.say(f"{actor.id} loved to {activity.verb}, and {rhyme_line(activity)}")


def settle_jostle(world: World, prize: Entity) -> None:
    prize.meters["shaky"] = prize.meters.get("shaky", 0.0) + 1.0
    if prize.meters["shaky"] >= THRESHOLD:
        world.say(f"The {prize.label} wobbled and went quiet.")


def reconcile(world: World, hero: Entity, friend: Entity, prize: Entity, helper: Helper) -> None:
    hero.memes["sorry"] = hero.memes.get("sorry", 0.0) + 1.0
    friend.memes["forgiving"] = friend.memes.get("forgiving", 0.0) + 1.0
    hero.memes["joy"] = hero.memes.get("joy", 0.0) + 1.0
    friend.memes["joy"] = friend.memes.get("joy", 0.0) + 1.0
    world.say(
        f"{hero.id} and {friend.id} shared a grin and a kind little truce. "
        f"They used {helper.label} to {helper.fix}, and the {prize.label} hummed again."
    )
    world.say(
        f"Then {hero.id} and {friend.id} listened side by side as the rhyme came back around."
    )


def tell(world: World, hero_name: str, friend_name: str) -> World:
    hero = world.add(Entity(id=hero_name, kind="character", type="child"))
    friend = world.add(Entity(id=friend_name, kind="character", type="child"))
    prize = world.add(Entity(
        id="radio",
        kind="thing",
        type="radio",
        label="radio",
        plural=False,
        owner=hero.id,
        carried_by=hero.id,
    ))
    helper = world.add(Entity(
        id="tape",
        kind="thing",
        type="helper",
        label="tape",
        plural=False,
    ))

    world.say(
        f"Pokie was a little bright-eyed child who loved a radio that sang in a row."
    )
    world.say(
        f"On a sunny day at the neighborhood park, Pokie drove a little cart slow."
    )
    world.say(
        f"The radio played a rhyme that went, “Drive and jive, and keep hearts alive.”"
    )

    world.para()
    world.say(
        f"Pokie wanted to {ACTIVITIES['ride'].verb} through {world.setting.place}, "
        f"for the tune felt so neat."
    )
    if predict_break(world, hero, ACTIVITIES["ride"], prize.id):
        world.say(
            f"But the path had a bump, and Pokie knew the cart might give the radio a shake."
        )
    _do_activity(world, hero, ACTIVITIES["ride"])
    settle_jostle(world, prize)

    world.para()
    world.say(
        f"Then the rhyme stopped short, and Pokie frowned at the quiet little break."
    )
    world.say(
        f"Pokie and Mara looked at the cart, then at the radio, and both felt the same small ache."
    )
    reconcile(world, hero, friend, prize, helper)

    world.para()
    world.say(
        f"So Pokie drove more gently, Mara kept time, and the radio sang its merry refrain."
    )
    world.say(
        f"In the neighborhood park, the pals rolled on, all smiles, in sunshine and rhyme again."
    )

    world.facts.update(hero=hero, friend=friend, prize=prize, helper=helper, activity=ACTIVITIES["ride"])
    return world


