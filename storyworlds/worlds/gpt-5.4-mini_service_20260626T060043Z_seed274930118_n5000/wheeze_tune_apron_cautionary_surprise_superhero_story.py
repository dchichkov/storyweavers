def _r_soil(world: World) -> list[str]:
    out: list[str] = []
    for actor in world.characters():
        for mess in ["mess", "smoke", "damage", "noise"]:
            if actor.meters[mess] < THRESHOLD:
                continue
            for item in world.worn_items(actor):
                if item.protective:
                    continue
                if item.region not in world.zone:
                    continue
                if world.covered(actor, item.region):
                    continue
                sig = ("soil", actor.id, item.id, mess)
                if sig in world.fired:
                    continue
                world.fired.add(sig)
                item.meters[mess] += 1
                item.meters["mess"] += 1
                out.append(f"{actor.pronoun('possessive').capitalize()} {item.label} got {item.label and 'messy'} from the work.")
    return out


def _r_worry(world: World) -> list[str]:
    out: list[str] = []
    for ent in world.characters():
        if ent.memes["worry"] >= THRESHOLD and ("worry", ent.id) not in world.fired:
            world.fired.add(("worry", ent.id))
            out.append(f"{ent.id} looked worried for a moment.")
    return out


CAUSAL_RULES: list[Rule] = [
    Rule("soil", "physical", _r_soil),
    Rule("worry", "social", _r_worry),
]


def propagate(world: World, narrate: bool = True) -> list[str]:
    produced: list[str] = []
    changed = True
    for _ in range(len(globals().get("CAUSAL_RULES", [])) + 4):
        changed = False
        for rule in CAUSAL_RULES:
            sents = rule.apply(world)
            if sents:
                changed = True
                produced.extend(sents)
    if narrate:
        for s in produced:
            world.say(s)
    return produced


def prize_at_risk(activity: Activity, prize: Prize) -> bool:
    return prize.region in activity.zone


def select_gear(activity: Activity, prize: Prize) -> Optional[Gear]:
    for gear in GEAR:
        if activity.mess in gear.guards and prize.region in gear.covers:
            return gear
    return None


def predict_mess(world: World, actor: Entity, activity: Activity, prize_id: str) -> dict:
    sim = world.copy()
    _do_activity(sim, sim.get(actor.id), activity, narrate=False)
    prize = sim.entities.get(prize_id)
    return {
        "soiled": bool(prize and prize.meters["mess"] >= THRESHOLD),
        "smoke": sum(e.meters["smoke"] for e in sim.characters()),
    }


def _do_activity(world: World, actor: Entity, activity: Activity, narrate: bool = True) -> None:
    if activity.id not in world.setting.affords:
        return
    world.zone = set(activity.zone)
    actor.meters[activity.mess] += 1
    actor.meters["smoke"] += 1 if activity.mess == "smoke" else 0
    actor.memes["courage"] += 1
    propagate(world, narrate=narrate)


