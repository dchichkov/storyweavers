def generation_prompts(world: World) -> list[str]:
    f = world.facts
    hero, mentor, act, prize = f["hero"], f["mentor"], f["activity"], f["prize_cfg"]
    return [
        f'Write a short superhero story for a child that includes the words "wheeze", "tune", and "apron".',
        f"Tell a cautionary superhero story where {hero.id} wants to {act.verb}, but {mentor.label} worries about {prize.phrase}.",
        f"Write a surprise-filled story where an apron helps a young hero stay safe during a smoky or noisy rescue.",
    ]


def story_qa(world: World) -> list[QAItem]:
    f = world.facts
    hero, mentor, prize, act = f["hero"], f["mentor"], f["prize"], f["activity"]
    trait = next((t for t in hero.traits if t != "little"), hero.type)
    qa: list[QAItem] = [
        QAItem(
            question=f"Who is the story about?",
            answer=f"It is about a little {trait} {hero.type} named {hero.id}, and {mentor.label} helps keep the mission safe.",
        ),
        QAItem(
            question=f"What did {hero.id} want to do at {world.setting.place}?",
            answer=f"{hero.id} wanted to {act.verb}, even though the smoke and noise made the plan tricky.",
        ),
        QAItem(
            question=f"Why did {mentor.label} warn {hero.id}?",
            answer=f"{mentor.label} warned {hero.id} because {prize.phrase} could get {act.soil}, and the smoke might make {hero.pronoun('object')} wheeze.",
        ),
    ]
    if f.get("resolved"):
        gear = _safe_fact(world, f, "gear")
        qa.append(
            QAItem(
                question=f"How did the apron help?",
                answer=f"The apron covered {hero.pronoun('possessive')} torso, so {hero.id} could {act.verb} without ruining {hero.pronoun('possessive')} {prize.label}.",
            )
        )
        qa.append(
            QAItem(
                question=f"What changed by the end?",
                answer=f"By the end, {hero.id} felt happier and safer, and the little hero was {act.gerund} with the apron on.",
            )
        )
    return qa


def world_knowledge_qa(world: World) -> list[QAItem]:
    tags = set(world.facts["activity"].tags)
    if world.facts.get("gear"):
        tags.add(world.facts["gear"].id)
    out: list[QAItem] = []
    for tag in globals().get("KNOWLEDGE_ORDER", sorted(globals().get("KNOWLEDGE", []))):
        if tag in tags:
            out.extend(QAItem(question=q, answer=a) for q, a in KNOWLEDGE[tag])
    return out


def format_qa(sample: StorySample) -> str:
    lines = ["== (1) Generation prompts -- asks that would produce this story =="]
    for i, p in enumerate(sample.prompts, 1):
        lines.append(f"{i}. {p}")
    lines.append("")
    lines.append("== (2) Story questions -- answerable from the story text ==")
    for item in sample.story_qa:
        lines.append(f"Q: {item.question}")
        lines.append(f"A: {item.answer}")
    lines.append("")
    lines.append("== (3) World-knowledge questions -- child level, no story needed ==")
    for item in sample.world_qa:
        lines.append(f"Q: {item.question}")
        lines.append(f"A: {item.answer}")
    return "\n".join(lines)


def dump_trace(world: World) -> str:
    lines = ["--- world model state ---"]
    for e in list(world.entities.values()):
        meters = {k: v for k, v in e.meters.items() if v}
        memes = {k: v for k, v in e.memes.items() if v}
        bits = []
        if meters:
            bits.append(f"meters={dict(meters)}")
        if memes:
            bits.append(f"memes={dict(memes)}")
        if e.protective:
            bits.append(f"covers={sorted(e.covers)}")
        elif e.region:
            bits.append(f"region={e.region}")
        lines.append(f"  {e.id:8} ({e.type:7}) {' '.join(bits)}")
    lines.append(f"  fired rules: {sorted(n for n, *_ in world.fired)}")
    return "\n".join(lines)


