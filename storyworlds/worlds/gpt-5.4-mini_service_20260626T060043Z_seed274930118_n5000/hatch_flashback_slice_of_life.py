def _r_stuck(world: World) -> list[str]:
    out: list[str] = []
    for actor in world.characters():
        if actor.meters["stuck"] < THRESHOLD:
            continue
        sig = ("worry", actor.id)
        if sig in world.fired:
            continue
        world.fired.add(sig)
        actor.memes["worry"] += 1
        out.append(f"{actor.id} frowned, because the hatch felt stuck.")
    return out


def _r_dust(world: World) -> list[str]:
    out: list[str] = []
    hatch = world.entities.get("hatch")
    if not hatch or hatch.meters["dust"] < THRESHOLD:
        return out
    for actor in world.characters():
        sig = ("dust", actor.id)
        if sig in world.fired:
            continue
        world.fired.add(sig)
        actor.memes["worry"] += 0.5
        out.append("A little dust puffed up around the hatch.")
    return out


CAUSAL_RULES = [Rule("stuck", _r_stuck), Rule("dust", _r_dust)]


def propagate(world: World, narrate: bool = True) -> list[str]:
    produced: list[str] = []
    changed = True
    for _ in range(len(globals().get("CAUSAL_RULES", [])) + 4):
        changed = False
        for rule in CAUSAL_RULES:
            sents = rule.apply(world)
            if sents:
                changed = True
                produced.extend(sents)
    if narrate:
        for s in produced:
            world.say(s)
    return produced


def setting_detail(setting: Setting) -> str:
    return f"{setting.place.capitalize()} was quiet in the soft home light."


def predict_opening(world: World, actor: Entity, activity: Activity, prize_id: str) -> dict:
    sim = world.copy()
    _do_activity(sim, sim.get(actor.id), activity, narrate=False)
    prize = sim.get(prize_id)
    return {
        "dusty": bool(prize.meters["dust"] >= THRESHOLD),
        "stuck": bool(prize.meters["stuck"] >= THRESHOLD),
    }


def _do_activity(world: World, actor: Entity, activity: Activity, narrate: bool = True) -> None:
    if activity.id not in world.setting.affords:
        return
    actor.meters[activity.mess] += 1
    actor.memes["curiosity"] += 1
    world.entities["hatch"].meters["stuck"] += 1
    world.entities["hatch"].meters["dust"] += 1
    propagate(world, narrate=narrate)


def introduce(world: World, hero: Entity) -> None:
    trait = next((t for t in hero.traits if t != "little"), "")
    world.say(f"{hero.id} was a little {trait} {hero.type} who noticed small things at home.")


def flashback(world: World, hero: Entity, hatch: Entity) -> None:
    hero.memes["memory"] += 1
    world.say(
        f"That made {hero.id} think back to a day long ago, when {hero.id} had "
        f"watched {hatch.label} close with a soft click after a game."
    )
    world.say(
        f"In the flashback, {hero.id} had tucked a tiny keepsake nearby so it would stay safe."
    )


def wants(world: World, hero: Entity, parent: Entity, activity: Activity, prize: Entity) -> None:
    hero.memes["curiosity"] += 1
    world.say(
        f"{hero.id} wanted to {activity.verb} and see {prize.phrase} again, "
        f"but {hero.pronoun('possessive')} {parent.type} looked worried about the old hatch."
    )


def warns(world: World, parent: Entity, hero: Entity, activity: Activity, prize: Entity) -> bool:
    pred = predict_opening(world, hero, activity, prize.id)
    if not pred["dusty"]:
        return False
    world.facts["predicted_dust"] = True
    world.say(
        f'"If we rush it, {prize.label} could get dusty," {hero.pronoun("possessive")} '
        f"{parent.type} said. \"Let's open it gently.\""
    )
    return True


def defies(world: World, hero: Entity, activity: Activity) -> None:
    hero.memes["worry"] += 0.25
    world.say(f"{hero.id} almost reached for the hatch anyway, then paused.")
    world.say(f"{hero.pronoun().capitalize()} tried to {activity.rush},")


def offer_help(world: World, parent: Entity, hero: Entity, activity: Activity, prize: Entity) -> Optional[Gear]:
    gear = None
    if activity.id == "open_hatch":
        gear = world.add(Entity(
            id="cloth",
            type="thing",
            label="a soft cloth",
            protective=True,
            covers={"hands"},
        ))
        gear.worn_by = hero.id
    if gear is None:
        return None
    world.say(
        f"Then {hero.pronoun('possessive')} {parent.type} found {gear.label} and said, "
        f"\"We can use this to lift the edge and keep our fingers safe.\""
    )
    return Gear(
        id="cloth",
        label="a soft cloth",
        covers={"hands"},
        guards={"dust"},
        prep="use a soft cloth to lift the edge",
        tail="used the soft cloth to lift the edge together",
    )


def accept(world: World, parent: Entity, hero: Entity, activity: Activity, prize: Entity, gear: Gear) -> None:
    hero.memes["joy"] += 1
    hero.memes["comfort"] += 1
    hero.memes["worry"] = 0.0
    world.say(
        f"{hero.id} nodded and held the cloth while {hero.pronoun('possessive')} {parent.type} "
        f"opened the hatch slowly."
    )
    world.say(
        f"They {gear.tail}. Inside, {prize.phrase} was right where the flashback said it would be, "
        f"and it stayed clean."
    )


def tell(setting: Setting, activity: Activity, prize_cfg: Prize, hero_name: str = "Mina",
         hero_type: str = "girl", parent_type: str = "mother", trait: str = "curious") -> World:
    world = World(setting)
    hero = world.add(Entity(id=hero_name, kind="character", type=hero_type, traits=["little", trait, "careful"]))
    parent = world.add(Entity(id="Parent", kind="character", type=parent_type, label="parent"))
    hatch = world.add(Entity(id="hatch", type="thing", label="the hatch"))
    prize = world.add(Entity(
        id="prize",
        type=prize_cfg.type,
        label=prize_cfg.label,
        phrase=prize_cfg.phrase,
        owner=hero.id,
        caretaker=parent.id,
        region=prize_cfg.region,
        plural=prize_cfg.plural,
    ))

    introduce(world, hero)
    world.say(setting_detail(setting))
    flashback(world, hero, hatch)
    wants(world, hero, parent, activity, prize)

    world.para()
    warns(world, parent, hero, activity, prize)
    defies(world, hero, activity)

    world.para()
    gear = offer_help(world, parent, hero, activity, prize)
    if gear:
        accept(world, parent, hero, activity, prize, gear)

    world.facts.update(
        hero=hero,
        parent=parent,
        hatch=hatch,
        prize=prize,
        activity=activity,
        setting=setting,
        gear=gear,
    )
    return world


