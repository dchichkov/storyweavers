    def __post_init__(self) -> None:
        for k in ["dust", "mud", "wrinkle", "workload"]:
            self.meters.setdefault(k, 0.0)
        for k in ["joy", "pride", "worry", "amusement", "surprise", "confidence"]:
            self.memes.setdefault(k, 0.0)

