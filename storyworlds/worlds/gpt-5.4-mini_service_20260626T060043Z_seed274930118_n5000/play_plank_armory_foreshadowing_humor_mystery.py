def _r_plank_shift(world: World) -> list[str]:
    out: list[str] = []
    plank = world.entities.get("plank")
    if not plank:
        return out
    if plank.meters["shift"] < THRESHOLD:
        return out
    sig = ("shift",)
    if sig in world.fired:
        return out
    world.fired.add(sig)
    plank.meters["wobble"] += 1
    out.append("The plank gave a tiny wobble, as if it knew a secret.")
    return out


def _r_reveal(world: World) -> list[str]:
    out: list[str] = []
    plank = world.entities.get("plank")
    treasure = world.entities.get("key")
    if not plank or not treasure:
        return out
    if plank.meters["shift"] < THRESHOLD or treasure.meters["hidden"] < THRESHOLD:
        return out
    sig = ("reveal",)
    if sig in world.fired:
        return out
    world.fired.add(sig)
    treasure.meters["found"] += 1
    out.append("Something shiny clicked under the plank.")
    return out


def _r_humor(world: World) -> list[str]:
    out: list[str] = []
    toy_helm = world.entities.get("helmet")
    if not toy_helm:
        return out
    if toy_helm.meters["askew"] < THRESHOLD:
        return out
    sig = ("humor",)
    if sig in world.fired:
        return out
    world.fired.add(sig)
    out.append("The little helmet sat sideways, looking very serious about being silly.")
    return out


CAUSAL_RULES: list[Rule] = [
    Rule("plank_shift", "physical", _r_plank_shift),
    Rule("reveal", "mystery", _r_reveal),
    Rule("humor", "humor", _r_humor),
]


def propagate(world: World, narrate: bool = True) -> list[str]:
    produced: list[str] = []
    changed = True
    for _ in range(len(globals().get("CAUSAL_RULES", [])) + 4):
        changed = False
        for rule in CAUSAL_RULES:
            sents = rule.apply(world)
            if sents:
                changed = True
                produced.extend(sents)
    if narrate:
        for s in produced:
            world.say(s)
    return produced


def predict_mystery(world: World, actor: Entity, activity: Activity, prize_id: str) -> dict:
    sim = world.copy()
    _do_activity(sim, sim.get(actor.id), activity, narrate=False)
    prize = sim.entities.get(prize_id)
    key = sim.entities.get("key")
    return {
        "revealed": bool(key and key.meters["found"] >= THRESHOLD),
        "messy": bool(prize and prize.meters["mess"] >= THRESHOLD),
    }


def _do_activity(world: World, actor: Entity, activity: Activity, narrate: bool = True) -> None:
    if activity.id not in world.setting.affords:
        return
    world.zone = set(activity.zone)
    actor.meters[activity.mess] += 1
    actor.meters["play"] += 1
    propagate(world, narrate=narrate)


def intro(world: World, hero: Entity) -> None:
    trait = next((t for t in hero.traits if t != "little"), "curious")
    world.say(f"{hero.id} was a little {trait} {hero.type} who loved clues almost as much as play.")


def set_scene(world: World, hero: Entity, helper: Entity) -> None:
    place = world.setting.place
    world.say(f"On a quiet afternoon, {hero.id} and {hero.pronoun('possessive')} {helper.label_word} were in {place}.")
    world.say("Near the wall stood a toy armory with shiny helmets, cardboard shields, and one curious wooden plank.")


def want_play(world: World, hero: Entity, activity: Activity, plank: Entity) -> None:
    hero.meters["curiosity"] += 1
    world.say(f"{hero.id} wanted to {activity.verb}, especially with the plank nearby.")
    if plank.meters["hint"] >= THRESHOLD:
        world.say("The plank looked ordinary, but it seemed to be pointing at something for later.")


def hint_clue(world: World, helper: Entity, prize: Entity, activity: Activity) -> None:
    world.say(f"{helper.label_word} noticed a tiny clue: the plank made a soft thump right under {prize.label}.")
    world.say(f'"That is funny," {helper.label_word} said. "A plank should not act like a detective."')


def warn(world: World, helper: Entity, hero: Entity, activity: Activity, prize: Entity) -> bool:
    pred = predict_mystery(world, hero, activity, prize.id)
    if not pred["revealed"] and not pred["messy"]:
        return False
    if pred["revealed"]:
        world.facts["mystery_hint"] = True
    world.say(f'"Careful," {helper.label_word} said. "That plank may be hiding a surprise."')
    return True


def laugh_turn(world: World, hero: Entity, helper: Entity, activity: Activity) -> None:
    world.say(f"{hero.id} gave the plank a gentle push, expecting a big dramatic secret.")
    world.say(f"Instead, it only gave a silly little squeak, which made both of them laugh.")


def reveal(world: World, hero: Entity, helper: Entity, prize: Entity) -> None:
    key = world.entities["key"]
    if key.meters["found"] < THRESHOLD:
        return
    hero.meters["joy"] += 1
    helper.meters["relief"] += 1
    world.say(f"Under the plank was a tiny brass key from the armory chest.")
    world.say(f"{helper.label_word} picked it up and smiled. The mystery was simple after all: the key had rolled away during play.")
    world.say(f"{hero.id} held the key up like a treasure, and the whole armory suddenly seemed funny instead of spooky.")
    world.say(f"In the end, the {prize.label} stayed safe, the plank stayed ordinary, and the lost key was found exactly where the clue had pointed.")


def tell(setting: Setting, activity: Activity, prize_cfg: Prize, hero_name: str = "Milo",
         hero_type: str = "boy", parent_type: str = "father", hero_traits: Optional[list[str]] = None) -> World:
    world = World(setting)
    hero = world.add(Entity(id=hero_name, kind="character", type=hero_type, traits=["little"] + (hero_traits or ["curious", "brave"])))
    helper = world.add(Entity(id="Helper", kind="character", type=parent_type, label="the parent"))
    prize = world.add(Entity(id="prize", type=prize_cfg.type, label=prize_cfg.label, phrase=prize_cfg.phrase, owner=hero.id, caretaker=helper.id, region=prize_cfg.region, plural=prize_cfg.plural))
    plank = world.add(Entity(id="plank", type="plank", label="plank", phrase="a smooth wooden plank"))
    key = world.add(Entity(id="key", type="key", label="key", phrase="a tiny brass key"))
    helmet = world.add(Entity(id="helmet", type="helmet", label="helmet", phrase="a toy helmet"))

    key.meters["hidden"] += 1
    plank.meters["hint"] += 1
    helmet.meters["askew"] += 1

    intro(world, hero)
    set_scene(world, hero, helper)
    world.para()
    want_play(world, hero, activity, plank)
    hint_clue(world, helper, prize, activity)
    warn(world, helper, hero, activity, prize)
    laugh_turn(world, hero, helper, activity)
    _do_activity(world, hero, activity, narrate=True)
    reveal(world, hero, helper, prize)

    world.facts.update(hero=hero, helper=helper, prize=prize, activity=activity, setting=setting)
    return world


