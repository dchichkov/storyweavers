class World:
    def __init__(self, setting: Setting) -> None:
        self.setting = setting
        self.entities: dict[str, Entity] = {}
        self.paragraphs: list[list[str]] = [[]]
        self.fired: set[tuple] = set()
        self.facts: dict = {}

    def add(self, ent: Entity) -> Entity:
        self.entities[ent.id] = ent
        return ent

    def get(self, eid: str) -> Entity:
        if eid not in self.entities:
            label = str(eid).replace("_", " ")
            self.entities[eid] = Entity(str(eid), label=label)
        return self.entities[eid]

    def characters(self) -> list[Entity]:
        return [e for e in self.entities.values() if e.kind == "character"]

    def say(self, text: str) -> None:
        if text:
            self.paragraphs[-1].append(text)

    def para(self) -> None:
        if self.paragraphs[-1]:
            self.paragraphs.append([])

    def render(self) -> str:
        return "\n\n".join(" ".join(p) for p in self.paragraphs if p)

    def copy(self) -> "World":
        import copy as _copy
        w = World(self.setting)
        w.entities = _copy.deepcopy(self.entities)
        w.fired = set(self.fired)
        w.paragraphs = [[]]
        return w


THRESHOLD = 1.0


def _r_too_little_time(world: World) -> list[str]:
    out: list[str] = []
    for hero in world.characters():
        if hero.meters.get("time_spent", 0.0) < THRESHOLD:
            continue
        if hero.memes.get("care", 0.0) < THRESHOLD:
            continue
        sick = world.entities.get("Sicko")
        if not sick:
            continue
        if sick.memes.get("lonely", 0.0) < THRESHOLD:
            continue
        sig = ("improve", hero.id)
        if sig in world.fired:
            continue
        world.fired.add(sig)
        hero.memes["hope"] = hero.memes.get("hope", 0.0) + 1
        sick.memes["cheer"] = sick.memes.get("cheer", 0.0) + 1
        out.append("The little sharing made the whole ship feel kinder.")
    return out


CAUSAL_RULES = [("improve", _r_too_little_time)]


def propagate(world: World, narrate: bool = True) -> list[str]:
    out: list[str] = []
    changed = True
    for _ in range(len(globals().get("CAUSAL_RULES", [])) + 4):
        changed = False
        for _, rule in CAUSAL_RULES:
            sent = rule(world)
            if sent:
                changed = True
                out.extend(sent)
    if narrate:
        for s in out:
            world.say(s)
    return out


def invite_sicko(world: World, crew: Entity, sicko: Entity, activity: Activity) -> None:
    crew.memes["care"] = crew.memes.get("care", 0.0) + 1
    sicko.memes["lonely"] = sicko.memes.get("lonely", 0.0) + 1
    world.say(
        f"On the {world.setting.place}, the crew saw that Sicko was looking pale, "
        f"so they slowed their work and spoke soft and kind."
    )
    world.say(
        f"They wanted to {activity.verb}, but they first shared a little time with "
        f"Sicko, like warm soup shared on a cold deck."
    )


def choose_sharing(world: World, crew: Entity, sicko: Entity, activity: Activity, prize: Entity) -> None:
    crew.meters["time_spent"] = crew.meters.get("time_spent", 0.0) + 1
    sicko.meters["time_spent"] = sicko.meters.get("time_spent", 0.0) + 1
    world.say(
        f"The captain pointed to the {prize.label} and said they could take turns, "
        f"one helping with the {activity.keyword} and one staying with Sicko."
    )
    world.say(
        f"That fair plan let the crew {activity.verb} without forgetting the poor mate."
    )
    propagate(world, narrate=True)


def end_image(world: World, hero: Entity, sicko: Entity, prize: Entity) -> None:
    world.say(
        f"By sunset, the deck was calm again. {hero.id} had {prize.phrase}, "
        f"Sicko was smiling, and the crew had learned that sharing time can help a ship improve."
    )


