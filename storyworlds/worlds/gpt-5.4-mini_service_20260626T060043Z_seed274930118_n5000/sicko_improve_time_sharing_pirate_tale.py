def resolve_params(args: argparse.Namespace, rng: random.Random) -> StoryParams:
    combos = [c for c in valid_combos()
              if (getattr(args, "place", None) is None or c[0] == getattr(args, "place", None))
              and (getattr(args, "activity", None) is None or c[1] == getattr(args, "activity", None))
              and (getattr(args, "prize", None) is None or c[2] == getattr(args, "prize", None))]
    if not combos:
        return _fallback_storyparams(args, rng, StoryParams, globals())
    place, activity, prize = rng.choice(list(combos))
    name = getattr(args, "name", None) or rng.choice(GIRL_NAMES + BOY_NAMES)
    trait = getattr(args, "trait", None) or rng.choice(TRAITS)
    return StoryParams(place=place, activity=activity, prize=prize, name=name, parent=getattr(args, "parent", None), trait=trait)


def tell(params: StoryParams) -> World:
    world = World(_safe_lookup(SETTINGS, params.place))
    hero = world.add(Entity(id=params.name, kind="character", type="pirate"))
    captain = world.add(Entity(id="Captain", kind="character", type=params.parent, label="the captain"))
    sicko = world.add(Entity(id="Sicko", kind="character", type="pirate"))
    prize = world.add(Entity(id="Prize", type=_safe_lookup(PRIZES, params.prize).type, label=_safe_lookup(PRIZES, params.prize).label, phrase=_safe_lookup(PRIZES, params.prize).phrase))
    activity = _safe_lookup(ACTIVITIES, params.activity)

    hero.memes["care"] = 0.0
    sicko.memes["lonely"] = 1.0
    hero.meters["time_spent"] = 0.0

    world.say(
        f"{hero.id} was a {params.trait} pirate aboard {world.setting.place}, and the crew knew Sicko had gone quiet."
    )
    world.say(
        f"They had work to do: {activity.gerund} with the {prize.label} could help the ship improve."
    )
    world.para()
    invite_sicko(world, captain, sicko, activity)
    world.para()
    choose_sharing(world, captain, sicko, activity, prize)
    world.para()
    end_image(world, hero, sicko, prize)
    world.facts.update(hero=hero, captain=captain, sicko=sicko, prize=prize, activity=activity, params=params)
    return world


