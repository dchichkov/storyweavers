def explain_rejection(activity: Activity, prize: Prize) -> str:
    return f"(No story: {activity.gerund} would not honestly threaten {prize.label} here, so there is no useful lesson to tell.)"


def resolve_params(args: argparse.Namespace, rng: random.Random) -> StoryParams:
    if getattr(args, "activity", None) and getattr(args, "prize", None):
        act, pr = _safe_lookup(ACTIVITIES, getattr(args, "activity", None)), _safe_lookup(PRIZES, getattr(args, "prize", None))
        if not (prize_at_risk(act, pr) and select_gear(act, pr)):
            return _fallback_storyparams(args, rng, StoryParams, globals())
    combos = [c for c in valid_combos()
              if (getattr(args, "place", None) is None or c[0] == getattr(args, "place", None))
              and (getattr(args, "activity", None) is None or c[1] == getattr(args, "activity", None))
              and (getattr(args, "prize", None) is None or c[2] == getattr(args, "prize", None))]
    if not combos:
        return _fallback_storyparams(args, rng, StoryParams, globals())
    place, activity, prize_id = rng.choice(list(combos))
    prize = _safe_lookup(PRIZES, prize_id)
    gender = getattr(args, "gender", None) or rng.choice(sorted(prize.genders))
    name = getattr(args, "name", None) or rng.choice(GIRL_NAMES if gender == "girl" else BOY_NAMES)
    parent = getattr(args, "parent", None) or rng.choice(["mother", "father"])
    trait = getattr(args, "trait", None) if hasattr(args, "trait") and getattr(args, "trait", None) else rng.choice(TRAITS)
    return StoryParams(place=place, activity=activity, prize=prize_id, name=name, gender=gender, parent=parent, trait=trait)


def predict_mess(world: World, actor: Entity, activity: Activity, prize_id: str) -> dict:
    sim = world.copy()
    _do_activity(sim, sim.get(actor.id), activity, narrate=False)
    prize = sim.get(prize_id)
    return {"soiled": bool(prize.meters.get("dirty", 0) >= THRESHOLD), "conflict": bool(sim.facts.get("conflict"))}


def _do_activity(world: World, actor: Entity, activity: Activity, narrate: bool = True) -> None:
    if activity.id == "salsa_dance":
        actor.memes["joy"] = actor.memes.get("joy", 0) + 1
        actor.meters["energy"] = actor.meters.get("energy", 0) + 1
        world.zone = {"floor", "table"}
    elif activity.id == "tidy_space":
        actor.meters["energy"] = actor.meters.get("energy", 0) + 1
        world.zone = {"floor"}
    if narrate:
        propagate(world, narrate=True)


def propagate(world: World, narrate: bool = True) -> list[str]:
    produced: list[str] = []
    changed = True
    for _ in range(len(globals().get("CAUSAL_RULES", [])) + 4):
        changed = False
        for rule in CAUSAL_RULES:
            sents = rule(world)
            if sents:
                changed = True
                produced.extend(sents)
    if narrate:
        for s in produced:
            world.say(s)
    return produced


def _r_jostle(world: World) -> list[str]:
    out = []
    hero = world.facts.get("hero")
    if not hero:
        return out
    actor = world.get(hero.id)
    if actor.meters.get("energy", 0) < THRESHOLD:
        return out
    for prize in list(world.entities.values()):
        if prize.type in {"lamp", "juice", "tray"} and prize.meters.get("protected", 0) < THRESHOLD:
            sig = ("jostle", prize.id)
            if sig in world.fired:
                continue
            world.fired.add(sig)
            prize.meters["dirty"] = prize.meters.get("dirty", 0) + 1
            prize.meters["dirty"] += 0
            out.append(f"The fast steps bumped the {prize.label}.")
    return out


def _r_conflict(world: World) -> list[str]:
    hero = world.facts.get("hero")
    if not hero:
        return []
    actor = world.get(hero.id)
    if actor.memes.get("worry", 0) >= THRESHOLD and actor.memes.get("stubborn", 0) >= THRESHOLD:
        if ("conflict", actor.id) in world.fired:
            return []
        world.fired.add(("conflict", actor.id))
        actor.memes["frustrated"] = actor.memes.get("frustrated", 0) + 1
        return ["__conflict__"]
    return []


CAUSAL_RULES = [_r_jostle, _r_conflict]


def tell(setting: Setting, activity: Activity, prize_cfg: Prize, hero_name: str, hero_type: str,
         hero_traits: Optional[list[str]] = None, parent_type: str = "mother") -> World:
    world = World(setting)
    hero = world.add(Entity(id=hero_name, kind="character", type=hero_type, meters={"energy": 0.0}, memes={"sleepy": 1.0}))
    parent = world.add(Entity(id="parent", kind="character", type=parent_type, label="the parent", memes={"calm": 1.0}))
    prize = world.add(Entity(id="prize", type=prize_cfg.type, label=prize_cfg.label, phrase=prize_cfg.phrase, owner=hero.id, caretaker=parent.id, region=prize_cfg.region))
    world.facts.update(hero=hero, parent=parent, prize=prize, activity=activity, setting=setting)

    world.say(f"{hero.id} was a little {next((t for t in (hero_traits or []) if t != 'little'), 'sleepy')} {hero.type} at a slumber party in {setting.place}.")
    world.say(f"{hero.pronoun().capitalize()} loved {activity.gerund}, especially when the room felt cozy and the music had a salsa beat.")
    world.say(f"On the side table waited {prize.phrase}.")

    world.para()
    world.say(f"Later that night, {hero.id} wanted to {activity.verb} right away.")
    world.say(f"{parent.pronoun().capitalize()} pointed at the room and said the fast steps might bump the lamp or spill the juice.")
    hero.memes["worry"] = hero.memes.get("worry", 0) + 1
    hero.memes["stubborn"] = hero.memes.get("stubborn", 0) + 1
    world.say(f"{hero.id} frowned, because {hero.pronoun('possessive')} feet were already twitching with the salsa rhythm.")

    if activity.id == "salsa_dance":
        _do_activity(world, hero, activity, narrate=True)
    world.para()
    gear = select_gear(activity, prize)
    if gear:
        if gear.id == "side_table":
            world.say(f"Then {parent.pronoun('possessive')} idea was simple: {gear.prep}.")
            prize.meters["protected"] = 1.0
            world.say(f"{hero.id} helped, and the room changed at once. The lamp stood safely on the table, and the juice was out of the way.")
        else:
            world.say(f"Then {parent.pronoun('possessive')} idea was simple: {gear.prep}.")
        hero.memes["worry"] = 0.0
        hero.memes["relief"] = hero.memes.get("relief", 0) + 1
        hero.memes["joy"] = hero.memes.get("joy", 0) + 1
        world.say(f"{hero.id} took one careful breath, and the sleepy child seemed to transform into a bright, steady dancer.")
        world.say(f"So {hero.id} began {activity.gerund}, and the little {hero.type} learned that tidying first can make fun feel easier and safer.")
    return world


def generation_prompts(world: World) -> list[str]:
    f = world.facts
    hero, parent, act = f["hero"], f["parent"], f["activity"]
    return [
        f'Write a short slice-of-life story for a young child that includes a slumber party and the word "{act.keyword}".',
        f"Tell a gentle story about {hero.id}, a sleepy child who wants to {act.verb} while {parent.label} worries about the room.",
        "Write a tiny story where a child changes from sleepy to lively, learns a lesson, and ends up dancing happily.",
    ]


