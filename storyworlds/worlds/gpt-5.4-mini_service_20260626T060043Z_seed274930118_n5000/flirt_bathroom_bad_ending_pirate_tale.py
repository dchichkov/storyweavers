def intro(world: World, hero: Entity, parent: Entity, prize: Entity) -> None:
    world.say(
        f"{hero.id} was a little {hero.pronoun('possessive')} "
        f"{hero.type} who liked pirate games and shiny things."
    )
    world.say(
        f"One day, {hero.id}'s {parent.label} gave {hero.pronoun('object')} "
        f"{prize.phrase} to wear."
    )


def setup(world: World, hero: Entity, activity: Activity) -> None:
    hero.memes["want"] = hero.memes.get("want", 0) + 1
    world.say(
        f"{hero.id} liked the bathroom because the mirror was smooth and bright."
    )
    world.say(
        f"{hero.id} wanted to {activity.verb}, because {activity.gerund} felt like a tiny pirate show."
    )


