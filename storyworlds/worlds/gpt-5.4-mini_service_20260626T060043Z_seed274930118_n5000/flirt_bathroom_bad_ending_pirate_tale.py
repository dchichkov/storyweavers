def reasonableness_gate(activity: Activity, prize: Prize, setting: Setting) -> bool:
    if setting.place != "the bathroom":
        return False
    if activity.id != "flirt":
        return False
    # Flirting is only a meaningful conflict if the prized item is on the torso.
    return prize.region == "torso"


ASP_RULES = r"""
% A story is valid when the bathroom setting hosts flirtation and the prize
% being worn can be affected by the social blow-up.
valid_story(P, A, R) :- place(P), activity(A), prize(R),
                        bathroom(P), flirt(A), torso_prize(R).
"""


def asp_facts() -> str:
    import asp

    lines: list[str] = []
    for pid, s in SETTINGS.items():
        lines.append(asp.fact("place", pid))
        if s.place == "the bathroom":
            lines.append(asp.fact("bathroom", pid))
    for aid, a in ACTIVITIES.items():
        lines.append(asp.fact("activity", aid))
        if aid == "flirt":
            lines.append(asp.fact("flirt", aid))
        for tag in sorted(a.tags):
            lines.append(asp.fact("tag", aid, tag))
    for rid, r in PRIZES.items():
        lines.append(asp.fact("prize", rid))
        if r.region == "torso":
            lines.append(asp.fact("torso_prize", rid))
    return "\n".join(lines)


def asp_program(show: str) -> str:
    return f"{asp_facts()}\n{ASP_RULES}\n{show}\n"


def asp_valid() -> list[tuple]:
    import asp

    model = asp.one_model(asp_program("#show valid_story/3."))
    return sorted(set(asp.atoms(model, "valid_story")))


def asp_verify() -> int:
    py = {("bathroom", "flirt", "hat"), ("bathroom", "flirt", "sash")}
    clingo_set = set(asp_valid())
    if clingo_set == py:
        print(f"OK: clingo gate matches Python gate ({len(py)} stories).")
        return 0
    print("MISMATCH between clingo and Python gate:")
    print("  only in clingo:", sorted(clingo_set - py))
    print("  only in python:", sorted(py - clingo_set))
    return 1


