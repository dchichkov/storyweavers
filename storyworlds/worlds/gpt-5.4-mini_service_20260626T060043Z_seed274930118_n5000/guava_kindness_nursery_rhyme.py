def resolve_params(args: argparse.Namespace, rng: random.Random) -> StoryParams:
    if getattr(args, "place", None) and getattr(args, "place", None) not in SETTINGS:
        return _fallback_storyparams(args, rng, StoryParams, globals())
    combos = valid_combos()
    if getattr(args, "place", None):
        combos = [c for c in combos if c[0] == getattr(args, "place", None)]
    if getattr(args, "fruit", None):
        combos = [c for c in combos if c[1] == getattr(args, "fruit", None)]
    if not combos:
        return _fallback_storyparams(args, rng, StoryParams, globals())
    place, fruit, _ = (list(rng.choice(combos)) + [None, None, None])[:3]
    child = getattr(args, "child", None) or rng.choice(list(CHILDREN))
    friend = getattr(args, "friend", None) or rng.choice([k for k in CHILDREN if k != child.lower()])
    if friend.lower() == child.lower():
        friend = rng.choice([k for k in CHILDREN if k != child.lower()])
    return StoryParams(place=place, child=child, friend=friend, fruit=fruit)


def tell(params: StoryParams) -> World:
    world = World(_safe_lookup(SETTINGS, params.place))
    child = world.add(Entity(id=params.child.title(), kind="character", type=CHILDREN[params.child].type,
                             label=params.child.title(), traits=list(CHILDREN[params.child].traits),
                             meters={"hunger": 0.0}, memes={"kindness": 0.0, "joy": 0.0}))
    friend = world.add(Entity(id=params.friend.title(), kind="character", type=CHILDREN[params.friend].type,
                              label=params.friend.title(), traits=list(CHILDREN[params.friend].traits),
                              meters={"hunger": 1.0}, memes={"hope": 1.0, "joy": 0.0}))
    fruit = world.add(Entity(id=params.fruit, type="fruit", label=params.fruit, phrase=_safe_lookup(FRUITS, params.fruit).phrase,
                             owner=child.id))
    world.facts.update(child=child, friend=friend, fruit=fruit, params=params)

    world.say(f"Over in {world.setting.place}, there was {child.label}, as light as a feather and bright as a star.")
    world.say(f"{child.label} found {fruit.phrase} and held it close, for the guava smelled sweet as summer honey.")
    world.say(f"Near by stood {friend.label}, whose tummy rumbled low, and {friend.pronoun()} looked at the fruit with hopeful eyes.")

    world.para()
    child.memes["kindness"] += 1.0
    world.say(f"{child.label} saw the wish in {friend.label}'s face and chose a kinder way.")
    world.say(f'"Take a bite with me," said {child.label}. "We can share the guava and make the day feel sunny."')
    friend.memes["joy"] += 1.0
    friend.meters["hunger"] = 0.0
    child.meters["hunger"] = 0.0
    world.say(f"So they sat in the soft grass, and each had a sweet turn. The guava grew smaller, but their smiles grew wide.")

    world.para()
    world.say(f"And that is how the little guava story went: not with a snatch or a fuss, but with kindness and a happy crunch.")
    world.say(f"Now {child.label} and {friend.label} still remember the fruit, the sharing, and the warm little shine it left behind.")
    return world


