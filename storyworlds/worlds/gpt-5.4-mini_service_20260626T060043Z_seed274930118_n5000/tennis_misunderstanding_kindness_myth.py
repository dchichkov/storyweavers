def predict_mess(world: World, hero: Entity, activity: Activity, prize_id: str) -> dict:
    sim = world.copy()
    _do_activity(sim, sim.get(hero.id), activity, narrate=False)
    prize = sim.get(prize_id)
    return {"soiled": bool(prize.meters.get("dusty", 0.0) >= THRESHOLD)}


def _do_activity(world: World, hero: Entity, activity: Activity, narrate: bool = True) -> None:
    world.zone = set(activity.zone)
    hero.meters[activity.mess] = hero.meters.get(activity.mess, 0.0) + 1
    hero.memes["joy"] = hero.memes.get("joy", 0.0) + 1
    for item in world.worn_items(hero):
        if item.region in world.zone:
            item.meters[activity.mess] = item.meters.get(activity.mess, 0.0) + 1
            item.meters["dirty"] = item.meters.get("dirty", 0.0) + 1
    if narrate:
        world.say(f"{hero.id} played, and the court stirred under swift feet.")


def tell(setting: Setting, activity: Activity, prize_cfg: Prize,
         hero_name: str, hero_type: str, mentor_name: str,
         trait: str) -> World:
    world = World(setting)
    hero = world.add(Entity(id=hero_name, kind="character", type=hero_type))
    mentor = world.add(Entity(id=mentor_name, kind="character", type="woman", label="the mentor"))
    prize = world.add(Entity(
        id="prize",
        type=prize_cfg.label,
        label=prize_cfg.label,
        phrase=prize_cfg.phrase,
        owner=hero.id,
        caretaker=mentor.id,
        worn_by=hero.id,
        region=prize_cfg.region,
        plural=prize_cfg.plural,
    ))
    hero.memes["love_game"] = 1.0
    hero.memes["hope"] = 1.0
    hero.memes["misunderstanding"] = 0.0
    mentor.memes["kindness"] = 1.0

    world.say(f"{hero.id} was a {trait} young {hero.type} who loved {activity.gerund}.")
    world.say(f"{hero.id} treasured {hero.pronoun('possessive')} {prize.label} and carried it as if it were a charm.")

    world.para()
    world.say(f"At {setting.place}, under a {setting.sky} sky, the air felt fit for a match.")
    world.say(f"{hero.id} wanted to {activity.verb}, but {mentor.label} lifted a hand and spoke of a trial.")
    if predict_mess(world, hero, activity, prize.id)["soiled"]:
        world.say(f'"Win the next rally and I will give you a blessing," said {mentor_name}.')
        world.say(f"{hero.id} heard the words the wrong way and thought the promise was a taunt.")
        hero.memes["misunderstanding"] += 1
        hero.memes["hurt"] = hero.memes.get("hurt", 0.0) + 1
        world.say(f"So {hero.id} frowned and gripped {hero.pronoun('possessive')} {prize.label} more tightly.")
        _do_activity(world, hero, activity, narrate=True)
        hero.memes["conflict"] = hero.memes.get("conflict", 0.0) + 1

    world.para()
    gift = select_gift(activity, prize)
    if gift is None:
        pass
    world.say(f"Then {mentor_name} stepped closer with a kind answer.")
    world.say(f'"I did not mean to mock you," {mentor_name} said. "Let us {gift.prep} and play together."')
    world.add(Entity(id=gift.id, type="gift", label=gift.label, worn_by=hero.id))
    world.say(gift.kindness)
    hero.memes["misunderstanding"] = 0.0
    hero.memes["hurt"] = 0.0
    hero.memes["joy"] = hero.memes.get("joy", 0.0) + 1
    hero.memes["kindness"] = hero.memes.get("kindness", 0.0) + 1
    mentor.memes["kindness"] += 1
    world.say(f"{hero.id} saw the truth at last and nodded.")
    world.say(f"Together they {gift.tail}, and the game shone bright and gentle again.")
    world.say(f"In the end, {hero.id}'s {prize.label} stayed safe, and the court remembered kindness.")

    world.facts.update(
        hero=hero,
        mentor=mentor,
        prize=prize,
        activity=activity,
        setting=setting,
        gift=gift,
        misunderstanding=hero.memes["hurt"] > 0,
        resolved=True,
    )
    return world


