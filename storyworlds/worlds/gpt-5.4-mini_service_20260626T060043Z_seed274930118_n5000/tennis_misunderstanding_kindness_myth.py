def prize_at_risk(activity: Activity, prize: Prize) -> bool:
    return prize.region in activity.zone


def select_gift(activity: Activity, prize: Prize) -> Optional[Gift]:
    if prize.region == "hands":
        return GIFTS["wristwrap"]
    if prize.region == "feet":
        return GIFTS["shoes"]
    if prize.region == "torso":
        return GIFTS["towel"]
    return None


def valid_combos() -> list[tuple[str, str]]:
    combos = []
    for place, setting in SETTINGS.items():
        for act_id in setting.affords:
            act = _safe_lookup(ACTIVITIES, act_id)
            for prize_id, prize in PRIZES.items():
                if prize_at_risk(act, prize) and select_gift(act, prize):
                    combos.append((place, prize_id))
    return combos


