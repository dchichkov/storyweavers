class World:
    def __init__(self, setting: Setting) -> None:
        self.setting = setting
        self.entities: dict[str, Entity] = {}
        self.fired: set[tuple] = set()
        self.zone: set[str] = set()
        self.weather: str = ""
        self.paragraphs: list[list[str]] = [[]]
        self.facts: dict = {}

    def add(self, ent: Entity) -> Entity:
        self.entities[ent.id] = ent
        return ent

    def get(self, eid: str) -> Entity:
        if eid not in self.entities:
            label = str(eid).replace("_", " ")
            self.entities[eid] = Entity(str(eid), label=label)
        return self.entities[eid]

    def characters(self) -> list[Entity]:
        return [e for e in self.entities.values() if e.kind == "character"]

    def worn_items(self, actor: Entity) -> list[Entity]:
        return [e for e in self.entities.values() if e.worn_by == actor.id]

    def covered(self, actor: Entity, region: str) -> bool:
        return any(g.protective and region in g.covers for g in self.worn_items(actor))

    def say(self, text: str) -> None:
        if text:
            self.paragraphs[-1].append(text)

    def para(self) -> None:
        if self.paragraphs[-1]:
            self.paragraphs.append([])

    def render(self) -> str:
        return "\n\n".join(" ".join(p) for p in self.paragraphs if p)

    def copy(self) -> "World":
        import copy as _copy

        clone = World(self.setting)
        clone.entities = _copy.deepcopy(self.entities)
        clone.fired = set(self.fired)
        clone.zone = set(self.zone)
        clone.weather = self.weather
        clone.paragraphs = [[]]
        clone.facts = dict(self.facts)
        return clone


def _r_spill(world: World) -> list[str]:
    out: list[str] = []
    for actor in world.characters():
        if actor.m("citric") < THRESHOLD:
            continue
        for item in world.worn_items(actor):
            if item.protective or item.region not in world.zone:
                continue
            if world.covered(actor, item.region):
                continue
            sig = ("spill", item.id)
            if sig in world.fired:
                continue
            world.fired.add(sig)
            item.add_meter("citric", 1)
            item.add_meter("messy", 1)
            out.append(f"{actor.label_word}'s {item.label_word} got sticky with citric juice.")
    return out


def _r_laugh(world: World) -> list[str]:
    out: list[str] = []
    for actor in world.characters():
        if actor.e("humor") < THRESHOLD or actor.e("curiosity") < THRESHOLD:
            continue
        sig = ("laugh", actor.id)
        if sig in world.fired:
            continue
        world.fired.add(sig)
        actor.add_meme("joy", 1)
        out.append(f"{actor.label_word} giggled at the silly fizzing smell in the air.")
    return out


CAUSAL_RULES = [
    _r_spill,
    _r_laugh,
]


def propagate(world: World, narrate: bool = True) -> list[str]:
    produced: list[str] = []
    changed = True
    for _ in range(len(globals().get("CAUSAL_RULES", [])) + 4):
        changed = False
        for rule in CAUSAL_RULES:
            sents = rule(world)
            if sents:
                changed = True
                produced.extend(sents)
    if narrate:
        for s in produced:
            world.say(s)
    return produced


def activity_risk(activity: Activity, prize: Prize) -> bool:
    return prize.region in activity.zone


def select_gear(activity: Activity, prize: Prize) -> Optional[Gear]:
    for g in GEAR:
        if activity.mess in g.guards and prize.region in g.covers:
            return g
    return None


def predict_mess(world: World, actor: Entity, activity: Activity, prize_id: str) -> dict:
    sim = world.copy()
    _do_activity(sim, sim.get(actor.id), activity, narrate=False)
    prize = sim.entities.get(prize_id)
    return {"soiled": bool(prize and prize.m("messy") >= THRESHOLD)}


def _do_activity(world: World, actor: Entity, activity: Activity, narrate: bool = True) -> None:
    if activity.id not in world.setting.affords:
        return
    world.zone = set(activity.zone)
    actor.add_meter(activity.mess, 1)
    actor.add_meme("curiosity", 1)
    actor.add_meme("humor", 1)
    propagate(world, narrate=narrate)


def tell(world_setting: Setting, activity: Activity, prize_cfg: Prize, hero_name: str,
         hero_type: str, parent_type: str, trait: str) -> World:
    world = World(world_setting)
    world.weather = activity.weather if not world_setting.indoor else ""
    hero = world.add(Entity(
        id=hero_name, kind="character", type=hero_type,
        label=hero_name, meters={"curiosity": 1}, memes={"humor": 1, "joy": 0},
    ))
    parent = world.add(Entity(
        id="Parent", kind="character", type=parent_type, label="the captain",
        meters={"care": 1},
    ))
    prize = world.add(Entity(
        id="prize", type=prize_cfg.type, label=prize_cfg.label, phrase=prize_cfg.phrase,
        owner=hero.id, caretaker=parent.id, region=prize_cfg.region, plural=prize_cfg.plural,
    ))

    hero.add_meme("love_space", 1)
    hero.add_meme("curiosity", 1)
    hero.add_meme("humor", 1)

    world.say(f"{hero.id} was a little {trait} {hero.type} who loved space maps and moonlit surprises.")
    world.say(f"{hero.pronoun().capitalize()} liked to explore {activity.gerund}; even the stars seemed to wink back.")
    world.say(f"One launch day, {parent.label_word} bought {hero.pronoun('object')} {prize.phrase}.")
    prize.worn_by = hero.id
    world.say(f"{hero.id} treasured the {prize.label} and wore {prize.it()} like a bright badge for the voyage.")

    world.para()
    world.say(f"At {world_setting.place}, the air smelled clean and the domes hummed like sleepy robots.")
    world.say(f"{hero.id} wanted to {activity.verb}, but {hero.pronoun('possessive')} {parent.label_word} raised a careful hand.")
    pred = predict_mess(world, hero, activity, prize.id)
    if pred["soiled"]:
        world.say(f'"You\'ll get your {prize.label} {activity.soil}," {parent.label_word} said.')
    world.say(f"{hero.id} still tried to {activity.rush}, and the joke of it made {hero.pronoun('object')} snicker.")

    hero.add_meme("curiosity", 1)
    hero.add_meme("humor", 1)
    hero.add_meme("stubborn", 1)
    _do_activity(world, hero, activity, narrate=True)

    world.para()
    gear_def = select_gear(activity, prize)
    if gear_def is None:
        pass
    gear = world.add(Entity(
        id=gear_def.id, type="gear", label=gear_def.label, owner=hero.id,
        caretaker=parent.id, protective=True, covers=set(gear_def.covers), plural=gear_def.plural
    ))
    gear.worn_by = hero.id
    if predict_mess(world, hero, activity, prize.id)["soiled"]:
        gear.worn_by = None
        del world.entities[gear.id]
        pass

    world.say(f"Then {parent.label_word} smiled and said, \"How about we {gear_def.prep} and go together?\"")
    hero.add_meme("joy", 2)
    hero.memes["conflict"] = 0.0
    world.say(f"{hero.id}'s eyes went wide. {hero.pronoun().capitalize()} hugged {hero.pronoun('possessive')} {parent.label_word} and laughed.")
    world.say(f'They {gear_def.tail}. Soon {hero.id} was {activity.gerund}, {prize.label} stayed clean, and the mission felt like a joke told by the stars.')

    world.facts.update(hero=hero, parent=parent, prize=prize, activity=activity, gear=gear,
                       setting=world_setting, trait=trait, predicted_soil=activity.soil if pred["soiled"] else "")
    return world


