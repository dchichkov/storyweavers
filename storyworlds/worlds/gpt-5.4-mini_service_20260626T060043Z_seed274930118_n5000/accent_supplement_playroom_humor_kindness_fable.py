def _r_confusion(world: World) -> list[str]:
    out: list[str] = []
    for e in world.characters():
        if e.memes["humor"] < THRESHOLD:
            continue
        if e.meters["missing"] < THRESHOLD:
            continue
        sig = ("confusion", e.id)
        if sig in world.fired:
            continue
        world.fired.add(sig)
        e.memes["embarrassment"] += 1
        out.append(f"The missing accent marks made the words sound a little goofy.")
    return out


def _r_kindness(world: World) -> list[str]:
    out: list[str] = []
    helper = world.get("child") if "child" in world.entities else None
    friend = world.get("friend") if "friend" in world.entities else None
    supplement = world.get("supplement") if "supplement" in world.entities else None
    if not helper or not friend or not supplement:
        return out
    if helper.memes["kindness"] < THRESHOLD or supplement.worn_by != helper.id:
        return out
    if friend.memes["embarrassment"] < THRESHOLD:
        return out
    sig = ("kindness",)
    if sig in world.fired:
        return out
    world.fired.add(sig)
    friend.memes["relief"] += 1
    friend.memes["embarrassment"] = 0.0
    helper.memes["kindness"] += 0.5
    out.append("Kind words and careful hands helped the shy friend smile again.")
    return out


CAUSAL_RULES = [Rule("confusion", _r_confusion), Rule("kindness", _r_kindness)]


def propagate(world: World, narrate: bool = True) -> list[str]:
    produced: list[str] = []
    changed = True
    for _ in range(len(globals().get("CAUSAL_RULES", [])) + 4):
        changed = False
        for rule in CAUSAL_RULES:
            sents = rule.apply(world)
            if sents:
                changed = True
                produced.extend(sents)
    if narrate:
        for s in produced:
            world.say(s)
    return produced


def predict_fix(world: World) -> dict:
    sim = world.copy()
    simulate_turn(sim, narrate=False)
    friend = sim.entities.get("friend")
    return {
        "embarrassed": bool(friend and friend.memes["embarrassment"] >= THRESHOLD),
        "relieved": bool(friend and friend.memes["relief"] >= THRESHOLD),
    }


def simulate_setup(world: World, hero: Entity, friend: Entity, prize: Entity, activity: Activity) -> None:
    hero.memes["kindness"] += 1
    world.say(
        f"{hero.id} was a cheerful child in the playroom who loved {activity.gerund}."
    )
    world.say(
        f"Near the toy shelf, {friend.id} had a name card that needed a little accent."
    )
    world.say(
        f"{hero.id} also found a small supplement packet of accent marks beside the crayons."
    )
    prize.worn_by = hero.id
    world.say(
        f"{hero.id} kept {prize.phrase} ready, because the playroom game was to make the word cards sound right."
    )


def simulate_turn(world: World, narrate: bool = True) -> None:
    hero = world.get("child")
    friend = world.get("friend")
    prize = world.get("namecard")
    supplement = world.get("supplement")

    hero.memes["humor"] += 1
    hero.meters["missing"] += 1
    friend.memes["embarrassment"] += 1
    world.missing_accents.add("accent")
    world.cards = ["cafe", "resume", "role"]
    if narrate:
        world.say(
            f"{hero.id} tried to read the cards aloud without the accent marks, and the words sounded very silly."
        )
        world.say(
            f"{{}}".format("Everyone giggled a little, but " + friend.id + " looked shy when their own name needed an accent.")
        )
    propagate(world, narrate=narrate)


def simulate_resolution(world: World) -> None:
    hero = world.get("child")
    friend = world.get("friend")
    supplement = world.get("supplement")
    prize = world.get("namecard")

    hero.memes["kindness"] += 1
    supplement.worn_by = hero.id
    world.missing_accents.clear()
    hero.meters["missing"] = 0.0
    world.say(
        f"Then {hero.id} smiled, used the supplement packet, and placed the accent marks where they belonged."
    )
    world.say(
        f"{hero.id} read the cards again, this time kindly and clearly, and {friend.id} stood taller with relief."
    )
    world.say(
        f"By the end, the playroom cards were tidy, {prize.phrase} was complete, and the little lesson was clear: humor is nicer when it helps, not hurts."
    )
    friend.memes["relief"] += 1
    propagate(world, narrate=True)


def tell(setting: Setting, activity: Activity, prize_cfg: Prize, hero_name: str = "child", friend_name: str = "friend") -> World:
    world = World(setting)

    hero = world.add(Entity(
        id="child",
        kind="character",
        type="girl",
        label=hero_name,
        traits=["kind", "curious"],
    ))
    friend = world.add(Entity(
        id="friend",
        kind="character",
        type="boy",
        label=friend_name,
        traits=["shy", "gentle"],
    ))
    prize = world.add(Entity(
        id="namecard",
        type=prize_cfg.type,
        label=prize_cfg.label,
        phrase=prize_cfg.phrase,
        plural=prize_cfg.plural,
        needs_accent=prize_cfg.needs_accent,
        caretaker=hero.id,
    ))
    supplement = world.add(Entity(
        id="supplement",
        type="supplement",
        label="accent supplement",
        phrase="a small accent supplement packet",
        supplement=True,
        plural=False,
    ))

    world.facts.update(hero=hero, friend=friend, prize=prize, supplement=supplement, activity=activity, setting=setting)
    simulate_setup(world, hero, friend, prize, activity)
    world.para()
    simulate_turn(world, narrate=True)
    world.para()
    simulate_resolution(world)
    return world


