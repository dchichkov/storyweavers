def reason_gate(params: StoryParams) -> None:
    if params.place not in PLACES:
        pass
    if params.tool not in TOOLS:
        pass
    place = _safe_lookup(PLACES, params.place)
    if not place.has_sharp_edge:
        pass


ASP_RULES = r"""
#show valid/3.
#show valid_story/4.

valid(Place, tool, putter) :- place(Place), sharp_edge(Place), tool(tool).

valid_story(Place, tool, putter, Name) :- valid(Place, tool, putter), nomad(Name).
"""


def asp_facts() -> str:
    import asp

    lines: list[str] = []
    for pid, p in PLACES.items():
        lines.append(asp.fact("place", pid))
        if p.has_sharp_edge:
            lines.append(asp.fact("sharp_edge", pid))
        if p.has_soft_sand:
            lines.append(asp.fact("soft_sand", pid))
    for tid in TOOLS:
        lines.append(asp.fact("tool", tid))
    for n in NAMES:
        lines.append(asp.fact("nomad", n))
    return "\n".join(lines)


def asp_program(show: str) -> str:
    return f"{asp_facts()}\n{ASP_RULES}\n{show}\n"


def valid_combos() -> list[tuple[str, str, str]]:
    out = []
    for pid, p in PLACES.items():
        if not p.has_sharp_edge:
            continue
        for tid in TOOLS:
            out.append((pid, tid, "putter"))
    return out


def asp_valid_combos() -> list[tuple]:
    import asp

    model = asp.one_model(asp_program("#show valid/3."))
    return sorted(set(asp.atoms(model, "valid")))


def asp_valid_stories() -> list[tuple]:
    import asp

    model = asp.one_model(asp_program("#show valid_story/4."))
    return sorted(set(asp.atoms(model, "valid_story")))


def asp_verify() -> int:
    py = set(valid_combos())
    cl = set(asp_valid_combos())
    if py == cl:
        print(f"OK: clingo gate matches valid_combos() ({len(py)} combos).")
        return 0
    print("MISMATCH between clingo and valid_combos():")
    if py - cl:
        print("  only in python:", sorted(py - cl))
    if cl - py:
        print("  only in clingo:", sorted(cl - py))
    return 1


