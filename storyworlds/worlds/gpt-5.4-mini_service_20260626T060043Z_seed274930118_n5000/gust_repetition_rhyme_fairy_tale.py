def resolve_params(args: argparse.Namespace, rng: random.Random) -> StoryParams:
    if getattr(args, "activity", None) and getattr(args, "prize", None):
        if not (prize_at_risk(_safe_lookup(ACTIVITIES, getattr(args, "activity", None)), _safe_lookup(PRIZES, getattr(args, "prize", None))) and select_gear(_safe_lookup(ACTIVITIES, getattr(args, "activity", None)), _safe_lookup(PRIZES, getattr(args, "prize", None)))):
            return _fallback_storyparams(args, rng, StoryParams, globals())
    combos = [c for c in valid_combos()
              if (getattr(args, "place", None) is None or c[0] == getattr(args, "place", None))
              and (getattr(args, "activity", None) is None or c[1] == getattr(args, "activity", None))
              and (getattr(args, "prize", None) is None or c[2] == getattr(args, "prize", None))]
    if not combos:
        return _fallback_storyparams(args, rng, StoryParams, globals())
    place, activity, prize = rng.choice(list(combos))
    gender = getattr(args, "gender", None) or rng.choice(["girl", "boy"])
    name = getattr(args, "name", None) or rng.choice(NAMES)
    return StoryParams(place=place, activity=activity, prize=prize, name=name, gender=gender, parent=getattr(args, "parent", None))


def tell(setting: Setting, activity: Activity, prize_cfg: Prize, hero_name: str, gender: str, parent_label: str) -> World:
    world = World(setting)
    hero_type = TYPES.get(gender, [gender])[0]
    hero = world.add(Entity(id=hero_name, kind="character", type=hero_type))
    guardian = world.add(Entity(id="Guardian", kind="character", type="guardian", label=parent_label))
    prize = world.add(Entity(id="Prize", type=prize_cfg.id, label=prize_cfg.label, phrase=prize_cfg.phrase, owner=hero.id, caretaker=guardian.id, region=prize_cfg.region, plural=prize_cfg.plural))
    prize.worn_by = hero.id

    intro(world, hero)
    loves(world, hero, activity, prize)
    world.para()
    arrive(world, hero, guardian, activity)
    warn(world, guardian, hero, activity, prize)
    defy(world, hero)
    blow(world, hero, activity)
    world.para()
    gear = fix(world, guardian, hero, activity, prize)
    if gear:
        accept(world, guardian, hero, activity, prize, gear)
    world.facts.update(hero=hero, guardian=guardian, prize=prize, activity=activity, gear=gear, setting=setting)
    return world


def generation_prompts(world: World) -> list[str]:
    f = world.facts
    hero, prize, activity = _safe_fact((globals().get("world") or locals().get("world") or locals().get("mw") or locals().get("w")), f, "hero"), _safe_fact((globals().get("world") or locals().get("world") or locals().get("mw") or locals().get("w")), f, "prize"), _safe_fact((globals().get("world") or locals().get("world") or locals().get("mw") or locals().get("w")), f, "activity")
