def resolve_params(args: argparse.Namespace, rng: random.Random):
    if getattr(args, "activity", None) and getattr(args, "prize", None):
        act = _safe_lookup(ACTIVITIES, getattr(args, "activity", None))
        pr = _safe_lookup(PRIZES, getattr(args, "prize", None))
        if not prize_at_risk(act, pr):
            return _fallback_storyparams(args, rng, StoryParams, globals())
    combos = [c for c in valid_combos()
              if (getattr(args, "place", None) is None or c[0] == getattr(args, "place", None))
              and (getattr(args, "activity", None) is None or c[1] == getattr(args, "activity", None))
              and (getattr(args, "prize", None) is None or c[2] == getattr(args, "prize", None))]
    if not combos:
        return _fallback_storyparams(args, rng, StoryParams, globals())
    place, activity, prize = rng.choice(list(combos))
    gender = getattr(args, "gender", None) or rng.choice(["girl", "boy"])
    if gender not in _safe_lookup(PRIZES, prize).genders:
        return _fallback_storyparams(args, rng, StoryParams, globals())
    name = getattr(args, "name", None) or rng.choice(GIRL_NAMES if gender == "girl" else BOY_NAMES)
    parent = getattr(args, "parent", None) or rng.choice(["mother", "father"])
    trait = getattr(args, "trait", None) or rng.choice(TRAITS)
    return StoryParams(place=place, activity=activity, prize=prize, gender=gender, name=name, parent=parent, trait=trait)


def _do_activity(world: World, actor: Entity, activity: Activity, narrate: bool = True) -> None:
    world.zone = set(activity.zone)
    actor.meters[activity.mess] = actor.meters.get(activity.mess, 0.0) + 1
    actor.memes["worry"] = actor.memes.get("worry", 0.0) + 1
    if narrate:
        world.say(f"{actor.pronoun().capitalize()} started {activity.gerund}.")


def predict_mess(world: World, actor: Entity, activity: Activity, prize_id: str) -> dict:
    sim = world.copy()
    _do_activity(sim, sim.get(actor.id), activity, narrate=False)
    prize = sim.entities[prize_id]
    return {"soiled": True, "mess": activity.soil if prize.region in activity.zone else ""}


def tell(setting: Setting, activity: Activity, prize_cfg: Prize, name: str, gender: str, trait: str, parent_type: str) -> World:
    world = World(setting)
    child = world.add(Entity(id=name, kind="character", type=gender, meters={}, memes={}))
    parent = world.add(Entity(id="Parent", kind="character", type=parent_type))
    prize = world.add(Entity(id="Prize", type=prize_cfg.type, label=prize_cfg.label, phrase=prize_cfg.phrase, owner=child.id, caretaker=parent.id, region=prize_cfg.region, plural=prize_cfg.plural))
    tool = select_tool(activity, prize_cfg)

    world.say(f"{child.id} was a {trait} little {gender} who liked helping in {setting.place}.")
    world.say(f"That day, {child.id} wanted to {activity.verb}, and {activity.foreshadow}")
    world.say(f"{child.id}'s {parent_type} had just bought {prize_cfg.phrase}, and that felt relevant because it was so new.")

    world.para()
    world.say(f"At {setting.place}, {child.id} leaned over the bowl and the lid gave a tiny twitch.")
    world.say(f"{child.id} kept going anyway, and that was the first sign that a bad ending was nearby.")
    world.say(f"Then the {activity.keyword} mixture burst into a little spew across the table.")

    if tool:
        world.para()
        world.say(f"{child.id} blinked, then remembered that {tool.label} was the relevant tool.")
        world.say(f"{child.id} and {parent_type} worked together to {tool.prep}, and soon {child.id} {tool.tail}.")
        world.say(f"The {activity.soil} spew was gone, the table looked neat again, and the kitchen felt warm and safe.")
        world.say(f"{child.id} learned {activity.lesson}")
    else:
        world.para()
        world.say(f"{child.id} froze, and the mess spread farther than anyone wanted.")
        world.say(f"It turned into a bad ending for the table, because there was no good tool nearby.")
        world.say(f"Even so, {parent_type} helped {child.id} clean up slowly, and {child.id} learned {activity.lesson}")

    world.facts.update(child=child, parent=parent, prize=prize, activity=activity, setting=setting, tool=tool, trait=trait, gender=gender)
    return world


