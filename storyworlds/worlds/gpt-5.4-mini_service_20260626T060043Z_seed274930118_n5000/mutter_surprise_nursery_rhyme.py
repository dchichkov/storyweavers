def _r_mutter(world: World) -> list[str]:
    out = []
    child = world.get("child")
    if child.meters.get("lost", 0) >= THRESHOLD and "mutter" not in world.fired:
        world.fired.add("mutter")
        child.memes["worry"] = child.memes.get("worry", 0) + 1
        out.append(f'{child.id} gave a little mutter, "Oh dear, oh dear."')
    return out


def _r_surprise(world: World) -> list[str]:
    out = []
    child = world.get("child")
    parent = world.get("parent")
    item = world.get("surprise")
    if child.memes.get("worry", 0) >= THRESHOLD and "surprise" not in world.fired:
        world.fired.add("surprise")
        child.memes["wonder"] = child.memes.get("wonder", 0) + 1
        child.meters["found"] = child.meters.get("found", 0) + 1
        out.append(
            f"Then {parent.pronoun('possessive')} {item.label} was found in the nursery basket."
        )
        out.append(f'It was a sweet surprise, and {child.id} forgot the gloomy mutter.')
    return out


def _r_joy(world: World) -> list[str]:
    out = []
    child = world.get("child")
    if child.meters.get("found", 0) >= THRESHOLD and "joy" not in world.fired:
        world.fired.add("joy")
        child.memes["joy"] = child.memes.get("joy", 0) + 1
        out.append(f"{child.id} smiled a smile as bright as day.")
    return out


CAUSAL_RULES = [Rule("mutter", _r_mutter), Rule("surprise", _r_surprise), Rule("joy", _r_joy)]


def propagate(world: World, narrate: bool = True) -> list[str]:
    lines: list[str] = []
    changed = True
    for _ in range(len(globals().get("CAUSAL_RULES", [])) + 4):
        changed = False
        for rule in CAUSAL_RULES:
            sents = rule.apply(world)
            if sents:
                changed = True
                lines.extend(sents)
    if narrate:
        for s in lines:
            world.say(s)
    return lines


def _do_story(world: World) -> None:
    propagate(world, narrate=True)
    child = world.get("child")
    parent = world.get("parent")
    item = world.get("surprise")
    if child.memes.get("joy", 0) >= THRESHOLD:
        world.say(
            f"With a clap and a pat, {parent.id} gave the {item.label}, "
            f"and {child.id} held it snug and neat."
        )
        world.say(
            f"Now there was no mutter at all, only a merry little beat."
        )


def _setup(params: StoryParams) -> World:
    if params.gender not in {"girl", "boy"}:
        pass
    if params.surprise not in SURPRISES:
        pass
    world = World(SETTINGS["nursery"])
    child = world.add(Entity(id=params.name, kind="character", type=params.gender))
    parent = world.add(Entity(id="parent", kind="character", type=params.parent, label=params.parent))
    surprise = _safe_lookup(SURPRISES, params.surprise)
    item = world.add(Entity(
        id="surprise", kind="thing", type=surprise.kind, label=surprise.label, phrase=surprise.phrase,
        owner=parent.id, caretaker=parent.id
    ))
    world.facts.update(child=child, parent=parent, surprise=item, params=params, setting=world.setting)
    return world


def tell(params: StoryParams) -> World:
    world = _setup(params)
    child = world.get("child")
    parent = world.get("parent")
    item = world.get("surprise")

    child.meters["lost"] += 1

    world.say(f'In the nursery soft and still, {child.id} felt a little blue.')
    world.say(
        f"{child.id} looked here and there, but the thing they wanted was not in view."
    )
    world.para()
    world.say(
        f'{parent.pronoun("possessive").capitalize()} {parent.label} hummed a hushy tune, '
        f"and listened close to every murmur."
    )

    world.para()
    _do_story(world)
    world.say(f"In the end, {child.id} held the {item.label}, warm and snug and true.")
    return world


def story_qa(world: World) -> list[QAItem]:
    c = _safe_fact(world, world.facts, "child")
    p = _safe_fact(world, world.facts, "parent")
    item = _safe_fact(world, world.facts, "surprise")
    return [
        QAItem(
            question=f"Why did {c.id} mutter in the nursery?",
            answer=f"{c.id} muttered because {c.pronoun('subject')} felt worried when something was missing.",
        ),
        QAItem(
            question=f"What surprise did {p.id} find?",
            answer=f"{p.id} found {item.phrase} tucked in the nursery basket.",
        ),
        QAItem(
            question=f"How did {c.id} feel at the end?",
            answer=f"{c.id} felt happy and calm after the surprise was found.",
        ),
    ]


