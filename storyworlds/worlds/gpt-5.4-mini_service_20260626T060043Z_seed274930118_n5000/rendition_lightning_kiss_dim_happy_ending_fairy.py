class World:
    def __init__(self, setting: Setting) -> None:
        self.setting = setting
        self.entities: dict[str, Entity] = {}
        self.fired: set[tuple] = set()
        self.zone: set[str] = set()
        self.weather: str = ""
        self.paragraphs: list[list[str]] = [[]]
        self.facts: dict = {}

    def add(self, ent: Entity) -> Entity:
        self.entities[ent.id] = ent
        return ent

    def get(self, eid: str) -> Entity:
        if eid not in self.entities:
            label = str(eid).replace("_", " ")
            self.entities[eid] = Entity(str(eid), label=label)
        return self.entities[eid]

    def characters(self) -> list[Entity]:
        return [e for e in self.entities.values() if e.kind == "character"]

    def worn_items(self, actor: Entity) -> list[Entity]:
        return [e for e in self.entities.values() if e.worn_by == actor.id]

    def covered(self, actor: Entity, region: str) -> bool:
        return any(g.protective and region in g.covers for g in self.worn_items(actor))

    def say(self, text: str) -> None:
        if text:
            self.paragraphs[-1].append(text)

    def para(self) -> None:
        if self.paragraphs[-1]:
            self.paragraphs.append([])

    def render(self) -> str:
        return "\n\n".join(" ".join(p) for p in self.paragraphs if p)

    def copy(self) -> "World":
        import copy as _copy

        clone = World(self.setting)
        clone.entities = _copy.deepcopy(self.entities)
        clone.fired = set(self.fired)
        clone.zone = set(self.zone)
        clone.weather = self.weather
        clone.paragraphs = [[]]
        return clone


def _r_soil(world: World) -> list[str]:
    out: list[str] = []
    for actor in world.characters():
        for mess in ("wet", "dim"):
            if actor.meters.get(mess, 0.0) < THRESHOLD:
                continue
            for item in world.worn_items(actor):
                if item.protective or item.region not in world.zone:
                    continue
                if world.covered(actor, item.region):
                    continue
                sig = ("soil", actor.id, item.id, mess)
                if sig in world.fired:
                    continue
                world.fired.add(sig)
                item.meters[mess] = item.meters.get(mess, 0.0) + 1
                out.append(f"{actor.pronoun('possessive').capitalize()} {item.label} got {mess}.")
    return out


def _r_worry(world: World) -> list[str]:
    out: list[str] = []
    for item in list(world.entities.values()):
        if item.meters.get("dim", 0.0) < THRESHOLD:
            continue
        if not item.caretaker:
            continue
        sig = ("worry", item.id)
        if sig in world.fired:
            continue
        world.fired.add(sig)
        caretaker = world.get(item.caretaker)
        caretaker.memes["worry"] = caretaker.memes.get("worry", 0.0) + 1
        out.append(f"That would trouble {caretaker.label}.")
    return out


def _r_comfort(world: World) -> list[str]:
    out: list[str] = []
    for actor in world.characters():
        if actor.memes.get("comforted", 0.0) < THRESHOLD:
            continue
        sig = ("comfort", actor.id)
        if sig in world.fired:
            continue
        world.fired.add(sig)
        actor.memes["fear"] = 0.0
        actor.memes["joy"] = actor.memes.get("joy", 0.0) + 1
        out.append(f"{actor.id} felt brave again.")
    return out


def propagate(world: World, narrate: bool = True) -> list[str]:
    produced: list[str] = []
    changed = True
    for _ in range(len(globals().get("CAUSAL_RULES", [])) + 4):
        changed = False
        for rule in (_r_soil, _r_worry, _r_comfort):
            sents = rule(world)
            if sents:
                changed = True
                produced.extend(sents)
    if narrate:
        for s in produced:
            world.say(s)
    return produced


def prize_at_risk(activity: Activity, prize: Prize) -> bool:
    return prize.region in activity.zone


def select_gear(activity: Activity, prize: Prize) -> Optional[Gear]:
    for gear in GEAR:
        if activity.mess in gear.guards and prize.region in gear.covers:
            return gear
    return None


def predict_mess(world: World, actor: Entity, activity: Activity, prize_id: str) -> dict:
    sim = world.copy()
    do_activity(sim, sim.get(actor.id), activity, narrate=False)
    prize = sim.entities[prize_id]
    return {"soiled": prize.meters.get(activity.mess, 0.0) >= THRESHOLD}


def do_activity(world: World, actor: Entity, activity: Activity, narrate: bool = True) -> None:
    if activity.id not in world.setting.affords:
        return
    world.zone = set(activity.zone)
    actor.meters[activity.mess] = actor.meters.get(activity.mess, 0.0) + 1
    actor.memes["joy"] = actor.memes.get("joy", 0.0) + 1
    propagate(world, narrate=narrate)


def tell(world_setting: Setting, activity: Activity, prize_cfg: Prize,
         hero_name: str = "Luna", hero_type: str = "fairy",
         parent_type: str = "mother", trait: str = "gentle") -> World:
    world = World(world_setting)
    world.weather = "" if world_setting.indoor else activity.weather

    hero = world.add(Entity(
        id=hero_name, kind="character", type=hero_type, label=hero_name
    ))
    parent = world.add(Entity(
        id="Parent", kind="character", type=parent_type, label=f"the {parent_type}"
    ))
    prize = world.add(Entity(
        id="prize",
        type=prize_cfg.type,
        label=prize_cfg.label,
        phrase=prize_cfg.phrase,
        owner=hero.id,
        caretaker=parent.id,
        worn_by=hero.id,
        region=prize_cfg.region,
        plural=prize_cfg.plural,
    ))

    hero.memes["love_music"] = 1
    hero.memes["love"] = 1
    world.say(
        f"{hero.id} was a little {trait} {hero.type} who loved a bright rendition of a fairy song."
    )
    world.say(
        f"{hero.id} adored {prize.phrase} and wore {prize.it()} like a treasure from a moonlit tale."
    )

    world.para()
    if world_setting.indoor:
        world.say(f"One evening, {hero.id} and {hero.pronoun('possessive')} {parent_type} stayed in {world_setting.place}.")
    else:
        world.say(f"One evening, {hero.id} and {hero.pronoun('possessive')} {parent_type} went to {world_setting.place}.")
    world.say(f"The sky flashed with lightning, and the garden went quiet.")

    hero.memes["fear"] = 1
    world.say(
        f"{hero.id} wanted to {activity.verb}, but {hero.pronoun('possessive')} {parent_type} worried that the lightning would make the {prize.label} dim."
    )

    if not prize_at_risk(activity, prize):
        pass

    predicted = predict_mess(world, hero, activity, prize.id)
    if not predicted["soiled"]:
        pass

    world.say(
        f'"If you rush out now, your {prize.label} may get {activity.soil}," {parent_type} said.'
    )
    hero.memes["defiance"] = 1
    world.say(f"{hero.id} pouted, but the wish to sing still tugged at {hero.pronoun('possessive')} heart.")
    world.say(f"{hero.id} tried to {activity.rush},")
    hero.memes["grabbed_by"] = 1
    world.say(
        f"but {hero.pronoun('possessive')} {parent_type} held {hero.pronoun('possessive')} hand and said, "
        f'"We can still make a safe rendition."'
    )

    world.para()
    gear_def = select_gear(activity, prize)
    if gear_def is None:
        pass
    gear = world.add(Entity(
        id=gear_def.id,
        type="gear",
        label=gear_def.label,
        owner=hero.id,
        caretaker=parent.id,
        protective=True,
        covers=set(gear_def.covers),
        plural=gear_def.plural,
    ))
    gear.worn_by = hero.id
    hero.memes["comforted"] = 1
    world.say(
        f"{hero.pronoun('possessive').capitalize()} {parent_type} smiled and said, "
        f'"How about we {gear_def.prep} and sing together under the roof?"'
    )
    world.say(f"{hero.id} nodded, and the storm no longer felt so big.")
    hero.memes["joy"] = hero.memes.get("joy", 0.0) + 1
    hero.memes["love"] = hero.memes.get("love", 0.0) + 1
    hero.memes["fear"] = 0.0
    world.say(
        f"They {gear_def.tail}. Soon {hero.id} was giving a tender rendition by the window, "
        f"{prize.label} stayed bright, and the lightning only painted silver lines on the glass."
    )

    world.facts.update(
        hero=hero,
        parent=parent,
        prize=prize,
        activity=activity,
        setting=world_setting,
        gear=gear_def,
    )
    return world


