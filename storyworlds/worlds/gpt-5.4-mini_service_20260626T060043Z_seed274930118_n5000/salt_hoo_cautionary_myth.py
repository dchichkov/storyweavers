class World:
    def __init__(self, setting: Setting) -> None:
        self.setting = setting
        self.entities: dict[str, Entity] = {}
        self.paragraphs: list[list[str]] = [[]]
        self.fired: set[tuple] = set()
        self.facts: dict = {}
        self.wind: str = ""

    def add(self, ent: Entity) -> Entity:
        self.entities[ent.id] = ent
        return ent

    def get(self, eid: str) -> Entity:
        if eid not in self.entities:
            label = str(eid).replace("_", " ")
            self.entities[eid] = Entity(str(eid), label=label)
        return self.entities[eid]

    def say(self, text: str) -> None:
        if text:
            self.paragraphs[-1].append(text)

    def para(self) -> None:
        if self.paragraphs[-1]:
            self.paragraphs.append([])

    def render(self) -> str:
        return "\n\n".join(" ".join(p) for p in self.paragraphs if p)

    def copy(self) -> "World":
        import copy as _copy
        w = World(self.setting)
        w.entities = _copy.deepcopy(self.entities)
        w.fired = set(self.fired)
        w.wind = self.wind
        w.paragraphs = [[]]
        return w


def join2(a: str, b: str) -> str:
    return f"{a} and {b}"


def article(word: str) -> str:
    return "an" if word[:1].lower() in "aeiou" else "a"


def clean_place(name: str) -> str:
    return name.removeprefix("the ")


def risked(prize: Prize, activity: Activity, setting: Setting) -> bool:
    return prize.risk_key in activity.tags and activity.id in setting.affords


def best_charm(activity: Activity, prize: Prize) -> Optional[Charm]:
    for charm in CHARMS:
        if activity.keyword in charm.protection and prize.risk_key in charm.protection:
            return charm
    return None


def valid_combos() -> list[tuple[str, str, str]]:
    out = []
    for place, setting in SETTINGS.items():
        for act_id in setting.affords:
            act = _safe_lookup(ACTIVITIES, act_id)
            for prize_id, prize in PRIZES.items():
                if risked(prize, act, setting) and best_charm(act, prize):
                    out.append((place, act_id, prize_id))
    return out


def _predict_spill(world: World, activity: Activity, prize: Prize) -> bool:
    return risked(prize, activity, world.setting)


def tell(setting: Setting, activity: Activity, prize_cfg: Prize, name: str, gender: str,
         parent_type: str, trait: str) -> World:
    world = World(setting)
    world.wind = activity.weather

    hero = world.add(Entity(id=name, kind="character", type=gender, meters={}, memes={}))
    parent = world.add(Entity(id="Guardian", kind="character", type=parent_type, label="the elder"))
    prize = world.add(Entity(
        id="salt", type=prize_cfg.type, label=prize_cfg.label, phrase=prize_cfg.phrase,
        owner=hero.id, caretaker=parent.id, plural=prize_cfg.plural
    ))

    hero.memes["curiosity"] = 1
    hero.memes["trust"] = 0
    prize.meters["salt"] = 1

    world.say(
        f"{hero.id} was a little {trait} {hero.type} who loved the night path and the owl's soft hoo."
    )
    world.say(
        f"The elder kept {hero.pronoun('possessive')} {prize.label} ready, for in that land even salt was a gift."
    )

    world.para()
    world.say(
        f"One {activity.weather} night, {hero.id} and {hero.pronoun('possessive')} {parent.label} went to {setting.place}."
    )
    world.say(
        f"The wind moved through the stones, and the owl answered from above with a long hoo."
    )
    world.say(
        f"{hero.id} wanted to {activity.verb}, but {hero.pronoun('possessive')} {parent.label} held up a hand."
    )

    if _predict_spill(world, activity, prize):
        world.say(
            f'"Do not rush," {parent.label} said. "If you hurry, the salt will scatter, and the road will remember."'
        )
        hero.memes["fear"] += 1
        hero.memes["temptation"] += 1
        world.say(
            f"{hero.id} heard the warning but still tried to {activity.rush}."
        )

        if activity.keyword == "salt":
            hero.meters["spill"] += 1
            prize.meters["spilled"] += 1

        world.say(
            f"The elder stepped beside {hero.pronoun('object')} and tied a cloth around the salt bowl."
        )
        charm = best_charm(activity, prize)
        if charm:
            world.say(
                f'"Take the {charm.label}," said the elder. "{charm.clue}."'
            )
            hero.memes["trust"] += 1
            hero.memes["fear"] = 0
            world.say(
                f"{hero.id} slowed down, carried the salt with both hands, and listened to the hoo instead of the hurry."
            )
            world.say(
                f"By the end, {hero.id} reached {setting.place} safely, and the salt stayed bright inside the cloth."
            )
        else:
            pass
    else:
        pass

    world.facts.update(hero=hero, parent=parent, prize=prize, activity=activity, setting=setting)
    return world


