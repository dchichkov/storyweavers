def warn(world: World, guardian: Entity, hero: Entity, activity: Activity, prize: Entity) -> bool:
    pred = predict_mess(world, hero, activity, prize.id)
    if not pred["soiled"]:
        return False
    world.facts["predicted_soil"] = activity.soil
    world.say(f'"If you play now," said {guardian.label_word}, "your {prize.label_word} may end up {activity.soil}."')
    return True


