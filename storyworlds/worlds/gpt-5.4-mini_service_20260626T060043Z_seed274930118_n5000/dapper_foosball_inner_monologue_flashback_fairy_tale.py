def trouble(world: World, hero: Entity, activity: Activity) -> None:
    hero.memes["worry"] = hero.memes.get("worry", 0.0) + 1.0
    world.say(f"{hero.id} took a little breath, then tried to {activity.rush}.")


def offer_fix(world: World, guardian: Entity, hero: Entity, activity: Activity, prize: Entity) -> Optional[Gear]:
    gear_def = select_gear(activity, prize)
    if gear_def is None:
        return None
    gear = world.add(Entity(id=gear_def.id, type="gear", label=gear_def.label, protective=True, covers=set(gear_def.covers), plural=gear_def.plural))
    gear.worn_by = hero.id
    if predict_mess(world, hero, activity, prize.id)["soiled"]:
        gear.worn_by = None
        del world.entities[gear.id]
        return None
    world.say(f"Then {guardian.label_word} smiled and said, \"How about we {gear_def.prep} first?\"")
    return gear


def accept(world: World, hero: Entity, guardian: Entity, activity: Activity, prize: Entity, gear_def: Gear) -> None:
    hero.memes["joy"] = hero.memes.get("joy", 0.0) + 1.0
    hero.memes["worry"] = 0.0
    world.say(f"{hero.id} smiled, and {hero.pronoun()} nodded to {hero.pronoun('possessive')} {guardian.label_word}.")
    world.say(f"They {gear_def.tail}, and soon {hero.id} was {activity.gerund}, while {prize.label_word} stayed clean and bright.")


def tell(setting: Setting, activity: Activity, prize_cfg: Prize, hero_name: str = "Ellis", hero_type: str = "boy", parent_type: str = "mother", hero_traits: Optional[list[str]] = None) -> World:
    world = World(setting)
    hero = world.add(Entity(id=hero_name, kind="character", type=hero_type, traits=["little"] + (hero_traits or ["dapper", "earnest"])))
    guardian = world.add(Entity(id="Guardian", kind="character", type=parent_type, label="the guardian"))
    prize = world.add(Entity(id="Prize", type=prize_cfg.type, label=prize_cfg.label, phrase=prize_cfg.phrase, owner=hero.id, caretaker=guardian.id, region=prize_cfg.region, plural=prize_cfg.plural))

    introduce(world, hero)
    loves_game(world, hero, activity)
    show_prize(world, hero, prize)

    world.para()
    arrive(world, hero, guardian, setting)
    wants(world, hero, activity)
    flashback(world, hero, prize)
    inner_monologue(world, hero, activity, prize)
    warn(world, guardian, hero, activity, prize)
    trouble(world, hero, activity)

    world.para()
    gear_def = offer_fix(world, guardian, hero, activity, prize)
    if gear_def:
        accept(world, hero, guardian, activity, prize, gear_def)

    world.facts.update(hero=hero, guardian=guardian, prize=prize, prize_cfg=prize_cfg, activity=activity, setting=setting, gear=gear_def, resolved=gear_def is not None)
    return world


