def valid_combos() -> list[tuple[str, str, str]]:
    combos = []
    for place, setting in SETTINGS.items():
        for act_id in setting.affords:
            for prize_id, prize in PRIZES.items():
                if act_id == "signal" and prize.region == "head":
                    combos.append((place, act_id, prize_id))
                if act_id == "stroll" and prize.region in {"feet", "torso"}:
                    combos.append((place, act_id, prize_id))
    return combos


def select_gadget(activity: Activity, prize: Prize) -> Optional[Gadget]:
    for gadget in GADGETS:
        if activity.mess in gadget.guards and prize.region in gadget.covers:
            return gadget
    return None


ASP_RULES = r"""
prize_at_risk(A,P) :- splashes(A,R), worn_on(P,R).
protects(G,A,P) :- prize_at_risk(A,P), gear(G), mess_of(A,M), guards(G,M), covers(G,R), worn_on(P,R).
valid(Place,A,P) :- affords(Place,A), prize_at_risk(A,P), protects(_,A,P).
"""


def asp_facts() -> str:
    import asp
    lines = []
    for pid, s in SETTINGS.items():
        lines.append(asp.fact("setting", pid))
        if s.indoors:
            lines.append(asp.fact("indoors", pid))
        for a in sorted(s.affords):
            lines.append(asp.fact("affords", pid, a))
    for aid, a in ACTIVITIES.items():
        lines.append(asp.fact("activity", aid))
        lines.append(asp.fact("mess_of", aid, a.mess))
        lines.append(asp.fact("splashes", aid, a.region if hasattr(a, "region") else ("head" if aid == "signal" else "feet")))
    for pid, p in PRIZES.items():
        lines.append(asp.fact("prize", pid))
        lines.append(asp.fact("worn_on", pid, p.region))
    for g in GADGETS:
        lines.append(asp.fact("gear", g.id))
        for m in sorted(g.guards):
            lines.append(asp.fact("guards", g.id, m))
        for c in sorted(g.covers):
            lines.append(asp.fact("covers", g.id, c))
    return "\n".join(lines)


def asp_program(show: str) -> str:
    return f"{asp_facts()}\n{ASP_RULES}\n{show}\n"


def _do_activity(world: World, actor: Entity, activity: Activity, narrate: bool = True) -> None:
    world.zone = {"head"} if activity.id == "signal" else {"feet", "torso"}
    actor.meters[activity.mess] = actor.meters.get(activity.mess, 0.0) + 1
    actor.memes["joy"] = actor.memes.get("joy", 0.0) + 1
    for item in world.worn_items(actor):
        if item.region in world.zone:
            item.meters[activity.mess] = item.meters.get(activity.mess, 0.0) + 1
            item.meters["dirty"] = item.meters.get("dirty", 0.0) + 1
            if narrate:
                world.say(f"{actor.pronoun('possessive').capitalize()} {item.label} got messy.")
    if narrate:
        world.say(f"{actor.pronoun().capitalize()} was {activity.gerund}.")


def tell(setting: Setting, activity: Activity, prize_cfg: Prize, name: str, parent_type: str) -> World:
    world = World(setting)
    hero = world.add(Entity(name, kind="character", type="child"))
    parent = world.add(Entity("Parent", kind="character", type=parent_type, label=f"the {parent_type}"))
    prize = world.add(Entity("Prize", type=prize_cfg.type, label=prize_cfg.label, phrase=prize_cfg.phrase, owner=hero.id, caretaker=parent.id, region=prize_cfg.region, plural=prize_cfg.plural))
    hero.memes["love"] = 1
    prize.worn_by = hero.id

    world.say(f"{hero.id} was one of a quintuplet, and {hero.id} loved exploring {setting.place}.")
    world.say(f"{hero.id} wore {hero.pronoun('possessive')} {prize.label} and dreamed of {activity.verb}.")
    world.say(f"All five siblings liked the same adventure, so they had to take turns and share.")

    world.para()
    world.say(f"One day, the quintuplet reached {setting.place} and found the beacon blinking again and again.")
    world.say(f"{hero.id} wanted to {activity.verb}, but the others wanted a turn first.")
    world.say(f"That started a little conflict, because each sibling said, 'I go next!'")

    world.para()
    world.say(f"{parent.id} lifted a hand and said, 'We can solve this with a calm plan.'")
    gadget = select_gadget(activity, prize)
    if gadget is None:
        pass
    gadget_ent = world.add(Entity(gadget.id, type="gadget", label=gadget.label, protective=True, covers=set(gadget.covers), plural=gadget.plural))
    gadget_ent.worn_by = hero.id
    world.say(f"{hero.id}'s {parent_type} said to {gadget.prep}, then everyone would get a turn.")
    world.say(f"The quintuplet agreed, and the repeating turns made the waiting feel fair instead of angry.")
    _do_activity(world, hero, activity, narrate=False)
    world.say(f"They {gadget.tail}, and soon {hero.id} was {activity.gerund} without ruining {hero.pronoun('possessive')} {prize.label}.")
    world.say(f"In the end, the five siblings were smiling under the stars, and the little {prize.label} stayed safe.")

    world.facts.update(hero=hero, parent=parent, prize=prize, activity=activity, setting=setting, gadget=gadget_ent)
    return world


