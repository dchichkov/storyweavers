class World:
    def __init__(self, setting: Setting) -> None:
        self.setting = setting
        self.entities: dict[str, Entity] = {}
        self.fired: set[tuple] = set()
        self.paragraphs: list[list[str]] = [[]]
        self.zone: set[str] = set()
        self.weather: str = "snowy"
        self.facts: dict = {}

    def add(self, ent: Entity) -> Entity:
        self.entities[ent.id] = ent
        return ent

    def get(self, eid: str) -> Entity:
        if eid not in self.entities:
            label = str(eid).replace("_", " ")
            self.entities[eid] = Entity(str(eid), label=label)
        return self.entities[eid]

    def characters(self) -> list[Entity]:
        return [e for e in self.entities.values() if e.kind == "character"]

    def worn_items(self, actor: Entity) -> list[Entity]:
        return [e for e in self.entities.values() if e.worn_by == actor.id]

    def say(self, text: str) -> None:
        if text:
            self.paragraphs[-1].append(text)

    def para(self) -> None:
        if self.paragraphs[-1]:
            self.paragraphs.append([])

    def render(self) -> str:
        return "\n\n".join(" ".join(p) for p in self.paragraphs if p)

    def copy(self) -> "World":
        import copy as _copy
        clone = World(self.setting)
        clone.entities = _copy.deepcopy(self.entities)
        clone.fired = set(self.fired)
        clone.zone = set(self.zone)
        clone.weather = self.weather
        clone.paragraphs = [[]]
        return clone


def _mess_rule(world: World) -> list[str]:
    out: list[str] = []
    for actor in world.characters():
        if actor.meters.get("snow", 0) < THRESHOLD:
            continue
        for item in world.worn_items(actor):
            if item.worn_by != actor.id:
                continue
            if item.region not in world.zone:
                continue
            if ("mess", item.id) in world.fired:
                continue
            world.fired.add(("mess", item.id))
            item.meters["wet"] = item.meters.get("wet", 0) + 1
            item.meters["cold"] = item.meters.get("cold", 0) + 1
            out.append(f"{actor.pronoun('possessive').capitalize()} {item.label} got wet and cold.")
    return out


def _kindness_rule(world: World) -> list[str]:
    out: list[str] = []
    for actor in world.characters():
        if actor.memes.get("kindness", 0) < THRESHOLD:
            continue
        if ("kindness", actor.id) in world.fired:
            continue
        world.fired.add(("kindness", actor.id))
        actor.memes["joy"] = actor.memes.get("joy", 0) + 1
        out.append(f"{actor.id} felt warm inside from being kind.")
    return out


CAUSAL_RULES = [_mess_rule, _kindness_rule]


def propagate(world: World, narrate: bool = True) -> list[str]:
    produced: list[str] = []
    changed = True
    for _ in range(len(globals().get("CAUSAL_RULES", [])) + 4):
        changed = False
        for rule in CAUSAL_RULES:
            sents = rule(world)
            if sents:
                changed = True
                produced.extend(sents)
    if narrate:
        for s in produced:
            world.say(s)
    return produced


def predict_mess(world: World, actor: Entity, activity: Activity, prize_id: str) -> dict:
    sim = world.copy()
    sim.zone = set(activity.zone)
    sim.get(actor.id).meters[activity.mess] = sim.get(actor.id).meters.get(activity.mess, 0) + 1
    propagate(sim, narrate=False)
    prize = sim.entities[prize_id]
    return {"soiled": prize.meters.get("wet", 0) >= THRESHOLD}


def prize_at_risk(activity: Activity, prize: Prize) -> bool:
    return prize.region in activity.zone


def select_gift(activity: Activity, prize: Prize) -> Optional[Gift]:
    for gift in GIFTS:
        if activity.mess in gift.guards and prize.region in gift.covers:
            return gift
    return None


def introduce(world: World, hero: Entity) -> None:
    trait = next((t for t in hero.traits if t != "little"), "gentle")
    world.say(f"{hero.id} was a little {trait} {hero.type} who liked quiet winter walks.")


def loves(world: World, hero: Entity, activity: Activity) -> None:
    hero.memes["love"] = hero.memes.get("love", 0) + 1
    world.say(f"{hero.id} loved {activity.gerund} by the snowy curb, where even tiny steps felt like play.")


def arrives(world: World, hero: Entity, friend: Entity, activity: Activity) -> None:
    world.say(f"One snowy day, {hero.id} and {friend.id} came to the snowy curb.")
    world.say(f"The curb was bright with white snow and little tracks.")
    world.say(f"{hero.id} wanted to {activity.verb}, but there was a small problem waiting there.")


def warns(world: World, friend: Entity, hero: Entity, activity: Activity, prize: Entity) -> bool:
    pred = predict_mess(world, hero, activity, prize.id)
    if not pred["soiled"]:
        return False
    friend.memes["care"] = friend.memes.get("care", 0) + 1
    world.say(f'"If you do that, your {prize.label} will get wet," {friend.id} said kindly.')
    return True


def tries(world: World, hero: Entity, activity: Activity) -> None:
    hero.memes["want"] = hero.memes.get("want", 0) + 1
    hero.meters[activity.mess] = hero.meters.get(activity.mess, 0) + 1
    world.say(f"{hero.id} still tried to {activity.rush}, and the snow puffed up around {hero.pronoun('object')}.")
    propagate(world, narrate=True)


def offers_gift(world: World, friend: Entity, hero: Entity, activity: Activity, prize: Entity) -> Optional[Gift]:
    gift_def = select_gift(activity, prize)
    if gift_def is None:
        return None
    gift = world.add(Entity(
        id=gift_def.id,
        kind="thing",
        type="gift",
        label=gift_def.label,
        owner=hero.id,
        caretaker=friend.id,
        plural=gift_def.plural,
    ))
    gift.worn_by = hero.id
    if predict_mess(world, hero, activity, prize.id)["soiled"]:
        gift.worn_by = None
        del world.entities[gift.id]
        return None
    world.say(f'{friend.id} smiled. "Let us use {gift_def.label} first," {friend.id} said.')
    world.say(f"{friend.id} added, {gift_def.prep}.")
    return gift


def accept(world: World, hero: Entity, friend: Entity, activity: Activity, prize: Entity, gift_def: Gift) -> None:
    hero.memes["kindness"] = hero.memes.get("kindness", 0) + 1
    hero.memes["joy"] = hero.memes.get("joy", 0) + 1
    hero.memes["worry"] = 0
    world.say(f"{hero.id} nodded and smiled back. Kindness made the cold curb feel softer.")
    world.say(f"They {gift_def.tail}. Soon {hero.id} was {activity.gerund}, and {prize.label} stayed dry and safe.")
    world.say(f"At the end, the two friends laughed together beside the snowy curb.")


def tell(setting: Setting, activity: Activity, prize_cfg: Prize, hero_name: str = "Mason", hero_type: str = "mouse",
         hero_traits: Optional[list[str]] = None, friend_type: str = "rabbit") -> World:
    world = World(setting)
    hero = world.add(Entity(id=hero_name, kind="character", type=hero_type, traits=["little"] + (hero_traits or ["kind", "curious"])))
    friend = world.add(Entity(id="Pip", kind="character", type=friend_type, traits=["gentle"]))
    prize = world.add(Entity(
        id="prize",
        kind="thing",
        type=prize_cfg.type,
        label=prize_cfg.label,
        phrase=prize_cfg.phrase,
        owner=hero.id,
        caretaker=friend.id,
        worn_by=hero.id,
        plural=prize_cfg.plural,
    ))
    introduce(world, hero)
    loves(world, hero, activity)
    world.say(f"{friend.id} had given {hero.id} {prize_cfg.phrase}.")
    world.say(f"{hero.id} loved {prize.label} very much.")

    world.para()
    arrives(world, hero, friend, activity)
    warns(world, friend, hero, activity, prize)
    tries(world, hero, activity)

    world.para()
    gift_def = offers_gift(world, friend, hero, activity, prize)
    if gift_def:
        accept(world, hero, friend, activity, prize, gift_def)

    world.facts.update(hero=hero, friend=friend, prize=prize, prize_cfg=prize_cfg, activity=activity, setting=setting, gift=gift_def)
    return world


