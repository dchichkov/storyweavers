def tell(setting: Setting, prize: Prize, name: str, gender: str, parent_type: str, trait: str) -> World:
    world = World(setting)
    detective = world.add(Entity(id=name, kind="character", type=_story_name_type(gender), traits=["little", trait, "detective"]))
    parent = world.add(Entity(id="Parent", kind="character", type=parent_type))
    soup = world.add(Entity(id="Gazpacho", type="gazpacho", label="gazpacho", phrase=prize.phrase, owner=detective.id, caretaker=parent.id))
    spoon = world.add(Entity(id="Spoon", type="spoon", label="spoon", owner=parent.id))
    cat = world.add(Entity(id="Cat", kind="character", type="cat", label="the cat"))
    clue1 = world.add(Entity(id="Clue1", type="cucumber", label="cucumber slice"))
    clue2 = world.add(Entity(id="Clue2", type="parsley", label="parsley leaf"))
    clue3 = world.add(Entity(id="Clue3", type="spoon", label="shiny spoon"))

    detective.memes["curiosity"] = 1
    detective.memes["hunger"] = 1
    parent.memes["care"] = 1

    world.say(f"{detective.id} was a little {trait} detective who noticed every tiny clue.")
    world.say(f"{parent.pronoun('possessive').capitalize()} {parent.type} wanted to pamper {detective.pronoun('object')} with {soup.phrase}.")
    world.say(f"{detective.id} loved {soup.label} because it was {prize.trait}, cool, and delicious.")

    world.para()
    world.say(f"At {setting.place}, the lunch table was ready, but the spoon was missing.")
    world.say(f"{detective.id} frowned and began the case.")
    world.say(f"{detective.pronoun('subject').capitalize()} followed {clue1.label}, then {clue2.label}, as if the clues were leaving crumbs on purpose.")
    detective.memes["investigation"] = 1
    parent.memes["amused"] = 1

    world.para()
    world.say(f"Near the chair, {detective.id} found {cat.id} wearing {clue3.label} like a tiny shiny hat.")
    world.say(f"The cat blinked very seriously, which made the whole mystery feel even sillier.")
    detective.memes["joy"] = 1
    detective.memes["relief"] = 1

    world.para()
    world.say(f"{parent.pronoun('possessive').capitalize()} {parent.type} laughed, picked up the spoon, and said, \"Case solved.\"")
    world.say(f"Then {parent.id} gently pampered {detective.id} with the first cold spoonful of gazpacho.")
    world.say(f"{detective.id} smiled at the delectable soup, and even the cat got a respectful tiny taste from a clean dish.")

    world.facts.update(
        detective=detective,
        parent=parent,
        soup=soup,
        spoon=spoon,
        cat=cat,
        clues=[clue1, clue2, clue3],
        setting=setting,
        prize=prize,
    )
    return world


