def wants(world: World, hero: Entity, activity: Activity, prize: Entity) -> None:
    hero.memes["want"] = hero.memes.get("want", 0.0) + 1
    world.say(f"{hero.id} wanted to {activity.verb}, but the {prize.label} label looked mixed up in the food-dim corner.")


def warn(world: World, helper: Entity, hero: Entity, activity: Activity, prize: Entity) -> bool:
    pred = predict_mess(world, hero, activity, prize.id)
    if not pred["soiled"]:
        return False
    world.facts["predicted"] = True
    world.say(f'"Careful," {helper.id} said. "If you rush, the {prize.label} card may get all mixed and mushy."')
    return True


def joke(world: World, hero: Entity, activity: Activity) -> None:
    world.say(f"{hero.id} giggled, then tried to {activity.rush}, which was a bit of a silly shuffle.")


def kind_fix(world: World, helper: Entity, hero: Entity, activity: Activity, prize: Entity, gear: Gear) -> None:
    hero.memes["kindness"] = hero.memes.get("kindness", 0.0) + 1
    hero.memes["joy"] = hero.memes.get("joy", 0.0) + 1
    world.say(f"{helper.id} smiled and said, \"Let's be kind and keep the line in rhyme: left for bread, right for jam, and the pen can help us span.\"")
    world.say(f"{hero.id} used the pen to fix the sign, and soon the snacks were sorted fine.")
    world.say(f"They {gear.tail}. The {prize.label} stayed clean, and the lunch nook turned bright and keen.")


def tell(setting: Setting, activity: Activity, prize_cfg: Prize, hero_name: str = "Nina",
         hero_type: str = "girl", helper_type: str = "teacher") -> World:
    world = World(setting)
    hero = world.add(Entity(id=hero_name, kind="character", type=hero_type))
    helper = world.add(Entity(id="Helper", kind="character", type=helper_type, label="helper"))
    prize = world.add(Entity(id="prize", type=prize_cfg.type, label=prize_cfg.label,
                             phrase=prize_cfg.phrase, owner=hero.id, region=prize_cfg.region,
                             plural=prize_cfg.plural))
    pen = world.add(Entity(id="pen", type="pen", label="pen", phrase="a blue pen", owner=hero.id))
    intro(world, hero)
    loves_pen(world, hero, pen)
    world.para()
    arrives(world, hero, helper)
    wants(world, hero, activity, prize)
    warn(world, helper, hero, activity, prize)
    joke(world, hero, activity)
    world.para()
    gear = select_gear(activity, prize)
    if gear is not None:
        kind_fix(world, helper, hero, activity, prize, gear)
    world.facts.update(hero=hero, helper=helper, prize=prize, activity=activity, gear=gear, pen=pen)
    return world


