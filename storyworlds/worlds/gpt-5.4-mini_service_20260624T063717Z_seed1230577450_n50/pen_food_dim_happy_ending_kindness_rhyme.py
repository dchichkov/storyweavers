def _r_smudge(world: World) -> list[str]:
    out = []
    for actor in world.characters():
        if actor.meters.get("sticky", 0.0) < THRESHOLD:
            continue
        for item in world.worn_items(actor):
            if item.type != "paper" or "table" not in world.zone:
                continue
            sig = ("smudge", actor.id, item.id)
            if sig in world.fired:
                continue
            world.fired.add(sig)
            item.meters["smudged"] = item.meters.get("smudged", 0.0) + 1
            out.append(f"The paper got a little smudged near the food table.")
    return out


def _r_tidy(world: World) -> list[str]:
    out = []
    for item in list(world.entities.values()):
        if item.meters.get("smudged", 0.0) < THRESHOLD:
            continue
        sig = ("tidy", item.id)
        if sig in world.fired:
            continue
        world.fired.add(sig)
        out.append("A tidy fix could still make the table right.")
    return out


CAUSAL_RULES = [Rule("smudge", _r_smudge), Rule("tidy", _r_tidy)]


def propagate(world: World, narrate: bool = True) -> list[str]:
    out: list[str] = []
    changed = True
    for _ in range(len(globals().get("CAUSAL_RULES", [])) + 4):
        changed = False
        for rule in CAUSAL_RULES:
            bits = rule.apply(world)
            if bits:
                changed = True
                out.extend(bits)
    if narrate:
        for s in out:
            world.say(s)
    return out


def prize_at_risk(activity: Activity, prize: Prize) -> bool:
    return prize.region in activity.zone


def select_gear(activity: Activity, prize: Prize) -> Optional[Gear]:
    for gear in GEAR:
        if activity.mess in gear.guards and prize.region in gear.covers:
            return gear
    return None


def predict_mess(world: World, actor: Entity, activity: Activity, prize_id: str) -> dict:
    sim = world.copy()
    do_activity(sim, sim.get(actor.id), activity, narrate=False)
    prize = sim.entities[prize_id]
    return {"soiled": bool(prize.meters.get("smudged", 0.0) >= THRESHOLD)}


def do_activity(world: World, actor: Entity, activity: Activity, narrate: bool = True) -> None:
    if activity.id not in world.setting.affords:
        return
    world.zone = set(activity.zone)
    actor.meters[activity.mess] = actor.meters.get(activity.mess, 0.0) + 1
    actor.memes["glee"] = actor.memes.get("glee", 0.0) + 1
    propagate(world, narrate=narrate)


