def _r_stuck(world: World) -> list[str]:
    out: list[str] = []
    trouble = world.facts["trouble"]
    for crew in world.facts["crew"]:
        if crew.meters["push"] < THRESHOLD:
            continue
        sig = ("stuck", crew.id)
        if sig in world.fired:
            continue
        world.fired.add(sig)
        if trouble.makes_stuck:
            world.get("ship").meters["stuck"] += 1
            for mate in world.facts["crew"]:
                mate.memes["frustration"] += 1
            out.append("__stuck__")
    return out


def _r_kindness(world: World) -> list[str]:
    out: list[str] = []
    for crew in world.facts["crew"]:
        if crew.memes["kindness"] < THRESHOLD:
            continue
        sig = ("calm", crew.id)
        if sig in world.fired:
            continue
        world.fired.add(sig)
        crew.memes["hope"] += 1
        out.append(f"{crew.id} remembered to stay gentle.")
    return out


CAUSAL_RULES = [Rule("stuck", "physical", _r_stuck), Rule("kindness", "social", _r_kindness)]


def propagate(world: World, narrate: bool = True) -> list[str]:
    produced: list[str] = []
    changed = True
    for _ in range(len(globals().get("CAUSAL_RULES", [])) + 4):
        changed = False
        for rule in CAUSAL_RULES:
            sents = rule.apply(world)
            if sents:
                changed = True
                produced.extend(s for s in sents if not s.startswith("__"))
    if narrate:
        for s in produced:
            world.say(s)
    return produced


def reasonable_trouble(t: Trouble) -> bool:
    return t.makes_stuck and bool(t.tags & {"space", "ship"})


def sensible_tools() -> list[HelpTool]:
    return [t for t in HELP_TOOLS.values() if t.sense >= SENSE_MIN]


