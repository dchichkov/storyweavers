    hero, parent, prize, mystery, tool = f["hero"], f["parent"], f["prize"], f["mystery"], (f.get("tool") or next(iter(TOOLS.values())))
    qa = [
        QAItem(
            question=f"What did {hero.id} gasp about at the start of the story?",
            answer=f"{hero.id} gasped when {mystery.clue} made the room feel strange and quiet.",
        ),
        QAItem(
            question=f"What did {hero.id} have to obey before solving the mystery?",
            answer=f"{hero.id} had to obey {hero.pronoun('possessive')} {parent.label} and check the clues before rushing ahead.",
        ),
        QAItem(
            question=f"What prize stayed safe while the mystery was solved?",
            answer=f"The {prize.label} stayed safe: {prize.phrase} remained in the room while the clues were shared.",
        ),
        QAItem(
            question=f"How did the characters work together in the story?",
            answer=f"They shared the clues carefully, used {tool.label}, and followed the rhyme until the answer made sense.",
        ),
    ]
    return qa


