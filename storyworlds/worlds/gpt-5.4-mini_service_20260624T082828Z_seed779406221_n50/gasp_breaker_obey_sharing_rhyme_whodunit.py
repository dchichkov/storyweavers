def valid_combos() -> list[tuple[str, str, str]]:
    combos = []
    for place, setting in SETTINGS.items():
        for myst in setting.affords:
            for prize_id in PRIZES:
                combos.append((place, myst, prize_id))
    return combos


def reasonableness_gate(place: str, mystery: str, prize: str) -> None:
    if place not in SETTINGS:
        pass
    if mystery not in MYSTERIES:
        pass
    if prize not in PRIZES:
        pass
    if mystery not in _safe_lookup(SETTINGS, place).affords:
        pass


def valid_name(gender: str, prize: str) -> None:
    if gender not in {"girl", "boy"}:
        pass
    _ = _safe_lookup(PRIZES, prize)


def choose_tool(mystery: Mystery) -> Tool:
    for tool in TOOLS:
        if mystery.id in tool.helps:
            return tool
    return _safe_lookup(TOOLS, 0)


def generate_story_state(params: StoryParams) -> World:
    world = World(_safe_lookup(SETTINGS, params.place))
    hero = world.add(Entity(
        id=params.name,
        kind="character",
        type=params.gender,
        label=params.name,
        meters={"worry": 0.0},
        memes={"curiosity": 1.0, "obey": 0.0, "gasp": 0.0},
    ))
    parent = world.add(Entity(
        id="Parent",
        kind="character",
        type=params.parent,
        label=f"the {params.parent}",
        meters={"calm": 1.0},
        memes={"care": 1.0},
    ))
    prize = world.add(Entity(
        id="Prize",
        type=_safe_lookup(PRIZES, params.prize).type,
        label=_safe_lookup(PRIZES, params.prize).label,
        phrase=_safe_lookup(PRIZES, params.prize).phrase,
        owner=hero.id,
        caretaker=parent.id,
        plural=_safe_lookup(PRIZES, params.prize).plural,
    ))
    mystery = _safe_lookup(MYSTERIES, params.mystery)
    tool = choose_tool(mystery)

    # Act 1
    world.say(f"{hero.id} was a {params.trait} little {params.gender} who loved a good mystery.")
    world.say(f"{hero.pronoun().capitalize()} had {prize.phrase}, and {hero.pronoun('possessive')} {parent.label} kept it tidy.")
    world.say(f"That day, the room felt hushed, and {mystery.clue}.")

    # Act 2
    world.para()
    hero.meters["worry"] += 1.0
    hero.memes["gasp"] += 1.0
    world.say(f"{hero.id} let out a small gasp and looked around.")
    world.say(f"{hero.id} wanted to {mystery.verb}, but {hero.pronoun('possessive')} {parent.label} said, \"Obey first. We will check the clues.\"")
    hero.memes["obey"] += 1.0
    world.say(f"So {hero.id} chose to obey, picked up {tool.label}, and began to {tool.use}.")
    world.say(f"They shared the clues carefully, one idea at a time.")

    # Act 3
    world.para()
    world.say(f"At last, the answer came clear: {mystery.reveal}.")
    world.say(f"{hero.id} smiled, because the mystery made sense now.")
    world.say(f"The lights stayed on, the {prize.label} stayed safe, and {hero.id} shared the solved rhyme with {hero.pronoun('possessive')} {parent.label}.")
    world.facts.update(hero=hero, parent=parent, prize=prize, mystery=mystery, tool=tool, params=params)
    return world


