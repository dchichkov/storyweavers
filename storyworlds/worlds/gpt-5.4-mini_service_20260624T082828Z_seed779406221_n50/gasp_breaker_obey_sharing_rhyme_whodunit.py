ASP_RULES = r"""
% A story is valid when the mystery theme fits the place, and the prize is
% something that can be searched for and shared in the same scene.
valid(Place, M, P) :- affords(Place, M), mystery(M), prize(P), shareable(P), clueable(M).

% A compatible story must include the requested key words across the theme set.
keyword_story(Place, M, P) :- valid(Place, M, P), has_word(M, gasp), has_word(M, breaker), has_word(M, obey).
"""


def asp_facts() -> str:
    import asp
    lines: list[str] = []
    for pid, setting in SETTINGS.items():
        lines.append(asp.fact("place", pid))
        for a in sorted(setting.affords):
            lines.append(asp.fact("affords", pid, a))
    for mid, myst in MYSTERIES.items():
        lines.append(asp.fact("mystery", mid))
        for t in sorted(myst.tags):
            lines.append(asp.fact("has_word", mid, t))
        lines.append(asp.fact("clueable", mid))
    for pid, prize in PRIZES.items():
        lines.append(asp.fact("prize", pid))
        lines.append(asp.fact("shareable", pid))
    return "\n".join(lines)


def asp_program(show: str) -> str:
    return f"{asp_facts()}\n{ASP_RULES}\n{show}\n"


def asp_valid_combos() -> list[tuple]:
    import asp
    model = asp.one_model(asp_program("#show valid/3."))
    return sorted(set(asp.atoms(model, "valid")))


def asp_verify() -> int:
    clingo_set = set(asp_valid_combos())
    python_set = set(valid_combos())
    if clingo_set == python_set:
        print(f"OK: clingo gate matches valid_combos() ({len(clingo_set)} combos).")
        return 0
    print("MISMATCH between clingo and valid_combos():")
    if clingo_set - python_set:
        print("  only in clingo:", sorted(clingo_set - python_set))
    if python_set - clingo_set:
        print("  only in python:", sorted(python_set - clingo_set))
    return 1


