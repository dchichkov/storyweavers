def asp_facts() -> str:
    import asp
    lines = []
    for pid in SETTINGS:
        lines.append(asp.fact("setting", pid))
        for a in sorted(_safe_lookup(SETTINGS, pid).affords):
            lines.append(asp.fact("affords", pid, a))
    for aid in ACTIVITIES:
        lines.append(asp.fact("activity", aid))
    for pid in PRIZES:
        lines.append(asp.fact("prize", pid))
    for tid, t in TRANSFORMS.items():
        lines.append(asp.fact("transform", tid))
        if t.helps:
            lines.append(asp.fact("helps", tid))
    return "\n".join(lines)


def asp_program(show: str) -> str:
    return f"{asp_facts()}\n{ASP_RULES}\n{show}\n"


def tell(setting: Setting, activity: Activity, prize: Prize, hero_name: str, hero_type: str,
         trait: str, transform: Transform) -> World:
    world = World(setting)
    hero = world.add(Entity(
        id=hero_name,
        kind="character",
        type=hero_type,
        traits=["little", trait, "worried"],
    ))
    prize_ent = world.add(Entity(
        id="prize",
        type=prize.type,
        label=prize.label,
        phrase=prize.phrase,
        owner=hero.id,
    ))
    # Setup
    world.say(f"{hero.id} was a little {trait} {hero_type} who wanted to earn a gold star.")
    world.say(f"{hero.pronoun('subject').capitalize()} loved {activity.gerund} at {setting.place}.")
    world.say(f"One day {hero.id} saw {hero.pronoun('possessive')} face in the glass and found a zit.")
    world.say(f"{hero.id} frowned and whispered, “Oh dear me, oh my, that zit will make folks stare.”")

    # Inner monologue
    world.para()
    hero.memes["worry"] += 1
    world.say(f'Inside {hero.id} thought, “If I hide it, I can still earn the star.”')
    world.say(f'“I must look tidy and spry,” {hero.id} thought, “or the teacher will sigh.”')

    # Transformation attempt
    world.para()
    world.say(f"{hero.id} went {activity.rush} with {transform.prep}.")
    hero.meters["transformation"] += 1
    world.say(f"But {transform.reveal}, and {hero.id} only {transform.effect}.")
    world.say(f"The room felt small, and {hero.id}'s tummy felt wobbly.")

    # Bad ending
    world.para()
    hero.memes["panic"] += 1
    world.say(f"When it was {hero.id}'s turn, the words came out in a tiny squeak.")
    world.say(f"{hero.id} forgot the rhyme, then fumbled the card, and the class went quiet.")
    world.say(f"{hero.id} did not earn the gold star that day.")
    world.say(f"Still, {hero.id} went home slow and small, with the zit on {hero.pronoun('possessive')} chin and a brave little breath.")

    world.facts.update(
        hero=hero,
        prize=prize_ent,
        activity=activity,
        transform=transform,
        setting=setting,
        bad_ending=True,
    )
    return world


