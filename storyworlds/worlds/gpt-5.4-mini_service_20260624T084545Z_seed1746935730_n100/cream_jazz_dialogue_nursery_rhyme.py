def prize_at_risk(activity: Activity, prize: Prize) -> bool:
    return activity.id == "jazz" and prize.locus == "table"


def select_gear(activity: Activity, prize: Prize) -> Optional[Gear]:
    for g in GEAR:
        if "spill" in g.guards and prize.locus in g.covers:
            return g
    return None


def valid_combos() -> list[tuple[str, str, str]]:
    out = []
    for place, setting in SETTINGS.items():
        for act_id in setting.affords:
            act = _safe_lookup(ACTIVITIES, act_id)
            for prize_id, prize in PRIZES.items():
                if prize_at_risk(act, prize) and select_gear(act, prize):
                    out.append((place, act_id, prize_id))
    return out


def _do_activity(world: World, actor: Entity, activity: Activity, narrate: bool = True) -> None:
    if activity.id not in world.setting.affords:
        return
    world.active_activity = activity.id
    actor.memes["joy"] += 1
    if activity.id == "jazz":
        cream = world.entities.get("cream")
        if cream and cream.on == "table":
            cream.meters["spill"] += 1
            if cream.carried_by is None and not cream.protective:
                cream.memes["tension"] += 1
    if narrate:
        world.say(f"{actor.cap()} started {activity.gerund}.")


def predict_spill(world: World, actor: Entity, activity: Activity, prize_id: str) -> bool:
    sim = world.copy()
    _do_activity(sim, sim.get(actor.id), activity, narrate=False)
    prize = sim.entities[prize_id]
    return prize.meters["spill"] >= THRESHOLD and not prize.protective


def introduce(world: World, hero: Entity) -> None:
    world.say(f"{hero.cap()} was a little {next(t for t in hero.memes.keys() if False) if False else hero.type} who loved a tune.")


def tell(world_setting: Setting, activity: Activity, prize_cfg: Prize,
         hero_name: str = "Mina", hero_type: str = "girl",
         hero_traits: Optional[list[str]] = None, parent_type: str = "mother") -> World:
    world = World(world_setting)
    hero = world.add(Entity(id=hero_name, kind="character", type=hero_type, memes={"joy": 0.0, "worry": 0.0, "love": 0.0, "tension": 0.0}))
    parent = world.add(Entity(id="Parent", kind="character", type=parent_type, label="mom" if parent_type == "mother" else "dad"))
    cream = world.add(Entity(id="cream", type="bowl", label="cream", phrase=prize_cfg.phrase, owner=hero.id, caretaker=parent.id, on="table"))
    hero.memes["love"] += 1
    world.say(f"{hero_name} was a {random.choice(hero_traits or ['tiny', 'cheery'])} little {hero_type} who loved jazz.")
    world.say(f"{hero_name} also loved the sweet cream in the bowl.")
    world.para()
    world.say(f"One day in {world_setting.place}, {hero_name} tapped the keys and smiled.")
    world.say(f'{hero_name} said, "I want to {activity.verb}!"')
    world.say(f'{parent.cap()} said, "Softly now, dear one. That music may {activity.effect}, and the cream may spill."')
    hero.memes["worry"] += 1
    world.para()
    _do_activity(world, hero, activity)
    if predict_spill(world, hero, activity, cream.id):
        world.say(f'{hero_name} said, "But I love jazz!"')
        world.say(f'{parent.cap()} lifted the bowl and said, "We can keep the cream safe."')
        gear = select_gear(activity, cream)
        if gear is None:
            gear = next(iter(globals().get("GEARS", globals().get("GEAR", []))))
        tray = world.add(Entity(id=gear.id, type="thing", label=gear.label, protective=True, covers=set(gear.covers)))
        tray.carried_by = hero.id
        cream.protective = True
        world.say(f'{parent.cap()} smiled. "Let us {gear.prep}."')
        world.say(f'{hero_name} grinned and agreed.')
        world.para()
        _do_activity(world, hero, activity)
        world.say(f'Then they {gear.tail}, and the cream stayed neat and still.')
        world.say(f'{hero_name} played jazz, and the bowl of cream shone like moonlight.')
    world.facts.update(hero=hero, parent=parent, cream=cream, activity=activity, setting=world_setting)
    return world


