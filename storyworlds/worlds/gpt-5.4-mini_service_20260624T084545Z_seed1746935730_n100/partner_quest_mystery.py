def valid_combos() -> list[tuple[str, str, str]]:
    combos = []
    for place, setting in SETTINGS.items():
        for q in setting.affords:
            qid = q.split(",")[-1]
            if qid in QUESTS:
                for prize in PRIZES:
                    combos.append((place, qid, prize))
    return combos


def resolve_params(args: argparse.Namespace, rng: random.Random) -> StoryParams:
    combos = [c for c in valid_combos()
              if (getattr(args, "place", None) is None or c[0] == getattr(args, "place", None))
              and (getattr(args, "quest", None) is None or c[1] == getattr(args, "quest", None))
              and (getattr(args, "prize", None) is None or c[2] == getattr(args, "prize", None))]
    if not combos:
        return _fallback_storyparams(args, rng, StoryParams, globals())
    place, quest, prize = rng.choice(list(combos))
    name = getattr(args, "name", None) or rng.choice(HERO_NAMES)
    partner = getattr(args, "partner", None) or rng.choice(PARTNER_NAMES)
    return StoryParams(place=place, quest=quest, prize=prize, name=name, partner=partner)


def _do_quest(world: World, hero: Entity, quest: Quest, prize: Entity, partner: Entity) -> None:
    hero.meters["search"] += 1
    hero.memes["curiosity"] += 1
    partner.memes["help"] += 1
    world.say(
        f"{hero.id} and {partner.id} began a {quest.keyword} {quest.gerund} in {world.setting.place}."
    )
    world.say(f"{quest.trail} {quest.clue_verb} them first, so they followed it carefully.")
    world.say(f"It felt like a small quest, but {quest.tension}.")


def _clue_turn(world: World, hero: Entity, partner: Entity, quest: Quest, prize: Entity) -> None:
    hero.memes["wonder"] += 1
    partner.memes["calm"] += 1
    prize.meters["noticed"] = 1
    world.say(
        f"Then {partner.id} {quest.clue_verb} something odd near the {prize.location}: "
        f"{quest.reveal}."
    )
    world.say(
        f"{hero.id} checked the {prize.label} and saw how the clue fit the mystery."
    )


def _resolve(world: World, hero: Entity, partner: Entity, quest: Quest, prize: Entity) -> None:
    hero.memes["joy"] += 1
    hero.memes["worry"] = 0
    partner.memes["joy"] += 1
    world.say(
        f"At last, they smiled. The {quest.id} quest was done, and {hero.id} carried the {prize.label} "
        f"back with {partner.id} beside {hero.pronoun('object')}."
    )
    world.say(
        f"By the end, {quest.reveal}, and the whole place felt quiet and solved."
    )


def tell(params: StoryParams) -> World:
    world = World(_safe_lookup(SETTINGS, params.place))

    hero = world.add(Entity(id=params.name, kind="character", type="girl" if params.name in {"Mina", "Nora", "June"} else "boy"))
    partner = world.add(Entity(id=params.partner, kind="character", type="friend"))
    quest = _safe_lookup(QUESTS, params.quest)
    prize = world.add(Entity(id="prize", type=_safe_lookup(PRIZES, params.prize).type, label=_safe_lookup(PRIZES, params.prize).label, phrase=_safe_lookup(PRIZES, params.prize).phrase))

    world.say(f"{hero.id} and {partner.id} were a good partner team.")
    world.say(f"One day, they heard about a {quest.keyword} {quest.verb} in {world.setting.place}.")
    world.say(f"{hero.id} wanted to help, because {quest.tension}.")

    world.para()
    _do_quest(world, hero, quest, prize, partner)

    world.para()
    _clue_turn(world, hero, partner, quest, prize)

    world.para()
    _resolve(world, hero, partner, quest, prize)

    world.facts.update(hero=hero, partner=partner, quest=quest, prize=prize)
    return world


