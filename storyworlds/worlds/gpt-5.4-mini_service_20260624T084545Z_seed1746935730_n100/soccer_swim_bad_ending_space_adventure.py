def _r_soil(world: World) -> list[str]:
    out: list[str] = []
    for actor in world.characters():
        for kind in ("dusty", "wet"):
            if actor.meters.get(kind, 0.0) < THRESHOLD:
                continue
            for item in world.worn_items(actor):
                if item.protective or item.region not in world.zone:
                    continue
                if world.covered(actor, item.region):
                    continue
                sig = ("soil", item.id, kind)
                if sig in world.fired:
                    continue
                world.fired.add(sig)
                item.meters[kind] = item.meters.get(kind, 0.0) + 1
                item.meters["broken"] = item.meters.get("broken", 0.0) + (1 if kind == "dusty" else 0)
                if kind == "wet":
                    out.append(f"{actor.pronoun('possessive').capitalize()} {item.label} got wet.")
                else:
                    out.append(f"{actor.pronoun('possessive').capitalize()} {item.label} got scratched and dusty.")
    return out


def _r_alarm(world: World) -> list[str]:
    out: list[str] = []
    for item in list(world.entities.values()):
        if item.meters.get("broken", 0.0) < THRESHOLD:
            continue
        sig = ("alarm", item.id)
        if sig in world.fired:
            continue
        world.fired.add(sig)
        out.append("An alarm chirped through the station.")
    return out


CAUSAL_RULES = [Rule("soil", _r_soil), Rule("alarm", _r_alarm)]


def propagate(world: World, narrate: bool = True) -> list[str]:
    produced: list[str] = []
    changed = True
    for _ in range(len(globals().get("CAUSAL_RULES", [])) + 4):
        changed = False
        for rule in CAUSAL_RULES:
            sents = rule.apply(world)
            if sents:
                changed = True
                produced.extend(sents)
    if narrate:
        for s in produced:
            world.say(s)
    return produced


