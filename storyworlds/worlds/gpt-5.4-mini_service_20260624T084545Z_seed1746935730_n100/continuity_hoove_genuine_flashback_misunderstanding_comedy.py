def introduce(world: World, hero: Entity, parent: Entity, prize: Entity) -> None:
    world.say(
        f"{hero.id} was a little {next(t for t in hero.traits if t != 'little')} {hero.type} "
        f"who cared about continuity in the silliest shows."
    )
    world.say(
        f"{hero.id}'s {parent.type} had given {hero.pronoun('object')} {prize.phrase}, "
        f"and {hero.id} loved how it looked in the parade mirror."
    )


def flashback(world: World, hero: Entity, prize: Entity) -> None:
    hero.memes["memory"] += 1
    world.say(
        f"Flashback: yesterday, the ribbon on {hero.pronoun('possessive')} left hoove had slipped off, "
        f"and everyone had giggled until it was found in a bucket."
    )
    world.say(
        f"That was why {hero.id} kept checking the {prize.label}; {hero.id} wanted everything to look genuine."
    )


def misunderstanding(world: World, hero: Entity, parent: Entity, activity: Activity, prize: Entity) -> None:
    hero.memes["confusion"] += 1
    hero.memes["worry"] += 1
    world.say(
        f"One day, {hero.id} heard {parent.pronoun('possessive')} {parent.type} say, "
        f"\"Keep the continuity!\""
    )
    world.say(
        f"{hero.id} nodded very hard, but then misunderstood and thought it meant, "
        f"\"Keep going no matter how slippery the floor gets.\""
    )
    world.say(
        f"So {hero.id} tried to {activity.rush}, while {hero.pronoun('possessive')} {prize.label} bobbed like a tiny flag."
    )


def predict_mess(world: World, actor: Entity, activity: Activity, prize_id: str) -> dict:
    sim = world.copy()
    _do_activity(sim, sim.get(actor.id), activity, narrate=False)
    prize = sim.get(prize_id)
    return {"soiled": prize.meters["dirty"] >= THRESHOLD}


def _do_activity(world: World, actor: Entity, activity: Activity, narrate: bool = True) -> None:
    world.zone = set(activity.zone)
    actor.meters[activity.mess] += 1
    actor.memes["joy"] += 1
    for item in world.worn_items(actor):
        if item.protective:
            continue
        if item.region in world.zone and not world.covered(actor, item.region):
            item.meters["dirty"] += 1
            item.meters[activity.mess] += 1
            if narrate:
                world.say(f"{actor.pronoun('possessive').capitalize()} {item.label} got {activity.soil}.")


def warning(world: World, parent: Entity, hero: Entity, activity: Activity, prize: Entity) -> bool:
    pred = predict_mess(world, hero, activity, prize.id)
    if not pred["soiled"]:
        return False
    hero.memes["worry"] += 1
    world.say(
        f"\"If you do that, your {prize.label} will get {activity.soil},\" {parent.pronoun('possessive')} {parent.type} said."
    )
    return True


def compromise(world: World, parent: Entity, hero: Entity, activity: Activity, prize: Entity) -> Optional[Gear]:
    gear_def = select_gear(activity, prize)
    if gear_def is None:
        return None
    gear = world.add(Entity(
        id=gear_def.id,
        kind="thing",
        type="gear",
        label=gear_def.label,
        protective=True,
        covers=set(gear_def.covers),
        plural=gear_def.plural,
        owner=hero.id,
        caretaker=parent.id,
        worn_by=hero.id,
    ))
    if predict_mess(world, hero, activity, prize.id)["soiled"]:
        gear.worn_by = None
        del world.entities[gear.id]
        return None
    world.say(
        f"Then {parent.id} smiled the way grown-ups do when they solve a sticky joke and said, "
        f"\"How about we {gear_def.prep} and try again?\""
    )
    return gear


