def generation_prompts(world: World) -> list[str]:
    f = world.facts
    hero, parent, activity, prize = f["hero"], f["parent"], f["activity"], f["prize"]
    return [
        f'Write a funny short story for a young child about "{activity.keyword}" and a missing {prize.label}.',
        f"Tell a comedy story where {hero.id} tries to follow continuity, but {parent.type} warns about the {prize.phrase}.",
        f'Write a gentle, silly story that uses the words "flashback", "misunderstanding", and "genuine".',
    ]


def story_qa(world: World) -> list[QAItem]:
    f = world.facts
    hero, parent, activity, prize = f["hero"], f["parent"], f["activity"], f["prize"]
    gear = f.get("gear")
    qa = [
        QAItem(
            question=f"Who is the story mostly about?",
            answer=f"It is mostly about {hero.id}, a little {hero.type}, and {parent.type}, who helps {hero.id} fix a funny problem.",
        ),
        QAItem(
            question=f"Why did {hero.id} worry about the {prize.label}?",
            answer=f"{hero.id} worried because {prize.label} was genuine and special, and {activity.soil} would have made it look messy.",
        ),
        QAItem(
            question=f"What mistake did {hero.id} make after hearing \"keep the continuity\"?",
            answer=f"{hero.id} misunderstood the words and thought it meant to keep going straight into the slippery fun instead of stopping to think.",
        ),
    ]
    if gear:
        qa.append(QAItem(
            question=f"How did the {gear.label} help?",
            answer=f"The {gear.label} covered the right part of {hero.id}'s body, so {hero.id} could {activity.verb} without ruining the {prize.label}.",
        ))
    return qa


def world_knowledge_qa(world: World) -> list[QAItem]:
    tags = world.facts["activity"].tags
    out: list[QAItem] = []
    for tag, pairs in KNOWLEDGE.items():
        if tag in tags:
            out.extend(QAItem(question=q, answer=a) for q, a in pairs)
    return out


def format_qa(sample: StorySample) -> str:
    lines = ["== (1) Generation prompts =="]
    for i, p in enumerate(sample.prompts, 1):
        lines.append(f"{i}. {p}")
    lines.append("")
    lines.append("== (2) Story questions ==")
    for item in sample.story_qa:
        lines.append(f"Q: {item.question}")
        lines.append(f"A: {item.answer}")
    lines.append("")
    lines.append("== (3) World questions ==")
    for item in sample.world_qa:
        lines.append(f"Q: {item.question}")
        lines.append(f"A: {item.answer}")
    return "\n".join(lines)


def dump_trace(world: World) -> str:
    lines = ["--- world model state ---"]
    for e in list(world.entities.values()):
        meters = {k: v for k, v in e.meters.items() if v}
        memes = {k: v for k, v in e.memes.items() if v}
        bits = []
        if meters:
            bits.append(f"meters={dict(meters)}")
        if memes:
            bits.append(f"memes={dict(memes)}")
        if e.protective:
            bits.append(f"covers={sorted(e.covers)}")
        lines.append(f"  {e.id:8} ({e.type:7}) {' '.join(bits)}")
    return "\n".join(lines)


