def _r_scoot(world: World) -> list[str]:
    out: list[str] = []
    for actor in world.characters():
        if actor.meters["rush"] < THRESHOLD:
            continue
        sig = ("calm_after_scoot", actor.id)
        if sig in world.fired:
            continue
        world.fired.add(sig)
        actor.memes["excitement"] += 0.5
        actor.meters["calm"] += 0.2
        out.append(f"{actor.id} zoomed along with a soft little scoot.")
    return out


def _r_wrinkle(world: World) -> list[str]:
    out: list[str] = []
    for actor in world.characters():
        if actor.meters["rush"] < THRESHOLD:
            continue
        for item in world.worn_items(actor):
            if item.protective or item.region not in world.zone:
                continue
            if world.covered(actor, item.region):
                continue
            sig = ("wrinkle", actor.id, item.id)
            if sig in world.fired:
                continue
            world.fired.add(sig)
            item.meters["wrinkle"] += 1
            item.meters["tidiness"] += 1
            out.append(f"{actor.pronoun('possessive').capitalize()} {item.label} got wrinkled.")
    return out


def _r_loss(world: World) -> list[str]:
    out: list[str] = []
    for actor in world.characters():
        if actor.memes["worry"] < THRESHOLD:
            continue
        for item in list(world.entities.values()):
            if item.owner != actor.id or item.type != "visa":
                continue
            if item.meters["loss"] >= THRESHOLD:
                continue
            sig = ("loss", actor.id, item.id)
            if sig in world.fired:
                continue
            world.fired.add(sig)
            item.meters["loss"] += 1
            out.append(f"{actor.pronoun('possessive').capitalize()} visa stayed safe in the little pocket.")
    return out


CAUSAL_RULES = [
    Rule("scoot", _r_scoot),
    Rule("wrinkle", _r_wrinkle),
    Rule("loss", _r_loss),
]


def propagate(world: World, narrate: bool = True) -> list[str]:
    produced: list[str] = []
    changed = True
    for _ in range(len(globals().get("CAUSAL_RULES", [])) + 4):
        changed = False
        for rule in CAUSAL_RULES:
            sents = rule.apply(world)
            if sents:
                changed = True
                produced.extend(sents)
    if narrate:
        for s in produced:
            world.say(s)
    return produced


def prize_at_risk(activity: Activity, prize: Prize) -> bool:
    return prize.region in activity.zone


def select_gear(activity: Activity, prize: Prize) -> Optional[Gear]:
    for gear in GEAR:
        if activity.mess in gear.guards and prize.region in gear.covers:
            return gear
    return None


def predict_mess(world: World, actor: Entity, activity: Activity, prize_id: str) -> dict:
    sim = world.copy()
    _do_activity(sim, sim.get(actor.id), activity, narrate=False)
    prize = sim.entities.get(prize_id)
    return {"wrinkled": bool(prize and prize.meters["wrinkle"] >= THRESHOLD)}


def _do_activity(world: World, actor: Entity, activity: Activity, narrate: bool = True) -> None:
    if activity.id not in world.setting.affords:
        return
    world.zone = set(activity.zone)
    actor.meters[activity.mess] += 1
    actor.meters["rush"] += 1
    actor.memes["excitement"] += 1
    propagate(world, narrate=narrate)


