def _r_spill(world: World) -> list[str]:
    out: list[str] = []
    for actor in world.characters():
        if actor.meters["spill"] < THRESHOLD:
            continue
        for item in world.worn_items(actor):
            if item.label == "apron":
                continue
            if item.plural:
                continue
            if item.id in {"bowl", "table"}:
                continue
            sig = ("spill", actor.id, item.id)
            if sig in world.fired:
                continue
            if item.id == "bread" and item.meters["dry"] < THRESHOLD:
                continue
            world.fired.add(sig)
            item.meters["dirty"] += 1
            out.append(f"{item.label.capitalize()} got a little messy.")
    return out


def _r_worry(world: World) -> list[str]:
    out: list[str] = []
    for item in list(world.entities.values()):
        if item.meters["dirty"] < THRESHOLD or not item.caretaker:
            continue
        sig = ("worry", item.id)
        if sig in world.fired:
            continue
        world.fired.add(sig)
        carer = world.get(item.caretaker)
        carer.memes["worry"] += 1
        out.append(f"That made {carer.label} worry a little.")
    return out


def _r_help(world: World) -> list[str]:
    out: list[str] = []
    if world.facts.get("surprise_helper"):
        sig = ("help", world.facts["surprise_helper"])
        if sig not in world.fired:
            world.fired.add(sig)
            helper = world.get(world.facts["surprise_helper"])
            helper.memes["kindness"] += 1
            out.append("A surprise helper came in ready to help.")
    return out


CAUSAL_RULES: list[Rule] = [
    Rule("spill", "physical", _r_spill),
    Rule("worry", "social", _r_worry),
    Rule("help", "social", _r_help),
]


def propagate(world: World, narrate: bool = True) -> list[str]:
    produced: list[str] = []
    changed = True
    for _ in range(len(globals().get("CAUSAL_RULES", [])) + 4):
        changed = False
        for rule in CAUSAL_RULES:
            sents = rule.apply(world)
            if sents:
                changed = True
                produced.extend(sents)
    if narrate:
        for s in produced:
            world.say(s)
    return produced


def _do_activity(world: World, actor: Entity, activity: Activity, narrate: bool = True) -> None:
    if activity.id not in world.setting.affords:
        return
    world.zone = set(activity.zone)
    actor.meters["spill"] += 1
    actor.memes["joy"] += 1
    propagate(world, narrate=narrate)


def predict_spill(world: World, actor: Entity, activity: Activity, prize_id: str) -> dict:
    sim = world.copy()
    _do_activity(sim, sim.get(actor.id), activity, narrate=False)
    prize = sim.get(prize_id)
    return {
        "dirty": bool(prize.meters["dirty"] >= THRESHOLD),
        "worry": sum(e.memes["worry"] for e in sim.characters()),
    }


