def warn(world: World, parent: Entity, hero: Entity, activity: Activity, prize: Entity) -> bool:
    pred = predict_spill(world, hero, activity, prize.id)
    if not pred["dirty"]:
        return False
    world.facts["lesson"] = "slow and careful"
    world.say(
        f'"If you rush, your {prize.label} will get {activity.soil}," {parent.label} said softly. '
        f'"Let’s be slow and careful."'
    )
    return True


