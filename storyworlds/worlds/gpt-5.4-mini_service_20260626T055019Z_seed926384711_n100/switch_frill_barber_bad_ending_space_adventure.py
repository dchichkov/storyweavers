def prize_at_risk(activity: Activity, prize: Prize) -> bool:
    return prize.region in activity.zone


def select_gear(activity: Activity, prize: Prize) -> Optional[Gear]:
    for gear in GEAR:
        if prize.region in gear.covers and activity.mess in gear.guards:
            return gear
    return None


def valid_combos() -> list[tuple[str, str, str]]:
    out = []
    for place, setting in SETTINGS.items():
        for act_id in setting.affords:
            act = _safe_lookup(ACTIVITIES, act_id)
            for prize_id, prize in PRIZES.items():
                if prize_at_risk(act, prize) and select_gear(act, prize):
                    out.append((place, act_id, prize_id))
    return out


def explain_rejection(act: Activity, prize: Prize) -> str:
    return (
        f"(No story: {act.gerund} would not reasonably threaten {prize.label} here, "
        f"or no sensible gear protects that item. Try a different prize or activity.)"
    )


# ---------------------------------------------------------------------------
# Simulation rules
# ---------------------------------------------------------------------------
def apply_switch(world: World, hero: Entity, prize: Entity) -> None:
    if hero.meters.get("sparks", 0) < 1:
        return
    if prize.meters.get("burnt", 0) >= 1:
        return
    sig = ("burn", prize.id)
    if sig in world.fired:
        return
    world.fired.add(sig)
    prize.meters["burnt"] = 1
    prize.meters["dirty"] = 1
    world.say(f"{hero.pronoun('possessive').capitalize()} {prize.label} caught a black scorch mark.")


def simulate(world: World, hero: Entity, act: Activity, prize: Entity, gear: Optional[Gear] = None) -> None:
    hero.meters[act.mess] = hero.meters.get(act.mess, 0) + 1
    hero.memes["excitement"] = hero.memes.get("excitement", 0) + 1
    if gear:
        gear_item = Entity(id=gear.id, kind="thing", type="gear", label=gear.label, plural=gear.plural)
        gear_item.worn_by = hero.id
        world.add(gear_item)
    if act.id == "switch":
        apply_switch(world, hero, prize)
    elif act.id == "frill":
        world.say(f"The {prize.label} shimmered, but the frill made the panel crackle.")
    elif act.id == "barber":
        world.say(f"The barber pod buzzed around {hero.id}'s head with a soft trim.")
    # bad ending trigger: the switch causes a ship failure if the prize burns
    if act.id == "switch" and prize.meters.get("burnt", 0) >= 1:
        world.facts["ending"] = "bad"
        world.say("A warning light blinked once, then the starship lurched into the dark.")


def preview_bad_end(world: World, hero: Entity, act: Activity, prize: Entity) -> bool:
    sim = world.copy()
    simulate(sim, sim.get(hero.id), act, sim.get(prize.id))
    return sim.facts.get("ending") == "bad"


