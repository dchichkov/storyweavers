def generation_prompts(world: World) -> list[str]:
    f = world.facts
    return [
        f'Write a short space-adventure story for a young child that includes the words "{f["activity"].keyword}", "switch", and "barber".',
        f"Tell a gentle spaceship story where {f['hero'].id} wants to {f['activity'].verb} but a helper worries about {f['prize'].label}.",
        f"Write a tiny adventure with a frill, a barber, and a bad ending on a starship.",
    ]


def story_qa(world: World) -> list[QAItem]:
    f = world.facts
    hero: Entity = _safe_fact(world, f, "hero")
    helper: Entity = _safe_fact(world, f, "helper")
    prize: Entity = _safe_fact(world, f, "prize")
    activity: Activity = _safe_fact(world, f, "activity")
    return [
        QAItem(
            question=f"What did {hero.id} want to do on the ship?",
            answer=f"{hero.id} wanted to {activity.verb}.",
        ),
        QAItem(
            question=f"Who warned {hero.id} about the danger?",
            answer=f"{helper.id} warned {hero.id} that the old switch could cause trouble.",
        ),
        QAItem(
            question=f"What got ruined when the plan went wrong?",
            answer=f"{hero.pronoun('possessive').capitalize()} {prize.label} got ruined in the bad ending.",
        ),
    ]


def world_knowledge_qa(world: World) -> list[QAItem]:
    return [
        QAItem(question="What is a switch?", answer="A switch is a lever or button that can turn a machine on or off."),
        QAItem(question="What is a frill?", answer="A frill is a fancy ruffle or decorative trim."),
        QAItem(question="What does a barber do?", answer="A barber trims and cuts hair."),
    ]


def format_qa(sample: StorySample) -> str:
    lines = ["== prompts =="]
    for p in sample.prompts:
        lines.append(f"- {p}")
    lines.append("")
    lines.append("== story questions ==")
    for item in sample.story_qa:
        lines.append(f"Q: {item.question}")
        lines.append(f"A: {item.answer}")
    lines.append("")
    lines.append("== world questions ==")
    for item in sample.world_qa:
        lines.append(f"Q: {item.question}")
        lines.append(f"A: {item.answer}")
    return "\n".join(lines)


def dump_trace(world: World) -> str:
    lines = ["--- world trace ---"]
    for e in list(world.entities.values()):
        meters = {k: v for k, v in e.meters.items() if v}
        memes = {k: v for k, v in e.memes.items() if v}
        parts = []
        if meters:
            parts.append(f"meters={meters}")
        if memes:
            parts.append(f"memes={memes}")
        if e.label:
            parts.append(f"label={e.label}")
        lines.append(f"{e.id}: {e.type} {' '.join(parts)}")
    return "\n".join(lines)


