def tell(setting: Setting, activity: Activity, prize_cfg: Prize, hero_name: str, gender: str,
         helper_role: str, trait: str) -> World:
    world = World(setting.place)
    hero = world.add(Entity(id=hero_name, kind="character", type=gender, traits=[trait, "small"]))
    helper = world.add(Entity(id="Helper", kind="character", type=helper_role))
    prize = world.add(Entity(id="Prize", kind="thing", type=prize_cfg.type, label=prize_cfg.label,
                             phrase=prize_cfg.phrase, owner=hero.id, region=prize_cfg.region,
                             plural=prize_cfg.plural))

    world.say(f"{hero.id} was a {trait} little {gender} aboard a silver starship.")
    world.say(f"{hero.id} loved {activity.gerund} and kept {hero.pronoun('possessive')} {prize.label} close.")
    world.say(f"One day, {hero.id} and {hero.pronoun('possessive')} {helper_role} went to {setting.place}.")
    world.para()

    world.say(f"{hero.id} wanted to {activity.verb}, but the captain had warned about the old switch.")
    if preview_bad_end(world, hero, activity, prize):
        world.say(f'"If you touch that {activity.keyword}, you may ruin the ship," {helper.id} said.')
    world.say(f"{hero.id} still ran toward the {activity.keyword}.")
    if activity.id == "barber":
        world.say(f"The barber pod buzzed nearby, but the room still smelled of oil and dust.")
    if activity.id == "frill":
        world.say(f"The frill spun like a bright ribbon near the control panel.")
    if activity.id == "switch":
        world.say(f"{hero.id} reached for the switch anyway.")
    world.para()

    gear = select_gear(activity, prize)
    if gear:
        world.say(f"{helper.id} tried a safer plan: {gear.prep}.")
    simulate(world, hero, activity, prize, gear=None)
    world.say(f"The choice went wrong fast: {hero.id} was {activity.gerund}, and the {prize.label} was ruined.")
    world.say("The ship drifted past the moonlit dust, and nobody could turn it back.")

    world.facts.update(
        hero=hero,
        helper=helper,
        prize=prize,
        activity=activity,
        setting=setting,
        gear=gear,
        bad_ending=True,
    )
    return world


