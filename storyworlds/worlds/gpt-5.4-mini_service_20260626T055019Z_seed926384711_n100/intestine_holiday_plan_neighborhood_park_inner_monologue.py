def _run_activity(world: World, actor: Entity, activity: Activity, narrate: bool = True) -> None:
    actor.meters[activity.mess] += 1
    actor.memes["worry"] += 1
    world.zone = {"torso"}
    if narrate:
        world.say(f"{actor.id} tried to {activity.verb}, and the air felt suddenly heavier.")


def _predict(world: World, actor: Entity, activity: Activity, prize_id: str) -> dict:
    sim = world.copy()
    _run_activity(sim, sim.get(actor.id), activity, narrate=False)
    prize = sim.get(prize_id)
    return {"soiled": prize.meters["damp"] >= THRESHOLD}


def tell(setting: Setting, activity: Activity, prize_cfg: Prize,
         hero_name: str, ghost_name: str, trait: str) -> World:
    world = World(setting)
    world.weather = activity.weather

    hero = world.add(Entity(
        id=hero_name,
        kind="character",
        type="child",
        traits=[trait, "careful"],
    ))
    ghost = world.add(Entity(
        id=ghost_name,
        kind="character",
        type="ghost",
        traits=["quiet", "friendly"],
    ))
    prize = world.add(Entity(
        id="prize",
        type=prize_cfg.type,
        label=prize_cfg.label,
        phrase=prize_cfg.phrase,
        owner=hero.id,
        caretaker=hero.id,
        region=prize_cfg.region,
        plural=prize_cfg.plural,
    ))
    hero.memes["friendship"] += 1
    ghost.memes["spook"] += 1

    world.say(
        f"At {setting.place}, {hero.id} found {ghost.id} near a swing that swayed without any wind."
    )
    world.say(
        f"{ghost.id} was not mean or fierce; {ghost.id} just seemed to speak in a hush."
    )
    world.say(
        f"{hero.id} had brought {prize.phrase}, because tonight was a holiday and there was a plan to keep."
    )

    world.para()
    world.say(
        f"{hero.id} wanted to {activity.verb}, but {ghost.id} stayed still and listened."
    )
    ghost.memes["worry"] += 1
    hero.memes["plan"] += 1

    world.say(
        f"In {ghost.id}'s inner monologue, the thought went around and around: "
        f"what if being a ghost made everyone step away?"
    )
    world.say(
        f"Then {ghost.id} noticed a sign from the school table about the intestine, "
        f"and the twisty word made {ghost.id} think of winding paths and brave tummies."
    )

    pred = _predict(world, hero, activity, prize.id)
    if pred["soiled"]:
        world.say(
            f"{hero.id} saw that {prize.label} would get damp if the plan changed too quickly."
        )

    world.para()
    world.say(
        f"{hero.id} said, 'You can help me keep the plan.'"
    )
    hero.memes["friendship"] += 1
    ghost.memes["warmth"] += 1

    gear = select_gear(activity, prize)
    if gear is None:
        gear = next(iter(globals().get("GEARS", globals().get("GEAR", []))))

    gear_ent = world.add(Entity(
        id=gear.id,
        type="gear",
        label=gear.label,
        owner=hero.id,
        caretaker=hero.id,
        protective=True,
        covers=set(gear.covers),
        plural=gear.plural,
    ))
    gear_ent.worn_by = hero.id
    world.say(
        f"{hero.id} and {ghost.id} {gear.prep}."
    )

    if _predict(world, hero, activity, prize.id)["soiled"]:
        pass

    hero.memes["joy"] += 1
    hero.memes["worry"] = 0.0
    ghost.memes["worry"] = 0.0
    ghost.memes["friendship"] += 1

    world.para()
    world.say(
        f"At last, {hero.id} could {activity.gerund}, and {prize.label} stayed clean."
    )
    world.say(
        f"{ghost.id} stopped feeling like a shadow and started feeling like a friend."
    )
    world.say(
        f"The holiday plan held together in the soft park dusk, with one child, one ghost, and one bright lantern."
    )

    world.facts.update(
        hero=hero,
        ghost=ghost,
        prize=prize,
        activity=activity,
        setting=setting,
        gear=gear_ent,
    )
    return world


