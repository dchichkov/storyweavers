def _narrate_collision(world: World, hero: Entity, prize: Entity, mystery: Mystery) -> None:
    if world.fired.__contains__("collision"):
        return
    world.fired.add("collision")
    prize.meters["tear"] += 1
    hero.memes["surprise"] += 1
    world.say(
        f"With one clumsy bump, there was a collision, and the {prize.label} got a tear."
    )
    world.say(
        f"{hero.pronoun().capitalize()} stared at the beige fabric and said, "
        f'"That was not supposed to happen."'
    )


def _solve_with_dialogue_and_magic(world: World, hero: Entity, helper: Entity, prize: Entity, mystery: Mystery) -> None:
    if "solve" in world.fired:
        return
    world.fired.add("solve")
    helper.memes["calm"] = helper.memes.get("calm", 0.0) + 1
    hero.memes["curiosity"] = hero.memes.get("curiosity", 0.0) + 1
    world.say(
        f'{helper.pronoun().capitalize()} said, "Let\'s use dialogue first. '
        f'What did you notice?"'
    )
    world.say(
        f'{hero.pronoun().capitalize()} answered, "The clue was {mystery.clue}, '
        f'and the weird sound was {mystery.weird_sound}."'
    )
    world.say(
        f'Then {helper.id} whispered the magic word, "{mystery.magic_word}!" '
        f'and the room felt as if a tiny comedy curtain had been lifted.'
    )
    world.say(
        f"That helped them solve the mystery: {mystery.reveal}."
    )
    if prize.meters.get("tear", 0.0) >= THRESHOLD:
        prize.meters["tear"] = 0.0
        world.say(
            f"They patched the tear with beige thread, so the {prize.label} looked neat again."
        )
    hero.memes["relief"] = hero.memes.get("relief", 0.0) + 1


def tell(setting: Setting, mystery: Mystery, prize_cfg: Prize, hero_name: str, gender: str, helper_kind: str) -> World:
    world = World(setting)
    hero = world.add(Entity(id=hero_name, kind="character", type=gender))
    helper = world.add(Entity(id="Helper", kind="character", type=helper_kind, label=f"the {helper_kind}"))
    prize = world.add(Entity(id="prize", type=prize_cfg.label, label=prize_cfg.label, phrase=prize_cfg.phrase, caretaker=helper.id))

    world.say(
        f"{hero_name} was a curious little {gender} who loved solving mysteries with jokes."
    )
    world.say(
        f"One day, {hero_name} found {prize_cfg.phrase} in {setting.place}, and the beige color made everything look extra serious for no good reason."
    )
    world.para()
    world.say(
        f"In the middle of the room, {mystery.clue}."
    )
    world.say(
        f"{hero_name} leaned closer, but a clumsy collision made the {prize.label} tear."
    )
    _narrate_collision(world, hero, prize, mystery)
    world.para()
    world.say(
        f"That turned the mystery into a silly little problem to solve."
    )
    _solve_with_dialogue_and_magic(world, hero, helper, prize, mystery)

    world.facts.update(
        hero=hero,
        helper=helper,
        prize=prize,
        mystery=mystery,
        setting=setting,
    )
    return world


