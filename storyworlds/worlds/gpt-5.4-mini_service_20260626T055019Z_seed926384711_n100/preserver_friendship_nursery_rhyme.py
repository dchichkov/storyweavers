def _r_soak(world: World) -> list[str]:
    out: list[str] = []
    for actor in world.characters():
        for mess in ("wet", "dirty"):
            if actor.meters[mess] < THRESHOLD:
                continue
            for item in world.worn_items(actor):
                if item.protective or item.region not in world.zone:
                    continue
                if world.covered(actor, item.region):
                    continue
                sig = ("soak", actor.id, item.id, mess)
                if sig in world.fired:
                    continue
                world.fired.add(sig)
                item.meters[mess] += 1
                item.meters["dirty"] += 1
                out.append(f"{actor.id}'s {item.label} got {mess} and dirty.")
    return out


def _r_worry(world: World) -> list[str]:
    out: list[str] = []
    for item in list(world.entities.values()):
        if item.meters["dirty"] < THRESHOLD or not item.caretaker:
            continue
        sig = ("worry", item.id)
        if sig in world.fired:
            continue
        world.fired.add(sig)
        carer = world.get(item.caretaker)
        carer.memes["worry"] += 1
        out.append(f"That would make {carer.label} worry.")
    return out


def _r_conflict(world: World) -> list[str]:
    for actor in world.characters():
        if actor.memes["defiance"] < THRESHOLD or actor.memes["held"] < THRESHOLD:
            continue
        sig = ("conflict", actor.id)
        if sig in world.fired:
            continue
        world.fired.add(sig)
        actor.memes["conflict"] += 1
        return ["__conflict__"]
    return []


CAUSAL_RULES = [
    Rule("soak", "physical", _r_soak),
    Rule("worry", "social", _r_worry),
    Rule("conflict", "social", _r_conflict),
]


def propagate(world: World, narrate: bool = True) -> list[str]:
    produced: list[str] = []
    changed = True
    for _ in range(len(globals().get("CAUSAL_RULES", [])) + 4):
        changed = False
        for rule in CAUSAL_RULES:
            sents = rule.apply(world)
            if sents:
                changed = True
                produced.extend(s for s in sents if s != "__conflict__")
    if narrate:
        for s in produced:
            world.say(s)
    return produced


def prize_at_risk(activity: Activity, prize: Prize) -> bool:
    return prize.region in activity.zone


def select_gear(activity: Activity, prize: Prize) -> Optional[Gear]:
    for gear in GEAR:
        if activity.mess in gear.guards and prize.region in gear.covers:
            return gear
    return None


def predict_mess(world: World, actor: Entity, activity: Activity, prize_id: str) -> dict:
    sim = world.copy()
    _do_activity(sim, sim.get(actor.id), activity, narrate=False)
    prize = sim.entities.get(prize_id)
    return {
        "soiled": bool(prize and prize.meters["dirty"] >= THRESHOLD),
    }


def _do_activity(world: World, actor: Entity, activity: Activity, narrate: bool = True) -> None:
    if activity.id not in world.setting.affords:
        return
    world.zone = set(activity.zone)
    actor.meters[activity.mess] += 1
    actor.memes["joy"] += 1
    propagate(world, narrate=narrate)


def setting_detail(setting: Setting, activity: Activity) -> str:
    if setting.indoor:
        return f"The {setting.place} was still, and a little table waited near the window."
    if activity.weather == "rainy":
        return f"The air smelled fresh, and {setting.place} shone after the rain."
    return f"{setting.place.capitalize()} looked bright and neat for play."


def introduce(world: World, friend1: Entity, friend2: Entity) -> None:
    world.say(
        f"Little {friend1.id} and {friend2.id} were friends so true, "
        f"with a shared smile and a shiny point of view."
    )


def loves(world: World, friend1: Entity, friend2: Entity, prize: Entity) -> None:
    friend1.memes["love"] += 1
    friend2.memes["love"] += 1
    prize.worn_by = friend1.id
    world.say(
        f"They loved their {prize.label} together, soft and small; "
        f"they kept it near like a treasure ball."
    )


def arrive(world: World, friend1: Entity, friend2: Entity, activity: Activity) -> None:
    day = "One rainy day," if world.weather == "rainy" else "One day,"
    go = "went to" if not world.setting.indoor else "came to"
    world.say(
        f"{day} {friend1.id} and {friend2.id} {go} {world.setting.place} for play."
    )
    world.say(setting_detail(world.setting, activity))


def wants(world: World, hero: Entity, activity: Activity) -> None:
    hero.memes["desire"] += 1
    world.say(
        f"{hero.id} wanted to {activity.verb}, quick as a wink, "
        f"but a little worry made {hero.pronoun('possessive')} heart think."
    )


def warn(world: World, friend: Entity, hero: Entity, activity: Activity, prize: Entity) -> bool:
    pred = predict_mess(world, hero, activity, prize.id)
    if not pred["soiled"]:
        return False
    world.facts["predicted_soil"] = activity.soil
    world.say(
        f'"If you {activity.verb}," {friend.id} said with care, '
        f'"your {prize.label} will get {activity.soil} there."'
    )
    return True


def defies(world: World, hero: Entity, activity: Activity) -> None:
    hero.memes["defiance"] += 1
    world.say(
        f"{hero.id} frowned a tiny frown and tried to {activity.rush}, "
        f"though the warning tugged down."
    )


def hold_hand(world: World, friend: Entity, hero: Entity) -> None:
    hero.memes["held"] += 1
    propagate(world, narrate=False)
    world.say(
        f"Then {friend.id} held {hero.pronoun('possessive')} hand so warm, "
        f"and said, \"We can be careful and still keep our charm.\""
    )


def compromise(world: World, friend: Entity, hero: Entity, activity: Activity, prize: Entity) -> Optional[Gear]:
    gear_def = select_gear(activity, prize)
    if gear_def is None:
        return None
    gear = world.add(
        Entity(
            id=gear_def.id,
            type="gear",
            label=gear_def.label,
            owner=hero.id,
            caretaker=friend.id,
            protective=True,
            covers=set(gear_def.covers),
            plural=gear_def.plural,
        )
    )
    gear.worn_by = hero.id
    if predict_mess(world, hero, activity, prize.id)["soiled"]:
        gear.worn_by = None
        del world.entities[gear.id]
        return None
    world.say(
        f'"Let us use {gear_def.label}," {friend.id} said with a grin; '
        f'"then your {prize.label} can stay tucked safe within."'
    )
    return gear_def


def resolve(world: World, friend: Entity, hero: Entity, activity: Activity, prize: Entity, gear_def: Gear) -> None:
    hero.memes["joy"] += 1
    hero.memes["love"] += 1
    hero.memes["conflict"] = 0.0
    world.say(
        f"{hero.id} smiled a moon-bright smile and gave {friend.id} a hug; "
        f"then off they went, snug as a bug."
    )
    world.say(
        f"They used {gear_def.label} and played all day; "
        f"{hero.id} was {activity.gerund}, while {prize.label} stayed safe away."
    )


