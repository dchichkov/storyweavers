def story_qa(world: World) -> list[QAItem]:
    f = world.facts
    hero, parent, prize, act = f["hero"], f["parent"], f["prize"], f["activity"]
    pw = parent.label_word
    sub, obj, pos = (hero.pronoun("subject"), hero.pronoun("object"),
                     hero.pronoun("possessive"))
    where = "inside" if world.setting.indoor else "outside"
    place = world.setting.place
    trait = next((t for t in hero.traits if t != "little"), hero.type)
    qa: list[QAItem] = [
        QAItem(
            question=(
                f"Who finds a note about a forbidden liqueur at {place}, "
                f"and what does {pos} {pw} do about it?"
            ),
            answer=(
                f"A little {trait} {hero.type} named {hero.id} finds a note "
                f"with a red stain at {place}. {pos.capitalize()} {pw} "
                f"prohibits tasting the liqueur because it could be dangerous "
                f"and ruin {pos} {prize.label}."
            ),
        ),
        QAItem(
            question=(
                f"Why does {hero.id}'s {pw} grab {hero.id}'s hand at {place}?"
            ),
            answer=(
                f"{hero.id} tried to {act.rush} despite the warning. "
                f"{pos.capitalize()} {pw} grabbed {pos} hand to stop {obj} "
                f"and offered a safer plan instead."
            ),
        ),
    ]
    if f.get("conflict"):
        soil = f.get("predicted_soil", "sticky and stained")
        qa.append(QAItem(
            question=(
                f"What was {pos} {pw} worried would happen to {pos} {prize.label} "
                f"if {hero.id} {act.gerund}?"
            ),
            answer=(
                f"{pos.capitalize()} {pw} was worried that the liqueur would "
                f"spill and make {pos} {prize.label} {soil}, which would need "
                f"extra cleaning."
            ),
        ))
    if f.get("resolved"):
        gear = f["gear"]
        qa.append(QAItem(
            question=(
                f"How did {gear.label} help {hero.id} and {pos} {pw} solve the "
                f"mystery of the liqueur at {place}?"
            ),
            answer=(
                f"{gear.label.capitalize()} kept {pos} {prize.label} safe while "
                f"they {act.gerund}. Together they followed clues in the note "
                f"and discovered the liqueur was a harmless prize for puzzle "
                f"solving. They solved the mystery without ever tasting it."
            ),
        ))
    return qa


def world_knowledge_qa(world: World) -> list[QAItem]:
    f = world.facts
    tags = set(f["activity"].tags)
    if f.get("gear"):
        tags.add(f["gear"].id)
    out: list[QAItem] = []
    for tag in KNOWLEDGE_ORDER:
        if tag in tags:
            out.extend(QAItem(question=q, answer=a) for q, a in KNOWLEDGE[tag])
    return out


def format_qa(sample: StorySample) -> str:
    lines = ["== (1) Generation prompts =="]
    for i, p in enumerate(sample.prompts, 1):
        lines.append(f"{i}. {p}")
    lines.append("")
    lines.append("== (2) Story questions ==")
    for item in sample.story_qa:
        lines.append(f"Q: {item.question}")
        lines.append(f"A: {item.answer}")
    lines.append("")
    lines.append("== (3) World-knowledge questions ==")
    for item in sample.world_qa:
        lines.append(f"Q: {item.question}")
        lines.append(f"A: {item.answer}")
    return "\n".join(lines)


def dump_trace(world: World) -> str:
    lines = ["--- world model state ---"]
    for e in world.entities.values():
        meters = {k: v for k, v in e.meters.items() if v}
        memes = {k: v for k, v in e.memes.items() if v}
        bits = []
        if meters:
            bits.append(f"meters={dict(meters)}")
        if memes:
            bits.append(f"memes={dict(memes)}")
        if e.protective:
            bits.append(f"covers={sorted(e.covers)}")
        elif e.region:
            bits.append(f"region={e.region}")
        lines.append(f"  {e.id:8} ({e.type:7}) {' '.join(bits)}")
    lines.append(f"  fired rules: {sorted(set(n for n, *_ in world.fired))}")
    return "\n".join(lines)


