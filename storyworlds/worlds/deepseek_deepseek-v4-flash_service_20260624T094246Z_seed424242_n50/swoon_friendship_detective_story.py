def story_qa(world: World) -> list[QAItem]:
    f = world.facts
    hero, friend, prize, act = f["hero"], f["friend"], f["prize"], f["activity"]
    sub_h, obj_h, pos_h = (hero.pronoun("subject"), hero.pronoun("object"),
                            hero.pronoun("possessive"))
    sub_f, obj_f, pos_f = (friend.pronoun("subject"), friend.pronoun("object"),
                            friend.pronoun("possessive"))
    place = world.setting.place
    qa: list[QAItem] = [
        QAItem(
            question=f"Who are the two friends in the story?",
            answer=f"The friends are {hero.id} and {friend.id}. {sub_h} is a {hero.type} and {sub_f} is a {friend.type}.",
        ),
        QAItem(
            question=f"What went missing in the story?",
            answer=f"The {prize.label} went missing. It was {prize.phrase}.",
        ),
        QAItem(
            question=f"Where did they search for clues?",
            answer=f"They searched at {place}.",
        ),
    ]
    if f.get("swoon"):
        qa.append(QAItem(
            question=f"How did {hero.id} feel when {sub_h} found the clue?",
            answer=f"{hero.id} felt a swoon of happiness. {sub_h} was very excited.",
        ))
    if f.get("friendship"):
        qa.append(QAItem(
            question=f"How did the friends feel at the end?",
            answer=f"They felt happy and their friendship grew stronger. They hugged each other.",
        ))
    return qa


def world_knowledge_qa(world: World) -> list[QAItem]:
    f = world.facts
    tags = set(f["activity"].tags)
    tags.add("friendship")
    tags.add("swoon")
    out: list[QAItem] = []
    for tag in globals().get("KNOWLEDGE_ORDER", sorted(globals().get("KNOWLEDGE", []))):
        if tag in tags:
            out.extend(QAItem(question=q, answer=a) for q, a in KNOWLEDGE[tag])
    return out


def format_qa(sample: StorySample) -> str:
    lines = ["== (1) Generation prompts =="]
    for i, p in enumerate(sample.prompts, 1):
        lines.append(f"{i}. {p}")
    lines.append("")
    lines.append("== (2) Story questions ==")
    for item in sample.story_qa:
        lines.append(f"Q: {item.question}")
        lines.append(f"A: {item.answer}")
    lines.append("")
    lines.append("== (3) World-knowledge questions ==")
    for item in sample.world_qa:
        lines.append(f"Q: {item.question}")
        lines.append(f"A: {item.answer}")
    return "\n".join(lines)


